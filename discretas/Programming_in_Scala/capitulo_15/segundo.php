<h2>Patrones de variable</h2>
<p>Un patrón de variable coincide con cualquier objeto, al igual que un comodín. A diferencia de un comodín, Scala vincula la variable a cualquier objeto que sea. Luego puedes usar esta variable para actuar sobre el objeto. Por ejemplo, la Lista 15.6 muestra una coincidencia de patrones que tiene un caso especial para cero y un caso por defecto para todos los demás valores. El caso por defecto usa un patrón de variable para que tenga un nombre para el valor, sin importar cuál sea.</p>

<pre><code>scala
