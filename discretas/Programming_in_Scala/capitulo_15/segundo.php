    <h2>15.2 Tipos de patrones</h2>

<p>El ejemplo anterior mostró varios tipos de patrones en rápida sucesión. Ahora tómate un minuto para observar cada uno.​</p>

<p>La sintaxis de los patrones es sencilla, así que no te preocupes demasiado por eso. Todos los patrones se ven exactamente como la expresión correspondiente. Por ejemplo, dada la jerarquía de la Lista 15.1, el patrón <code>Var(x)</code> coincide con cualquier expresión de variable, vinculando <code>x</code> al nombre de la variable. Usado como una expresión, <code>Var(x)</code>—exactamente la misma sintaxis—recrea un objeto equivalente, suponiendo que <code>x</code> ya está vinculado al nombre de la variable. Dado que la sintaxis de los patrones es tan transparente, lo principal a lo que debes prestar atención es simplemente a qué tipos de patrones son posibles.​</p>

<h3>Patrones comodín</h3>

<p>El patrón comodín (<code>_</code>) coincide con cualquier objeto. Ya lo has visto usado como una alternativa predeterminada, de captura total, así:​</p>

<pre><code>expr match {
  case BinOp(op, left, right) =>
    println(expr + " es una operación binaria")
  case _ =>
}</code></pre>

<p>Los comodines también se pueden usar para ignorar partes de un objeto que no te importan. Por ejemplo, el ejemplo anterior en realidad no se preocupa por cuáles son los elementos de una operación binaria. Solo verifica si es una operación binaria en absoluto. Por lo tanto, el código puede usar igualmente el patrón comodín para los elementos de <code>BinOp</code>, como se muestra en la Lista 15.4:​</p>

<pre><code>expr match {
  case BinOp(_, _, _) => println(expr + " es una operación binaria")
  case _ => println("Es otra cosa")
}</code></pre>

