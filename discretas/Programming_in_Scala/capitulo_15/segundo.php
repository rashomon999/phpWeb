<h2>Patrones de secuencia</h2>
<p>
Puedes hacer coincidir tipos de secuencia como <code>List</code> o <code>Array</code> de la misma manera que haces coincidir clases caso. Usa la misma sintaxis, pero ahora puedes especificar cualquier número de elementos dentro del patrón. Por ejemplo, la Lista 15.8 muestra un patrón que verifica una lista de tres elementos que comienza con cero:
</p>

<pre><code>expr match {
  case List(0, _, _) =&gt; println("found it")
  case _ =&gt;
}
</code></pre>
<p><strong>Lista 15.8 · Un patrón de secuencia con longitud fija.</strong></p>

<p>
Si deseas hacer coincidir una secuencia sin especificar cuán larga puede ser, puedes especificar <code>_* </code> como el último elemento del patrón. Este patrón de apariencia extraña coincide con cualquier número de elementos dentro de una secuencia, incluidos cero elementos. La Lista 15.9 muestra un ejemplo que coincide con cualquier lista que comience con cero, sin importar cuán larga sea la lista.
</p>

<pre><code>expr match {
  case List(0, _*) =&gt; println("found it")
  case _ =&gt;
}
</code></pre>
