    <p>¿Cómo sabe el compilador de Scala que Pi es una constante importada del objeto java.lang.Math, y no una variable que representa el valor del selector? Scala usa una regla léxica simple para la desambiguación: un nombre simple que comienza con una letra minúscula se considera una variable de patrón; todas las demás referencias se consideran constantes. Para ver la diferencia, crea un alias en minúscula para pi y prueba con eso:</p>

<pre><code>scala
Copiar
Editar
