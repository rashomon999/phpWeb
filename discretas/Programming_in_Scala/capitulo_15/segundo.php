</code></pre>

<p>Si lo necesitas, aún puedes usar un nombre en minúscula como constante de patrón, utilizando uno de dos trucos. Primero, si la constante es un campo de algún objeto, puedes anteponerle un calificador. Por ejemplo, pi es un patrón de variable, pero this.pi o obj.pi son constantes aunque comiencen con letras minúsculas. Si eso no funciona (porque pi es una variable local, por ejemplo), puedes encerrar el nombre de la variable entre comillas invertidas (backticks). Por ejemplo, <code>`pi`</code> se interpretará nuevamente como una constante, no como una variable:</p>

