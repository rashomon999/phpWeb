</code></pre>

<p>Aquí el compilador ni siquiera te permitirá agregar un caso por defecto. Como pi es un patrón de variable, coincidirá con todas las entradas, por lo que ningún caso posterior puede ser alcanzado:</p>

<pre><code>scala
Copiar
Editar
