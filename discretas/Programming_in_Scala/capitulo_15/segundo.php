<h3>Patrones constantes</h3>

<p>Un patrón constante coincide solo consigo mismo. Cualquier literal puede usarse como una constante. Por ejemplo, <code>5</code>, <code>true</code> y <code>"hello"</code> son todos patrones constantes. Además, cualquier <code>val</code> u objeto singleton puede usarse como una constante. Por ejemplo, <code>Nil</code>, un objeto singleton, es un patrón que coincide solo con la lista vacía. La Lista 15.5 muestra algunos ejemplos de patrones constantes:​</p>

<pre><code>def describe(x: Any) = x match {
  case 5 => "cinco"
  case true => "verdad"
  case "hello" => "¡hola!"
  case Nil => "la lista vacía"
  case _ => "algo más"
}</code></pre>

<p><em>Lista 15.5 · Una coincidencia de patrones con patrones constantes.</em></p>

