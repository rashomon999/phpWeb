<h2>Patrones de constructor</h2>
<p>
Los constructores son donde la coincidencia de patrones se vuelve realmente poderosa. Un patrón de constructor se ve como <code>BinOp("+", e, Number(0))</code>. Consiste en un nombre (<code>BinOp</code>) y luego una serie de patrones entre paréntesis: <code>"+"</code>, <code>e</code> y <code>Number(0)</code>. Suponiendo que el nombre designa una clase caso, tal patrón significa primero verificar que el objeto sea un miembro de la clase caso nombrada, y luego verificar que los parámetros del constructor del objeto coincidan con los patrones adicionales suministrados.
</p>

<p>
Estos patrones adicionales significan que los patrones de Scala admiten coincidencias profundas. Tales patrones no solo verifican el objeto de nivel superior proporcionado, sino que también verifican el contenido del objeto contra patrones adicionales. Dado que los patrones adicionales pueden ser ellos mismos patrones de constructor, puedes usarlos para verificar profundamente un objeto. Por ejemplo, el patrón mostrado en la Lista 15.7 verifica que el objeto de nivel superior sea un <code>BinOp</code>, que su tercer parámetro de constructor sea un <code>Number</code>, y que el campo <code>value</code> de ese número sea 0. Este patrón tiene una sola línea de largo pero verifica tres niveles de profundidad.
</p>

<pre><code>expr match {
  case BinOp("+", e, Number(0)) =&gt; println("a deep match")
  case _ =&gt;
}
</code></pre>
