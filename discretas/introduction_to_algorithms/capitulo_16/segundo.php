    <h3>16.2 Elementos de la estrategia voraz</h3>

<p>
Un algoritmo voraz obtiene una solución óptima a un problema realizando una secuencia de elecciones. En cada punto de decisión, el algoritmo elige la opción que parece mejor en ese momento. Esta estrategia heurística no siempre produce una solución óptima, pero como vimos en el problema de selección de actividades, a veces sí lo hace. Esta sección discute algunas de las propiedades generales de los métodos voraces.
</p>

<p>
El proceso que seguimos en la Sección 16.1 para desarrollar un algoritmo voraz fue un poco más complejo de lo habitual. Pasamos por los siguientes pasos:
</p>

<ol>
  <li>Determinar la subestructura óptima del problema.</li>
  <li>Desarrollar una solución recursiva. (Para el problema de selección de actividades, formulamos la recurrencia (16.2), pero evitamos desarrollar un algoritmo recursivo basado en esta recurrencia.)</li>
  <li>Mostrar que si hacemos la elección voraz, entonces solo queda un subproblema.</li>
  <li>Probar que siempre es seguro hacer la elección voraz. (Los pasos 3 y 4 pueden ocurrir en cualquier orden.)</li>
  <li>Desarrollar un algoritmo recursivo que implemente la estrategia voraz.</li>
  <li>Convertir el algoritmo recursivo en un algoritmo iterativo.</li>
</ol>

<p>
Al pasar por estos pasos, vimos en gran detalle los fundamentos de programación dinámica de un algoritmo voraz. Por ejemplo, en el problema de selección de actividades, primero definimos los subproblemas \( S_{ij} \), donde tanto \( i \) como \( j \) variaban. Luego descubrimos que si siempre hacíamos la elección voraz, podíamos restringir los subproblemas a ser de la forma \( S_k \).
</p>

<p>
Alternativamente, podríamos haber diseñado nuestra subestructura óptima teniendo en mente una elección voraz, de modo que la elección dejara solo un subproblema por resolver. En el problema de selección de actividades, podríamos haber comenzado eliminando el segundo subíndice y definiendo subproblemas de la forma \( S_k \). Luego, podríamos haber probado que una elección voraz (la primera actividad \( a_m \) en finalizar en \( S_k \)), combinada con una solución óptima al conjunto restante \( S_m \) de actividades compatibles, produce una solución óptima para \( S_k \).
</p>
 
