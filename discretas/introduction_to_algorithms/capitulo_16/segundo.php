    <p>El procedimiento <code>GREEDY-ACTIVITY-SELECTOR</code> es una versión iterativa del procedimiento <code>RECURSIVE-ACTIVITY-SELECTOR</code>. También asume que las actividades de entrada están ordenadas por tiempos de finalización monotonamente crecientes. Recoge las actividades seleccionadas en un conjunto <code>A</code> y devuelve este conjunto cuando termina.</p>

    <pre><code>GREEDY-ACTIVITY-SELECTOR(s, f)
    1 n = s.length
    2 A = {a_1}
    3 k = 1
    4 for m = 2 to n
    5   if s[m] ≥ f[k]
    6     A = A ∪ {a_m}
    7     k = m
    8 return A
    </code></pre>

    <p>El procedimiento funciona de la siguiente manera. La variable <code>k</code> indexa la adición
    más reciente a <code>A</code>, correspondiente a la actividad \( a_k \) en la versión recursiva.
    Dado que consideramos las actividades en orden de tiempos de finalización monotonamente crecientes,
    \( f_k \) es siempre el tiempo de finalización máximo de cualquier actividad en <code>A</code>. Es decir,</p>

    <p> 
    \[
    f_k = \max \{ f_i \mid a_i \in A \}
    \]
    </p>

    <p>Las líneas 2 y 3 seleccionan la actividad \( a_1 \), inicializan <code>A</code> para contener solo 
    esta actividad, e inicializan <code>k</code> para indexar esta actividad. El ciclo <code>for</code> de
    las líneas 4 a 7 encuentra la actividad más temprana en \( S_k \) que termina. El ciclo considera cada 
    actividad \( a_m \) por turno y añade \( a_m \) a <code>A</code> si es compatible con todas las actividades
    previamente seleccionadas; tal actividad es la más temprana en \( S_k \) que termina. Para verificar si la
    actividad \( a_m \) es compatible con cada actividad actualmente en <code>A</code>, basta con verificar 
    (en la línea 5) que su tiempo de inicio \( s_m \) no sea anterior al tiempo de finalización \( f_k \) de
    la actividad añadida más recientemente a <code>A</code>, según la ecuación anterior.</p>

    <p>Si la actividad \( a_m \) es compatible, entonces las líneas 6 y 7 añaden la actividad \( a_m \) a 
    <code>A</code> y actualizan \( k \) a \( m \). El conjunto <code>A</code> que devuelve la llamada 
    <code>GREEDY-ACTIVITY-SELECTOR(s, f)</code> es precisamente el conjunto que devuelve la llamada
    <code>RECURSIVE-ACTIVITY-SELECTOR(s, f, 0, n)</code>.</p>

    <p>Al igual que la versión recursiva, <code>GREEDY-ACTIVITY-SELECTOR</code> programa un conjunto
    de \( n \) actividades en tiempo \( \Theta(n) \), asumiendo que las actividades ya estaban ordenadas
    inicialmente por sus tiempos de finalización.</p>

