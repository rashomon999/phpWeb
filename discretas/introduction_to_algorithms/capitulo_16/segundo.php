        <h4>Parentesis, analisis codigo recursivo:</h4>
        <p>Cuando haces la llamada inicial:</p>

<pre><code>
RECURSIVE-ACTIVITY-SELECTOR(s, f, 0, n)
</code></pre>

<p>El parámetro <code>k = 0</code> corresponde a una actividad ficticia <code>a<sub>0</sub></code> que tiene:</p>

<p>\[
f_0 = 0
\]</p>

<p>Es decir, esta actividad ficticia termina en el tiempo 0, antes de que empiece cualquier otra actividad real.</p>

<p>Ahora, en el ciclo:</p>

<pre><code>
m = k + 1 = 0 + 1 = 1
while m ≤ n and s[m] &lt; f[k]  // s[1] &lt; f[0] ?
    m = m + 1
</code></pre>

<p>Evaluamos si la actividad <code>a<sub>1</sub></code> comienza antes de que termine la actividad ficticia <code>a<sub>0</sub></code>:</p>

<p>\[
s[1] \text{ es el tiempo de inicio de la primera actividad real.}
\]</p>

<p>\[
f[0] = 0.
\]</p>

<p>Entonces la condición <code>s[1] &lt; 0</code> es falsa (porque ningún tiempo de inicio real será menor que 0).</p>

<p>Por lo tanto:</p>

<ul>
<li>No entra al <code>while</code>, no incrementa <code>m</code>.</li>
<li>Sale del ciclo con <code>m = 1</code>.</li>
</ul>

<p>Entonces la primera actividad que selecciona es <code>a<sub>1</sub></code> (la primera real), porque es la primera compatible con la actividad ficticia que terminó en 0.</p>

<p>Esto hace que la primera actividad que el algoritmo toma sea la que comienza más temprano posible (en orden de tiempos de finalización), y luego continúa recursivamente desde ahí.</p>
   
<p>Si te refieres a que la segunda actividad empieza antes que la primera pero termina después
Esto sí puede pasar. Por ejemplo:</p>

<ul>
  <li>
    \( a_1 \): comienza en 2, termina en 4
  </li>
  <li>
    \( a_2 \): comienza en 1, termina en 5
  </li>
</ul>

<p>En este caso, aunque \( a_2 \) empieza antes, la actividad \( a_1 \) termina primero (porque 
\( f_1 = 4 < f_2 = 5 \)).</p>

<p>El algoritmo selecciona primero la actividad que termina primero — no la que comienza primero — porque el conjunto de actividades está ordenado según \( f_i \).</p>
