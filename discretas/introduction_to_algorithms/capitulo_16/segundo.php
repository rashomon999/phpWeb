    <h2>Parentesis explicativo</h2>
    <h2> ¿Qué pasa si en un óptimo no está la actividad que termina más temprano?</h2>

    <p>Supongamos que estamos trabajando con un subproblema \( S_k \), que contiene varias actividades.</p>

    <p>Entre ellas, hay una actividad \( a_m \) que es la que termina más temprano.</p>

    <p>Y tenemos un conjunto óptimo \( A_k \) (es decir, el más grande posible de actividades compatibles dentro de \( S_k \)).</p>

    <h3> Pero resulta que...</h3>
    <p>Ese conjunto óptimo no incluye a \( a_m \), sino que incluye a otra actividad \( a_j \), que:</p>
    <ul>
    <li>también es compatible con las demás de \( A_k \),</li>
    <li>pero termina después que \( a_m \): \( f_m &lt; f_j \)</li>
    </ul>

    <h3> ¿Qué dice el teorema?</h3>
    <p>El teorema dice que puedes reemplazar \( a_j \) por \( a_m \) y seguir teniendo un conjunto compatible de igual tamaño.</p>

    <p><strong>¿Por qué?</strong></p>
    
    <ul>
    <li>Todas las actividades de \( A_k \) son compatibles entre sí.</li>
    <li>\( a_j \) es la que termina más temprano dentro de \( A_k \).</li>
    <li>Pero \( a_m \) termina aún más temprano que \( a_j \).</li>
    <li>Entonces, si todas las demás actividades son compatibles con \( a_j \), también lo serán con \( a_m \), porque termina incluso antes.</li>
    </ul>

    <h3> ¿Qué logramos?</h3>
    <p>Haciendo ese reemplazo:</p>
    <ul>
    <li>construimos un nuevo conjunto \( A'_k \),</li>
    <li>tiene el mismo tamaño que el óptimo original \( A_k \),</li>
    <li>es mutuamente compatible (las actividades no se solapan),</li>
    <li>e incluye a \( a_m \), la actividad greedy que termina primero.</li>
    </ul>

    <p>Esto demuestra que <strong>siempre hay un óptimo que incluye la greedy choice</strong>, y por eso puedes
    elegirla sin miedo.</p>

    <p>
    El teorema garantiza que existe al menos un conjunto óptimo que incluye a 
    \( a_m \) (la actividad <em>greedy</em>). Por eso, podemos elegirla sin miedo.
  </p>

  <p>
    Matemáticamente es una eliminación y una unión:
  </p>

  <p style="text-align: center;">
    \( A_k' = A_k - \{ a_j \} \cup \{ a_m \} \)
  </p>

  <p>
    Pero conceptualmente es un reemplazo de \( a_j \) por \( a_m \), porque:
  </p>

  <ul>
    <li>Quitamos una,</li>
    <li>Ponemos otra,</li>
    <li>Y el conjunto sigue siendo igual de bueno (óptimo en tamaño y compatibilidad).</li>
  </ul>

