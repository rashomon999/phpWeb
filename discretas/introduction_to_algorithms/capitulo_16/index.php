  <h1>16.1 Un problema de selección de actividades</h1>
  <p>
    Nuestro primer ejemplo es el problema de programar varias actividades que compiten entre sí y que requieren el uso exclusivo de un recurso común, con el objetivo de seleccionar un conjunto máximo de actividades mutuamente compatibles.
  </p>
  <p>
    Supongamos que tenemos un conjunto <em>S = {a₁, a₂, ..., aₙ}</em> de <em>n</em> actividades propuestas que desean utilizar un recurso, como una sala de conferencias, que solo puede albergar una actividad a la vez. Cada actividad <em>aᵢ</em> tiene un tiempo de inicio <em>sᵢ</em> y un tiempo de finalización <em>fᵢ</em>, donde <em>0 ≤ sᵢ &lt; fᵢ &lt; ∞</em>. Si se selecciona, la actividad <em>aᵢ</em> se lleva a cabo durante el intervalo de tiempo semiabierto <em>[sᵢ, fᵢ)</em>.
  </p>
  <p>
    Dos actividades <em>aᵢ</em> y <em>aⱼ</em> son compatibles si los intervalos <em>[sᵢ, fᵢ)</em> y <em>[sⱼ, fⱼ)</em> no se superponen. Es decir, <em>aᵢ</em> y <em>aⱼ</em> son compatibles si <em>sᵢ ≥ fⱼ</em> o <em>sⱼ ≥ fᵢ</em>. En el problema de selección de actividades, deseamos seleccionar un subconjunto de tamaño máximo de actividades mutuamente compatibles.
  </p>
  <p>
    Suponemos que las actividades están ordenadas en orden estrictamente creciente según su tiempo de finalización:
  </p>
  <p>
    <strong>f₁ ≤ f₂ ≤ f₃ ≤ ... ≤ fₙ₋₁ ≤ fₙ</strong> (Ecuación 16.1)
  </p>
  <p>
    (Más adelante veremos la ventaja que proporciona esta suposición).
  </p>
  <p>
    Por ejemplo, considera el siguiente conjunto <em>S</em> de actividades:
  </p>
  <table border="1" cellpadding="5">
    <tr>
      <th>i</th>
      <td>1</td><td>2</td><td>3</td><td>4</td><td>5</td><td>6</td><td>7</td><td>8</td><td>9</td><td>10</td><td>11</td>
      <hr>
    </tr>
     
    <tr>
      <th>sᵢ</th>
      <td>1</td><td>3</td><td>0</td><td>5</td><td>3</td><td>5</td><td>6</td><td>8</td><td>8</td><td>2</td><td>12</td>
    </tr>
    <tr>
      <th>fᵢ</th>
      <td>4</td><td>5</td><td>6</td><td>7</td><td>9</td><td>9</td><td>10</td><td>11</td><td>12</td><td>14</td><td>16</td>
