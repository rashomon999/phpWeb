  <p>
    Luego observaremos que solo necesitamos considerar una opción: la decisión voraz, y que al tomar esta decisión, 
    solo queda un subproblema. Con base en estas observaciones, desarrollaremos un algoritmo voraz recursivo para
    resolver el problema de programación de actividades. Completaremos el desarrollo de la solución voraz convirtiendo
    el algoritmo recursivo en uno iterativo.
  </p>
  <p>
    Aunque los pasos que seguiremos en esta sección son ligeramente más complejos de lo habitual al desarrollar 
    un algoritmo voraz, ilustran la relación entre los algoritmos voraces y la programación dinámica.
  </p>





  <h1>La subestructura óptima del problema de selección de actividades</h1>

  <p>
    Podemos verificar fácilmente que el problema de selección de actividades presenta subestructura óptima. Denotemos por \( S_{ij} \) al conjunto de actividades que comienzan después de que finaliza la actividad \( a_i \) y que terminan antes de que comience la actividad \( a_j \).
  </p>

  <p>
    Supongamos que deseamos encontrar un conjunto máximo de actividades mutuamente compatibles en \( S_{ij} \), y supongamos además que dicho conjunto máximo es \( A_{ij} \), el cual incluye alguna actividad \( a_k \).
  </p>

  <p>
    Al incluir \( a_k \) en una solución óptima, nos quedan dos subproblemas: encontrar actividades mutuamente compatibles en el conjunto \( S_{ik} \) (actividades que comienzan después de que finaliza \( a_i \) y que terminan antes de que comience \( a_k \)), y encontrar actividades mutuamente compatibles en el conjunto \( S_{kj} \) (actividades que comienzan después de que finaliza \( a_k \) y que terminan antes de que comience \( a_j \)).
  </p>

  <p>
    Sean \( A_{ik} = A_{ij} \cap S_{ik} \) y \( A_{kj} = A_{ij} \cap S_{kj} \), de modo que \( A_{ik} \) contiene las actividades en \( A_{ij} \) que terminan antes de que comience \( a_k \), y \( A_{kj} \) contiene las actividades que comienzan después de que finaliza \( a_k \).
  </p>

  <p>
    Así, tenemos que:
  </p>

  <p style="text-align: center;">
    \( A_{ij} = A_{ik} \cup \{a_k\} \cup A_{kj} \)
  </p>

  <p>
    y por tanto, el conjunto \( A_{ij} \) de tamaño máximo de actividades mutuamente compatibles en \( S_{ij} \) consta de:
  </p>

  <p style="text-align: center;">
    \( |A_{ij}| = |A_{ik}| + |A_{kj}| + 1 \)
  </p>

  <p>
    El clásico argumento de "cortar y pegar" muestra que la solución óptima \( A_{ij} \) también debe incluir soluciones óptimas a los dos subproblemas \( S_{ik} \) y \( S_{kj} \). Si pudiéramos encontrar un conjunto \( A'_{kj} \) de actividades mutuamente compatibles en \( S_{kj} \) tal que \( |A'_{kj}| > |A_{kj}| \), entonces podríamos usar \( A'_{kj} \) en lugar de \( A_{kj} \) para el subproblema \( S_{ij} \).
  </p>


