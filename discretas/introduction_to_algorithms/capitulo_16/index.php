    Para este ejemplo, el subconjunto <em>{a₃, a₉, a₁₁}</em> consiste en actividades mutuamente compatibles. Sin embargo, 
    no es el subconjunto máximo, ya que <em>{a₁, a₄, a₈, a₁₁}</em> es más grande. De hecho, <em>{a₁, a₄, a₈, a₁₁}</em> es
    un subconjunto de tamaño máximo de actividades mutuamente compatibles; otro subconjunto máximo es 
    <em>{a₂, a₄, a₉, a₁₁}</em>.

  </p>
  <p>
    Resolveremos este problema en varios pasos. Empezaremos pensando en una solución mediante programación dinámica, 
    en la cual se consideran varias opciones al determinar qué subproblemas utilizar en una solución óptima.
