    <p>
    Entonces habríamos construido un conjunto de actividades de tamaño:
  </p>

  <p style="text-align: center;">
    \( |A_{ik}| + |A'_{kj}| + 1 > |A_{ik}| + |A_{kj}| + 1 = |A_{ij}| \)
  </p>

  <p>
    lo cual contradice la suposición de que \( A_{ij} \) es una solución óptima. Un argumento simétrico se aplica para las actividades en \( S_{ik} \).
  </p>

  <p>
    Esta forma de caracterizar la subestructura óptima sugiere que podríamos resolver el problema de selección de actividades mediante programación dinámica. Si denotamos el tamaño de una solución óptima para el conjunto \( S_{ij} \) por \( c[i, j] \), entonces tendríamos la siguiente recurrencia:
  </p>

  <p style="text-align: center;">
    \( c[i, j] = c[i, k] + c[k, j] + 1 \)
  </p>

  <p>
    Por supuesto, si no supiéramos qué actividad \( a_k \) pertenece a una solución óptima para \( S_{ij} \), tendríamos que examinar todas las actividades en \( S_{ij} \) para decidir cuál elegir, de manera que:
  </p>

  <p style="text-align: center;">
    \( c[i, j] = 
    \begin{cases}
      0 & \text{si } S_{ij} = \emptyset \\
      \max_{a_k \in S_{ij}} \{ c[i, k] + c[k, j] + 1 \} & \text{si } S_{ij} \ne \emptyset
    \end{cases}
    \quad \text{(Ecuación 16.2)} \)
  </p>

  <p>
    Podríamos entonces desarrollar un algoritmo recursivo con <em>memoization</em>, o trabajar de forma ascendente (bottom-up) llenando una tabla conforme avanzamos. Pero estaríamos pasando por alto otra característica importante del problema de selección de actividades que podemos aprovechar en gran medida.
  </p>
  



  <h2>Tomando la elección voraz</h2>
  <p>
    ¿Y si pudiéramos elegir una actividad para añadir a nuestra solución óptima sin tener que resolver primero todos los subproblemas? Eso podría evitarnos tener que considerar todas las elecciones implícitas en la recurrencia (16.2).
    De hecho, para el problema de selección de actividades, solo necesitamos considerar una elección: <strong>la elección voraz</strong>.
  </p>

  <p>
    ¿Qué entendemos por elección voraz en el problema de selección de actividades?
    La intuición sugiere que deberíamos elegir una actividad que deje el recurso disponible para la mayor cantidad posible de otras actividades.
    Ahora bien, de entre las actividades que terminaremos eligiendo, una de ellas debe ser la primera en finalizar.
    Por lo tanto, nuestra intuición nos dice que debemos elegir la actividad de <em>S</em> con el <strong>tiempo de finalización más temprano</strong>, ya que eso dejaría el recurso disponible para el mayor número de actividades posteriores posible.
  </p>

  <p>
    (Si más de una actividad en <em>S</em> tiene el mismo tiempo de finalización más temprano, entonces podemos elegir cualquiera de ellas).
    En otras palabras, como las actividades están ordenadas en orden creciente de tiempo de finalización, la elección voraz es la actividad \( a_1 \).
  </p>

  <p>
    Elegir la primera actividad en finalizar no es la única manera de pensar en una elección voraz para este problema;
    el <em>Ejercicio 16.1-3</em> te pide explorar otras posibilidades.
  </p>

  <p>
    Si hacemos la elección voraz, solo queda un subproblema por resolver: encontrar las actividades que comienzan después de que finaliza \( a_1 \).
    ¿Por qué no tenemos que considerar actividades que terminan antes de que comience \( a_1 \)?
    Dado que \( s_1 &lt; f_1 \), y \( f_1 \) es el tiempo de finalización más temprano de todas las actividades, ninguna actividad puede tener un tiempo de finalización menor o igual a \( s_1 \).
    Por lo tanto, todas las actividades compatibles con \( a_1 \) deben comenzar después de que esta finaliza.
  </p>

  <p>
    Además, ya hemos establecido que el problema de selección de actividades presenta subestructura óptima.
    Sea \( S_k = \{ a_i \in S \mid s_i \geq f_k \} \) el conjunto de actividades que comienzan después de que finaliza la actividad \( a_k \).
    Si hacemos la elección voraz de la actividad \( a_1 \), entonces \( S_1 \) queda como el único subproblema por resolver.
  </p>

  <p>
    La subestructura óptima nos dice que si \( a_1 \) está en la solución óptima, entonces una solución óptima
     para el problema original consiste en \( a_1 \) y todas las actividades de una solución óptima del subproblema \( S_1 \).
     (Nota:Entonces: \(S_1\) conjunto de actividades que empiezan después de que termina \(a_1\))
  </p>

  <p>
    Queda una gran pregunta: ¿nuestra intuición es correcta?
    ¿La elección voraz —en la que elegimos la primera actividad en finalizar— siempre forma parte de alguna solución óptima?
    El siguiente teorema demuestra que sí lo es.
  </p>

  <p style="font-size: 0.9em; color: gray;">
    Nota: A veces nos referimos a los conjuntos \( S_k \) como subproblemas y no solo como conjuntos de actividades.
    Siempre quedará claro por el contexto si estamos hablando de \( S_k \) como conjunto de actividades o como subproblema cuyo conjunto de entrada es ese.
  </p>



  <h2>Teorema 16.1</h2>
  <p>
    Consideremos cualquier subproblema no vacío \( S_k \), y sea \( a_m \) una actividad en \( S_k \) con el tiempo de finalización más temprano.
    Entonces, \( a_m \) está incluida en algún subconjunto de tamaño máximo de actividades mutuamente compatibles de \( S_k \).
  </p>

  <h3>Demostración</h3>
  <p>
    Sea \( A_k \) un subconjunto de tamaño máximo de actividades mutuamente compatibles en \( S_k \), y sea \( a_j \) la actividad en \( A_k \) con el tiempo de finalización más temprano.
    Si \( a_j = a_m \), hemos terminado, ya que hemos mostrado que \( a_m \) está en algún subconjunto de tamaño máximo de actividades compatibles.
  </p>
  <p>
    Si \( a_j \ne a_m \), definimos el conjunto \( A'_k = A_k - \{a_j\} \cup \{a_m\} \), es decir, sustituimos \( a_j \) por \( a_m \).
    Las actividades en \( A'_k \) son disjuntas, lo cual se cumple porque las actividades en \( A_k \) lo son, \( a_j \) es la primera en finalizar y \( f_m \le f_j \).
  </p>
  <p>
    Como \( |A'_k| = |A_k| \), concluimos que \( A'_k \) también es un subconjunto de tamaño máximo de actividades mutuamente compatibles de \( S_k \), y que incluye a \( a_m \).
  </p>

  <p>
    Así, vemos que aunque podríamos resolver el problema de selección de actividades con programación dinámica, no es necesario. 
    (Además, aún no hemos analizado si el problema realmente tiene subproblemas superpuestos).
  </p>
  <p>
    En su lugar, podemos simplemente elegir repetidamente la actividad que termina primero, conservar solo aquellas compatibles con ella y repetir hasta que no queden más actividades.
    Además, como siempre elegimos la actividad con el tiempo de finalización más temprano, los tiempos de finalización de las actividades elegidas deben aumentar estrictamente.
  </p>
  <p>
    Podemos considerar cada actividad una sola vez en orden creciente de tiempos de finalización.
  </p>

  <h2>Algoritmo voraz recursivo</h2>
  <p>
    Ahora que hemos visto cómo evitar la programación dinámica usando un algoritmo voraz con diseño de arriba hacia abajo (top-down), podemos escribir un procedimiento recursivo para resolver el problema de selección de actividades.
  </p>

  <p>
    El procedimiento <code>RECURSIVE-ACTIVITY-SELECTOR</code> toma los tiempos de inicio y fin de las actividades representados como arreglos <code>s</code> y <code>f</code>, el índice <code>k</code> que define el subproblema \( S_k \) a resolver, y el tamaño <code>n</code> del problema original.
  </p>
  <p>
    Se asume que las actividades ya están ordenadas por tiempos de finalización en orden creciente.
    Si no lo están, se pueden ordenar en tiempo \( \mathcal{O}(n \log n) \).
    Para comenzar, se añade una actividad ficticia \( a_0 \) con \( f_0 = 0 \), de forma que el subproblema \( S_0 \) es el conjunto completo de actividades.
  </p>

  <h3>Llamada inicial:</h3>
  <pre>RECURSIVE-ACTIVITY-SELECTOR(s, f, 0, n)</pre>

  <h3>Pseudocódigo:</h3>
  <pre>
RECURSIVE-ACTIVITY-SELECTOR(s, f, k, n)
1  m = k + 1
2  while m ≤ n and s[m] < f[k]  // encontrar la primera actividad compatible en S_k
3      m = m + 1
4  if m ≤ n
5      return {a_m} ∪ RECURSIVE-ACTIVITY-SELECTOR(s, f, m, n)
6  else
7      return ∅
  </pre>

  <p>
    En cada llamada recursiva <code>RECURSIVE-ACTIVITY-SELECTOR(s, f, k, n)</code>, el ciclo <code>while</code> busca la primera actividad \( a_m \) que sea compatible con \( a_k \), es decir, tal que \( s_m \ge f_k \).
  </p>

  <p>
    Si se encuentra dicha actividad, la línea 5 devuelve la unión de \( \{a_m\} \) con el subconjunto de tamaño máximo que devuelve la llamada recursiva para \( S_m \).
    Si no se encuentra ninguna (es decir, \( m > n \)), entonces el conjunto \( S_k = \emptyset \), y se devuelve el conjunto vacío.
  </p>
 

