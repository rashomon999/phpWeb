    <h1>Algoritmos Voraces</h1>
  <p>
    Los algoritmos para problemas de optimización suelen seguir una secuencia de pasos, con un conjunto de
    decisiones en cada paso. Para muchos problemas de optimización, usar programación dinámica para determinar
    las mejores decisiones es excesivo; algoritmos más simples y eficientes pueden ser suficientes.
  </p>
  <p>
    Un algoritmo voraz siempre toma la decisión que parece ser la mejor en ese momento. Es decir, toma una
    decisión localmente óptima con la esperanza de que esta decisión conduzca a una solución globalmente óptima. 
    Este capítulo explora problemas de optimización para los cuales los algoritmos voraces proporcionan 
    soluciones óptimas.
  </p>
  <p>
    Antes de leer este capítulo, deberías leer sobre programación dinámica en el Capítulo 15, en particular
     la Sección 15.3.
  </p>
  <p>
    Los algoritmos voraces no siempre producen soluciones óptimas, pero para muchos problemas sí lo hacen. 
    Primero examinaremos, en la Sección 16.1, un problema simple pero no trivial: el problema de selección de 
    actividades, para el cual un algoritmo voraz calcula de forma eficiente una solución óptima. Llegaremos al 
    algoritmo voraz considerando primero un enfoque de programación dinámica y luego mostrando que siempre se 
    pueden tomar decisiones voraces para llegar a una solución óptima.
  </p>
  <p>
    La Sección 16.2 repasa los elementos básicos del enfoque voraz, proporcionando una forma directa de demostrar 
    la corrección de los algoritmos voraces. La Sección 16.3 presenta una aplicación importante de las técnicas
    voraces: el diseño de códigos de compresión de datos (Huffman). En la Sección 16.4, investigamos parte de la 
    teoría subyacente a las estructuras combinatorias llamadas “matroides”, para las cuales un algoritmo voraz 
    siempre produce una solución óptima.
  </p>
  <p>
    Finalmente, la Sección 16.5 aplica matroides para resolver un problema de programación de tareas de tiempo
    unitario con fechas límite y penalizaciones.
  </p>
  <p>
    El método voraz es bastante poderoso y funciona bien para una amplia gama de problemas. En capítulos
    posteriores se presentarán muchos algoritmos que pueden considerarse aplicaciones del método voraz, incluidos
    los algoritmos de árbol de expansión mínima (Capítulo 23), el algoritmo de Dijkstra para caminos más cortos 
    desde una única fuente (Capítulo 24), y la heurística voraz de Chvátal para la cobertura de conjuntos (Capítulo 35).
  </p>
  <p>
    Los algoritmos de árbol de expansión mínima proporcionan un ejemplo clásico del método voraz. Aunque puedes
