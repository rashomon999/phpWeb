    <h3>parentesis explicativo:</h3>
    <p>✅ <strong>En el problema de la mochila fraccionaria:</strong></p>

<p>🔁 <strong>¿Qué se hace?</strong><br>
Se calcula el valor por unidad de peso de cada objeto:<br>
\( \frac{v_i}{w_i} \) para cada objeto \( i \).</p>

<p>Se ordenan los objetos de mayor a menor según ese valor.</p>

<p>Se recorren en orden:</p>

<ul>
  <li>En cada paso, el ladrón toma tanto como pueda del objeto actual (todo o una fracción, si ya no cabe completo).</li>
</ul>

<p>Así se llena la mochila hasta llegar al peso límite \( W \).</p>

<p>📌 <strong>¿Por qué funciona?</strong><br>
Porque cada decisión voraz (local) —tomar el objeto con mayor valor por peso— también contribuye a la mejor solución global, es decir, maximiza el valor total sin necesidad de mirar futuras decisiones.<br>
Esto es lo que se llama la <em>propiedad de elección voraz</em>.</p>

<hr>

<p>⚠️ <strong>Pero en el problema de la mochila 0-1:</strong><br>
No se pueden tomar fracciones. O tomas todo un objeto, o no lo tomas.</p>

<p>🔁 <strong>¿Qué pasa si haces lo mismo?</strong><br>
Si eliges el objeto con mayor \( \frac{v_i}{w_i} \), como en el ejemplo del libro (60 por 10 lb = 6), puede que ocupes espacio que no te deja tomar combinaciones más valiosas.</p>

<p>❌ <strong>¿Por qué no funciona?</strong><br>
Porque tomar la mejor opción localmente puede hacerte perder combinaciones más valiosas a futuro. Es decir:</p>

<p>Tomar el objeto 1 evita que tomes objetos 2 y 3 juntos, que dan más valor total.</p>

<p>Entonces el problema no cumple la propiedad de elección voraz.</p>

<p>Por eso aquí se necesita <strong>programación dinámica</strong>: hay que comparar todas las combinaciones posibles (tomarlo o no tomarlo), resolviendo subproblemas con inclusión/exclusión.</p>

<hr>

<p>🔑 <strong>¿Y qué tiene que ver con subproblemas?</strong></p>

<p><strong>En la mochila fraccionaria:</strong><br>
Elegir el mejor objeto reduce el problema a uno más pequeño, simplemente:</p>

<ul>
  <li>Mochila con menos peso disponible.</li>
  <li>Siguiente objeto de la lista.</li>
</ul>

<p>No necesitas comparar con otras opciones, por eso se dice que el subproblema se crea después de elegir, no antes.</p>

<p><strong>En la mochila 0-1:</strong><br>
Necesitas comparar antes de decidir.</p>

<ul>
  <li>¿Qué pasa si incluyo el objeto?</li>
  <li>¿Qué pasa si lo excluyo?</li>
</ul>

<p>Esos son subproblemas distintos que tienes que resolver antes de tomar la decisión. Esto es lo típico de la programación dinámica.</p>
    <hr>

    
<p>✅ <strong>Sí se puede decir que en uno el orden importa más que en el otro, así:</strong></p>

<p>🥇 <strong>En la mochila fraccionaria, el orden lo es todo:</strong><br>
El algoritmo depende directamente de ordenar los objetos por valor/peso 
\(
\frac{v_i}{w_i}
\).</p>

<p>Una vez están ordenados, simplemente se recorren en ese orden, tomando todo lo que se pueda de cada uno.</p>

<p>La elección voraz se hace con base en ese orden, y no necesitas volver atrás ni comparar múltiples caminos.</p>

<p> <strong>Resumen:</strong> El orden determina toda la estrategia.<br>
Si el orden es correcto (de mayor a menor valor por peso), entonces tomar los objetos en ese orden garantiza una solución óptima.</p>

<hr>

<p> <strong>En la mochila 0-1, el orden no ayuda tanto:</strong><br>
No puedes simplemente ordenar los objetos y tomarlos en ese orden.</p>

<p>La combinación de objetos es lo que importa, no su orden individual.</p>

<p>Un objeto de mucho valor/peso podría impedirte tomar una combinación más rentable.</p>

<p> <strong>Resumen:</strong> El orden no basta.<br>
Necesitas evaluar muchas combinaciones posibles para ver cuál conjunto de objetos entra en la mochila y da el mayor valor.</p>
