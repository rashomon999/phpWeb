    <p>
Más generalmente, diseñamos algoritmos voraces de acuerdo con la siguiente secuencia de pasos:
</p>

<ol>
  <li>Plantear el problema de optimización como uno en el que hacemos una elección y nos queda un subproblema por resolver.</li>
  <li>Probar que siempre existe una solución óptima al problema original que realiza la elección voraz, de modo que la elección voraz sea siempre segura.</li>
  <li>Demostrar la subestructura óptima mostrando que, habiendo hecho la elección voraz, lo que queda es un subproblema con la propiedad de que si combinamos una solución óptima al subproblema con la elección voraz que hemos hecho, obtenemos una solución óptima al problema original.</li>
</ol>

<p>
Usaremos este proceso más directo en las siguientes secciones de este capítulo. Sin embargo, debajo de cada algoritmo voraz, casi siempre hay una solución de programación dinámica más engorrosa.
</p>


    <p>
¿Cómo podemos saber si un algoritmo voraz resolverá un problema de optimización en particular? No hay un método que funcione todo el tiempo, pero la propiedad de elección voraz y la subestructura óptima son los dos ingredientes clave. Si podemos demostrar que el problema tiene estas propiedades, entonces estamos bien encaminados para desarrollar un algoritmo voraz para él.
</p>

    <hr>
    <h4>Parentesis explicativo</h4>
    <br>
    <p><strong>🔁 ¿Qué es un subproblema en este contexto?</strong><br>
Supongamos que tienes un conjunto de actividades ordenadas por su tiempo de finalización, y quieres seleccionar el mayor número posible de actividades compatibles (que no se traslapen).</p>

<p>Cuando haces una elección voraz, por ejemplo, eliges la actividad que termina más pronto. Supongamos que esa actividad es \( a_1 \).</p>

<p>Al elegir \( a_1 \), ya no puedes usar actividades que empiecen antes de que \( a_1 \) termine. Así que te queda un subproblema: seleccionar actividades compatibles con \( a_1 \), es decir, aquellas que comienzan después de que \( a_1 \) termine.</p>

<p>Ese conjunto de actividades restantes compatibles se llama \( S_1 \).</p>

<p><strong>🔗 ¿Qué significa “combinar la solución óptima al subproblema con la elección voraz”?</strong><br>
La elección voraz fue escoger \( a_1 \).</p>

<p>El subproblema es: "¿Cuál es la mejor combinación de actividades compatibles después de \( a_1 \)?"</p>

<p>Si resuelves ese subproblema de forma óptima, obtienes un conjunto óptimo de actividades, digamos: \( \{a_4, a_8, a_{11}\} \)</p>

<p>Entonces, al combinar la actividad \( a_1 \) (que fue nuestra elección voraz) con la solución óptima del subproblema \( \{a_4, a_8, a_{11}\} \), obtienes:</p>

<p>\( \{a_1, a_4, a_8, a_{11}\} \)</p>

<p>que es la solución óptima total al problema original.</p>

<p><strong>✅ ¿Por qué funciona?</strong><br>
Porque hemos probado que hacer la elección voraz no impide encontrar la mejor solución posible. La clave está en dos propiedades:</p>

<ul>
  <li><strong>Subestructura óptima:</strong> resolver un subproblema óptimamente ayuda a resolver el problema completo óptimamente.</li>
  <li><strong>Propiedad de elección voraz:</strong> siempre existe una solución óptima que incluye esa elección voraz.</li>
</ul>

