<h2>Propiedad de elección voraz</h2>

<p>El primer ingrediente clave es la <strong>propiedad de elección voraz</strong>: podemos construir una solución óptima global haciendo elecciones localmente óptimas (voraces). En otras palabras, cuando estamos considerando qué elección hacer, hacemos la elección que parece mejor en el problema actual, sin considerar los resultados de los subproblemas.</p>

<p>Aquí es donde los algoritmos voraces difieren de la programación dinámica. En programación dinámica, hacemos una elección en cada paso, pero la elección usualmente depende de las soluciones a los subproblemas. En consecuencia, típicamente resolvemos problemas de programación dinámica de forma ascendente, progresando desde subproblemas más pequeños hacia subproblemas más grandes. (Alternativamente, podemos resolverlos de arriba hacia abajo, pero memorizando. Por supuesto, aunque el código funcione de arriba hacia abajo, todavía debemos resolver los subproblemas antes de hacer una elección.)</p>

<p>En un algoritmo voraz, hacemos la elección que parezca mejor en ese momento y luego resolvemos el subproblema que queda. La elección realizada por un algoritmo voraz puede depender de elecciones hechas hasta ahora, pero no puede depender de elecciones futuras ni de las soluciones a subproblemas. Así, a diferencia de la programación dinámica, que resuelve los subproblemas antes de hacer la primera elección, un algoritmo voraz hace su primera elección antes de resolver cualquier subproblema.</p>

<p>Un algoritmo de programación dinámica procede de manera ascendente, mientras que una estrategia voraz usualmente progresa de arriba hacia abajo, haciendo una elección voraz tras otra, reduciendo cada instancia del problema a una más pequeña.</p>

<p>Por supuesto, debemos probar que una elección voraz en cada paso produce una solución óptima global. Típicamente, como en el caso del Teorema 16.1, la demostración examina una solución óptima global a algún subproblema. Luego muestra cómo modificar la solución para sustituir la elección voraz por alguna otra elección, resultando en un subproblema similar, pero más pequeño.</p>

<p>Usualmente podemos hacer la elección voraz de forma más eficiente que cuando debemos considerar un conjunto más amplio de elecciones. Por ejemplo, en el problema de selección de actividades, asumiendo que ya habíamos ordenado las actividades en orden monótonamente creciente de tiempos de finalización, necesitábamos examinar cada actividad solo una vez. Mediante el preprocesamiento de la entrada o usando una estructura de datos apropiada (a menudo una cola de prioridad), a menudo podemos hacer elecciones voraces rápidamente, dando así lugar a un algoritmo eficiente.</p>



<h2>Voraces vs Programación Dinámica</h2>

<p>
Debido a que tanto las estrategias voraces como las de programación dinámica explotan la subestructura óptima, podrías verte tentado a generar una solución de programación dinámica para un problema cuando una solución voraz es suficiente o, por el contrario, podrías pensar erróneamente que una solución voraz funciona cuando en realidad se requiere una solución de programación dinámica. 
Para ilustrar las sutilezas entre las dos técnicas, investiguemos dos variantes de un problema clásico de optimización.
</p>

<p>
El problema de la mochila 0-1 es el siguiente. Un ladrón que roba una tienda encuentra \( n \) objetos. El objeto \( i \)-ésimo vale \( v_i \) dólares y pesa \( w_i \) libras, donde \( v_i \) y \( w_i \) son enteros. El ladrón quiere llevarse la carga más valiosa posible, pero puede cargar como máximo \( W \) libras en su mochila, para algún entero \( W \). ¿Qué objetos debería llevarse? 
(Llamamos a este el problema de la mochila 0-1 porque para cada objeto, el ladrón debe o bien tomarlo o dejarlo; no puede tomar una fracción del objeto ni tomarlo más de una vez.)
</p>

<p>
En el problema de la mochila fraccionaria, la configuración es la misma, pero el ladrón puede tomar fracciones de objetos, en lugar de tener que tomar una decisión binaria (0-1) por cada objeto.
Puedes pensar en un objeto del problema 0-1 como un lingote de oro, y un objeto del problema fraccionario como polvo de oro.
</p>

<p>
Ambos problemas de mochila exhiben la propiedad de subestructura óptima. Para el problema 0-1, considera la carga más valiosa que pesa como máximo \( W \) libras. Si removemos el objeto \( j \) de esta carga, la carga restante debe ser la más valiosa que pese como máximo \( W - w_j \) que el ladrón puede tomar de los \( n - 1 \) objetos originales excluyendo \( j \). 
Para el problema fraccionario comparable, considera que si removemos un peso \( w \) de un objeto \( j \) de la carga óptima, la carga restante debe ser la más valiosa que pese como máximo \( W - w \) que el ladrón puede tomar de los \( n - 1 \) objetos originales más \( w_j - w \) libras del objeto \( j \).
</p>


