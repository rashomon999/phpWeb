  <math xmlns="http://www.w3.org/1998/Math/MathML" display="inline"><mi>b</mi></math> son cualesquiera.
</p>

<p>
  Así que la comparación 
  <math xmlns="http://www.w3.org/1998/Math/MathML" display="inline"><mi>a</mi><mo>≥</mo><mi>b</mi></math> y 
  <math xmlns="http://www.w3.org/1998/Math/MathML" display="inline"><mi>x</mi><mo>≤</mo><mi>y</mi></math> solo dice que dentro de esos pares, uno es mayor o igual al otro, pero para comparar 
