    <p>\[
    (45 \cdot 1 + 13 \cdot 3 + 12 \cdot 3 + 16 \cdot 3 + 9 \cdot 4 + 5 \cdot 4) \cdot 1,000 = 224,000 \text{ bits}
    \]</p>

    <p>para representar el archivo, un ahorro de aproximadamente el 25%. De hecho, este es un código óptimo de 
    caracteres para este archivo, como veremos.</p>
    
 
    

    <h2>Códigos prefijo</h2>

    <p>Consideramos aquí solo códigos en los que ninguna palabra de código es también un prefijo de alguna
    otra palabra de código. A estos códigos se les llama <strong>códigos prefijo</strong>.</p>

    <p><sup>3</sup>Aunque no lo demostraremos aquí, un código prefijo siempre puede lograr la compresión de datos
    óptima entre cualquier código de caracteres, por lo que no perdemos generalidad al restringir nuestra atención
    a los códigos prefijo.</p>

    <p>La codificación siempre es simple para cualquier código binario de caracteres; simplemente concatenamos
    las palabras de código que representan cada carácter del archivo. Por ejemplo, con el código prefijo de longitud
    variable de la Figura 16.3, codificamos el archivo de 3 caracteres <code>abc</code> como  0\( \cdot \)101\( \cdot \)
    100  = 0101100,
    donde “\( \cdot \)” denota la concatenación.</p>


    <img src="../../../img/arbol.png" alt="">

    <p>Los códigos prefijo son deseables porque simplifican la decodificación. Dado que ninguna palabra de código es
    prefijo de otra, la palabra de código que comienza un archivo codificado es inequívoca. Simplemente podemos 
    identificar la palabra de código inicial, traducirla al carácter original, y repetir el proceso de decodificación 
    en el resto del archivo codificado. En nuestro ejemplo, la cadena <code>001011101</code> se analiza de manera
    única como 0\( \cdot \) 0 \( \cdot \)101\( \cdot \) 1101, que se decodifica como <code>aabe</code>.</p>

    <p>El proceso de decodificación necesita una representación conveniente del código prefijo para que podamos 
    identificar fácilmente la palabra de código inicial. Un árbol binario cuyas hojas son los caracteres dados
    proporciona una de esas representaciones. Interpretamos la palabra de código binaria de un carácter como el 
    camino simple desde la raíz hasta ese carácter, donde 0 significa “ir al hijo izquierdo” y 1 significa 
    “ir al hijo derecho.” La Figura 16.4 muestra los árboles para los dos códigos de nuestro ejemplo. Nótese que
    estos no son árboles binarios de búsqueda, ya que las hojas no necesitan aparecer en orden y los nodos internos
    no contienen claves de caracteres.</p>

    
<p>Un código óptimo para un archivo siempre está representado por un árbol binario completo, en el que cada nodo no hoja tiene dos hijos (ver Ejercicio 16.3-2). El código de longitud fija en nuestro ejemplo no es óptimo ya que su árbol, mostrado en la Figura 16.4(a), no es un árbol binario completo: contiene palabras de código que comienzan 10..., pero ninguna que comience 11....</p>

<p>Dado que ahora podemos restringir nuestra atención a árboles binarios completos, podemos decir que si \( C \) es el alfabeto del que se extraen los caracteres y todas las frecuencias de caracteres son positivas, entonces el árbol para un código prefijo óptimo tiene exactamente \( |C| \) hojas, una para cada letra del alfabeto, y exactamente \( |C| - 1 \) nodos internos (ver Ejercicio B.5-3).</p>

<p>Dado un árbol \( T \) correspondiente a un código prefijo, podemos calcular fácilmente el número de bits necesarios para codificar un archivo. Para cada carácter \( c \) en el alfabeto \( C \), sea el atributo \( c:\text{freq} \) la frecuencia de \( c \) en el archivo y sea \( d_T(c) \) la profundidad de la hoja de \( c \) en el árbol. Nótese que \( d_T(c) \) es también la longitud de la palabra de código para el carácter \( c \).</p>

<p>El número de bits necesarios para codificar un archivo es entonces:</p>

<p>
  \[
  B(T) = \sum_{c \in C} c. \text{freq} \cdot d_T(c) \tag{16.4}
  \]
</p>

<p>lo cual definimos como el costo del árbol \( T \).</p>



<h2>Construcción de un código de Huffman</h2>

<p>Huffman inventó un algoritmo voraz que construye un código prefijo óptimo llamado <strong>código de Huffman</strong>. De acuerdo con nuestras observaciones en la Sección 16.2, su demostración de corrección se basa en la propiedad de elección voraz y en la subestructura óptima.</p>

<p>En lugar de demostrar que estas propiedades se cumplen y luego desarrollar el pseudocódigo, presentamos primero el pseudocódigo. Hacerlo así ayudará a clarificar cómo el algoritmo realiza elecciones voraces.</p>

<p>En el siguiente pseudocódigo, asumimos que \( C \) es un conjunto de \( n \) caracteres y que cada carácter \( c \in C \) es un objeto con un atributo \( c:\text{freq} \) que da su frecuencia. El algoritmo construye el árbol \( T \) correspondiente al código óptimo de forma ascendente. Comienza con un conjunto de \( |C| \) hojas y realiza una secuencia de \( |C| - 1 \) operaciones de “fusión” para crear el árbol final.</p>

<p>El algoritmo utiliza una <strong>cola de prioridad mínima</strong> \( Q \), ordenada por el atributo <code>freq</code>, para identificar los dos objetos menos frecuentes que se deben fusionar. Cuando fusionamos dos objetos, el resultado es un nuevo objeto cuya frecuencia es la suma de las frecuencias de los dos objetos fusionados.</p>

<pre>
HUFFMAN(C)
1  n = |C|
2  Q = C
3  for i = 1 to n - 1
4      asignar un nuevo nodo z
5      z.left = x = EXTRACT-MIN(Q)
6      z.right = y = EXTRACT-MIN(Q)
7      z.freq = x.freq + y.freq
8      INSERT(Q, z)
9  return EXTRACT-MIN(Q) // devuelve la raíz del árbol
</pre>

<p>Para nuestro ejemplo, el algoritmo de Huffman procede como se muestra en la Figura 16.5. Como el alfabeto contiene 6 letras, el tamaño inicial de la cola es \( n = 6 \), y se requieren 5 pasos de fusión para construir el árbol. El árbol final representa el código prefijo óptimo. La palabra de código para una letra es la secuencia de etiquetas de arista en el camino simple desde la raíz hasta la letra.</p>

<p>La línea 2 inicializa la cola de prioridad mínima \( Q \) con los caracteres en \( C \). El bucle <code>for</code> en las líneas 3–8 extrae repetidamente los dos nodos \( x \) e \( y \) de menor frecuencia.</p>

    
