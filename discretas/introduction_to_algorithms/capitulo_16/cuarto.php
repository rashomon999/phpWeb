  <math xmlns="http://www.w3.org/1998/Math/MathML" display="inline"><mi>x</mi><mo>,</mo><mi>y</mi></math>.
</p>
<p>✅ <strong>1. En la derivación algebraica aislada, usan solo propiedades numéricas:</strong></p>
  <p>
    La parte que concluye con:
  </p>
  <p style="font-family: 'Courier New', monospace; font-size: 1.1em; margin-left: 20px;">
    B(T) − B(T′) = (a.freq − x.freq) (d<sub>T</sub>(a) − d<sub>T</sub>(x)) ≥ 0
