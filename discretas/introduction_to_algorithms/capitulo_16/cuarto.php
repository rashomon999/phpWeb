    <h2>16.3 Códigos de Huffman</h2>

    <p>Los códigos de Huffman comprimen datos de manera muy eficaz: son típicos los ahorros del 20% al 90%, 
    dependiendo de las características de los datos que se estén comprimiendo. Consideramos que los datos 
    son una secuencia de caracteres. El algoritmo voraz de Huffman usa una tabla que indica con qué frecuencia
    ocurre cada carácter (es decir, su frecuencia) para construir una forma óptima de representar cada carácter 
    como una cadena binaria.</p>

    <p>Supongamos que tenemos un archivo de datos de 100,000 caracteres que deseamos almacenar de forma compacta. 
    Observamos que los caracteres en el archivo ocurren con las frecuencias dadas en la Figura 16.3. Es decir, solo 
    aparecen 6 caracteres diferentes, y el carácter <code>a</code> aparece 45,000 veces.</p>

    <img src="../../../img/huffman.png" alt="">

    <p>Tenemos muchas opciones sobre cómo representar dicho archivo de información. Aquí, consideramos el problema 
    de diseñar un código binario de caracteres (o simplemente código) en el cual cada carácter es representado por 
    una cadena binaria única, a la que llamamos palabra de código (codeword). Si usamos un código de longitud fija, 
    necesitamos 3 bits para representar 6 caracteres:
    <code>a = 000, b = 001, ..., f = 101</code>. Este método requiere 300,000 bits para codificar todo el archivo. 
    ¿Podemos hacerlo mejor?</p>

    <p>Un código de longitud variable puede hacerlo considerablemente mejor que un código de longitud fija, al
    asignar palabras de código cortas a los caracteres frecuentes y palabras de código largas a los caracteres 
    poco frecuentes. La Figura 16.3 muestra dicho código; aquí, la cadena de 1 bit <code>0</code> representa 
