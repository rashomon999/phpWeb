    <h2>Corrección del algoritmo de Huffman</h2>

<p>Para probar que el algoritmo voraz <code>HUFFMAN</code> es correcto, mostramos que el problema de determinar un código prefijo óptimo presenta las propiedades de elección voraz y de subestructura óptima. El siguiente lema muestra que la propiedad de elección voraz se cumple.</p>

<h3>Lema 16.2</h3>

<p>Sea \( C \) un alfabeto en el que cada carácter \( c \in C \) tiene frecuencia \( c.\text{freq} \). Sean \( x \) e \( y \) dos caracteres en \( C \) que tienen las frecuencias más bajas. Entonces, existe un código prefijo óptimo para \( C \) en el cual las palabras código de \( x \) e \( y \) tienen la misma longitud y difieren sólo en el último bit.</p>

<h4>Demostración</h4>

<p>La idea de la demostración es tomar el árbol \( T \) que representa un código prefijo óptimo arbitrario y modificarlo para obtener un árbol que represente otro código prefijo óptimo tal que los caracteres \( x \) e \( y \) aparezcan como hojas hermanas en la máxima profundidad del nuevo árbol. Si podemos construir tal árbol, entonces las palabras código para \( x \) e \( y \) tendrán la misma longitud y diferirán sólo en el último bit.</p>

<p>Sean \( a \) y \( b \) dos caracteres que son hojas hermanas de máxima profundidad en \( T \). Sin pérdida de generalidad, asumimos que \( a.\text{freq} \geq b.\text{freq} \) y \( x.\text{freq} \leq y.\text{freq} \). Dado que \( x.\text{freq} \) e \( y.\text{freq} \) son las dos frecuencias más bajas de las hojas, en orden, y \( a.\text{freq} \) y \( b.\text{freq} \) son dos frecuencias arbitrarias, en orden, tenemos que \( x.\text{freq} \leq a.\text{freq} \) e \( y.\text{freq} \leq b.\text{freq} \).</p>

<p>En el resto de la demostración, es posible que tengamos \( x.\text{freq} = a.\text{freq} \) o \( y.\text{freq} = b.\text{freq} \). Sin embargo, si tuviéramos \( x.\text{freq} = b.\text{freq} \), entonces también tendríamos \( a.\text{freq} = b.\text{freq} = x.\text{freq} = y.\text{freq} \) (ver el Ejercicio 16.3-1), y el lema sería trivialmente cierto. Por lo tanto, asumiremos que \( x.\text{freq} \ne b.\text{freq} \), lo cual implica que \( x \ne b \).</p>

<p>Como muestra la Figura 16.6, intercambiamos las posiciones de \( a \) y \( x \) en \( T \) para producir un árbol \( T' \), y luego intercambiamos las posiciones de \( b \) e \( y \) en \( T' \) para producir un árbol \( T'' \).</p>
<img src="../../../img/tree_ejemplo.png" alt="">
<h4>Figura 16.6</h4>
<p>Una ilustración del paso clave en la demostración del Lema 16.2. En el árbol óptimo \( T \), las hojas \( a \) y \( b \) son dos hermanas de profundidad máxima. Las hojas \( x \) e \( y \) son los dos caracteres con las frecuencias más bajas; aparecen en posiciones arbitrarias en \( T \). Suponiendo que \( x \ne b \), intercambiar las hojas \( a \) y \( x \) produce el árbol \( T' \), y luego intercambiar las hojas \( b \) e \( y \) produce el árbol \( T'' \). Como cada intercambio no incrementa el costo, el árbol resultante \( T'' \) también es un árbol óptimo.</p>

<p>...en la cual \( x \) e \( y \) son hojas hermanas de profundidad máxima. (Nótese que si \( x = b \) pero \( y \ne a \), entonces el árbol \( T'' \) no tiene a \( x \) e \( y \) como hojas hermanas de profundidad máxima. Como asumimos que \( x \ne b \), esta situación no puede ocurrir).</p>

<p>Por la ecuación (16.4), la diferencia de costo entre \( T \) y \( T' \) es</p>

<p>
\[
B(T) - B(T') = \sum_{c \in C} c.\text{freq} \cdot d_T(c) - \sum_{c \in C} c.\text{freq} \cdot d_{T'}(c)
\]
\[
= x.\text{freq} \cdot d_T(x) + a.\text{freq} \cdot d_T(a) - x.\text{freq} \cdot d_{T'}(x) - a.\text{freq} \cdot d_{T'}(a)
\]
\[
= x.\text{freq} \cdot d_T(x) + a.\text{freq} \cdot d_T(a) - x.\text{freq} \cdot d_T(a) - a.\text{freq} \cdot d_T(x)
\]
\[
= (a.\text{freq} - x.\text{freq}) \cdot (d_T(a) - d_T(x))  ;
\]

\[\geq 0\]
</p>

<p>porque tanto \( a.\text{freq} - x.\text{freq} \) como \( d_T(a) - d_T(x) \) son no negativos. Más específicamente, \( a.\text{freq} - x.\text{freq} \) es no negativo porque \( x \) es una hoja de frecuencia mínima, y \( d_T(a) - d_T(x) \) es no negativo porque \( a \) es una hoja de profundidad máxima en \( T \).</p>

<p>De manera similar, intercambiar \( y \) y \( b \) no incrementa el costo, y por lo tanto \( B(T') - B(T'') \) es no negativo. Por lo tanto, \( B(T'') \leq B(T) \), y como \( T \) es óptimo, tenemos que \( B(T) \leq B(T'') \), lo cual implica que \( B(T'') = B(T) \).</p>

<p>Así, \( T'' \) es un árbol óptimo en el cual \( x \) e \( y \) aparecen como hojas hermanas de profundidad máxima, de lo cual se sigue el lema.</p>


<p>El <strong>Lema 16.2</strong> implica que el proceso de construir un árbol óptimo mediante fusiones puede, sin pérdida de generalidad, comenzar con la elección voraz de fusionar aquellos dos caracteres con menor frecuencia.</p> <p>¿Por qué es esta una elección voraz? Podemos ver el costo de una única fusión como la suma de las frecuencias de los dos elementos que se están fusionando. El <em>Ejercicio 16.3-4</em> muestra que el costo total del árbol construido es igual a la suma de los costos de sus fusiones. De todas las posibles fusiones en cada paso, <code>HUFFMAN</code> elige aquella que incurre en el menor costo.</p> <p>El siguiente lema muestra que el problema de construir códigos prefijos óptimos tiene la propiedad de subestructura óptima.</p>

 
