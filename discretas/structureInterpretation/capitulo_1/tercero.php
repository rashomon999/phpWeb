    <p>La distinción entre declarativo e imperativo en este contexto tiene más que ver con la manera en que se describe cómo se resuelve un problema (imperativo) versus qué es el resultado o la propiedad de la operación (declarativo).</p>

<p><strong>Declarativamente</strong>, describimos lo que es la raíz cuadrada. Es decir, simplemente decimos que la raíz cuadrada de un número <i>x</i> es el número <i>y</i> tal que <i>y &ge; 0</i> y <i>y<sup>2</sup> = x</i>. Esta es una descripción matemática pura, un hecho abstracto. No se especifica cómo calcularla, solo qué es la raíz cuadrada.</p>

<p><strong>Imperativamente</strong>, especificamos cómo encontrar ese valor. Es el enfoque en el que decimos: "Para encontrar la raíz cuadrada de un número <i>x</i>, usa un método de aproximaciones sucesivas, como el de Newton, que ajusta continuamente la suposición de la raíz cuadrada hasta que se obtiene una aproximación suficientemente precisa". Aquí es donde entra el detalle de cómo hacer los cálculos paso a paso.</p>

<p>En resumen:</p>
<ul>
  <li><strong>Declarativo:</strong> Nos dice lo que es la raíz cuadrada (sin mostrar cómo calcularla).</li>
  <li><strong>Imperativo:</strong> Nos dice cómo calcularla paso a paso (como el método de Newton).</li>
</ul>

<p>La raíz cuadrada de 2 es el resultado que buscamos, pero el método de Newton es un enfoque imperativo para obtener ese resultado de forma aproximada, con un procedimiento específico para encontrarlo.</p>




<h4>Formalización del proceso en términos de procedimientos</h4>
    <p>Ahora formalicemos el proceso en términos de procedimientos. Comenzamos con un valor para el radicando (el número cuya raíz cuadrada estamos intentando calcular) y un valor para la suposición. Si la suposición es lo suficientemente buena para nuestros fines, hemos terminado; si no, debemos repetir el proceso con una suposición mejorada. Escribimos esta estrategia básica como un procedimiento:</p>
    <pre>
(define (sqrt-iter guess x)
  (if (good-enough? guess x)
      guess
      (sqrt-iter (improve guess x) x)))
    </pre>
    <p>Una suposición se mejora promediándola con el cociente del radicando y la antigua suposición:</p>
    <pre>
(define (improve guess x)
  (average guess (/ x guess)))
    </pre>
    <p>donde</p>
    <pre>
(define (average x y)
  (/ (+ x y) 2))
    </pre>
    <p>También debemos decir qué entendemos por "suficientemente bueno". Lo siguiente servirá como ilustración, pero realmente no es una prueba muy buena. (Ver Ejercicio 1.7.) La idea es mejorar la respuesta hasta que esté lo suficientemente cerca de modo que su cuadrado difiera del radicando por menos de una tolerancia predeterminada (aquí 0.001):</p>
    <pre>
(define (good-enough? guess x)
  (< (abs (- (square guess) x)) 0.001))
    </pre>
    <p>Normalmente, daremos a los predicados nombres que terminan con signos de interrogación, para ayudarnos a recordar que son predicados. Esto es solo una convención estilística. En cuanto al intérprete, el signo de interrogación es solo un carácter ordinario.</p>
    <p>Finalmente, necesitamos una forma de comenzar. Por ejemplo, siempre podemos suponer que la raíz cuadrada de cualquier número es 1:</p>
    <pre>
(define (sqrt x)
  (sqrt-iter 1.0 x))
    </pre>
    <p>Si escribimos estas definiciones en el intérprete, podemos usar <code>sqrt</code> de la misma manera que usamos cualquier otro procedimiento:</p>
    <pre>
(sqrt 9)
3.00009155413138

(sqrt (+ 100 37))
11.704699917758145

(sqrt (+ (sqrt 2) (sqrt 3)))
1.7739279023207892

(square (sqrt 1000))
1000.000369924366
    </pre>
    <p>El programa <code>sqrt</code> también ilustra que el simple lenguaje procedural que hemos introducido hasta ahora es suficiente para escribir cualquier programa puramente numérico que uno podría escribir, por ejemplo, en C o Pascal. Esto podría parecer sorprendente, ya que no hemos incluido en nuestro lenguaje ninguna construcción de repetición (looping) que dirija a la computadora a hacer algo una y otra vez. Por otro lado, <code>sqrt-iter</code> demuestra cómo se puede lograr la iteración sin necesidad de ninguna construcción especial, más allá de la capacidad ordinaria de llamar a un procedimiento.</p>
        
