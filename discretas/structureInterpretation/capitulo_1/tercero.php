    <p>
        Las expresiones condicionales se evalúan de la siguiente manera. El predicado ⟨p1⟩ se evalúa primero. Si su valor es falso, entonces se evalúa ⟨p2⟩. Si el valor de ⟨p2⟩ también es falso, entonces se evalúa ⟨p3⟩. Este proceso continúa hasta que se encuentra un predicado cuyo valor sea verdadero, en cuyo caso el intérprete devuelve el valor de la expresión consecuente ⟨e⟩ de la cláusula como el valor de la expresión condicional. Si ninguno de los ⟨p⟩ es verdadero, el valor de <code>cond</code> es indefinido.
    </p>
    <p>
        La palabra <strong>predicado</strong> se usa para procedimientos que devuelven verdadero o falso, así como para expresiones que se evalúan como verdadero o falso. El procedimiento de valor absoluto <code>abs</code> hace uso de los predicados primitivos <code>&gt;</code>, <code>&lt;</code> y <code>=</code>. Estos toman dos números como argumentos y prueban si el primer número es, respectivamente, mayor que, menor que o igual al segundo número, devolviendo verdadero o falso en consecuencia.
    </p>
    <p>
        Otra forma de escribir el procedimiento de valor absoluto es:
    </p>
    <pre>
        (define (abs x)
          (cond ((&lt; x 0) (- x))
                (else x)))
    </pre>
    <p>
        Lo que podría expresarse en inglés como "Si x es menor que cero, devolver −x; de lo contrario, devolver x." <code>else</code> es un símbolo especial que se puede usar en lugar de ⟨p⟩ en la cláusula final de un <code>cond</code>. Esto hace que el <code>cond</code> devuelva como su valor el valor de la expresión correspondiente ⟨e⟩ siempre que todas las cláusulas anteriores hayan sido omitidas. De hecho, cualquier expresión que siempre evalúe a un valor verdadero podría usarse como el ⟨p⟩ aquí.
    </p>    

    <p>Aquí hay otra forma de escribir el procedimiento de valor absoluto:</p>
<pre>
(define (abs x)
  (if (< x 0)
      (- x)
      x))
</pre>
<p>Esto usa la forma especial <code>if</code>, un tipo restringido de condicional que se puede usar cuando hay exactamente dos casos en el análisis de casos. La forma general de una expresión <code>if</code> es:</p>
<pre>
(if ⟨predicado⟩ ⟨consecuente⟩ ⟨alternativa⟩)
</pre>
<p>Para evaluar una expresión <code>if</code>, el intérprete comienza evaluando la parte del <code>predicado</code> de la expresión. Si el <code>predicado</code> evalúa a un valor verdadero, el intérprete luego evalúa el <code>consecuente</code> y devuelve su valor. De lo contrario, evalúa la <code>alternativa</code> y devuelve su valor.</p>

<p>Además de los predicados primitivos como <code>&lt;</code>, <code>=</code> y <code>&gt;</code>, existen operaciones de composición lógica que nos permiten construir predicados compuestos. Los tres más utilizados son estos:</p>
<ul>
  <li><code>(and ⟨e1⟩ . . . ⟨en⟩)</code> El intérprete evalúa las expresiones ⟨e⟩ una por una, en orden de izquierda a derecha. Si alguna ⟨e⟩ evalúa a falso, el valor de la expresión <code>and</code> es falso, y el resto de las ⟨e⟩'s no se evalúan. Si todas las ⟨e⟩'s evalúan a valores verdaderos, el valor de la expresión <code>and</code> es el valor de la última.</li>
  <li><code>(or ⟨e1⟩ . . . ⟨en⟩)</code> El intérprete evalúa las expresiones ⟨e⟩ una por una, en orden de izquierda a derecha. Si alguna ⟨e⟩ evalúa a un valor verdadero, ese valor se devuelve como el valor de la expresión <code>or</code>, y el resto de las ⟨e⟩'s no se evalúan. Si todas las ⟨e⟩'s evalúan a falso, el valor de la expresión <code>or</code> es falso.</li>
  <li><code>(not ⟨e⟩)</code> El valor de una expresión <code>not</code> es verdadero cuando la expresión ⟨e⟩ evalúa a falso, y falso en caso contrario.</li>
</ul>

<p>Nota que <code>and</code> y <code>or</code> son formas especiales, no procedimientos, porque las subexpresiones no necesariamente se evalúan todas. <code>not</code> es un procedimiento ordinario.</p>

<p>Como ejemplo de cómo se usan, la condición de que un número <code>x</code> esté en el rango <code>5 &lt; x &lt; 10</code> se puede expresar como:</p>
<pre>
(and (> x 5) (< x 10))
</pre>
<p>Como otro ejemplo, podemos definir un predicado para comprobar si un número es mayor o igual que otro como:</p>
<pre>
(define (>= x y) (or (> x y) (= x y)))
</pre>
<p>O alternativamente como:</p>
<pre>
(define (>= x y) (not (< x y)))
</pre>



<p><strong>1.1.7 Ejemplo: Raíces Cuadradas por el Método de Newton</strong></p>

<p>Los procedimientos, como se introdujeron anteriormente, son muy similares a las funciones matemáticas ordinarias. Especifican un valor que se determina mediante uno o más parámetros. Sin embargo, hay una diferencia importante entre las funciones matemáticas y los procedimientos informáticos. Los procedimientos deben ser efectivos.</p>

<p>Como ejemplo, consideremos el problema de calcular raíces cuadradas. Podemos definir la función de la raíz cuadrada como:</p>

<p><em>√x = el y tal que y ≥ 0 y y² = x.</em></p>

<p>Esto describe una función matemática perfectamente legítima. Podríamos usarla para reconocer si un número es la raíz cuadrada de otro, o para derivar hechos sobre las raíces cuadradas en general. Por otro lado, la definición no describe un procedimiento. De hecho, casi no nos dice nada sobre cómo encontrar realmente la raíz cuadrada de un número dado. No ayudará nada replantear esta definición en pseudo-Lisp:</p>

<pre>
(define (sqrt x)
  (the y (and (>= y 0)
              (= (square y) x))))
</pre>

<p>Esto solo plantea la pregunta.</p>

<p>El contraste entre función y procedimiento refleja la distinción general entre describir propiedades de las cosas y describir cómo hacer las cosas, o, como a veces se le llama, la distinción entre conocimiento declarativo y conocimiento imperativo. En matemáticas, generalmente nos preocupamos por descripciones declarativas (lo que es), mientras que en informática nos preocupamos por descripciones imperativas (cómo hacerlo).</p>

