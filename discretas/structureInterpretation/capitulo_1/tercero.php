    <h4>1.1.8 Procedimientos como Abstracciones de Caja Negra</h4>
  <p>
    La función <code>sqrt</code> es nuestro primer ejemplo de un proceso definido por un conjunto de procedimientos mutuamente definidos.
    Observa que la definición de <code>sqrt-iter</code> es recursiva; es decir, el procedimiento se define en términos de sí mismo.
    La idea de poder definir un procedimiento en términos de sí mismo puede ser desconcertante; puede parecer poco claro cómo una definición “circular”
    podría tener sentido, y mucho menos especificar un proceso bien definido que deba llevar a cabo una computadora.
    Esto se abordará con más detalle en la Sección 1.2. Pero primero, consideremos algunos otros puntos importantes ilustrados por el ejemplo de <code>sqrt</code>.
  </p>

  <p>
    Observa que el problema de calcular raíces cuadradas se descompone naturalmente en varios subproblemas:
    cómo saber si una suposición es suficientemente buena, cómo mejorar una suposición, y así sucesivamente.
    Cada una de estas tareas se lleva a cabo mediante un procedimiento separado.
    Todo el programa de <code>sqrt</code> puede verse como un conjunto de procedimientos (mostrado en la Figura 1.2)
    que refleja la descomposición del problema en subproblemas.
  </p>

  <p>
    La importancia de esta estrategia de descomposición no es simplemente que se esté dividiendo el programa en partes.
    Después de todo, podríamos tomar cualquier programa grande y dividirlo en partes: las primeras diez líneas, las siguientes diez líneas,
    las siguientes diez líneas, y así sucesivamente.
    Más bien, es crucial que cada procedimiento logre una tarea identificable que pueda usarse como un módulo para definir otros procedimientos.
    Por ejemplo, cuando definimos el procedimiento <code>good-enough?</code> en términos de <code>square</code>,
    podemos considerar el procedimiento <code>square</code> como una “caja negra”.
    En ese momento, no estamos preocupados por cómo el procedimiento calcula su resultado, solo por el hecho de que calcula el cuadrado.
    Los detalles de cómo se calcula el cuadrado pueden suprimirse, para ser considerados en un momento posterior.
    De hecho, en lo que respecta al procedimiento <code>good-enough?</code>, <code>square</code> no es exactamente un procedimiento,
    sino una abstracción de un procedimiento, una llamada <em>abstracción procedural</em>.
    A este nivel de abstracción, cualquier procedimiento que calcule el cuadrado es igualmente bueno.
  </p>

  <p>
    Así, considerando solo los valores que devuelven, los siguientes dos procedimientos para calcular el cuadrado de un número deberían ser indistinguibles.
    Ambos toman un argumento numérico y producen el cuadrado de ese número como valor.
  </p>

  <pre><code>(define (square x) (* x x))
(define (square x) (exp (double (log x))))
(define (double x) (+ x x))</code></pre>

  <p>
    Por lo tanto, una definición de procedimiento debería ser capaz de suprimir detalles.
    Los usuarios del procedimiento pueden no haberlo escrito ellos mismos, pero pueden haberlo obtenido de otro programador como una caja negra.
    Un usuario no debería necesitar saber cómo se implementa el procedimiento para usarlo.
  </p>




    <p>
    En informática (y en ingeniería en general), una caja negra es un sistema o componente del que solo te importa
    qué entra y qué sale, no cómo funciona por dentro.
    </p>

