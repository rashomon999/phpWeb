    para formar una expresión compuesta que representa la aplicación del procedimiento a esos números. Por ejemplo:
    </p>

    <pre><code>(+ 137 349)     ; 486
    (- 1000 334)   ; 666
    (* 5 99)       ; 495
    (/ 10 5)       ; 2
    (+ 2.7 10)     ; 12.7</code></pre>

