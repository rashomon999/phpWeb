    <h1>1.1 Los Elementos de la Programación</h1>

    <p>
    Un lenguaje de programación poderoso es más que solo un medio para instruir a una computadora para que realice tareas.
    El lenguaje también sirve como un marco dentro del cual organizamos nuestras ideas sobre los procesos.
    Así, cuando describimos un lenguaje, debemos prestar especial atención a los medios que el lenguaje proporciona para combinar ideas simples y formar ideas más complejas.
    Cada lenguaje poderoso tiene tres mecanismos para lograr esto:
    </p>

    <ul>
    <li><strong>Expresiones primitivas</strong>, que representan las entidades más simples con las que el lenguaje trabaja,</li>
    <li><strong>Medios de combinación</strong>, mediante los cuales se construyen elementos compuestos a partir de elementos más simples, y</li>
    <li><strong>Medios de abstracción</strong>, mediante los cuales los elementos compuestos pueden ser nombrados y manipulados como unidades.</li>
    </ul>

    <p>
    En programación, tratamos con dos tipos de elementos: <strong>procedimientos</strong> y <strong>datos</strong>.
    (Más adelante descubriremos que realmente no son tan distintos.)
    Informalmente, los datos son "cosas" que queremos manipular, y los procedimientos son descripciones de las reglas para manipular los datos.
    Así, cualquier lenguaje de programación poderoso debería ser capaz de describir datos primitivos y procedimientos primitivos y debería tener métodos para combinar y abstraer procedimientos y datos.
    </p>

    <p>
    En este capítulo, solo trataremos con datos numéricos simples para que podamos enfocarnos en las reglas para construir procedimientos.
    En capítulos posteriores veremos que estas mismas reglas nos permiten construir procedimientos para manipular datos compuestos también.
    </p>

