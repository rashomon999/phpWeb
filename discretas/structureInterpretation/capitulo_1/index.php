    <p>
    Una forma fácil de comenzar a programar es examinar algunas interacciones típicas con un intérprete del dialecto <strong>Scheme</strong> de Lisp.
    Imagina que estás sentado frente a una terminal de computadora.
    Escribes una expresión, y el intérprete responde mostrando el resultado de evaluar esa expresión.
    </p>

    <p>
    Un tipo de expresión primitiva que podrías escribir es un número.
    (Más precisamente, la expresión que escribes consiste en los números que representan el número en base 10.)
    Si le presentas un número a Lisp, el intérprete responderá imprimiendo:
    </p>

    <pre><code>486</code></pre>

