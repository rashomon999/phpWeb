    <h1>1.1.2 Nombrar y el Entorno</h1>

<p>
  Un aspecto crítico de un lenguaje de programación es el medio que proporciona para usar <strong>nombres</strong> con el fin de referirse a objetos computacionales.
  Decimos que el nombre identifica una <strong>variable</strong> cuyo valor es el objeto.
</p>

<p>
  En el dialecto <strong>Scheme</strong> de Lisp, nombramos cosas con <code>define</code>. Escribir:
</p>

<pre><code>(define size 2)</code></pre>

<p>
  hace que el intérprete asocie el valor <code>2</code> con el nombre <code>size</code>.
</p>

<p>
  Una vez que el nombre <code>size</code> ha sido asociado con el número <code>2</code>, podemos referirnos al valor <code>2</code> por su nombre:
</p>

<pre><code>size
; ⇒ 2

(* 5 size)
; ⇒ 10</code></pre>

<p>
  Aquí hay más ejemplos del uso de <code>define</code>:
</p>

    <pre><code>(define pi 3.14159)
    (define radius 10)
    (* pi (* radius radius))
    314.159

    (define circumference (* 2 pi radius))
    circumference
    62.8318</code></pre>

    <p>
        <code>define</code> es el medio más simple de <strong>abstracción</strong> en nuestro lenguaje, ya que nos permite usar nombres simples para referirnos a los resultados de operaciones compuestas,
        como la <code>circumference</code> calculada arriba.
    </p>

    <p>
        En general, los objetos computacionales pueden tener estructuras muy complejas, y sería extremadamente inconveniente tener que recordar y repetir sus detalles cada vez que queremos usarlos.
        De hecho, los programas complejos se construyen al crear, paso a paso, objetos computacionales de complejidad creciente.
    </p>

    <p>
        El intérprete hace que esta construcción paso a paso de programas sea particularmente conveniente porque las asociaciones nombre-objeto pueden crearse de manera incremental en interacciones sucesivas.
        Esta característica fomenta el <strong>desarrollo y prueba incremental</strong> de programas y es en gran parte responsable del hecho de que un programa en Lisp generalmente consista en un gran número de procedimientos relativamente simples.
    </p>

    <p>
        Debe quedar claro que la posibilidad de asociar valores con símbolos y luego recuperarlos significa que el intérprete debe mantener algún tipo de <strong>memoria</strong> que rastree los pares nombre-objeto.
        Esta memoria se llama el <strong>entorno</strong> (más precisamente, el <em>entorno global</em>, ya que más adelante veremos que un cálculo puede involucrar varios entornos diferentes).
    </p>



    <h1>1.1.3 Evaluación de Combinaciones</h1>

<p>
  Uno de nuestros objetivos en este capítulo es aislar cuestiones sobre cómo pensar de manera procedimental.
  Como ejemplo, consideremos que, al evaluar combinaciones, el intérprete está siguiendo un procedimiento.
</p>

<h2>Regla de Evaluación</h2>
<p>Para evaluar una combinación, se deben seguir estos pasos:</p>
<ol>
  <li>Evaluar las subexpresiones de la combinación.</li>
  <li>
    Aplicar el procedimiento que es el valor de la subexpresión más a la izquierda (el <strong>operador</strong>)
    a los argumentos que son los valores de las otras subexpresiones (los <strong>operandos</strong>).
  </li>
</ol>

<p>
  Esta regla simple ilustra puntos importantes sobre los procesos computacionales. El primer paso implica
  que debemos evaluar cada subexpresión antes de aplicar el procedimiento, lo cual hace que la regla
  sea de naturaleza <strong>recursiva</strong>: incluye como uno de sus pasos el aplicar la misma regla.
</p>

<p>
  La recursión permite expresar de forma concisa procesos que, de otro modo, serían complejos.
  Por ejemplo, al evaluar:
</p>

<pre><code>(* (+ 2 (* 4 6))
 (+ 3 5 7))</code></pre>

<p>
  La regla de evaluación se aplica a cuatro combinaciones distintas.
  Podemos visualizar este proceso como un <strong>árbol</strong> , como se muestra en la Figura 1.1.
  Donde cada nodo representa una combinación,
  con ramas para el operador y sus operandos. Los nodos terminales son operadores o números.
</p>
    <img src="../../img/Lisp.png" alt="">
<p>
    Al imaginar la evaluación en términos de árbol, los valores "filtran hacia arriba"
    desde los nodos terminales hacia combinaciones de niveles superiores. Este tipo de proceso
    se denomina <strong>acumulación en árbol</strong> (<em>tree accumulation</em>).
  </p>

  <h2>Casos Primitivos</h2>
  <p>Cuando llegamos a expresiones primitivas, aplicamos estas reglas:</p>
  <ul>
    <li>El valor de un número es el número mismo. ej:32</li>
    <li>El valor de un operador incorporado es la secuencia de instrucciones que realiza esa operación. ej: (+ 3 5)
    </li>
    <li>El valor de un nombre es el objeto asociado con ese nombre en el entorno. ej(define x 10)</li>
  </ul>

  <p>
    Podemos considerar la segunda regla como un caso especial de la tercera, estipulando que símbolos como + y * también están incluidos en el entorno global, y están asociados con las secuencias de instrucciones de la máquina que son sus "valores". El punto clave a notar es el papel del entorno en la determinación del significado de los símbolos en las expresiones. En un lenguaje interactivo como Lisp, carece de sentido hablar del valor de una expresión como (<code>+ x 1</code>) sin especificar información sobre el entorno que proporcionaría un significado para el símbolo x (o incluso para el símbolo +). Como veremos en el Capítulo 3, la noción general del entorno como un contexto en el que tiene lugar la evaluación desempeñará un papel importante en nuestra comprensión de la ejecución del programa.
  </p>
  
  <p>
    Nota que la regla de evaluación dada anteriormente no maneja definiciones. Por ejemplo, evaluar (<code>define x 3</code>) no aplica define a dos argumentos, uno de los cuales es el valor del símbolo x y el otro es 3, ya que el propósito de define es precisamente asociar x con un valor. (Es decir, (<code>define x 3</code>) no es una combinación).
  </p>
  
  <p>
    Estas excepciones a la regla general de evaluación se llaman formas especiales. Define es el único ejemplo de una forma especial que hemos visto hasta ahora, pero pronto veremos otras. Cada forma especial tiene su propia regla de evaluación. Los diversos tipos de expresiones (cada una con su propia regla de evaluación asociada) constituyen la sintaxis del lenguaje de programación. En comparación con la mayoría de los otros lenguajes de programación, Lisp tiene una sintaxis muy simple; es decir, la regla de evaluación para las expresiones puede describirse con una regla general simple junto con reglas especializadas para un pequeño número de formas especiales.
  </p>
