    <h2>parámetros y variables locales</h2>
  <ul>
    <li><strong>Los parámetros de una función son variables locales.</strong><br>
      Solo existen dentro del cuerpo de esa función y no afectan otras partes del programa.
    </li>
    <li><strong>El nombre de un parámetro no importa.</strong><br>
      <code>x</code> o <code>y</code>, da igual mientras se usen de forma coherente dentro de la función.
    </li>
    <li><strong>¡Cuidado con los nombres que ya tienen un significado!</strong><br>
      Si usas como parámetro el mismo nombre de una función existente (como <code>abs</code> o <code>square</code>), podrías ocultar (sobrescribir) esa función dentro de tu procedimiento y provocar errores.
    </li>
    <li><strong>Parámetros = variables “ligadas” o "bound variables"</strong><br>
      Solo tienen significado dentro del alcance (<em>scope</em>) de la función donde se definieron.
    </li>
    <li><strong>Funciones externas, como <code>abs</code>, son “variables libres”</strong><br>
      Y no deben ser reemplazadas accidentalmente por parámetros con el mismo nombre.
    </li>
  </ul>
