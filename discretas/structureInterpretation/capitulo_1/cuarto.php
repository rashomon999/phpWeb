    <h2>1.2.2 Recursión en Árbol</h2>

  <p>Otro patrón común de cómputo se llama <strong>recursión en árbol</strong>. Como ejemplo, consideremos el cálculo de la sucesión de números de Fibonacci, donde cada número es la suma de los dos anteriores:</p>

  <p>0, 1, 1, 2, 3, 5, 8, 13, 21, ...</p>

  <p>En general, los números de Fibonacci pueden definirse por la regla:</p>

  <p>
    \[
    \text{Fib}(n) =
    \begin{cases}
    0 & \text{si } n = 0, \\
    1 & \text{si } n = 1, \\
    \text{Fib}(n - 1) + \text{Fib}(n - 2) & \text{en otro caso.}
    \end{cases}
    \]
  </p>

  <p>Podemos traducir inmediatamente esta definición en un procedimiento recursivo para calcular números de Fibonacci:</p>

  <pre>
(define (fib n)
  (cond ((= n 0) 0)
        ((= n 1) 1)
        (else (+ (fib (- n 1))
                 (fib (- n 2))))))
  </pre>

  <p>Considera el patrón de este cálculo. Para computar <code>(fib 5)</code>, computamos <code>(fib 4)</code> y <code>(fib 3)</code>. Para <code>(fib 4)</code>, computamos <code>(fib 3)</code> y <code>(fib 2)</code>. En general, el proceso que se desarrolla se parece a un árbol. Observa que las ramas se dividen en dos en cada nivel (excepto en la parte inferior); esto refleja el hecho de que el procedimiento <code>fib</code> se llama a sí mismo dos veces en cada invocación.</p>

  <p>Este procedimiento es instructivo como ejemplo prototípico de recursión en árbol, pero es una forma terrible de calcular números de Fibonacci porque hace muchos cálculos redundantes. Por ejemplo, <code>(fib 3)</code> se calcula múltiples veces.</p>

  <p>De hecho, no es difícil mostrar que el número de veces que el procedimiento computa <code>(fib 0)</code> o <code>(fib 1)</code> (el número de hojas en el árbol) es exactamente \(\text{Fib}(n+1)\). Además, el valor de \(\text{Fib}(n)\) crece exponencialmente con respecto a \(n\). Más precisamente,</p>

  <p>
    \[
    \text{Fib}(n) \approx \frac{\varphi^n}{\sqrt{5}}, \quad \text{donde } \varphi = \frac{1 + \sqrt{5}}{2} \approx 1.6180,
    \]
  </p>

  <p>que es la proporción áurea, y cumple la ecuación \(\varphi^2 = \varphi + 1\).</p>

  <p>Así, el proceso usa un número de pasos que crece exponencialmente con la entrada. Por otro lado, el espacio requerido crece solo linealmente con la entrada, ya que solo necesitamos mantener un seguimiento de los nodos superiores en el árbol.</p>

  <p>En general:</p>
  <ul>
    <li>El número de <strong>pasos</strong> requeridos por un proceso de recursión en árbol es proporcional al <strong>número de nodos</strong> en el árbol.</li>
    <li>El <strong>espacio</strong> requerido es proporcional a la <strong>profundidad máxima</strong> del árbol.</li>
  </ul>

  <h3>Proceso Iterativo Alternativo</h3>

  <p>También podemos formular un proceso <strong>iterativo</strong> para calcular los números de Fibonacci. La idea es usar un par de enteros \(a\) y \(b\), inicializados con \(\text{Fib}(1) = 1\) y \(\text{Fib}(0) = 0\), y aplicar repetidamente las transformaciones simultáneas:</p>

  <p>
    \[
    a \leftarrow a + b, \quad b \leftarrow a.
    \]
  </p>

  <p>Después de aplicar esta transformación \(n\) veces, \(a\) y \(b\) serán iguales respectivamente a \(\text{Fib}(n + 1)\) y \(\text{Fib}(n)\). Entonces, podemos definir el procedimiento iterativo como:</p>

  <pre>
(define (fib n)
  (fib-iter 1 0 n))

(define (fib-iter a b count)
  (if (= count 0)
      b
      (fib-iter (+ a b) a (- count 1))))
  </pre>

  <p>Este segundo método para computar \(\text{Fib}(n)\) es una iteración lineal. La diferencia en la cantidad de pasos requeridos entre los dos métodos—uno lineal en \(n\), y el otro que crece tan rápido como \(\text{Fib}(n)\)—es enorme, incluso para entradas pequeñas.</p>

  <p>Sin embargo, no debemos concluir que los procesos de recursión en árbol son inútiles. Cuando trabajamos con datos estructurados jerárquicamente, la recursión en árbol es una herramienta natural y poderosa. Incluso en operaciones numéricas, puede ser útil para entender y diseñar programas.</p>

  <p>Por ejemplo, aunque el primer procedimiento <code>fib</code> es mucho menos eficiente que el segundo, es más directo, siendo poco más que una traducción de la definición matemática de la secuencia de Fibonacci. Para formular el algoritmo iterativo, se necesitó notar que el cálculo podía reescribirse como una iteración con tres variables de estado.</p>


