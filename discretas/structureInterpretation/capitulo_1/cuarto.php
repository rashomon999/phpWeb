    <strong>Procedimiento vs. Proceso:</strong> 
    <p>
    Un procedimiento es como una receta. Es un conjunto de pasos que defines en tu código, paso a paso, como 
    una función o subrutina.
    </p>
    <p>
    Un proceso es lo que realmente sucede cuando ejecutas esa receta. Es la actividad que ocurre en la 
    computadora cuando corre ese procedimiento.
    </p>
    <hr>



    <p>
    Cuando discutamos la implementación de procedimientos en máquinas de registros en el Capítulo 5, veremos que 
    cualquier <strong>proceso iterativo</strong> puede ser realizado "en hardware" como una máquina que tiene un 
    <strong>conjunto fijo de registros y sin memoria auxiliar</strong>. En contraste, realizar un 
    <strong>proceso recursivo</strong> requiere una máquina que utilice una estructura de datos auxiliar conocida como 
    <em>pila</em>.
    </p>

    <p>
    La mayoría de las implementaciones de lenguajes comunes (como Ada, Pascal y C) interpretan cualquier procedimiento 
    recursivo de manera que consume memoria que crece con el número de llamadas al procedimiento, incluso si el proceso 
    es, en principio, iterativo. Como consecuencia, estos lenguajes solo pueden describir procesos iterativos utilizando 
    <em>constructos especiales de repetición</em> como <code>do</code>, <code>repeat</code>, <code>until</code>, 
    <code>for</code> y <code>while</code>.
    </p>

    <p>
    La implementación de Scheme que consideraremos en el Capítulo 5 no tiene esta limitación. Ejecutará un 
    <strong>proceso iterativo en espacio constante</strong>, incluso cuando el proceso esté descrito por un procedimiento 
    recursivo. Una implementación con esta propiedad se llama <strong>recursiva en cola</strong>.
    </p>

    <p>
    Con una implementación recursiva en cola, la iteración puede expresarse usando el mecanismo ordinario de llamadas 
    a procedimientos, haciendo que los constructos especiales de iteración sean útiles solo como <em>azúcar sintáctica</em>.
    </p>


    <h3>Serie Fibonacci:</h3>

    <h4>Enfoque Recursivo:</h4>

    <h5>Complejidad Temporal:</h5>
    <p>El enfoque recursivo tiene una complejidad temporal <strong>O(2^n)</strong>, que es exponencial.</p>
    <p>Esto se debe a que la función <code>fib(n)</code> hace dos llamadas recursivas por cada llamada, lo que genera un número de llamadas que crece rápidamente a medida que <code>n</code> aumenta. El número total de llamadas a la función crece exponencialmente, lo que lleva a esta complejidad.</p>

    <h5>Complejidad Espacial:</h5>
    <p>En el enfoque recursivo, la complejidad espacial es <strong>O(n)</strong>, ya que depende de la profundidad máxima del árbol de recursión. La cantidad de espacio necesario para almacenar las llamadas recursivas en la pila de ejecución es proporcional a <code>n</code>.</p>

    <h4>Enfoque Iterativo:</h4>

    <h5>Complejidad Temporal:</h5>
    <p>El enfoque iterativo tiene una complejidad temporal <strong>O(n)</strong>, ya que el ciclo <code>for</code> recorre el rango de 2 hasta <code>n</code>, ejecutándose una vez por cada valor de <code>n</code>. Este enfoque es lineal.</p>

    <h5>Complejidad Espacial:</h5>
    <p>El enfoque iterativo tiene una complejidad espacial <strong>O(1)</strong> o constante. Esto se debe a que solo se necesitan unas pocas variables para mantener el estado del cálculo, y no se requiere memoria adicional a medida que el valor de <code>n</code> aumenta.</p>

    <h2>Resumen:</h2>
    <p>En resumen, el <strong>enfoque recursivo</strong> es mucho más costoso en términos de tiempo y espacio debido a las llamadas repetidas a la función y la pila de ejecución creciente, mientras que el <strong>enfoque iterativo</strong> es mucho más eficiente en ambos aspectos, ya que se ejecuta en tiempo lineal y usa una cantidad constante de espacio.</p>
    <hr>

    <h4> Proceso Iterativo</h4>
  <p>Un <strong>proceso iterativo</strong> es aquel en el que:</p>
  <ul>
    <li>El <strong>estado completo</strong> del proceso puede ser representado por un <strong>conjunto fijo de variables</strong>.</li>
    <li>Existe una <strong>regla constante</strong> que determina cómo se actualizan estas variables de un paso al siguiente.</li>
    <li>Opcionalmente, hay una <strong>condición de finalización</strong> que determina cuándo el proceso debe detenerse.</li>
    <li><strong>El espacio requerido</strong> por el proceso <strong>no crece con la cantidad de pasos</strong>; por lo tanto, puede ejecutarse en <strong>espacio constante</strong> (O(1)).</li>
  </ul>
  <p>Este tipo de proceso se llama <strong>iterativo lineal</strong> si el número de pasos crece linealmente con respecto a la entrada (por ejemplo, O(n)).</p>

  <h4>Proceso Recursivo</h4>
  <p>Un <strong>proceso recursivo</strong> es aquel en el que:</p>
  <ul>
    <li>Se construye una <strong>cadena de operaciones diferidas</strong>, como resultado de llamadas recursivas que se apilan antes de poder ser evaluadas.</li>
    <li>Para ejecutar el proceso, el sistema debe mantener un <strong>seguimiento de contexto o información pendiente</strong>, normalmente en una pila de llamadas.</li>
    <li><strong>El espacio requerido</strong> crece en proporción al número de operaciones diferidas (por ejemplo, la <strong>profundidad de la recursión</strong>).</li>
    <li>Si esta profundidad es proporcional a la entrada (por ejemplo, O(n)), entonces se denomina <strong>proceso recursivo lineal</strong>.</li>
  </ul>
