  <h1>Orden Aplicativo vs. Orden Normal</h1>

<p>
  Según la descripción de la evaluación dada en la <strong>Sección 1.1.3</strong>, el intérprete primero evalúa el operador y los operandos, y luego aplica el procedimiento resultante a los argumentos resultantes. Sin embargo, <strong>esta no es la única forma</strong> de realizar una evaluación.
</p>

<p>
  Existe un <strong>modelo alternativo de evaluación</strong> que <em>no evalúa los operandos</em> hasta que se necesiten sus valores. En lugar de eso, primero se sustituyen las expresiones de los operandos por los parámetros hasta obtener una expresión que solo involucre operadores primitivos, y luego se realiza la evaluación.
</p>

<p>Por ejemplo, si usáramos este método, la evaluación de <code>(f 5)</code> procedería con la siguiente secuencia de expansiones:</p>

<pre>
(sum-of-squares (+ 5 1) (* 5 2))
(+ (square (+ 5 1)) (square (* 5 2)))
(+ (* (+ 5 1) (+ 5 1)) (* (* 5 2) (* 5 2)))
</pre>

<p>Seguido por las reducciones:</p>

<pre>
(+ (* 6 6) (* 10 10))
(+ 36 100)
136
</pre>

<p>
  Esto da el <strong>mismo resultado</strong> que nuestro modelo de evaluación anterior, pero el <strong>proceso es diferente</strong>. En particular, las evaluaciones de <code>(+ 5 1)</code> y <code>(* 5 2)</code> se realizan <em>dos veces</em> aquí, lo que corresponde a la reducción de la expresión <code>(* x x)</code> con <code>x</code> reemplazado respectivamente por <code>(+ 5 1)</code> y <code>(* 5 2)</code>.
</p>

<p>
  Este método alternativo de <strong>“expandir completamente y luego reducir”</strong> se conoce como <strong>evaluación en orden normal</strong>, en contraste con el método <strong>“evaluar los argumentos y luego aplicar”</strong> que el intérprete realmente usa, llamado <strong>evaluación en orden aplicativo</strong>.
</p>

<p>
  Se puede demostrar que, para aplicaciones de procedimientos que pueden modelarse usando sustitución (incluyendo todos los procedimientos en los dos primeros capítulos de este libro) y que producen valores legítimos, <strong>la evaluación en orden normal y la evaluación en orden aplicativo producen el mismo valor</strong>.
</p>

<p><em>(Consulta el Ejercicio 1.5 para un ejemplo de un “valor ilegítimo” en el que la evaluación en orden normal y en orden aplicativo no dan el mismo resultado).</em></p>

<p>
  Lisp utiliza <strong>evaluación en orden aplicativo</strong>, en parte por la <strong>eficiencia adicional</strong> que se obtiene al evitar evaluaciones múltiples de expresiones (como las ilustradas con <code>(+ 5 1)</code> y <code>(* 5 2)</code>), y más significativamente porque <strong>la evaluación en orden normal se vuelve mucho más complicada</strong> cuando salimos del ámbito de los procedimientos que pueden modelarse mediante sustitución.
</p>

<p>
  Sin embargo, la <strong>evaluación en orden normal</strong> puede ser una herramienta extremadamente valiosa, y exploraremos algunas de sus implicaciones en los <strong>Capítulos 3 y 4</strong>.
</p>

