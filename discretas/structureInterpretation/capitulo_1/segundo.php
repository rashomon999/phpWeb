    <h4>1.1.5 El Modelo de Sustitución para la Aplicación de Procedimientos</h4>

  <p>
    Para evaluar una combinación cuyo operador nombra un procedimiento compuesto, el intérprete sigue prácticamente el mismo proceso que para las combinaciones cuyos operadores nombran procedimientos primitivos, como describimos en la Sección 1.1.3. Es decir, el intérprete evalúa los elementos de la combinación y aplica el procedimiento (que es el valor del operador de la combinación) a los argumentos (que son los valores de los operandos de la combinación).
  </p>

  <p>
    Podemos asumir que el mecanismo para aplicar procedimientos primitivos a argumentos está incorporado en el intérprete. Para los procedimientos compuestos, el proceso de aplicación es el siguiente:
  </p>

  <blockquote>
    <strong>Para aplicar un procedimiento compuesto a unos argumentos, se evalúa el cuerpo del procedimiento reemplazando cada parámetro formal por el argumento correspondiente.</strong>
  </blockquote>

  <p>
    Para ilustrar este proceso, evaluemos la combinación:
  </p>

  <pre><code>(f 5)</code></pre>

  <p>
    donde <code>f</code> es el procedimiento definido en la Sección 1.1.4. Comenzamos recuperando el cuerpo de <code>f</code>:
  </p>

  <pre><code>(sum-of-squares (+ a 1) (* a 2))</code></pre>

  <p>
    Luego, reemplazamos el parámetro formal <code>a</code> por el argumento <code>5</code>:
  </p>

  <pre><code>(sum-of-squares (+ 5 1) (* 5 2))</code></pre>

  <p>
    Así, el problema se reduce a la evaluación de una combinación con dos operandos y un operador <code>sum-of-squares</code>. Evaluar esta combinación implica tres subproblemas. Debemos evaluar el operador para obtener el procedimiento que se aplicará, y debemos evaluar los operandos para obtener los argumentos. Ahora, <code>(+ 5 1)</code> produce <code>6</code> y <code>(* 5 2)</code> produce <code>10</code>, por lo tanto, debemos aplicar el procedimiento <code>sum-of-squares</code> a <code>6</code> y <code>10</code>. Estos valores se sustituyen por los parámetros formales <code>x</code> e <code>y</code> en el cuerpo de <code>sum-of-squares</code>, reduciendo la expresión a:
  </p>

  <pre><code>(+ (square 6) (square 10))</code></pre>

  <p>
    Si usamos la definición de <code>square</code>, esto se reduce a:
  </p>

  <pre><code>(+ (* 6 6) (* 10 10))</code></pre>

  <p>
    lo cual se reduce por multiplicación a:
  </p>

  <pre><code>(+ 36 100)</code></pre>

  <p>
    y finalmente a:
  </p>

  <pre><code>136</code></pre>

  
