<h1>1.1.6 Expresiones Condicionales y Predicados</h1>
    <p>
        El poder expresivo de la clase de procedimientos que podemos definir en este punto es muy limitado, porque no tenemos forma de hacer pruebas y realizar diferentes operaciones dependiendo del resultado de una prueba. Por ejemplo, no podemos definir un procedimiento que calcule el valor absoluto de un número probando si el número es positivo, negativo o cero, y tomando acciones diferentes en cada caso según la regla:
    </p>
    <pre>
        |x| =
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  x  si x &gt; 0,
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  0  si x = 0,
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  -x si x &lt; 0.
    </pre>
    <p>
        Esta construcción se llama análisis de casos, y existe una forma especial en Lisp para anotar dicho análisis de casos. Se llama <code>cond</code> (que significa "condicional") y se usa de la siguiente manera:
    </p>
    <pre>
        (define (abs x)
          (cond ((&gt; x 0) x)
                ((= x 0) 0)
                ((&lt; x 0) (- x))))
    </pre>
    <p>
        La forma general de una expresión condicional es:
    </p>
    <pre>
        (cond (⟨p1⟩ ⟨e1⟩)
              (⟨p2⟩ ⟨e2⟩)
              . . .
              (⟨pn⟩ ⟨en⟩))
    </pre>
    <p>
        Consiste en el símbolo <code>cond</code> seguido de pares de expresiones entre paréntesis <code>(⟨p⟩ ⟨e⟩)</code>, llamados cláusulas. La primera expresión de cada par es un predicado, es decir, una expresión cuyo valor se interpreta como verdadero o falso.
    </p>
    

    
