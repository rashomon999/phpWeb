    <h4>1.1.4 Procedimientos Compuestos</h4>
    <p>Hemos identificado en Lisp algunos de los elementos que deben aparecer en cualquier lenguaje de
    programación poderoso:</p>
    <ul>
        <li>Los números y las operaciones aritméticas son datos y procedimientos primitivos.</li>
        <li>El anidamiento de combinaciones proporciona un medio para combinar operaciones.</li>
        <li>Las definiciones que asocian nombres con valores proporcionan un medio limitado de abstracción.</li>
    </ul>
    <p>Ahora aprenderemos sobre las definiciones de procedimientos, una técnica de abstracción mucho más
    poderosa mediante la cual una operación compuesta puede recibir un nombre y luego ser referida como una unidad.</p>

    <h4>Examinando la Idea de "Elevar al Cuadrado"</h4>
    <p>Comenzamos examinando cómo expresar la idea de "elevar al cuadrado". Podríamos decir: "Para elevar algo al cuadrado, multiplícalo por sí mismo". Esto se expresa en nuestro lenguaje como:</p>
    <pre>(define (square x) (* x x))</pre>
    <p>Podemos entender esto de la siguiente manera:</p>
    <pre>(define (square x) (* x x))</pre>
    <p>Para elevar algo al cuadrado, multiplícalo por sí mismo.</p>
    <p>Aquí tenemos un procedimiento compuesto, al que se le ha dado el nombre <code>square</code>. El procedimiento representa la operación de multiplicar algo por sí mismo. La cosa a multiplicar recibe un nombre local, <code>x</code>, que juega el mismo papel que un pronombre en el lenguaje natural. Evaluar la definición crea este procedimiento compuesto y lo asocia con el nombre <code>square</code>.</p>
    
    <h4>Forma General de una Definición de Procedimiento</h4>
    <p>La forma general de una definición de procedimiento es:</p>
    <pre>(define (⟨nombre⟩ ⟨parámetros formales⟩) ⟨cuerpo⟩)</pre>
    <p>El <code>⟨nombre⟩</code> es un símbolo que se asocia con la definición del procedimiento en el entorno. Los <code>⟨parámetros formales⟩</code> son los nombres que se utilizan dentro del cuerpo del procedimiento para referirse a los argumentos correspondientes del procedimiento. El <code>⟨cuerpo⟩</code> es una expresión que devolverá el valor de la aplicación del procedimiento cuando los parámetros formales sean reemplazados por los argumentos reales a los que se aplica el procedimiento. El <code>⟨nombre⟩</code> y los <code>⟨parámetros formales⟩</code> se agrupan entre paréntesis, tal como lo estarían en una llamada real al procedimiento que se está definiendo.</p>
    
    <h4>Uso de la Función</h4>
    <p>Después de haber definido <code>square</code>, ahora podemos usarla:</p>
    <pre>(square 21)</pre>
    <p>Resultado: <strong>441</strong></p>
    <pre>(square (+ 2 5))</pre>
    <p>Resultado: <strong>49</strong></p>
    <pre>(square (square 3))</pre>
    <p>Resultado: <strong>81</strong></p>
    
    <h4>Usando Procedimientos como Bloques de Construcción</h4>
    <p>También podemos usar <code>square</code> como un bloque de construcción para definir otros procedimientos. Por ejemplo, <code>x<sup>2</sup> + y<sup>2</sup></code> se puede expresar como:</p>
    <pre>(+ (square x) (square y))</pre>
    
    <p>Podemos definir fácilmente un procedimiento <code>sum-of-squares</code> que, dado dos números como argumentos, produzca la suma de sus cuadrados:</p>
    <pre>(define (sum-of-squares x y) (+ (square x) (square y)))</pre>
    <pre>(sum-of-squares 3 4)</pre>
    <p>Resultado: <strong>25</strong></p>
    
    <p>Ahora podemos usar <code>sum-of-squares</code> como un bloque de construcción en la creación de otros procedimientos:</p>
    <pre>(define (f a) (sum-of-squares (+ a 1) (* a 2)))</pre>
    <pre>(f 5)</pre>
    <p>Resultado: <strong>136</strong></p>
