<h2>Estadísticas de Orden</h2>

<p>
  La <strong>i-ésima estadística de orden</strong> de un conjunto de \( n \) números es el i-ésimo número más pequeño del conjunto.
</p>

<p>
  Podemos, por supuesto, seleccionar la i-ésima estadística de orden ordenando la entrada e indexando el i-ésimo elemento de la salida. Sin asumir nada sobre la distribución de la entrada, este método se ejecuta en tiempo \( \Omega(n \log n) \), como muestra la cota inferior demostrada en el Capítulo 8.
</p>

<p>
  En el Capítulo 9, mostramos que podemos encontrar el i-ésimo elemento más pequeño en tiempo \( O(n) \), incluso cuando los elementos son números reales arbitrarios.
</p>

<p>
  Presentamos un algoritmo aleatorizado con pseudocódigo ajustado que se ejecuta en tiempo \( \Theta(n^2) \) en el peor caso, pero cuyo tiempo de ejecución esperado es \( O(n) \). También proporcionamos un algoritmo más complicado que se ejecuta en tiempo \( O(n) \) en el peor caso.
</p>

