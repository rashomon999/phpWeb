    <h2>Introducción</h2>

<p>
  Esta sección presenta varios algoritmos que resuelven el siguiente problema de ordenamiento:
</p>

<p>
  <strong>Entrada:</strong> Una secuencia de \( n \) números \( \langle a_1, a_2, \dots, a_n \rangle \).<br>
  <strong>Salida:</strong> Una permutación (reordenamiento) \( \langle a'_1, a'_2, \dots, a'_n \rangle \) de la secuencia de entrada tal que \( a'_1 \leq a'_2 \leq \dots \leq a'_n \).
</p>

<p>
  La secuencia de entrada usualmente es un arreglo de \( n \) elementos, aunque puede representarse de alguna otra manera, como una lista enlazada.
</p>

<h3>La estructura de los datos</h3>

<p>
  En la práctica, los números a ordenar rara vez son valores aislados. Cada uno usualmente forma parte de una colección de datos llamada un <strong>registro</strong>. 
  Cada registro contiene una <strong>clave</strong>, que es el valor que se va a ordenar. El resto del registro consiste en <strong>datos satélite</strong>, que usualmente se 
  transportan junto con la clave. En la práctica, cuando un algoritmo de ordenamiento permuta las claves, también debe permutar los datos satélite.
</p>

<p>
  Si cada registro incluye una gran cantidad de datos satélite, a menudo permutamos un arreglo de punteros a los registros en lugar de los registros mismos, 
  con el fin de minimizar el movimiento de datos.
</p>

<p>
  En cierto sentido, son estos detalles de implementación los que distinguen un algoritmo de un programa completo. Un algoritmo de ordenamiento describe el 
  método por el cual determinamos el orden ordenado, sin importar si estamos ordenando números individuales o registros grandes que contienen muchos bytes de datos satélite.
</p>

<p>
  Por lo tanto, al enfocarnos en el problema del ordenamiento, típicamente asumimos que la entrada consiste únicamente en números.
</p>

<p>
  Traducir un algoritmo para ordenar números en un programa para ordenar registros es conceptualmente sencillo, aunque en una situación de ingeniería concreta 
  otras sutilezas pueden hacer que la tarea de programación real sea un desafío.
</p>

    
<h2>¿Por qué ordenar?</h2>

<p>
  Muchos científicos de la computación consideran el ordenamiento como el problema más fundamental en el estudio de los algoritmos. Hay varias razones:
</p>

<ul>
  <li>
    A veces una aplicación necesita inherentemente ordenar información. Por ejemplo, para preparar los estados de cuenta de los clientes, los bancos necesitan ordenar los cheques por número de cheque.
  </li>
  <li>
    Los algoritmos a menudo usan el ordenamiento como una subrutina clave. Por ejemplo, un programa que representa objetos gráficos que se superponen unos sobre otros podría tener que ordenar los objetos de acuerdo con una relación de “estar encima” para poder dibujarlos de abajo hacia arriba. Veremos numerosos algoritmos en este texto que usan el ordenamiento como subrutina.
  </li>
  <li>
    Podemos elegir entre una gran variedad de algoritmos de ordenamiento, y emplean un conjunto rico de técnicas. De hecho, muchas técnicas importantes que se utilizan a lo largo del diseño de algoritmos aparecen en el cuerpo de los algoritmos de ordenamiento que se han desarrollado a lo largo de los años. De esta manera, el ordenamiento también es un problema de interés histórico.
  </li>
  <li>
    Podemos demostrar una cota inferior no trivial para el ordenamiento (como lo haremos en el Capítulo 8). Nuestras mejores cotas superiores coinciden asintóticamente con la cota inferior, por lo que sabemos que nuestros algoritmos de ordenamiento son asintóticamente óptimos. Además, podemos usar la cota inferior del ordenamiento para demostrar cotas inferiores para ciertos otros problemas.
  </li>
  <li>
    Muchos problemas de ingeniería surgen al implementar algoritmos de ordenamiento. El programa de ordenamiento más rápido para una situación particular puede depender de muchos factores, como el conocimiento previo sobre las claves y los datos satélite, la jerarquía de memoria (cachés y memoria virtual) del computador anfitrión y el entorno del software. Muchos de estos temas se manejan mejor a nivel algorítmico, en lugar de simplemente “ajustar” el código.
  </li>
</ul>








                
<h2>Algoritmos de Ordenamiento</h2>

<p>
  Introdujimos dos algoritmos que ordenan \( n \) números reales en el Capítulo 2. El ordenamiento por inserción toma \( \Theta(n^2) \) tiempo en el peor caso. Sin embargo, como sus bucles internos son estrechos, es un algoritmo rápido de ordenamiento en <strong>el lugar</strong> para entradas pequeñas. (Recuerda que un algoritmo de ordenamiento ordena en sitio si solo un número constante de elementos del arreglo de entrada se almacenan fuera del arreglo).
</p>

<p>
  El ordenamiento por mezcla (merge sort) tiene un mejor tiempo de ejecución asintótico, \( \Theta(n \log n) \), pero el procedimiento <code>MERGE</code> que utiliza no opera en sitio.
</p>

<p>
  En esta parte, presentaremos dos algoritmos más que ordenan números reales arbitrarios. <strong>Heapsort</strong>, presentado en el Capítulo 6, ordena \( n \) números en sitio en tiempo \( O(n \log n) \). Utiliza una estructura de datos importante llamada <em>heap</em>, con la cual también podemos implementar una cola de prioridad.
</p>

<p>
  <strong>Quicksort</strong>, en el Capítulo 7, también ordena \( n \) números en sitio, pero su tiempo de ejecución en el peor caso es \( \Theta(n^2) \). Sin embargo, su tiempo de ejecución esperado es \( \Theta(n \log n) \), y generalmente supera a heapsort en la práctica. Al igual que el ordenamiento por inserción, quicksort tiene un código compacto, por lo que el factor constante oculto en su tiempo de ejecución es pequeño. Es un algoritmo popular para ordenar arreglos grandes.
</p>

<p>
  El ordenamiento por inserción, merge sort, heapsort y quicksort son todos <strong>ordenamientos por comparación</strong>: determinan el orden ordenado de un arreglo de entrada comparando elementos.
</p>

<p>
  El Capítulo 8 comienza introduciendo el <em>modelo de árbol de decisión</em> para estudiar las limitaciones de rendimiento de los ordenamientos por comparación. Usando este modelo, probamos una cota inferior de \( \Omega(n \log n) \) en el tiempo de ejecución en el peor caso de cualquier ordenamiento por comparación sobre \( n \) entradas, mostrando así que heapsort y merge sort son ordenamientos por comparación óptimos asintóticamente.
</p>

<p>
  Luego, el Capítulo 8 muestra que podemos superar esta cota inferior de \( \Omega(n \log n) \) si podemos obtener información sobre el orden de entrada por medios distintos a comparar elementos. Por ejemplo, el algoritmo de <strong>ordenamiento por conteo (counting sort)</strong> asume que los números de entrada están en el conjunto \( \{0, 1, \ldots, k\} \). Usando indexación de arreglos como herramienta para determinar el orden relativo, counting sort puede ordenar \( n \) números en tiempo \( \Theta(k + n) \). Así, cuando \( k = O(n) \), counting sort se ejecuta en tiempo lineal respecto al tamaño del arreglo de entrada.
</p>

<p>
  Un algoritmo relacionado, <strong>radix sort</strong>, puede usarse para extender el rango de counting sort. Si hay \( n \) enteros para ordenar, cada uno con \( d \) dígitos, y cada dígito puede tomar hasta \( k \) valores posibles, entonces radix sort puede ordenar los números en tiempo \( \Theta(d(n + k)) \). Cuando \( d \) es constante y \( k = O(n) \), radix sort se ejecuta en tiempo lineal.
</p>

<p>
  Un tercer algoritmo, <strong>bucket sort</strong>, requiere conocimiento de la distribución probabilística de los números en el arreglo de entrada. Puede ordenar \( n \) números reales distribuidos uniformemente en el intervalo semiabierto \( [0, 1) \) en tiempo promedio \( O(n) \).
</p>

<p>
  La siguiente tabla resume los tiempos de ejecución de los algoritmos de ordenamiento de los Capítulos 2 y 6–8. Como de costumbre, \( n \) denota el número de elementos a ordenar. Para counting sort, los elementos a ordenar son enteros en el conjunto \( \{0, 1, \ldots, k\} \). Para radix sort, cada elemento es un número de \( d \) dígitos, donde cada dígito toma \( k \) valores posibles. Para bucket sort, asumimos que las claves son números reales distribuidos uniformemente en el intervalo semiabierto \( [0, 1) \). La columna de la derecha proporciona el tiempo de ejecución promedio o esperado, indicando cuál de ellos proporciona cuando difiere del tiempo del peor caso. Omitimos el tiempo promedio de heapsort porque no lo analizamos en este libro.
</p>
