<h2>Capítulo 6: Heapsort</h2>

<p>
  En este capítulo, introducimos otro algoritmo de ordenamiento: <strong>heapsort</strong>.
  Al igual que <em>merge sort</em>, pero a diferencia de <em>insertion sort</em>, el tiempo de ejecución de heapsort es \( O(n \log n) \).
  Al igual que <em>insertion sort</em>, pero a diferencia de <em>merge sort</em>, heapsort ordena en <strong>el lugar:</strong> 
  solo un número constante de elementos del arreglo se almacenan fuera del mismo en cualquier momento.
  Así, heapsort combina los mejores atributos de los dos algoritmos de ordenamiento que ya hemos discutido.
</p>

<p>
  Heapsort también introduce otra técnica de diseño de algoritmos: el uso de una estructura de datos,
  en este caso, una que llamamos <strong>heap</strong>, para gestionar información. La estructura de datos heap
  no solo es útil para heapsort, sino que también constituye una cola de prioridad eficiente.
  La estructura heap volverá a aparecer en algoritmos de capítulos posteriores.
</p>

<p>
  El término “heap” fue acuñado originalmente en el contexto de heapsort, pero desde entonces ha llegado a referirse
  a “almacenamiento recolectado por el recolector de basura”, como el que proporcionan lenguajes de programación como
  Java y Lisp. Nuestra estructura de datos heap <strong>no es</strong> almacenamiento recolectado por el recolector de basura,
  y cada vez que nos refiramos a heaps en este libro, nos referiremos a una estructura de datos y no a un aspecto de
  la recolección de basura.
</p>

<h3>6.1 Heaps</h3>

<p>
  La estructura de datos <strong>heap binario</strong> es un objeto arreglo que podemos ver como un árbol binario
  casi completo (ver Sección B.5.3), como se muestra en la Figura 6.1.
</p>

<p>
  Cada nodo del árbol corresponde a un elemento del arreglo. El árbol está completamente lleno en todos los niveles,
  excepto posiblemente el más bajo, que se llena de izquierda a derecha hasta cierto punto.
</p>

<p>
  Un arreglo \( A \) que representa un heap es un objeto con dos atributos:
</p>

<ul>
  <li><code>A.length</code>: que (como de costumbre) da el número de elementos del arreglo.</li>
  <li><code>A.heap-size</code>: que representa cuántos elementos del heap están almacenados dentro del arreglo \( A \).</li>
</ul>

<p>
  Es decir, aunque \( A[1 \dots A.\text{length}] \) puede contener números, solo los elementos en \( A[1 \dots A.\text{heap-size}] \),
  donde \( 0 \leq A.\text{heap-size} \leq A.\text{length} \), son elementos válidos del heap.
</p>

<p>
  La raíz del árbol es \( A[1] \), y dado el índice \( i \) de un nodo, podemos calcular fácilmente los índices de su padre, hijo izquierdo e hijo derecho:
</p>

<pre>
PARENT(i)
    return ⌊i/2⌋

LEFT(i)
    return 2i

RIGHT(i)
    return 2i + 1
</pre>

<img src="../../../img/heaps.png" alt="">

<p>
 

  <strong>Figura 6.1</strong>: Un max-heap visto como (a) un árbol binario y (b) un arreglo. El número dentro del círculo
  en cada nodo del árbol es el valor almacenado en ese nodo. El número sobre un nodo es el índice correspondiente en el arreglo.
  Arriba y abajo del arreglo hay líneas que muestran las relaciones padre-hijo; los padres siempre están a la izquierda de sus hijos.
  El árbol tiene altura tres; el nodo en el índice 4 (con valor 8) tiene altura uno.
</p>

  

    <p>En la mayoría de las computadoras, el procedimiento <strong>LEFT</strong> puede calcular \(2i\) en una sola instrucción simplemente desplazando la representación binaria de \(i\) un bit hacia la izquierda. De manera similar, el procedimiento <strong>RIGHT</strong> puede calcular rápidamente \(2i + 1\) desplazando la representación binaria de \(i\) un bit hacia la izquierda y luego agregando un 1 como bit menos significativo. El procedimiento <strong>PARENT</strong> puede calcular \(\lfloor i/2 \rfloor\) desplazando \(i\) un bit hacia la derecha. Las buenas implementaciones de heapsort a menudo implementan estos procedimientos como “macros” o procedimientos “en línea”.</p>

<p>Existen dos tipos de montículos binarios: <strong>max-heaps</strong> y <strong>min-heaps</strong>. En ambos tipos, los valores en los nodos satisfacen una propiedad de montículo, cuyos detalles dependen del tipo de montículo.</p>

<p>En un <strong>max-heap</strong>, la propiedad de max-heap es que para cada nodo \(i\) que no sea la raíz, se cumple que:</p>

<p>\[
A[\text{PARENT}(i)] \geq A[i]
\]</p>

<p>Es decir, el valor de un nodo es, como máximo, el valor de su padre. Por tanto, el elemento más grande en un max-heap se encuentra en la raíz, y el subárbol enraizado en un nodo contiene valores no mayores que los contenidos en ese nodo.</p>

<p>Un <strong>min-heap</strong> está organizado de forma opuesta; la propiedad de min-heap es que para cada nodo \(i\) que no sea la raíz, se cumple que:</p>

<p>\[
A[\text{PARENT}(i)] \leq A[i]
\]</p>

<p>El elemento más pequeño en un min-heap está en la raíz.</p>

<p>Para el algoritmo <strong>heapsort</strong>, usamos <em>max-heaps</em>. Los <em>min-heaps</em> se usan comúnmente para implementar colas de prioridad, las cuales se discuten en la Sección 6.5. Seremos precisos al especificar si se necesita un max-heap o un min-heap para una aplicación particular, y cuando las propiedades apliquen a ambos, simplemente usaremos el término <strong>“heap”</strong>.</p>

<p>Viendo un heap como un árbol, definimos la <strong>altura</strong> de un nodo como el número de aristas en el camino descendente más largo y simple desde el nodo hasta una hoja, y definimos la altura del heap como la altura de su raíz. Dado que un heap de \(n\) elementos está basado en un árbol binario completo, su altura es \(\Theta(\lg n)\) (ver el Ejercicio 6.1-2).</p>

<p>Veremos que las operaciones básicas sobre montículos se ejecutan en un tiempo a lo sumo proporcional a la altura del árbol, y por tanto toman tiempo \(O(\lg n)\). El resto de este capítulo presenta algunos procedimientos básicos y muestra cómo se usan en un algoritmo de ordenamiento y en una estructura de datos de cola de prioridad.</p>

<ul>
  <li>El procedimiento <strong>MAX-HEAPIFY</strong>, que se ejecuta en tiempo \(O(\lg n)\), es la clave para mantener la propiedad de max-heap.</li>
  <li>El procedimiento <strong>BUILD-MAX-HEAP</strong>, que se ejecuta en tiempo lineal, produce un max-heap a partir de un arreglo de entrada desordenado.</li>
  <li>El procedimiento <strong>HEAPSORT</strong>, que se ejecuta en tiempo \(O(n \lg n)\), ordena un arreglo <em>en el lugar</em>.</li>
  <li>Los procedimientos <strong>MAX-HEAP-INSERT</strong>, <strong>HEAP-EXTRACT-MAX</strong>, <strong>HEAP-INCREASE-KEY</strong> y <strong>HEAP-MAXIMUM</strong>, que se ejecutan en tiempo \(O(\lg n)\), permiten que la estructura de datos heap implemente una cola de prioridad.</li>
</ul>


