<h3>Terminación:</h3>
<p>
Al terminar, \( i = 0 \). Por el invariante del ciclo, cada nodo \( 1, 2, \ldots, n \) es la raíz de un max-heap. En particular, el nodo 1 lo es.
</p>

<p>
Podemos calcular una cota superior simple para el tiempo de ejecución de <code>BUILD-MAX-HEAP</code> de la siguiente forma.
Cada llamada a <code>MAX-HEAPIFY</code> cuesta \( O(\log n) \), y <code>BUILD-MAX-HEAP</code> realiza \( O(n) \) llamadas.
Por lo tanto, el tiempo de ejecución es \( O(n \log n) \). Esta cota superior, aunque correcta, no es ajustada asintóticamente.
</p>

<p>
Podemos derivar una cota más precisa observando que el tiempo que tarda <code>MAX-HEAPIFY</code> en ejecutarse en un nodo varía
según la altura del nodo en el árbol, y que la mayoría de los nodos tienen alturas pequeñas. Nuestro análisis más ajustado se basa
en las siguientes propiedades: un heap con \( n \) elementos tiene altura \( \lfloor \log n \rfloor \) (ver ejercicio 6.1-2) y a lo sumo 
\( \left\lfloor \dfrac{n}{2^{h+1}} \right\rfloor \) nodos de altura \( h \) (ver ejercicio 6.3-3).
</p>

<p>
El tiempo requerido por <code>MAX-HEAPIFY</code> cuando se invoca sobre un nodo de altura \( h \) es \( O(h) \),
por lo que podemos expresar el coste total de <code>BUILD-MAX-HEAP</code> como acotado superiormente por:
</p>

