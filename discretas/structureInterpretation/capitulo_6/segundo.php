<h2>6.2 Mantenimiento de la propiedad de montículo</h2>

<p>En orden de mantener la propiedad de max-heap, llamamos al procedimiento <strong>MAX-HEAPIFY</strong>. Sus entradas son un arreglo <code>A</code> y un índice <code>i</code> dentro del arreglo. Cuando se llama, <strong>MAX-HEAPIFY</strong> asume que los subárboles binarios enraizados en <code>LEFT(i)</code> y <code>RIGHT(i)</code> son max-heaps, pero que <code>A[i]</code> podría ser menor que sus hijos, violando así la propiedad de max-heap. <strong>MAX-HEAPIFY</strong> deja que el valor en <code>A[i]</code> "descienda" en el max-heap de forma que el subárbol enraizado en el índice <code>i</code> obedezca la propiedad de max-heap.</p>

<pre>
MAX-HEAPIFY(A, i)
1   l ← LEFT(i)
2   r ← RIGHT(i)
3   if l ≤ A.heap-size and A[l] > A[i]
4       largest ← l
5   else largest ← i
6   if r ≤ A.heap-size and A[r] > A[largest]
7       largest ← r
8   if largest ≠ i
9       intercambiar A[i] con A[largest]
10      MAX-HEAPIFY(A, largest)
</pre>

<p>La Figura 6.2 ilustra la acción de <code>MAX-HEAPIFY</code>. En cada paso, se determina el mayor de los elementos <code>A[i]</code>, <code>A[LEFT(i)]</code> y <code>A[RIGHT(i)]</code>, y su índice se guarda en <code>largest</code>. Si <code>A[i]</code> es el mayor, entonces el subárbol enraizado en el nodo <code>i</code> ya es un max-heap y el procedimiento termina. De lo contrario, uno de los dos hijos tiene el mayor elemento, y <code>A[i]</code> se intercambia con <code>A[largest]</code>, lo que hace que el nodo <code>i</code> y sus hijos cumplan con la propiedad de max-heap. Sin embargo, el nodo indexado por <code>largest</code> ahora tiene el valor original de <code>A[i]</code>, y por tanto el subárbol enraizado en <code>largest</code> podría violar la propiedad de max-heap. En consecuencia, llamamos recursivamente a <code>MAX-HEAPIFY</code> en ese subárbol.</p>
<img src="../../../img/heap_2.png" alt="">
<p>La Figura 6.2 muestra la acción de <code>MAX-HEAPIFY(A, 2)</code>, donde <code>A.heap-size = 10</code>. (a) La configuración inicial, con <code>A[2]</code> en el nodo <code>i = 2</code> que viola la propiedad de max-heap ya que no es mayor que ambos hijos. La propiedad de max-heap se restablece para el nodo 2 en (b) al intercambiar <code>A[2]</code> con <code>A[4]</code>, lo que destruye la propiedad de max-heap para el nodo 4. La llamada recursiva <code>MAX-HEAPIFY(A, 4)</code> ahora tiene <code>i = 4</code>. Después de intercambiar <code>A[4]</code> con <code>A[9]</code>, como se muestra en (c), el nodo 4 queda corregido, y la llamada recursiva <code>MAX-HEAPIFY(A, 9)</code> no produce más cambios en la estructura de datos.</p>

<p>El tiempo de ejecución de <code>MAX-HEAPIFY</code> en un subárbol de tamaño \(n\) enraizado en un nodo dado \(i\) es el tiempo \(\Theta(1)\) necesario para ajustar las relaciones entre los elementos \(A[i]\), \(A[\text{LEFT}(i)]\) y \(A[\text{RIGHT}(i)]\), más el tiempo para ejecutar <code>MAX-HEAPIFY</code> en un subárbol enraizado en uno de los hijos del nodo \(i\) (suponiendo que la llamada recursiva ocurra). Los subárboles de los hijos tienen cada uno tamaño a lo sumo \(2n/3\)—el peor caso ocurre cuando el nivel inferior del árbol está exactamente lleno a la mitad—y por tanto podemos describir el tiempo de ejecución de <code>MAX-HEAPIFY</code> mediante la recurrencia:</p>

<p>\[
T(n) = T(2n/3) + \Theta(1)
\]</p>

<p>La solución a esta recurrencia, según el caso 2 del teorema maestro (Teorema 4.1), es:</p>

<p>\[
T(n) = O(\log n)
\]</p>

<p>Alternativamente, podemos caracterizar el tiempo de ejecución de <code>MAX-HEAPIFY</code> en un nodo de altura \(h\) como \(O(h)\).</p>

