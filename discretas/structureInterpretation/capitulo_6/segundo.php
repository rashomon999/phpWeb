    <h2>6.3 Construyendo un heap</h2>

<p>
Podemos usar el procedimiento <code>MAX-HEAPIFY</code> de forma ascendente (de abajo hacia arriba) para convertir un arreglo 
\( A[1 \ldots n] \), donde \( n = A.\text{length} \), en un max-heap. Por el ejercicio 6.1-7, los elementos en el subarreglo 
\( A[\lfloor n/2 \rfloor + 1 \ldots n] \) son todas hojas del árbol, y por tanto, cada una es un heap de un solo elemento al comienzo.
El procedimiento <code>BUILD-MAX-HEAP</code> recorre los nodos restantes del árbol y ejecuta <code>MAX-HEAPIFY</code> sobre cada uno.
</p>

<pre>
BUILD-MAX-HEAP(A)
1 A.heap-size = A.length
2 for i = ⌊A.length / 2⌋ downto 1
3     MAX-HEAPIFY(A, i)
</pre>

<p>
La Figura 6.3 muestra un ejemplo de la acción de <code>BUILD-MAX-HEAP</code>.
</p>

<p>
Para mostrar por qué <code>BUILD-MAX-HEAP</code> funciona correctamente, usamos el siguiente invariante del ciclo:
</p>

<p><strong>
Al inicio de cada iteración del bucle <code>for</code> de las líneas 2–3, cada nodo 
\( i+1, i+2, \ldots, n \) es la raíz de un max-heap.
</strong></p>

<p>
Necesitamos demostrar que este invariante es verdadero antes de la primera iteración del ciclo, que cada iteración mantiene el invariante,
y que el invariante proporciona una propiedad útil para demostrar la corrección al finalizar el ciclo.
</p>

<h3>Inicialización:</h3>
<p>
Antes de la primera iteración del ciclo, \( i = \lfloor n/2 \rfloor \). Cada nodo 
\( \lfloor n/2 \rfloor + 1, \lfloor n/2 \rfloor + 2, \ldots, n \) es una hoja y por tanto la raíz de un max-heap trivial.
</p>

<h3>Mantenimiento:</h3>
<p>
Para ver que cada iteración mantiene el invariante, observamos que los hijos del nodo \( i \) tienen índices mayores que \( i \).
Por tanto, por el invariante del ciclo, ellos son raíces de max-heaps. Esta es precisamente la condición requerida para que 
<code>MAX-HEAPIFY(A, i)</code> convierta al nodo \( i \) en la raíz de un max-heap. Además, la llamada a <code>MAX-HEAPIFY</code> preserva la
propiedad de que los nodos \( i+1, i+2, \ldots, n \) son todos raíces de max-heaps. Decrementar \( i \) en la actualización del ciclo 
<code>for</code> restablece el invariante para la siguiente iteración.
</p>
 
