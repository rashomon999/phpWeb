\sum_{h=0}^{\lfloor \log n \rfloor} \left\lceil \frac{n}{2^{h+1}} \right\rceil O(h) = O\left(n \sum_{h=0}^{\lfloor \log n \rfloor} \frac{h}{2^h} \right).
\]
</p>

<p>
Evaluamos esta última suma sustituyendo \( x = \frac{1}{2} \) en la fórmula (A.8), obteniendo:
</p>

<p>
\[
\sum_{h=0}^{\infty} \frac{h}{2^h} = \frac{1/2}{(1 - 1/2)^2} = 2.
\]
</p>

<p>
Por lo tanto, podemos acotar el tiempo de ejecución de <code>BUILD-MAX-HEAP</code> como:
</p>

<p>
\[
O\left(n \sum_{h=0}^{\lfloor \log n \rfloor} \frac{h}{2^h} \right) = O\left(n \sum_{h=0}^{\infty} \frac{h}{2^h} \right) = O(n).
\]
</p>

<p>
Por lo tanto, podemos construir un max-heap a partir de un arreglo desordenado en tiempo lineal.
</p>

