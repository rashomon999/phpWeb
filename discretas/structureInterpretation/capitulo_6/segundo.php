    <h4>Parentesis explicativo:</h4>
    <h2> ¿Qué hace exactamente <code>MAX-HEAPIFY</code>?</h2>
  <p>
    <code>MAX-HEAPIFY</code> no ordena todo el arreglo, sino que restaura la propiedad de max-heap en un subárbol cuya raíz está en el índice 
    \( i \).
  </p>

  <h2> ¿Qué significa restaurar la propiedad de max-heap?</h2>
  <p>
    En un <em>max-heap</em>, cada nodo debe ser <strong>mayor o igual que sus hijos</strong>. Si en cierto momento un nodo viola esta regla
    (por ejemplo, es menor que uno de sus hijos), entonces <code>MAX-HEAPIFY</code> lo <em>"empuja hacia abajo"</em> en el árbol intercambiándolo con su hijo mayor, y repite el proceso recursivamente hasta que el subárbol quede bien formado.
  </p>

  <h2> ¿Organiza? Sí, pero solo parcialmente:</h2>
  <ul>
    <li>Solo reorganiza los elementos en el subárbol cuyo nodo raíz es el índice \( i \).</li>
    <li>No ordena todo el arreglo (como lo haría <code>HEAPSORT</code> completo).</li>
    <li>Es una operación local que <strong>prepara el camino</strong> para algoritmos como <code>BUILD-MAX-HEAP</code> y <code>HEAPSORT</code>.</li>
  </ul>

