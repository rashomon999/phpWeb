el rompecabezas de Reve,  
<sup>∗</sup> propuesto en 1907 por Henry Dudeney en su libro <em>The Canterbury Puzzles</em>.</p>

<p>El rompecabezas de Reve involucra peregrinos desafiados por Reve a mover una pila de ruedas de queso  
de diferentes tamaños desde el primer taburete de cuatro a otro taburete sin colocar jamás una rueda de queso  
sobre otra de diámetro menor. El rompecabezas de Reve, expresado en términos de clavijas y discos, sigue las  
mismas reglas que el rompecabezas de la Torre de Hanoi, excepto que se usan cuatro clavijas. De forma similar, podemos generalizar el rompecabezas de la Torre de Hanoi cuando hay <em>p</em> clavijas, donde <em>p</em> es un entero mayor que tres.</p>

<p>Puede que te sorprenda que nadie haya podido establecer el número mínimo de movimientos  
necesarios para resolver la generalización de este rompecabezas para <em>p</em> clavijas. (Nota que se han publicado algunas afirmaciones de que este problema ha sido resuelto, pero no son aceptadas por expertos.) Sin embargo, en 2014 Thierry Bousch demostró que el número mínimo de movimientos necesarios cuando hay cuatro clavijas es igual al número de movimientos usados por un algoritmo inventado por Frame y Stewart en 1939. (Ver Ejercicios 38–45 y [St94] y [Bo14] para más información.)</p>

<p>El Ejemplo 3 ilustra cómo las relaciones de recurrencia pueden usarse para contar cadenas binarias de una longitud especificada que tienen una cierta propiedad.</p>

<p><strong>EJEMPLO 3</strong> Encuentra una relación de recurrencia y da las condiciones iniciales para el número de cadenas binarias de longitud <em>n</em> que no tienen dos ceros consecutivos. ¿Cuántas cadenas binarias así hay de longitud cinco?</p>

<p><strong>Solución:</strong> Sea \(a_n\) el número de cadenas binarias de longitud \(n\) que no tienen dos ceros consecutivos.  
Asumimos que \(n \geq 3\), de modo que la cadena binaria tiene al menos tres bits. Las cadenas de este tipo y longitud \(n\)  
pueden dividirse en aquellas que terminan en 1 y aquellas que terminan en 0. Las cadenas binarias de longitud \(n\) que terminan  
en 1 y que no tienen dos ceros consecutivos son precisamente las cadenas binarias de longitud \(n-1\) sin dos ceros consecutivos  
a las que se les añade un 1 al final. En consecuencia, hay \(a_{n-1}\) tales cadenas.</p>

<p>Las cadenas binarias de longitud \(n\) que terminan en 0 y que no tienen dos ceros consecutivos deben tener  
un 1 como penúltimo bit (posición \(n-1\)); de otro modo terminarían con un par de ceros. Por tanto, las cadenas binarias  
de longitud \(n\) que terminan en 0 y no tienen dos ceros consecutivos son precisamente las cadenas binarias  
de longitud \(n-2\) sin dos ceros consecutivos a las que se les añade "10" al final. En consecuencia, hay \(a_{n-2}\)  
tales cadenas.</p>

<p>Concluimos, como se ilustra en la Figura 4, que</p>

<p style="text-align: center; font-size: 1.2em;">
\( a_n = a_{n-1} + a_{n-2} \quad \text{para} \quad n \geq 3 \).
</p>

    
<p>Las condiciones iniciales son \(a_1 = 2\), porque ambas cadenas binarias de longitud uno, 0 y 1, no tienen ceros consecutivos, y \(a_2 = 3\), porque las cadenas binarias válidas de longitud dos son 01, 10 y 11. Para obtener \(a_5\), usamos la relación de recurrencia tres veces para encontrar que</p>

<p style="text-align: center;">
\( a_3 = a_2 + a_1 = 3 + 2 = 5, \) <br/>
\( a_4 = a_3 + a_2 = 5 + 3 = 8, \) <br/>
\( a_5 = a_4 + a_3 = 8 + 5 = 13. \quad \blacksquare \)
</p>

<p><strong>Observación:</strong> Nota que \(\{a_n\}\) satisface la misma relación de recurrencia que la secuencia de Fibonacci. Debido a que \(a_1 = f_3\) y \(a_2 = f_4\), se sigue que \(a_n = f_{n+2}\).</p>

<p>El Ejemplo 4 muestra cómo una relación de recurrencia puede usarse para modelar el número de códigos válidos que cumplen ciertas comprobaciones.</p>

<p><strong>EJEMPLO 4 Enumeración de códigos:</strong> Un sistema informático considera válida una cadena de dígitos decimales si contiene un número par de dígitos 0. Por ejemplo, 1230407869 es válido, mientras que 120987045608 no lo es. Sea \(a_n\) el número de códigos válidos de \(n\) dígitos. Encuentra una relación de recurrencia para \(a_n\).</p>

<p><strong>Solución:</strong> Nota que \(a_1 = 9\) porque hay 10 cadenas de un dígito, y sólo una, la cadena "0", no es válida. Se puede derivar una relación de recurrencia para esta secuencia considerando cómo se puede obtener una cadena válida de \(n\) dígitos a partir de cadenas de \(n-1\) dígitos. Hay dos formas de formar una cadena válida de \(n\) dígitos a partir de una cadena con un dígito menos.</p>

<p>Primero, una cadena válida de \(n\) dígitos puede obtenerse añadiendo un dígito distinto de 0 al final de una cadena válida de \(n-1\) dígitos. Esta adición puede hacerse de nueve maneras. Por lo tanto, una cadena válida de \(n\) dígitos puede formarse así de \(9 a_{n-1}\) maneras.</p>

<p>Segundo, una cadena válida de \(n\) dígitos puede obtenerse añadiendo un 0 al final de una cadena de longitud \(n-1\) que no sea válida (esto produce una cadena con un número par de ceros porque la cadena inválida de longitud \(n-1\) tiene un número impar de ceros). El número de formas de hacer esto es igual al número de cadenas inválidas de longitud \(n-1\). Como hay \(10^{n-1}\) cadenas de longitud \(n-1\), y \(a_{n-1}\) son válidas, hay \(10^{n-1} - a_{n-1}\) cadenas válidas de longitud \(n\) que se obtienen añadiendo un 0 a una cadena inválida de longitud \(n-1\).</p>

<p>Dado que todas las cadenas válidas de longitud \(n\) se producen de una de estas dos maneras, se sigue que</p>

<p style="text-align: center;">
\[
a_n = 9a_{n-1} + \left(10^{n-1} - a_{n-1}\right) = 8a_{n-1} + 10^{n-1}
\]
</p>

<p><strong>EJEMPLO 5</strong> Establece una relación de recurrencia para \(C_n\), el número de formas de parentizar el producto de \(n+1\) números, \(x_0 \cdot x_1 \cdot x_2 \cdots x_n\), para especificar el orden de multiplicación. Por ejemplo, \(C_3 = 5\) porque hay cinco formas de parentizar \(x_0 \cdot x_1 \cdot x_2 \cdot x_3\) para determinar el orden de multiplicación:</p>

<ul>
<li>\(((x_0 \cdot x_1) \cdot x_2) \cdot x_3\)</li>
<li>\((x_0 \cdot (x_1 \cdot x_2)) \cdot x_3\)</li>
<li>\((x_0 \cdot x_1) \cdot (x_2 \cdot x_3)\)</li>
<li>\(x_0 \cdot ((x_1 \cdot x_2) \cdot x_3)\)</li>
<li>\(x_0 \cdot (x_1 \cdot (x_2 \cdot x_3))\)</li>
</ul>

<p><strong>Solución:</strong> Para desarrollar una relación de recurrencia para \(C_n\), notamos que como insertemos los paréntesis en el producto \(x_0 \cdot x_1 \cdot x_2 \cdots x_n\), un operador “\(\cdot\)” permanece fuera de todos los paréntesis, es decir, el operador de la multiplicación final que se realiza. [Por ejemplo, en \((x_0 \cdot (x_1 \cdot x_2)) \cdot x_3\), es el operador “\(\cdot\)” final, mientras que en \((x_0 \cdot x_1) \cdot (x_2 \cdot x_3)\) es el segundo “\(\cdot\)”.] Este operador final aparece entre dos de los \(n+1\) números, digamos \(x_k\) y \(x_{k+1}\). Hay \(C_k C_{n-k-1}\) formas de insertar paréntesis para determinar el orden de multiplicación cuando el operador final está entre \(x_k\) y \(x_{k+1}\), porque hay \(C_k\) formas de insertar paréntesis en el producto \(x_0 \cdot x_1 \cdots x_k\) para determinar el orden en que se multiplican estos \(k+1\) números y \(C_{n-k-1}\) formas de insertar paréntesis en el producto \(x_{k+1} \cdot x_{k+2} \cdots x_n\) para determinar el orden de multiplicación de los \(n-k\) números restantes. Como este operador puede aparecer entre cualquiera de los \(n+1\) números, se sigue que</p>

<p style="text-align: center;">
\[
C_n = C_0 C_{n-1} + C_1 C_{n-2} + \cdots + C_{n-2} C_1 + C_{n-1} C_0 = \sum_{k=0}^{n-1} C_k C_{n-k-1}.
\]
</p>

<p>Nota que las condiciones iniciales son \(C_0 = 1\) y \(C_1 = 1\). \(\blacksquare\)</p>

<p>La relación de recurrencia en el Ejemplo 5 puede resolverse usando el método de funciones generadoras, que se 
    discutirá en la Sección 8.4. Se puede demostrar que \(C_n = \frac{\binom{2n}{n}}{n+1}\) (ver Ejercicio 43 
    en la Sección 8.4) y que \(C_n \sim \frac{4^n}{n^{3/2} \sqrt{\pi}}\) (ver [GrKnPa94]). La secuencia \(\{C_n\}\)
     es la secuencia de números de Catalán, nombrada así en honor a Eugene Charles Catalan. Esta secuencia aparece
      como solución de muchos problemas de conteo diferentes además del considerado aquí (ver el capítulo sobre números
       de Catalán en [MiRo91] o [RoTe03] para más detalles).</p>

