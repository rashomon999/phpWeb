    <h2>8.1.3 Algoritmos y Relaciones de Recurrencia</h2>

<p>Las relaciones de recurrencia juegan un papel importante en muchos aspectos del estudio de algoritmos y su complejidad. En la Sección 8.3 mostraremos cómo las relaciones de recurrencia pueden usarse para analizar la complejidad de algoritmos divide y vencerás, como el algoritmo merge sort introducido en la Sección 5.4. Como veremos en la Sección 8.3, los algoritmos divide y vencerás dividen recursivamente un problema en un número fijo de subproblemas no solapados hasta que se vuelven lo suficientemente simples para resolverse directamente.</p>

<p>Concluimos esta sección introduciendo otro paradigma algorítmico conocido como programación dinámica, que puede usarse para resolver muchos problemas de optimización de manera eficiente. Un algoritmo sigue el paradigma de programación dinámica cuando divide recursivamente un problema en subproblemas más simples que se solapan, y calcula la solución usando las soluciones de esos subproblemas. Generalmente, las relaciones de recurrencia se usan para encontrar la solución global a partir de las soluciones de los subproblemas.</p>

<p>La programación dinámica se ha usado para resolver problemas importantes en áreas tan diversas como economía, visión por computadora, reconocimiento de voz, inteligencia artificial, gráficos por computadora y bioinformática. En esta sección ilustraremos el uso de la programación dinámica construyendo un algoritmo para resolver un problema de programación de horarios.</p>

<p>Antes de eso, contaremos el origen curioso del nombre <em>programación dinámica</em>, introducido por el matemático Richard Bellman en los años 1950. Bellman trabajaba en la Corporación RAND en proyectos para el ejército de EE.UU., y en ese tiempo, el Secretario de Defensa de EE.UU. era hostil a la investigación matemática. Bellman decidió que, para asegurar financiación, necesitaba un nombre que no contuviera la palabra matemáticas para su método para resolver problemas de planificación y programación.</p>

<p>Decidió usar el adjetivo <strong>dinámico</strong> porque, según dijo, “es imposible usar la palabra dinámico en un sentido peyorativo” y pensó que la programación dinámica era “algo a lo que ni un congresista podría oponerse.”</p>

<h3>UN EJEMPLO DE PROGRAMACIÓN DINÁMICA</h3>

<p>El problema que usamos para ilustrar la programación dinámica está relacionado con el problema estudiado en el Ejemplo 7 de la Sección 3.1. En ese problema, nuestro objetivo era programar la mayor cantidad posible de charlas en una sola sala de conferencias. Estas charlas tienen horarios fijos de inicio y fin; una vez que una charla empieza, continúa hasta terminar; no pueden solaparse dos charlas al mismo tiempo; y una charla puede comenzar justo cuando otra termina. Desarrollamos un algoritmo voraz que siempre produce una programación óptima, como demostramos en el Ejemplo 12 de la Sección 5.1.</p>

<p>Ahora supongamos que nuestro objetivo no es programar la mayor cantidad posible de charlas, sino lograr la mayor asistencia combinada posible de las charlas programadas.</p>

<p>Formalizamos este problema suponiendo que tenemos \(n\) charlas, donde la charla \(j\) comienza en el tiempo \(s_j\), termina en el tiempo \(e_j\), y será asistida por \(w_j\) estudiantes. Queremos un horario que maximice el número total de asistentes. Es decir, deseamos programar un subconjunto de charlas que maximice la suma de los \(w_j\) de todas las charlas programadas. (Nótese que si un estudiante asiste a más de una charla, se cuenta tantas veces como charlas asista).</p>

<p>Denotamos por \(T(j)\) el número máximo total de asistentes para un horario óptimo con las primeras \(j\) charlas, por lo que \(T(n)\) es el número máximo total de asistentes para un horario óptimo para todas las \(n\) charlas.</p>

<p>Primero ordenamos las charlas en orden creciente de tiempo de finalización. Después de hacer esto, renumeramos las charlas para que</p>

<p style="text-align: center;">\( e_1 \leq e_2 \leq \cdots \leq e_n \).</p>

<p>Decimos que dos charlas son compatibles si pueden formar parte del mismo horario, es decir, si los tiempos en que se programan no se solapan (excepto la posibilidad de que una termine justo cuando la otra empieza).</p>

<p>Definimos \(p(j)\) como el mayor entero \(i\), con \(i < j\), para el cual \(e_i \leq s_j\), si tal entero existe; y \(p(j) = 0\) en caso contrario. Es decir, la charla \(p(j)\) es la charla que termina más tarde entre las compatibles con la charla \(j\) y que termina antes que la charla \(j\), si existe tal charla; y \(p(j) = 0\) si no existen tales charlas.</p>

<h3>EJEMPLO 6</h3>

<p>Considera siete charlas con estos horarios de inicio y fin, ilustrados en la Figura 5.</p>

<ul>
<li>Charla 1: empieza 8 A.M., termina 10 A.M.</li>
<li>Charla 2: empieza 9 A.M., termina 11 A.M.</li>
<li>Charla 3: empieza 10:30 A.M., termina 12 mediodía</li>
<li>Charla 4: empieza 9:30 A.M., termina 1 P.M.</li>
<li>Charla 5: empieza 8:30 A.M., termina 2 P.M.</li>
<li>Charla 6: empieza 11 A.M., termina 2 P.M.</li>
<li>Charla 7: empieza 1 P.M., termina 2 P.M.</li>
</ul>

<p>Encuentra \(p(j)\) para \(j = 1, 2, \ldots, 7\).</p>

<h4>Solución:</h4>

<p>Tenemos \(p(1) = 0\) y \(p(2) = 0\), porque ninguna charla termina antes de que comiencen las dos
     primeras charlas. Tenemos \(p(3) = 1\) porque la charla 3 y la charla 1 son compatibles, pero la 
     charla 3 y la charla 2 no son compatibles; \(p(4) = 0\) porque la charla 4 no es compatible con 
     ninguna de las charlas 1, 2 y 3; \(p(5) = 0\).</p>

   <img src="../../../../img/horario.png" alt="">


   <p>porque la charla 5 no es compatible con ninguna de las charlas 1, 2, 3 y 4; y \(p(6) = 2\) porque la charla 6 y la charla 2 son compatibles, pero la charla 6 no es compatible con ninguna de las charlas 3, 4 y 5. Finalmente, \(p(7) = 4\), porque la charla 7 y la charla 4 son compatibles, pero la charla 7 no es compatible con las charlas 5 ni 6. ◂</p>

<p>Para desarrollar un algoritmo de programación dinámica para este problema, primero desarrollamos una relación de recurrencia clave. Para ello, notamos que si \(j \leq n\), hay dos posibilidades para un horario óptimo con las primeras \(j\) charlas (recordemos que estamos suponiendo que las \(n\) charlas están ordenadas por tiempo de finalización creciente):</p>

<ol>
<li>La charla \(j\) pertenece al horario óptimo, o</li>
<li>No pertenece.</li>
</ol>

<p><strong>Caso (i):</strong> Sabemos que las charlas \(p(j)+1, \ldots, j-1\) no pertenecen a este horario, pues ninguna de estas otras charlas es compatible con la charla \(j\). Además, las otras charlas en este horario óptimo deben conformar un horario óptimo para las charlas \(1, 2, \ldots, p(j)\). Porque si existiera un mejor horario para las charlas \(1, 2, \ldots, p(j)\), al agregar la charla \(j\), tendríamos un horario mejor que el horario óptimo total. Por consiguiente, en el caso (i), tenemos:</p>

<p style="text-align: center;">\( T(j) = w_j + T(p(j)) \)</p>

<p><strong>Caso (ii):</strong> Cuando la charla \(j\) no pertenece a un horario óptimo, se sigue que un horario óptimo con las charlas \(1, 2, \ldots, j\) es el mismo que un horario óptimo con las charlas \(1, 2, \ldots, j-1\). Por consiguiente, en el caso (ii), tenemos:</p>

<p style="text-align: center;">\( T(j) = T(j - 1) \)</p>

<p>Combinando ambos casos llegamos a la relación de recurrencia:</p>

<p style="text-align: center;">\( T(j) = \max(w_j + T(p(j)), T(j - 1)) \)</p>

 
