</p>

<ul>
  <li>Cuando \( x > 1 \), se cumple que \( x < x^2 \)</li>
  <li>También, \( 1 < x^2 \)</li>
  <li>Y claramente \( 2x < 2x^2 \)</li>
</ul>

 
 
