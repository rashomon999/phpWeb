<h2>Aplicaciones del Teorema de los Números Primos</h2>

<p>Podemos usar el teorema para estimar la probabilidad de que un número elegido al azar sea primo. El teorema nos dice que el número de primos no mayores que \( x \) puede ser aproximado por \( \frac{x}{\ln(x)} \).</p>

<p>Así, la probabilidad de que un número entero positivo menor que \( n \) sea primo es aproximadamente:
\[
\frac{n/\ln(n)}{n} = \frac{1}{\ln(n)}
\]</p>

<p>A veces necesitamos encontrar un primo con una cantidad específica de dígitos. Usando el teorema y cálculo, se puede demostrar que la probabilidad de que un número \( n \) sea primo también es aproximadamente:
\[
\frac{1}{\ln(n)}
\]</p>

<p>Por ejemplo, la probabilidad de que un número cercano a \( 10^{1000} \) sea primo es aproximadamente:
\[
\frac{1}{\ln(10^{1000})} \approx \frac{1}{2300}
\]</p>

<p>(Nota: si elegimos solo números impares, duplicamos nuestras posibilidades de encontrar un primo).</p>

<h2>Importancia en Criptografía</h2>

<p>Usar la división por ensayo junto con el Teorema 2 da procedimientos para factorización y prueba de primalidad, pero no son algoritmos eficientes.</p>

<p>Se han desarrollado muchos algoritmos más prácticos para estas tareas. La factorización y la prueba de primalidad son fundamentales en aplicaciones criptográficas, lo cual ha impulsado el desarrollo de algoritmos más eficientes.</p>

<p>En los últimos 30 años, se han ideado métodos eficientes para generar grandes números primos. En 2002, Manindra Agrawal, Neeraj Kayal y Nitin Saxena demostraron que existe un algoritmo de tiempo polinomial respecto al número de bits para determinar si un número positivo es primo.</p>

<p>Los algoritmos basados en su trabajo usan aproximadamente \( O((\log n)^6) \) operaciones de bits para decidir si un número \( n \) es primo.</p>

<p>Sin embargo, factorizar grandes números sigue siendo mucho más difícil que probar si son primos. No se conoce un algoritmo de tiempo polinomial para la factorización de enteros.</p>

