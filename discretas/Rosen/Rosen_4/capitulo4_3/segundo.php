    <h1>Teorema 4: El Teorema de los Números Primos</h1>

<p>La razón entre \( \pi(x) \), el número de primos que no exceden \( x \), y \( \frac{x}{\ln(x)} \) se aproxima a 1 a medida que \( x \) crece sin límite. (Aquí, \( \ln(x) \) es el logaritmo natural de \( x \)).</p>

<p>El teorema de los números primos fue demostrado por primera vez en 1896 por el matemático francés Jacques Hadamard y el matemático belga Charles-Jean-Gustave-Nicolas de la Vallée-Poussin, usando la teoría de variables complejas. Aunque se han encontrado demostraciones que no usan variables complejas, todas las demostraciones conocidas del teorema son bastante complicadas.</p>

<p>Se han demostrado muchas mejoras del teorema, muchas de ellas tratando el error al estimar \( \pi(x) \) usando \( \frac{x}{\ln(x)} \), o con otras funciones. Aún quedan muchas preguntas sin resolver en esta área de estudio.</p>

