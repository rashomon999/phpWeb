<h1>4.3.5 Conjeturas y Problemas Abiertos Sobre Primos</h1>

<p>La teoría de números es conocida como una materia en la que es fácil formular conjeturas, algunas de las cuales son difíciles de probar y otras han permanecido como problemas abiertos durante muchos años.</p>

<p>Describiremos algunas conjeturas en teoría de números y discutiremos su estado en los Ejemplos 6–9.</p>

<h2>Ejemplo 6</h2>

<p>Sería útil tener una función \( f(n) \) tal que \( f(n) \) sea primo para todos los enteros positivos \( n \).</p>

<p>Si tuviéramos tal función, podríamos encontrar grandes números primos para su uso en criptografía y otras aplicaciones.</p>

<p>Buscando tal función, podríamos considerar funciones polinómicas, como algunos matemáticos hicieron hace varios siglos. Después de muchos cálculos, podríamos encontrar el polinomio:</p>

<p>\[
f(n) = n^2 - n + 41
\]</p>

<p>Este polinomio tiene la interesante propiedad de que \( f(n) \) es primo para todos los enteros positivos \( n \) que no exceden 40. (Por ejemplo, \( f(1) = 41 \), \( f(2) = 43 \), \( f(3) = 47 \), \( f(4) = 53 \), etc.)</p>

<p>Esto podría llevarnos a conjeturar que \( f(n) \) es primo para todos los enteros positivos \( n \). ¿Podemos resolver esta conjetura?</p>

<p><strong>Solución:</strong> No es sorprendente que esta conjetura sea falsa; no tenemos que buscar mucho para encontrar un entero positivo \( n \) tal que \( f(n) \) sea compuesto, ya que:</p>

<p>\[
f(41) = 41^2 - 41 + 41 = 41^2 = 1681
\]</p>

<p>Debido a que \( f(n) = n^2 - n + 41 \) es primo para \( 1 \leq n \leq 40 \), podríamos sentirnos tentados a buscar otro polinomio con la propiedad de que \( f(n) \) sea primo para todos los enteros positivos \( n \).</p>

<p>Sin embargo, se puede demostrar que para cualquier polinomio \( f(n) \) con coeficientes enteros, existe un entero positivo \( y \) tal que \( f(y) \) es compuesto. (Ver el Ejercicio 23 en los Ejercicios Suplementarios.)</p>


