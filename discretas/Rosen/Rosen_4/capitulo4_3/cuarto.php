<p>Usaremos el Teorema 6 para desarrollar varios resultados útiles. Uno de nuestros objetivos será probar la parte del teorema fundamental de la aritmética que afirma que un número entero positivo tiene como máximo una factorización prima. Demostraremos que si un número entero positivo tiene una factorización en primos, donde los primos se escriben en orden no decreciente, entonces esta factorización es única.</p>

<p>Primero, necesitamos desarrollar algunos resultados sobre divisibilidad.</p>

<h4>LEMMA 2</h4>
<p>Si \(a\), \(b\) y \(c\) son enteros positivos tales que \(\gcd(a, b) = 1\) y \(a \mid bc\), entonces \(a \mid c\).</p>

<p><strong>Prueba:</strong> Debido a que \(\gcd(a, b) = 1\), por el teorema de Bézout existen enteros \(s\) y \(t\) tales que:</p>
<p>
    \[
    sa + tb = 1.
    \]
</p>
<p>Multiplicando ambos lados de esta ecuación por \(c\), obtenemos:</p>
<p>
    \[
    sac + tbc = c.
    \]
</p>
<p>Ahora podemos usar el Teorema 1 de la Sección 4.1 para demostrar que \(a \mid c\). Por la parte (ii) de ese teorema, \(a \mid tbc\). Debido a que \(a \mid sac\) y \(a \mid tbc\), por la parte (i) de ese teorema, concluimos que \(a\) divide \(sac + tbc\). Dado que \(sac + tbc = c\), concluimos que \(a \mid c\), completando la prueba.</p>

<p>Usaremos la siguiente generalización del Lema 2 en la prueba de unicidad de las factorizaciones primarias. (La prueba del Lema 3 se deja como Ejercicio 64 en la Sección 5.1, ya que puede llevarse a cabo más fácilmente usando el método de inducción matemática, cubierto en esa sección.)</p>

<h4>LEMMA 3</h4>
<p>Si \(p\) es un primo y \(p \mid a_1 a_2 \cdots a_n\), donde cada \(a_i\) es un entero, entonces \(p \mid a_i\) para algún \(i\).</p>

<p>Ahora podemos demostrar que una factorización de un entero en primos es única. Es decir, mostraremos que todo entero puede escribirse como el producto de primos en orden no decreciente de, como máximo, una única manera. Esta es una parte del teorema fundamental de la aritmética. Demostraremos la otra parte, que todo número entero tiene una factorización en primos, en la Sección 5.2.</p>

<h4>Prueba (de la unicidad de la factorización prima de un número entero positivo):</h4>
<p>Usaremos una prueba por contradicción. Supongamos que el número entero positivo \(n\) puede escribirse como el producto de primos de dos maneras diferentes, es decir, \(n = p_1 p_2 \cdots p_s\) y \(n = q_1 q_2 \cdots q_t\), donde cada \(p_i\) y \(q_j\) es primo tal que \(p_1 \leq p_2 \leq \cdots \leq p_s\) y \(q_1 \leq q_2 \leq \cdots \leq q_t\).</p>
<p>Cuando eliminamos todos los primos comunes de las dos factorizaciones, tenemos:</p>
<p>
    \[
    p_{i_1} p_{i_2} \cdots p_{i_u} = q_{j_1} q_{j_2} \cdots q_{j_v},
    \]
</p>
<p>donde ningún primo ocurre en ambos lados de esta ecuación y \(u\) y \(v\) son enteros positivos. Por el Lema 3, se sigue que \(p_{i_1}\) divide \(q_{j_k}\) para algún \(k\). Dado que ningún primo divide a otro primo, esto es imposible. En consecuencia, puede haber como máximo una factorización de \(n\) en primos en orden no decreciente.</p>

<p>El Lema 2 también puede usarse para probar un resultado sobre dividir ambos lados de una congruencia por el mismo entero. Hemos demostrado (Teorema 5 en la Sección 4.1) que podemos multiplicar ambos lados de una congruencia por el mismo entero. Sin embargo, dividir ambos lados de una congruencia por un entero no siempre produce una congruencia válida, como muestra el Ejemplo 19.</p>
 
