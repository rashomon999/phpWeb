\]


\[
r_{n-1} = r_n q_n.
\]
</p>

<p>Eventualmente, ocurre un residuo cero en esta secuencia de divisiones sucesivas, porque la secuencia de restos \(a = r_0 > r_1 > r_2 > \dots \geq 0\) no puede contener más de \(a\) términos. Además, se sigue del Lema 1 que:</p>

<p>
\[
\gcd(a, b) = \gcd(r_0, r_1) = \gcd(r_1, r_2) = \dots = \gcd(r_{n-2}, r_{n-1}) = \gcd(r_{n-1}, r_n) = \gcd(r_n, 0) = r_n.
\]
</p>

<p>Por lo tanto, el máximo común divisor es el último residuo no nulo en la secuencia de divisiones.</p>

