    <p>Supongamos que \(a\) y \(b\) son enteros positivos con \(a \geq b\). Sea \(r_0 = a\) y \(r_1 = b\). Cuando aplicamos sucesivamente el algoritmo de división, obtenemos:</p>

<p>
\[
