<p>El algoritmo de Euclides se expresa en pseudocódigo en el Algoritmo 1.</p>

<h4>Algoritmo 1: El algoritmo de Euclides</h4>

<pre>
procedure gcd(a, b: positive integers)
    x := a
    y := b
    while y ≠ 0
        r := x mod y
        x := y
        y := r
    return x {gcd(a, b) es x}
</pre>

<p>En el Algoritmo 1, los valores iniciales de \(x\) y \(y\) son \(a\) y \(b\), respectivamente. En cada etapa del procedimiento, \(x\) es reemplazado por \(y\), y \(y\) es reemplazado por \(x \mod y\), que es el residuo cuando \(x\) se divide entre \(y\). Este proceso se repite mientras \(y \neq 0\). El algoritmo termina cuando \(y = 0\), y el valor de \(x\) en ese punto, el último residuo no nulo en el procedimiento, es el máximo común divisor de \(a\) y \(b\).</p>

<p>Estudiaremos la complejidad temporal del algoritmo de Euclides en la Sección 5.3, donde mostraremos que el número de divisiones requeridas para encontrar el máximo común divisor de \(a\) y \(b\), donde \(a \geq b\), es \(O(\log b)\).</p>









<h3>4.3.8  Mcd como combinaciones lineales</h3>

<p>Un resultado importante que utilizaremos a lo largo del resto de esta sección es que el máximo común divisor de dos enteros \(a\) y \(b\) puede expresarse en la forma:</p>
<p>
    \[
    sa + tb,
    \]
    donde \(s\) y \(t\) son enteros. En otras palabras, \(\gcd(a, b)\) puede expresarse como una combinación lineal con coeficientes enteros de \(a\) y \(b\). Por ejemplo, \(\gcd(6, 14) = 2\), y \(2 = (-2) \cdot 6 + 1 \cdot 14\). Enunciamos este hecho como el Teorema 6.
</p>

<h4>TEOREMA 6: El Teorema de Bézout</h4>
<p>Si \(a\) y \(b\) son enteros positivos, entonces existen enteros \(s\) y \(t\) tales que:</p>
<p>
    \[
    \gcd(a, b) = sa + tb.
    \]
</p>

<h4>Definición 6</h4>
<p>Si \(a\) y \(b\) son enteros positivos, entonces los enteros \(s\) y \(t\) tales que \(\gcd(a, b) = sa + tb\) se llaman coeficientes de Bézout de \(a\) y \(b\) (después de Étienne Bézout, un matemático francés del siglo XVIII). Además, la ecuación \(\gcd(a, b) = sa + tb\) se llama la identidad de Bézout.</p>

<p>No daremos una prueba formal del Teorema 6 aquí (ver Ejercicio 36 en la Sección 5.2 y [Ro10] para las pruebas). Presentaremos dos métodos diferentes que se pueden utilizar para encontrar una combinación lineal de dos enteros igual a su máximo común divisor. (En esta sección, supondremos que una combinación lineal tiene coeficientes enteros.)</p>

<p>El primer método consiste en trabajar hacia atrás a través de las divisiones del algoritmo de Euclides, por lo que este método requiere un pase hacia adelante y un pase hacia atrás a través de los pasos del algoritmo de Euclides. Ilustraremos cómo funciona este método con un ejemplo. La principal ventaja del segundo método, conocido como el algoritmo extendido de Euclides, es que utiliza un solo pase a través de los pasos del algoritmo de Euclides para encontrar los coeficientes de Bézout de \(a\) y \(b\), a diferencia del primer método, que utiliza dos pases. Para ejecutar este algoritmo extendido de Euclides, establecemos \(s_0 = 1\), \(s_1 = 0\), \(t_0 = 0\) y \(t_1 = 1\), y luego dejamos que:</p>
<p>
    \[
    s_j = s_{j-2} - q_{j-1} s_{j-1} \quad \text{y} \quad t_j = t_{j-2} - q_{j-1} t_{j-1}
    \]
</p>
<p>para \(j = 2, 3, \ldots, n\), donde los \(q_j\) son los cocientes en las divisiones usadas cuando el algoritmo de Euclides encuentra \(\gcd(a, b)\), como se muestra en el texto. Podemos probar por inducción fuerte (ver Ejercicio 44 en la Sección 5.2, o ver [Ro10]) que:</p>
<p>
    \[
    \gcd(a, b) = s_n a + t_n b.
    \]
</p>

 
