    <p>Ahora describimos cómo funciona el algoritmo de Euclides en generalidad. Utilizaremos divisiones sucesivas para reducir el problema de encontrar el máximo común divisor de dos enteros positivos al mismo problema con enteros más pequeños, hasta que uno de los enteros sea cero.</p>

<p>El algoritmo de Euclides se basa en el siguiente resultado sobre los máximos comunes divisores y el algoritmo de división.</p>

<p><strong>LEMMA 1</strong> Sea \(a = bq + r\), donde \(a\), \(b\), \(q\) y \(r\) son enteros. Entonces, \(\gcd(a, b) = \gcd(b, r)\).</p>

<p><strong>Prueba:</strong> Si podemos demostrar que los divisores comunes de \(a\) y \(b\) son los mismos que los divisores comunes de \(b\) y \(r\), habremos demostrado que \(\gcd(a, b) = \gcd(b, r)\), porque ambos pares deben tener el mismo máximo común divisor.</p>

<p>Supongamos que \(d\) divide tanto a \(a\) como a \(b\). Entonces, se sigue que \(d\) también divide \(a - bq = r\) (por el Teorema 1 de la Sección 4.1). Por lo tanto, cualquier divisor común de \(a\) y \(b\) también es un divisor común de \(b\) y \(r\).</p>

<p>Del mismo modo, supongamos que \(d\) divide tanto a \(b\) como a \(r\). Entonces, \(d\) también divide \(bq + r = a\). Por lo tanto, cualquier divisor común de \(b\) y \(r\) también es un divisor común de \(a\) y \(b\).</p>

<p>Consecuentemente, \(\gcd(a, b) = \gcd(b, r)\).</p>
