    <h3>Ejemplo 13</h3>
<p><em>Determina si los enteros 10, 17 y 21 son primos relativos por pares y si los enteros 10, 19 y 24 también lo son.</em></p>
<p><strong>Solución:</strong> Como \( \gcd(10, 17) = 1 \), \( \gcd(10, 21) = 1 \) y \( \gcd(17, 21) = 1 \), concluimos que 10, 17 y 21 son primos relativos por pares.</p>
<p>Sin embargo, como \( \gcd(10, 24) = 2 > 1 \), 10, 19 y 24 no son primos relativos por pares.</p>

    <p>Otra forma de encontrar el máximo común divisor de dos enteros positivos es usar sus 
    factorizaciones primarias. Supongamos que las factorizaciones primarias de los enteros 
    positivos \( a \) y \( b \) son</p>

    <p>\[
    a = p_1^{a_1} p_2^{a_2} \cdots p_n^{a_n}, \quad b = p_1^{b_1} p_2^{b_2} \cdots p_n^{b_n},
    \]</p>

    <p>donde cada exponente es un entero no negativo, y donde todos los primos que aparecen 
    en la factorización de \( a \) o de \( b \) están incluidos en ambas factorizaciones, usando 
    exponentes cero si es necesario.</p>

    <p>Entonces, el máximo común divisor \( \gcd(a, b) \) se da por</p>

    <p>\[
    \gcd(a, b) = p_1^{\min(a_1, b_1)} p_2^{\min(a_2, b_2)} \cdots p_n^{\min(a_n, b_n)},
    \]</p>

    <p>donde \( \min(x, y) \) representa el mínimo entre \( x \) y \( y \).</p>

    <p>Para demostrar que esta fórmula es válida, se debe mostrar que el entero en el lado derecho divide a 
    ambos \( a \) y \( b \), y que ningún entero mayor también lo hace. Esto es cierto porque el exponente
    de cada primo en la factorización no excede el exponente de ese primo en ninguna de las dos factorizaciones.
    Además, no se puede aumentar ninguno de los exponentes, ni incluir otros primos.</p>
