<p><strong>En resumen:</strong><br>
Para que 10, 17, 21 sean primos relativos por pares se debe verificar que:</p>

<ul>
<li>\(\gcd(10,17) = 1\) ✅</li>
<li>\(\gcd(10,21) = 1\) ✅</li>
<li>\(\gcd(17,21) = 1\) ✅</li>
</ul>


