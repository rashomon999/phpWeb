<p>Antes de presentar un algoritmo para una exponenciaci√≥n modular r√°pida basado en la expansi√≥n binaria del exponente, observa que podemos evitar usar grandes cantidades de memoria si calculamos \( b^n \bmod m \) sucesivamente para \( b^k \bmod m \) con \( k = 1, 2, \dots, n \), utilizando el hecho de que:</p>

