<p>En criptograf√≠a, es importante poder encontrar eficientemente \( b^n \bmod m \) sin usar una cantidad excesiva de memoria, donde \( b \), \( n \) y \( m \) son enteros grandes. Es poco pr√°ctico calcular primero \( b^n \) y luego hallar su residuo al dividir entre \( m \), porque \( b^n \) puede ser un n√∫mero enorme y se necesitar√≠a una gran cantidad de memoria para almacenar dichos valores.</p>

