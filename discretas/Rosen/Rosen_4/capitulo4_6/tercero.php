<h2>📌 ¿Qué es qué en el descifrado RSA?</h2>

<p>🔐 <strong>Objetivo:</strong> recuperar el mensaje original (llamado \( m \)) a partir del mensaje cifrado (\( c \)).</p>

<h3>🧩 Elementos involucrados:</h3>
<ul>
  <li>\( n = p \times q \): producto de dos números primos grandes. Se usa como parte de la clave pública y privada.</li>
  <li>\( e \): exponente público (clave pública). Se usa para cifrar.</li>
  <li>\( d \): exponente privado (clave privada). Se usa para descifrar.</li>
  <li>\( m \): mensaje original (en forma de número).</li>
  <li>\( c \): mensaje cifrado (lo que te llega).</li>
  <li>\( (p−1)(q−1) \): se llama \( \varphi(n) \) (phi de \( n \)), y se usa para calcular \( d \).</li>
</ul>

<h3>🔁 ¿Cómo se cifró originalmente?</h3>
<p>Se usó esta fórmula:</p>
\[ c = m^e \bmod n \]
<p>Es decir: el mensaje original \( m \) fue elevado a la potencia \( e \), y se tomó el módulo \( n \).</p>

<h3>🔓 ¿Cómo se descifra?</h3>
<p>Sabemos \( c \), y queremos volver a \( m \). Para eso usamos:</p>
\[ m = c^d \bmod n \]
<p>Este \( d \) se calcula de forma especial: es el inverso de \( e \) módulo \( (p−1)(q−1) \). Eso significa que cumple:</p>
\[ d \times e \equiv 1 \bmod (p−1)(q−1) \]

<h3>🧠 ¿Por qué funciona el descifrado?</h3>
<p>La razón por la que el descifrado recupera el mensaje original se basa en principios de la teoría de números, específicamente:</p>
<ul>
  <li><strong>Pequeño Teorema de Fermat:</strong> Si \( p \) es un número primo y \( m \) no es divisible por \( p \), entonces:</li>
  \[ m^{p−1} \equiv 1 \bmod p \]

  <li><strong>Teorema Chino del Resto:</strong> Permite combinar congruencias módulo \( p \) y \( q \) para obtener una congruencia módulo \( n = p \times q \).</li>
</ul>

<p>Estos teoremas aseguran que:</p>
\[ (m^e)^d \equiv m \bmod n \]

<h3>📘 Ejemplo práctico</h3>
<p>Supongamos que recibimos un mensaje cifrado \( c = 2790 \), y conocemos:</p>
<ul>
  <li>\( n = 3233 \)</li>
  <li>\( e = 17 \)</li>
  <li>\( d = 413 \)</li>
</ul>

<p>Para descifrar:</p>
\[ m \equiv 2790^{413} \bmod 3233 \]

<p>Calculando esta potencia modular (usando, por ejemplo, el algoritmo de exponenciación rápida), obtenemos el mensaje original \( m \).</p>

