<h1>4.6.9 Cifrado Homomórfico</h1>

<p>
  Un criptosistema, como RSA, puede utilizarse para cifrar archivos con el fin de mantenerlos en secreto.
  Hoy en día, muchos usuarios almacenan archivos cifrados en la nube, donde residen en computadoras remotas.
  A menudo es necesario ejecutar programas que usan datos almacenados en la nube.
  Estos datos son vulnerables a usuarios con acceso a la computadora remota donde están almacenados si ejecutamos programas en la nube sin descargar primero dichos datos.
  También son vulnerables a los espías cuando descargamos archivos, ejecutamos programas en nuestra computadora y luego subimos la salida a la nube.
</p>

<p>
  ¿Podríamos evitar estas vulnerabilidades ejecutando programas directamente sobre datos cifrados?
  Aunque esto pueda parecer poco realista a primera vista, en 1979, poco después de que se introdujera RSA, se planteó la pregunta de si podría existir un criptosistema
  que permitiera realizar cálculos arbitrarios sobre datos cifrados, de modo que se produjera el <strong>cifrado de la salida sin cifrar</strong> que resultaría de los datos de entrada sin cifrar.
  En un sistema así, no sería necesario descifrar los datos de entrada, ya que el programa podría ejecutarse en un sistema remoto sin revelar ni la entrada ni la salida.
</p>

<p>
  Así comenzó la búsqueda de un <strong>criptosistema completamente homomórfico</strong>, que permitiera ejecutar cálculos arbitrarios de forma remota sobre datos cifrados.
</p>

<h2>Ejemplo 11: RSA es Parcialmente Homomórfico</h2>

<p>
  Sea \( (n, e) \) una clave pública del criptosistema RSA y supongamos que \( M_1 \) y \( M_2 \) son mensajes en texto plano, tales que \( 0 \leq M_1 &lt; n \) y \( 0 \leq M_2 &lt; n \). Entonces:
</p>

<p>
  \[
  E_{(n,e)}(M_1) \cdot E_{(n,e)}(M_2) \mod n = (M_1^e \mod n \cdot M_2^e \mod n) \mod n = (M_1 M_2)^e \mod n = E_{(n,e)}(M_1 M_2)
  \]
</p>

<p>
  De esta ecuación se deduce que:
</p>

<p>
  \[
  E_{(n,e)}(M_1) \cdot_n E_{(n,e)}(M_2) = E_{(n,e)}(M_1 M_2)
  \]
  en \( \mathbb{Z}_n \). Por esta razón, decimos que <strong>RSA es homomórfico multiplicativo</strong>.
</p>

<p>
  Al cifrar con RSA, podemos llevar a cabo multiplicaciones sin necesidad de descifrar previamente, ya que el cifrado del producto de dos textos planos es igual al producto de sus cifrados.
</p>

<p>
  Sin embargo, <strong>no es cierto</strong> que:
</p>

<p>
  \[
  E_{(n,e)}(M_1) +_n E_{(n,e)}(M_2) = E_{(n,e)}(M_1 + M_2)
  \]
  para todos \( M_1 \) y \( M_2 \) en \( \mathbb{Z}_n \).
</p>

<p>
  (Esto puede verse fácilmente, por ejemplo, si \( M_2 = 1 \).) Es decir, al cifrar con RSA, no podemos sumar los cifrados de dos números y obtener el cifrado de su suma.
  Además, <strong>no se conoce ningún método</strong> para determinar \( E_{(n,e)}(M_1 + M_2) \) a partir de \( E_{(n,e)}(M_1) \) y \( E_{(n,e)}(M_2) \) sin descifrar ambos cifrados.
</p>

<p>
  Decimos entonces que RSA <strong>no es homomórfico aditivo</strong>.
  Como es homomórfico multiplicativo pero no aditivo, RSA se considera <strong>parcialmente homomórfico</strong>.
</p>

<h2>Avances hacia el cifrado completamente homomórfico</h2>

<p>
  En 2009, <strong>Craig Gentry</strong> describió el primer <strong>criptosistema completamente homomórfico</strong>, basado en lo que se conoce como <em>criptografía basada en retículas</em> (lattice-based cryptography).
  Desafortunadamente, aún no se han desarrollado criptosistemas completamente homomórficos que sean prácticos, ya que todos requieren cantidades extremadamente grandes de procesamiento y memoria.
</p>

<p>
  Sin embargo, hay esperanza de que los nuevos avances conduzcan a sistemas completamente homomórficos prácticos en un futuro no muy lejano.
</p>


