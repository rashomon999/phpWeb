<h2>4.6.7 RSA como un sistema de clave pública</h2>

<p>
  ¿Por qué el criptosistema RSA es adecuado para la criptografía de clave pública? 
  Primero, es posible construir rápidamente una clave pública al encontrar dos primos grandes 
  \( p \) y \( q \), cada uno con más de 300 dígitos, y luego encontrar un entero 
  \( e \) que sea relativamente primo con \( (p - 1)(q - 1) \).
</p>

<p>
  Cuando se conoce la factorización del módulo \( n \), es decir, cuando se conocen 
  \( p \) y \( q \), se puede encontrar rápidamente un inverso \( d \) de \( e \) módulo 
  \( (p - 1)(q - 1) \). 
  [Esto se hace usando el algoritmo de Euclides para encontrar los coeficientes de Bézout 
  \( s \) y \( t \) para \( d \) y \( (p - 1)(q - 1) \), lo que muestra que el inverso de 
  \( d \) módulo \( (p - 1)(q - 1) \) es \( s \mod (p - 1)(q - 1) \).] 
  Conociendo \( d \), podemos descifrar los mensajes enviados usando nuestra clave.
</p>

<p>
  Sin embargo, no se conoce ningún método que permita descifrar mensajes sin basarse en 
  encontrar una factorización de \( n \), o que no conduzca también a la factorización de \( n \).
</p>

<p>
  Se cree que la factorización es un problema difícil, a diferencia de encontrar grandes primos 
  \( p \) y \( q \), lo cual se puede hacer rápidamente. 
  Los métodos de factorización más eficientes que se conocen (hasta 2017) requerirían miles de millones de años 
  para factorizar enteros de 600 dígitos. 
  Por lo tanto, cuando \( p \) y \( q \) son primos de 300 dígitos, se cree que los mensajes cifrados 
  usando \( n = pq \) no pueden ser descifrados en un tiempo razonable a menos que se conozcan los primos \( p \) y \( q \).
</p>

<p>
  Aunque no se conoce un algoritmo de tiempo polinomial para factorizar enteros grandes, hay una investigación activa 
  en curso para encontrar nuevos métodos eficientes de factorización. 
  Enteros que hasta hace pocos años se pensaba que eran imposibles de factorizar en un tiempo razonable 
  ahora pueden ser factorizados rutinariamente. 
  Enteros con más de 230 dígitos ya han sido factorizados usando esfuerzos colaborativos.
</p>

<p>
  Cuando se descubran nuevas técnicas de factorización, será necesario usar primos aún más grandes para asegurar 
  el secreto de los mensajes. Desafortunadamente, los mensajes que antes se consideraban seguros pueden ser almacenados 
  y posteriormente descifrados por receptores no autorizados cuando se vuelva factible factorizar el 
  \( n = pq \) usado en la clave RSA. 
  (Cabe destacar que el sistema RSA será inseguro cuando esté disponible la computación cuántica.)
</p>

<p>
  El método RSA ahora se utiliza ampliamente. 
  Sin embargo, los sistemas criptográficos más usados siguen siendo los de clave privada. 
  El uso de la criptografía de clave pública, como el sistema RSA, está en aumento. 
  Aun así, hay aplicaciones que combinan sistemas de clave privada y pública. 
  Por ejemplo, un sistema de clave pública como RSA puede usarse para distribuir claves privadas 
  a pares de individuos que desean comunicarse. 
  Luego, estas personas usan un sistema de clave privada para cifrar y descifrar mensajes.
</p>


































<h2>4.6.8 Protocolos Criptográficos</h2>

<p>
  Hasta ahora hemos mostrado cómo la criptografía puede usarse para asegurar los mensajes. 
  Sin embargo, hay muchas otras aplicaciones importantes de la criptografía. Entre estas 
  aplicaciones están los <strong>protocolos criptográficos</strong>, que son intercambios de 
  mensajes realizados por dos o más partes para lograr un objetivo específico de seguridad.
  En particular, mostraremos cómo la criptografía puede usarse para que dos personas 
  intercambien una clave secreta a través de un canal de comunicación inseguro. También 
  mostraremos cómo puede usarse la criptografía para enviar mensajes secretos firmados de 
  forma que el receptor pueda estar seguro de que el mensaje provino del remitente que 
  dice ser.
</p>

<h3>Intercambio de claves</h3>

<p>
  Supongamos que Alicia y Bob desean compartir una clave común. El protocolo sigue estos pasos, 
  donde los cálculos se realizan en \( \mathbb{Z}_p \):
</p>

<ol>
  <li>Alicia y Bob acuerdan usar un primo \( p \) y una raíz primitiva \( a \) de \( p \).</li>
  <li>Alicia elige un número secreto \( k_1 \) y envía \( a^{k_1} \mod p \) a Bob.</li>
  <li>Bob elige un número secreto \( k_2 \) y envía \( a^{k_2} \mod p \) a Alicia.</li>
  <li>Alicia calcula \( (a^{k_2})^{k_1} \mod p \).</li>
  <li>Bob calcula \( (a^{k_1})^{k_2} \mod p \).</li>
</ol>

<p>
  Al finalizar este protocolo, ambos han calculado la misma clave compartida:
  \[
    (a^{k_2})^{k_1} \mod p = (a^{k_1})^{k_2} \mod p
  \]
</p>

<p>
  Este protocolo es seguro porque, aunque \( p \), \( a \), \( a^{k_1} \mod p \) y \( a^{k_2} \mod p \) 
  sean públicos, calcular \( k_1 \), \( k_2 \) o la clave común a partir de ellos requiere resolver 
  el problema del logaritmo discreto, lo cual es computacionalmente inviable si \( p \) y \( a \) 
  son suficientemente grandes (más de 300 y 100 dígitos decimales respectivamente).
</p>


