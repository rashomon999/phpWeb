    <div>
  <p>Cuando realizas la división normal de \( -11 \div 3 \) y decides que el cociente es \( -4 \), el proceso es el siguiente:</p>

  <p>Multiplicas \( -4 \times 3 = -12 \), que es el múltiplo más cercano a \( -11 \) pero menor que él.</p>

  <p>Luego, para encontrar el residuo, debes restar \( -12 \) de \( -11 \), es decir:</p>

  <p>
    \[
    \text{Residuo} = -11 - (-12) = -11 + 12 = 1.
    \]
  </p>

  <p>Entonces, efectivamente, en este caso:</p>
  <p><strong>Cociente = -4, Residuo = 1.</strong></p>

  <p>En resumen: La resta \( -11 - (-12) \) efectivamente es una forma de obtener el residuo al "ajustarlo" dentro del rango de \( 0 \leq r < 3 \).</p>

  <p>Por lo tanto, el cociente es \( -4 \) y el residuo es \( 1 \), que es el valor correcto para esta división, y se ajusta a la forma estándar de la aritmética modular.</p>
</div>

<p>Cuando divides \( -11 \div 3 \), si el cociente fuera \( -3 \), el cálculo sería:</p>

<p>
  \[
  3 \times (-3) = -9.
  \]
</p>

<p>Sin embargo, eso no es adecuado porque el resultado de \( -9 \) es más grande que \( -11 \), ya que \( -9 > -11 \). Por lo tanto, debes escoger el cociente menor que se ajuste más a \( -11 \).</p>

<p>El cociente correcto es \( -4 \), porque:</p>

<p>
  \[
  3 \times (-4) = -12.
  \]
</p>

<p>Esto da un valor menor que \( -11 \), y al restar \( -11 - (-12) \), obtenemos el residuo \( 1 \), que es el valor correcto en este caso.</p>
