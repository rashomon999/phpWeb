<h2>4.1.3 El Algoritmo de la División</h2>

<p>Cuando un entero se divide entre un entero positivo, hay un cociente y un residuo, como muestra el algoritmo de la división.</p>

<h3>Teorema 2 — El Algoritmo de la División</h3>
<p>Sea \( a \) un entero y \( d \) un entero positivo. Entonces existen enteros únicos \( q \) y \( r \), con \( 0 \leq r < d \), tales que</p>

<p>\[
a = dq + r.
\]</p>

<p>Dejamos la demostración del algoritmo de la división para la Sección 5.2. (Ver Ejemplo 5 y Ejercicio 37 en esa sección.)</p>

<h4>Observación</h4>
<p>El Teorema 2 en realidad no es un algoritmo. (¿Por qué no?) Sin embargo, utilizamos su nombre tradicional.</p>

<h3>Definición 2</h3>
<p>En la igualdad dada en el algoritmo de la división, \( d \) se llama el divisor, \( a \) el dividendo, \( q \) el cociente y \( r \) el residuo. Esta notación se usa para expresar el cociente y el residuo:</p>

<p>\[
q = a \, \text{div} \, d, \quad r = a \, \text{mod} \, d.
\]</p>

<h4>Observación</h4>
<p>Observa que tanto \( a \, \text{div} \, d \) como \( a \, \text{mod} \, d \) para un \( d \) fijo son funciones sobre el conjunto de los enteros. Además, cuando \( a \) es un entero y \( d \) es un entero positivo, tenemos:</p>

<p>\[
a \, \text{div} \, d = \left\lfloor \frac{a}{d} \right\rfloor, \quad a \, \text{mod} \, d = a - d\left\lfloor \frac{a}{d} \right\rfloor.
\]</p>

<hr>

<h3>Ejemplos que ilustran el Algoritmo de la División</h3>

<h4>Ejemplo 3</h4>
<p>¿Cuáles son el cociente y el residuo cuando 101 se divide entre 11?</p>

<h4>Solución</h4>
<p>Tenemos:</p>

<p>\[
101 = 11 \times 9 + 2.
\]</p>

<p>Por lo tanto, el cociente cuando 101 se divide entre 11 es:</p>

<p>\[
9 = 101 \, \text{div} \, 11,
\]</p>

<p>y el residuo es:</p>

<p>\[
2 = 101 \, \text{mod} \, 11.
\]</p>

<h3>Ejemplo 4</h3>
  <p>¿Cuáles son el cociente y el residuo cuando &minus;11 se divide entre 3?</p>
  <p><strong>Solución:</strong> Tenemos:</p>
  <p>
    \[
    -11 = 3(-4) + 1.
    \]
  </p>
  <p>Por lo tanto, el cociente cuando &minus;11 se divide entre 3 es:</p>
  <p>
    \[
    -4 = -11 \, \text{div} \, 3,
    \]
  </p>
  <p>y el residuo es:</p>
  <p>
    \[
    1 = -11 \, \text{mod} \, 3.
    \]
  </p>
  <p>
    Observa que el residuo no puede ser negativo. En consecuencia, el residuo no es &minus;2, aunque
  </p>
  <p>
    \[
    -11 = 3(-3) - 2,
    \]
  </p>
  <p>
    porque \( r = -2 \) no satisface \( 0 \leq r < 3 \). ◂
  </p>

  <p>
    Observa que el entero \( a \) es divisible por el entero \( d \) si y solo si el residuo es cero cuando \( a \) se divide entre \( d \).
  </p>

  <h4>Observación:</h4>
  <p>
    Un lenguaje de programación puede tener uno, o posiblemente dos, operadores para la aritmética modular, denotados por <code>mod</code> (en BASIC, Maple, Mathematica, EXCEL y SQL),
    <code>%</code> (en C, C++, Java y Python), <code>rem</code> (en Ada y Lisp), u otro símbolo.
  </p>
  <p>
    Ten cuidado al usarlos, porque para \( a &lt; 0 \), algunos de estos operadores devuelven \( a - m\lceil a/m \rceil \) en lugar de
    \( a \mod m = a - m\lfloor a/m \rfloor \) (como se muestra en el Ejercicio 24).
  </p>
  <p>
    Además, a diferencia de \( a \mod m \), algunos de estos operadores están definidos cuando \( m &lt; 0 \) e incluso cuando \( m = 0 \).
  </p>

