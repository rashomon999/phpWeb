<h4>Algoritmo 1: El algoritmo de Euclides</h4>

<pre>
procedure gcd(a, b: positive integers)
    x := a
    y := b
    while y ≠ 0
        r := x mod y
        x := y
        y := r
    return x {gcd(a, b) es x}
</pre>

<p>En el Algoritmo 1, los valores iniciales de \(x\) y \(y\) son \(a\) y \(b\), respectivamente.
En cada etapa del procedimiento, \(x\) es reemplazado por \(y\), y \(y\) es reemplazado por \(x \mod y\), 
que es el residuo cuando \(x\) se divide entre \(y\). Este proceso se repite mientras \(y \neq 0\). El 
algoritmo termina cuando \(y = 0\), y el valor de \(x\) en ese punto, el último residuo no nulo en el
procedimiento, es el máximo común divisor de \(a\) y \(b\).</p>

<p>Estudiaremos la complejidad temporal del algoritmo de Euclides en la Sección 5.3, donde mostraremos 
que el número de divisiones requeridas para encontrar el máximo común divisor de \(a\) y \(b\), donde
\(a \geq b\), es \(O(\log b)\).</p>

