     <h2>LA INFINITUD DE LOS PRIMOS</h2>
    <p>Se sabe desde hace mucho tiempo que hay 
    <input type="text" name="respuesta_100" value="<?php echo $respuesta_100; ?>" size="8">    
      primos. Esto significa que, siempre que 
    <span style="font-family:monospace;">p<sub>1</sub>, p<sub>2</sub>, ..., p<sub>n</sub></span> 
    sean los <span style="font-family:monospace;">n</span> primos más pequeños, sabemos que hay 
    un primo mayor que no está listado. Probaremos este hecho utilizando una prueba dada por Euclides 
    en su famoso texto matemático, <i>Los Elementos</i>. Esta prueba simple, pero elegante, es considerada 
    por muchos matemáticos como una de las más bellas en las matemáticas. Es la primera prueba presentada 
    en el libro <i>Proofs from THE BOOK</i> (AiZi[14]), donde <b>THE BOOK</b> se refiere a la colección 
    imaginaria de pruebas perfectas que, según el legendario matemático Paul Erdős, es mantenida por Dios. 
    Por cierto, hay una vasta cantidad de diferentes pruebas de que existen infinitos primos, y se publican 
    nuevas sorprendentemente con frecuencia.</p>

     <h4>TEOREMA 3: Existen 
     <input type="text"  value="<?php echo $respuesta_100; ?>" size="8" readonly>    
     primos.</h4>
    <h2>Demostración:</h2>
    <p>
    Probaremos este teorema utilizando una <strong>prueba por contradicción</strong>.
    Supongamos que existe solo un número finito de números primos, denotados por:
    \[
    p_1, p_2, \ldots, p_n
    \]
    Definimos el número:    
    \[
    Q = p_1 \cdot p_2 \cdot \ldots \cdot p_n + 1
    \]
    </p>

    <p>
    Por el <em>teorema fundamental de la aritmética</em>, \( Q \) es primo o bien puede escribirse como 
    el producto de dos o más primos.
    Sin embargo, ninguno de los primos \( p_j \) divide a \( Q \), porque si \( p_j \mid Q \), entonces también 
    debe dividir:
    \[
    Q - (p_1 \cdot p_2 \cdot \ldots \cdot p_n) = 1
    \]
    lo cual es 
     <input type="text" name="respuesta_101" value="<?php echo $respuesta_101; ?>" size="8">   
     , ya que ningún número primo divide a 
     <input type="text" name="respuesta_102" value="<?php echo $respuesta_102; ?>" size="8">   
     .
    </p>

    <p>
    Por lo tanto, debe existir un primo que no está en la lista \( p_1, p_2, \ldots, p_n \).
    Este primo es:
    </p>

    <ul>
    <li>\( Q \), si \( Q \) es primo, o</li>
    <li>un factor primo de \( Q \), si \( Q \) no es primo.</li>
    </ul>

    <p>
    Esto contradice nuestra suposición inicial de que \( p_1, p_2, \ldots, p_n \) son todos los primos existentes.
    Por lo tanto, concluimos que:
    \[
    \text{Existen infinitos números primos.}
    \]
</p>


