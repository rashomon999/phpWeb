<h4>Acoplamiento y Cohesión</h4>

<p>
    El acoplamiento es la medida de interdependencia entre los módulos de un software y la cohesión
    es la medida de relación estrecha entre los elementos de un módulo. Lo deseable en un diseño es
    tener un bajo acoplamiento y alta cohesión, de esta manera, un módulo va a ser independiente y
    agrupará los elementos estrechamente relacionados, dando así flexibilidad al momento de
    reemplazar los módulos del software.
</p>

