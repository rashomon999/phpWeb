    <strong>Diseño de interfaz de usuario</strong>
    <p>"El diseño de la interfaz de usuario se trata de usar
    tipografía, imágenes y otros elementos de diseño
    visual para convertir una interfaz básica en algo
    digerible y utilizable”</p>
    - Jonathan Widawski
    <hr>
    <strong>Principio de claridad: </strong>
    <p>"Su trabajo es crear una interfaz que minimice la brecha de
    conocimiento entre lo que los usuarios saben antes de ingresar a
    su sitio y lo que deben saber para usarlo correctamente".</p>

    Hágase las siguientes preguntas:
    <li>¿La navegación es intuitiva? ¿Se dirige y anima a los usuarios a pasar de una página a la
    siguiente con facilidad?</li>
    <li>¿Se han utilizado botones muy visibles que
    invitan a los usuarios a hacer clic en ellos? </li>
    <li>¿El propósito de cada elemento de su producto, sitio web o aplicación es claro y
    fácil de entender?</li>

    <strong>Principio de familiaridad/consistencia</strong>
    <p>Las mejores interfaces son familiares para los
    usuarios.</p>
    
    <p>La usabilidad, es decir, la facilidad con la que un usuario interactúa con un producto o un sitio web,
    está estrechamente relacionada con la familiaridad. Los usuarios dependen de elementos
    e interfaces que actúan de una manera familiar para su experiencia digital.</p>
 
    <p>Una interfaz de usuario consistente significa usar patrones de diseño similares, terminología idéntica
    en las indicaciones, menús y pantallas homogéneos y comandos consistentes en toda la interfaz.

    <strong>Ventajas:</strong>
    <li> Cuanto más familiarizado esté el usuario con su interfaz y
    más fácilmente pueda usar más volverá.</li>
    <li> Es más fácil para los diseñadores UI incorporar soluciones
    que funcionan.</li>
    <li> Reduce la curva de aprendizaje, el usuario quiere solucionar su necesidad no aprender a cómo usar un
    producto que lo hace.</li>
     
    <strong>Principio de Jerarquía</strong>
     
    <p>Consiste en disponer los elementos visuales de manera que explique el nivel de importancia de cada elemento y
    guíe a los usuarios a realizar la acción deseada. Como diseñador, su trabajo es organizar los elementos de diseño
    de la interfaz de usuario de una manera que facilite a los usuarios navegar dentro de su producto.</p>
 


