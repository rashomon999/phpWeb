    <strong>User testing</strong>
    <p>Objetivo: Evaluar si los usuarios pueden completar tareas en un prototipo de manera eficiente, efectiva y
    satisfactoria.</p>
    
    <p>Método: Se pide a los participantes que realicen tareas específicas mientras un observador registra sus acciones,
    dificultades y comentarios.</p>

    <strong>Tree testing</strong>
    <p>Técnica de evaluación de usabilidad para comprobar árboles de contenido o
    la arquitectura de información.</p>
    
    <p>A diferencia de las pruebas de usabilidad que analizan toda la interfaz, el Tree Testing solo evalúa la organización del
    contenido.</p>
    
    <strong>First Click </strong>
    <p> Cuando el primer clic es el correcto, las personas usuarias
    completan la tarea el 87% de las ocasiones.
    “First Click Usability Testing”</p>
    
    <strong>Entrevistas y Encuestas con Usuarios</strong>  
    <p>Permiten obtener retroalimentación directa sobre la experiencia de uso de un prototipo
    mediante preguntas abiertas (entrevistas) o cerradas (encuestas).</p>
    
    <strong>Test A/B </strong>
    <p>Consiste en presentar dos versiones de un prototipo (A y B) a diferentes grupos de
    usuarios para comparar cuál funciona mejor.</p>
     
