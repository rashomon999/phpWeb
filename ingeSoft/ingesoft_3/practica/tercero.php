    <strong> Heurísticas</strong>
    <p>Evaluación de una interfaz por parte de un experto en usabilidad basado en los principios de usabilidad (10
    heurísticas de Jacob Nielsen)</p>
    
    <strong>Recorrido cognitivo</strong>
    <p>Expertos en usabilidad se ponen en la piel de los usuarios, recorriendo la
    interfaz y completando diferentes tareas, tal y como lo haría el propio
    usuario. </p>
    
    <strong>Análisis Cognitivo de Tareas</strong>
    
    <p>Se examina cómo un usuario hipotético llevaría
    a cabo una tarea en un prototipo, dividiéndola
    en pasos y analizando su complejidad.</p>

    <strong>Revisión de Guías de Estilo y Consistencia</strong>
    <p>Se compara el prototipo con guías de diseño, estándares de accesibilidad y principios de usabilidad para 
    garantizar consistencia y cumplimiento de normas.</p>
