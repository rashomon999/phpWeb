    <h2>  8: Diseño estético y minimalista</h2>
    <p>Las interfaces no deben contener información irrelevante o rara vez necesaria. Cada unidad de información
    adicional en una interfaz compite con las unidades de información relevantes y reduce su visibilidad relativa.
    </p>
    <p>Esta heurística no implica necesariamente usar un diseño plano , sino asegurar que el contenido y el diseño
    visual se centren en lo esencial. Asegúrate de que los elementos visuales de la interfaz respalden los objetivos
    principales del usuario.
    </p>
    <h2>Ejemplos</h2>
    <li>La página de inicio de Google es probablemente uno de los productos mínimos más reconocibles de Internet.
    </li>
    <li>​​​​​​​​Una tetera ornamentada puede tener elementos decorativos excesivos, como un mango incómodo o una boquilla 
    difícil de lavar, que pueden interferir con la usabilidad. </li>
     
    <h2>Consejos</h2>
    <li>Mantenga el  contenido y el diseño visual de la interfaz de usuario centrados en lo esencial.
    </li>
    <li>No permita que elementos innecesarios distraigan a los usuarios de la información que realmente necesitan.
    </li>
    <li> Priorizar el contenido y las funciones para respaldar los objetivos principales.
    </li>

