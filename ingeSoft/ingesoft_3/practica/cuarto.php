    <p>El diseño de Arquitectura se encarga de especificar cómo un sistema de software debe estar
    organizado, definiendo la estructura del sistema. El diseño de arquitectura está relacionado con
    los requerimientos porque define los principales componentes estructurales y las relaciones entre
    ellos. La salida del proceso de diseño de la arquitectura es el modelo que describe cómo el sistema
    está organizado mediante el conjunto de componentes y su comunicación.</p>
    
    <p>
    En procesos de desarrollo ágil es importante aceptar que el diseño de la arquitectura debe
    realizarse en etapas tempranas ya que su modificación es muy costosa y podría afectar
    demasiados componentes.
    </p>
    <hr>
    Algunas características no funcionales de los sistemas de software que se deben tener en cuenta al
    momento de diseñar la arquitectura del sistema:
    <li> Performance: Localice las operaciones críticas y minimice las comunicaciones. Amplíe el
    uso de grandes componentes en lugar de granularidad fina.</li>
    <li>Seguridad: Utilice una arquitectura en capas con información crítica en las capas internas.
    Utilice pocos componentes con responsabilidades de las características de seguridad.</li>
    <li>Disponibilidad: Incluir redundancia en componentes y mecanismos de tolerancia de fallos.
    </li>
    <li>Mantenibilidad: Usar componentes reemplazables y de granularidad fina
    </li>
    
