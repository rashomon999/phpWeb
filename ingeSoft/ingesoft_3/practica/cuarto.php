    <h2>    Principios de diseño OO
    </h2>
    <p> Los principios son reglas, guías y conceptos que se definen para que los diseños sean mantenibles,
    flexibles, con características de calidad. En un enfoque orientado a objetos se menciona los
    siguientes principios base:</p>
    <strong>Abstracción</strong>
    Identificación de elementos relevantes de un problema o situación para lograr un propósito de
    análisis específico, quitando los elementos que no son parte importante del problema. En el
    contexto de diseño de software existen 2 mecanismos de abstracción claves: por parametrización
    y por especificación. La abstracción por parametrización es la representación de los datos en   
    términos de nombres de parámetros. Y la abstracción por especificación tiene 3 principales clases:
    abstracción por procedimientos, datos y control de iteración.
    <br><br>
    <strong>La abstracción por parametrización</strong> se presenta en el siguiente ejemplo: asume que estás
    desarrollando un sistema para la gestión de inventarios para una tienda. Se puede trabajar la
    gestión de inventario para los productos en general y no para cada tipo de producto, esto debido a
    que las operaciones de inventario son iguales para cualquier tipo de producto. (Python)
    <br><br>
    En el caso de la 
    <strong>abstracción por especificación</strong> se plantea el siguiente ejemplo: Al 
    desarrollar el componente de pagos en línea, donde se requiere implementar pagos con tarjeta de
    crédito y con paypal se sabe que son procesos diferentes, entonces se puede implementar definiendo una
    interfaz que permita invocar el procesar el pago, teniendo una implementación diferente en cada
    tipo de pago. (Java)

    <strong>Acoplamiento y Cohesión</strong>
    <p>El acoplamiento es la medida de interdependencia entre los módulos de un software y la cohesión
    es la medida de relación estrecha entre los elementos de un módulo. Lo deseable en un diseño es
    tener un bajo acoplamiento y alta cohesión, de esta manera, un módulo va a ser independiente y
    agrupará los elementos estrechamente relacionados, dando así flexibilidad al momento de
    reemplazar los módulos del software.</p>
    
    <strong>Descomposición y modularización</strong>
    <p>Es el proceso de dividir un software en componentes donde se logre definir interfaces que
    expresen lo que el componente hace y la forma en que se puede interactuar con él. Esta división
    de componentes se realiza buscando que cada componente tenga una funcionalidad y
    responsabilidad única, bien definida.</p>
    
    <strong>Encapsulamiento</strong>
    <p>Hace referencia al ocultamiento de información mediante la agrupación y empaquetamiento de
    los detalles internos de un componente. Esta forma de abstracción tiene como objetivo que esos
    detalles ocultos sean inaccesibles para las entidades externas</p>
        
    <strong>Separación de interfaces e implementación</strong>
    <p>Este mecanismo consiste en la creación de interfaces que son visibles para los componentes
    clientes que requieren de servicios de otros componentes proveedores y permiten el ocultamiento
    de cómo los componentes proveedores hacen el proceso.</p>

    <strong>    Suficiencia, completitud y sencillez (primitiveness)
    </strong>
    <p>Un componente logra las características de suficiencia y completitud si cumple con su objetivo sin
    tener características redundantes frente a la necesidad planteada por el cliente. Y la sencillez
    depende de su fácil implementación mediante el uso de patrones y buenas prácticas de desarrollo.</p>
    
    <strong>Separación de preocupaciones
    </strong>
    En los proyectos se busca la satisfacción del cliente mediante la definición de componentes que
    puedan representar intereses particulares de diferentes clientes o usuarios. Entonces, la
    separación de preocupaciones se realiza mediante la división en componentes especializados que
    dependen de los intereses de los diferentes clientes.
 
 

