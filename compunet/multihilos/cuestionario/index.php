<ol>
    <li><code>socket.getOutputStream()</code>
        <ul>
            <li>Devuelve un <strong>OutputStream</strong>, es decir, <strong>el canal de bytes que van desde el servidor hacia el cliente</strong>.</li>
            <li>En este momento, solo tienes <strong>bytes crudos</strong>, sin interpretar.</li>
        </ul>
    </li>
    <li><code>new DataOutputStream(...)</code>
        <ul>
            <li>Envuelve ese OutputStream en un <strong>DataOutputStream</strong>, que te permite <strong>escribir datos de manera más conveniente</strong> (por ejemplo, <code>writeBytes(String)</code> para enviar texto HTTP).</li>
            <li>Es <strong>el canal de salida listo para enviar la respuesta HTTP al cliente</strong>.</li>
        </ul>
    </li>
</ol>

