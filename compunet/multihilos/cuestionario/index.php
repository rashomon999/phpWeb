<h2>2️⃣ Línea 2:</h2>
<pre><code>BufferedReader br = new BufferedReader(new InputStreamReader(socket.getInputStream()));</code></pre>

<h3>Desglose</h3>
<ol>
    <li><code>socket.getInputStream()</code>
        <ul>
            <li>Devuelve un <strong>InputStream</strong>, es decir, <strong>el canal de bytes que vienen del cliente hacia el servidor</strong>.</li>
            <li>Aquí llegan <strong>todas las solicitudes HTTP como bytes</strong>.</li>
        </ul>
    </li>
    <li><code>new InputStreamReader(...)</code>
        <ul>
            <li>Convierte esos <strong>bytes en caracteres</strong>.</li>
            <li>Esto es necesario porque HTTP es <strong>texto</strong> (no objetos binarios).</li>
        </ul>
    </li>
    <li><code>new BufferedReader(...)</code>
        <ul>
            <li>Envuelve el InputStreamReader para <strong>leer líneas completas de texto</strong> fácilmente con <code>readLine()</code>.</li>
            <li>Esto permite procesar la <strong>request line</strong> y los <strong>headers HTTP</strong> sin complicaciones.</li>
        </ul>
    </li>
</ol>

