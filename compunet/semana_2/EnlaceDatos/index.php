de Ginebra a Lausana. Cada uno de los tres segmentos del viaje es “directo” entre dos ubicaciones “adyacentes”. Nótese que los 
tres segmentos de transporte son administrados por diferentes compañías y usan modos de transporte completamente diferentes 
(limusina, avión y tren). Aunque los modos de transporte son diferentes, cada uno proporciona el servicio básico de mover 
pasajeros de una ubicación a una ubicación adyacente. En esta analogía de transporte, el turista es un datagrama, cada segmento 
de transporte es un enlace, el modo de transporte es un protocolo de capa de enlace, y el agente de viajes es un protocolo de
enrutamiento.
</p>

 
