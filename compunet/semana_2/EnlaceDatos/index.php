    <h2>Capa de enlace y LANs</h2>

    <p>

    En los dos capítulos anteriores, aprendimos que la capa de red proporciona un servicio de comunicación entre 
    cualquier par de hosts de red. Entre los dos hosts, los datagramas viajan a través de una serie de enlaces de 
    comunicación, algunos cableados y otros inalámbricos, comenzando en el host de origen, pasando por una serie de 
    conmutadores de paquetes (packet switches) (conmutadores (switches) y enrutadores (routers)) y terminando en el host de destino. A medida que continuamos 
    descendiendo en la pila de protocolos, desde la capa de red hasta la capa de enlace, naturalmente nos preguntamos cómo 
    se envían los paquetes a través de los enlaces individuales que componen la ruta de comunicación de extremo a extremo. 
    ¿Cómo se encapsulan los datagramas de la capa de red en las tramas de la capa de enlace para su transmisión a través de 
    un solo enlace? ¿Se utilizan diferentes protocolos de capa de enlace en los distintos enlaces a lo largo de la ruta de 
    comunicación? ¿Cómo se resuelven los conflictos de transmisión en los enlaces de difusión? ¿Existe direccionamiento 
    en la capa de enlace y, si es así, cómo funciona el direccionamiento de la capa de enlace con el direccionamiento de 
    la capa de red que aprendimos en el Capítulo 4? ¿Y cuál es exactamente la diferencia entre un conmutador y un enrutador? 
    Responderemos a estas y otras preguntas importantes en este capítulo.
    </p>

    <p>Al hablar de la capa de enlace, veremos que existen dos tipos fundamentalmente diferentes de canales de capa de enlace:</p>

<ul>
  <li><strong>Primer tipo: canales de difusión (broadcast)</strong>
    <ul>
      <li>Conectan múltiples hosts en:
        <ul>
          <li>Redes LAN inalámbricas.</li>
          <li>Redes satelitales.</li>
          <li>Redes de acceso híbridas de fibra-coaxial (HFC).</li>
        </ul>
      </li>
      <li>Dado que muchos hosts están conectados al mismo canal de comunicación de difusión:
        <ul>
          <li>Se necesita un llamado protocolo de acceso al medio para coordinar la transmisión de tramas.</li>
        </ul>
      </li>
      <li>Coordinación de transmisiones:
        <ul>
          <li>En algunos casos: se puede utilizar un controlador central para coordinar las transmisiones.</li>
          <li>En otros casos: los propios hosts coordinan las transmisiones.</li>
        </ul>
      </li>
    </ul>
  </li>

  <li><strong>Segundo tipo: enlace de comunicación punto a punto (point-to-point)</strong>
    <ul>
      <li>Se encuentra, por ejemplo:
        <ul>
