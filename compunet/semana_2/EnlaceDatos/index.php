    <h3>6.1 Introducción a la capa de enlace </h3>
 <p> 
Comencemos con algo de terminología importante. En este capítulo, nos resultará conveniente referirnos a cualquier 
dispositivo que ejecute un protocolo de capa de enlace (es decir, capa 2) como un nodo. Los nodos incluyen hosts, 
enrutadores, conmutadores y puntos de acceso WiFi (tratados en el Capítulo 7). También nos referiremos a los canales de 
comunicación que conectan nodos adyacentes a lo largo de la ruta de comunicación como enlaces. Para que un datagrama sea 
transferido desde el host de origen hasta el host de destino, debe moverse a través de cada uno de los enlaces individuales
 en la ruta de extremo a extremo. Como ejemplo, en la red de la empresa que se muestra en la parte inferior de la Figura 6.1, 
 considere enviar un datagrama desde uno de los hosts inalámbricos a uno de los servidores. Este datagrama en realidad pasará 
 por seis enlaces: un enlace WiFi entre el host emisor y el punto de acceso WiFi, un enlace Ethernet entre el punto de acceso 
 y un conmutador de capa de enlace; un enlace entre el conmutador de capa de enlace y el enrutador, un enlace entre los dos 
 enrutadores; un enlace Ethernet entre el enrutador y un conmutador de capa de enlace; y finalmente un enlace Ethernet entre 
 el conmutador y el servidor. Sobre un enlace dado, un nodo transmisor encapsula el datagrama en una trama de capa de enlace 
 y transmite la trama al enlace.
</p>


