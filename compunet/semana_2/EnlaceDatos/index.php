<p>
<h3>6.1.1 Los servicios proporcionados por la capa de enlace  </h3>
 

Aunque el servicio básico de cualquier capa de enlace es mover un datagrama desde un nodo a un nodo adyacente a través 
de un único enlace de comunicación, los detalles del servicio proporcionado pueden variar de un protocolo de capa de 
enlace a otro. Los posibles servicios que puede ofrecer un protocolo de capa de enlace incluyen:
</p>

<ul>
<li>
<strong>Encapsulamiento (Framing).</strong> Casi todos los protocolos de capa de enlace encapsulan cada datagrama de capa 
de red dentro de una trama de capa de enlace antes de la transmisión sobre el enlace. Una trama consiste en un campo de datos, 
en el cual se inserta el datagrama de capa de red, y un número de campos de cabecera. La estructura de la trama es especificada 
por el protocolo de capa de enlace. Veremos varios formatos de trama diferentes cuando examinemos protocolos de capa de enlace 
específicos en la segunda mitad de este capítulo.
</li>

<li>
<strong>Acceso al enlace.</strong> Un protocolo de control de acceso al medio (MAC) especifica las reglas por las cuales 
una trama es transmitida sobre el enlace. Para enlaces punto a punto que tienen un único emisor en un extremo del enlace y 
un único receptor en el otro extremo del enlace, el protocolo MAC es simple (o inexistente): el emisor puede enviar una 
trama siempre que el enlace esté inactivo. El caso más interesante es cuando múltiples nodos comparten un único enlace de 
difusión —el llamado problema de acceso múltiple—. Aquí, el protocolo MAC sirve para coordinar las transmisiones de tramas de 
los muchos nodos.
</li>

<li>
<strong>Entrega confiable.</strong> Cuando un protocolo de capa de enlace proporciona un servicio de entrega confiable, 
garantiza mover cada datagrama de capa de red a través del enlace sin error. Recuerde que ciertos protocolos de capa de 
transporte (como TCP) también proporcionan un servicio de entrega confiable. Similar a un servicio de entrega confiable 
de capa de transporte, un servicio de entrega confiable de capa de enlace puede lograrse con acuses de recibo y 
retransmisiones (ver Sección 3.4). Un servicio de entrega confiable de capa de enlace se utiliza a menudo para enlaces 
propensos a altas tasas de error, como un enlace inalámbrico, con el objetivo de corregir un error localmente —en el enlace 
donde ocurre el error— en lugar de forzar una retransmisión de extremo a extremo de los datos por un protocolo de capa de 
transporte o de aplicación. Sin embargo, la entrega confiable de capa de enlace puede considerarse una sobrecarga innecesaria 
para enlaces con baja tasa de error, incluyendo fibra, coaxial y muchos enlaces de par trenzado de cobre. Por esta razón, 
muchos protocolos de capa de enlace cableados no proporcionan un servicio de entrega confiable.
</li>

<li>
<strong>Detección y corrección de errores.</strong> El hardware de capa de enlace en un nodo receptor puede decidir 
incorrectamente que un bit en una trama es cero cuando fue transmitido como uno, y viceversa. Tales errores de bit son 
introducidos por la atenuación de la señal y el ruido electromagnético. Debido a que no hay necesidad de reenviar un 
datagrama que tiene un error, muchos protocolos de capa de enlace proporcionan un mecanismo para detectar tales errores 
de bit. Esto se hace haciendo que el nodo transmisor incluya bits de detección de errores en la trama, y haciendo que el 
nodo receptor realice una verificación de errores. Recuerde de los Capítulos 3 y 4 que la capa de transporte y la capa de 
red de Internet también proporcionan una forma limitada de detección de errores —checksum de Internet—. 
La detección de errores en la capa de enlace suele ser más sofisticada y se implementa en hardware. La corrección de errores 
es similar a la detección de errores, excepto que un receptor no solo detecta cuándo han ocurrido errores de bit en la trama, 
sino que también determina exactamente dónde en la trama han ocurrido los errores (y luego corrige estos errores).
</li>
</ul>

<h3>6.1.2 ¿Dónde está implementada la capa de enlace?</h3>
<p>  
Antes de adentrarnos en nuestro estudio detallado de la capa de enlace, concluyamos esta introducción considerando la 
pregunta de dónde está implementada la capa de enlace. ¿Está implementada la capa de enlace de un host en hardware o software? 
¿Está implementada en una tarjeta o chip separado, y cómo interactúa con el resto de los componentes de hardware y del sistema 
operativo de un host?
</p>

<p>
La Figura 6.2 muestra una arquitectura típica de host. Las capacidades de Ethernet están integradas en el chipset de la 
placa base o implementadas mediante un chip Ethernet dedicado de bajo costo. En su mayor parte, la capa de enlace está 
implementada en un chip llamado adaptador de red, también conocido como controlador de interfaz de red (NIC). El adaptador 
de red implementa muchos servicios de la capa de enlace, incluyendo el encapsulamiento, el acceso al enlace, la detección de 
errores, y así sucesivamente. Así, gran parte de la funcionalidad de un controlador de capa de enlace está implementada en 
hardware. Por ejemplo, los adaptadores de la serie 700 de Intel [Intel 2020] implementan los protocolos Ethernet que 
estudiaremos en la Sección 6.5; el controlador Atheros AR5006 [Atheros 2020] implementa los protocolos WiFi 802.11 que 
estudiaremos en el Capítulo 7.
</p>

<p>
En el lado de envío, el controlador toma un datagrama que ha sido creado y almacenado en la memoria del host por las capas 
superiores de la pila de protocolos, encapsula el datagrama en una trama de capa de enlace (rellenando los diversos campos de
la trama) y luego transmite la trama en el enlace de comunicación, siguiendo el protocolo de acceso al enlace. En el lado de 
recepción, un controlador recibe la trama completa y extrae el datagrama de capa de red. Si la capa de enlace realiza detección 
de errores, entonces es el controlador de envío el que establece los bits de detección de errores en la cabecera de la trama y 
es el controlador de recepción el que realiza la detección de errores.
</p>

<p>
La Figura 6.2 muestra que, aunque la mayor parte de la capa de enlace está implementada en hardware, parte de la capa de 
enlace está implementada en software que se ejecuta en la CPU del host. Los componentes de software de la capa de enlace 
implementan funcionalidades de nivel superior de la capa de enlace, como ensamblar la información de direccionamiento de la 
capa de enlace y activar el hardware del controlador. En el lado de recepción, el software de la capa de enlace responde a 
las interrupciones del controlador (por ejemplo, debido a la recepción de una o más tramas), manejando condiciones de error 
y pasando un datagrama hacia la capa de red. Así, la capa de enlace es una combinación de hardware y software —el lugar en 
la pila de protocolos donde el software se encuentra con el hardware—. [Intel 2020] proporciona una visión general legible 
(así como una descripción detallada) del controlador XL710 desde el punto de vista de la programación de software.
</p>

 <img src="../../../img/guia_121.png" alt="">
<p>
    Figura 6.2 ♦ Adaptador de red: su relación con otros componentes del host y con la funcionalidad de la pila de protocolos
</p>

<p>Controller: El chipset de la placa base es un conjunto de chips en la placa base que gestiona la comunicación entre el CPU, 
    la memoria y otros periféricos, incluyendo el adaptador de red. Puede incluir capacidades de Ethernet integradas 
