        Determinar la Subred, el Rango de Direcciones Utilizables y la Dirección de Broadcast para un Host Dado
    </h3>

    <p>A menudo es necesario determinar la subred, el rango utilizable de direcciones IP de host y la dirección de broadcast 
    para un host dado. Este proceso se describe a continuación.</p>

    <p>1. Identificar la dirección IP y la máscara de subred del host.</p>

    <p>2. Identificar el LSB para determinar el intervalo de las subredes.</p>

    <p>3. Identificar la subred a la que pertenece el host. Para hacer esto, encuentre el ID de subred que esté más cercano a la 
    dirección IP del host sin pasarse. (Piense en el programa de concursos de TV: The price is right - acérquese lo más 
    posible al precio sin pasarse.)</p>

    <p>4. Identificar la dirección de broadcast para la subred. Para hacer esto, encuentre el siguiente ID de subred y reste 
    una dirección.</p>

    <p>5. Identificar el rango utilizable de direcciones IP de host. Para hacer esto, sume una dirección al ID de subred y 
    reste una dirección de la dirección de broadcast.</p>

