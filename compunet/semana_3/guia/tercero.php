    <p>La figura anterior muestra el esquema de direccionamiento IP para la misma región utilizando VLSM. A la organización 
    todavía se le asigna la red 172.16.0.0 /16, pero ahora a la región se le asigna la subred 172.16.8.0 /21. Esta subred 
    tiene un total de once bits de host disponibles.</p>

    <p>Dentro de la región, la subred 172.16.8.0 /21 se subdivide de la siguiente manera:</p>

    <p>1. Las redes conectadas al enrutador HQ se asignan a las subredes 172.16.8.0 /24 y 172.16.9.0 /24. Se elige un prefijo 
    de 24 bits en función del número de hosts que la subred necesita soportar. Cada una de estas subredes necesita soportar 
    200 hosts. Para soportar 200 hosts, se deben reservar ocho bits de host. Si se reservan ocho bits de host, se pueden dar 
    24 bits a la parte de red. (Por ejemplo: 11 bits de host totales - 8 bits de host reservados = 3 bits adicionales dados a 
    la red. 21 bits de red existentes + 3 bits de red adicionales = 24 bits de red totales). Aunque esto crea 254 direcciones 
    IP de host posibles, este excedente es aceptable por dos razones:</p>

    <p>a. 254 direcciones IP de host posibles es lo más cercano a 200 sin estar por debajo. Si solo se reservaran siete bits 
        de host, solo habría 126 direcciones IP de host posibles.</p>

    <p>b. 54 direcciones IP de host adicionales dejan espacio para el crecimiento dentro de la subred. Como regla general, 
    agregue un 20 por ciento al número real de hosts para permitir el crecimiento. Por ejemplo, las redes conectadas al 
    enrutador HQ necesitan soportar 200 hosts, por lo que planear para 240 hosts permite un margen de crecimiento dentro de 
    la subred.</p>

    <p>2. La siguiente subred disponible después de la subred 172.16.9.0 /24 es la 172.16.10.0 /24. Esta subred se subdivide 
    aún más en subredes usando 26 bits de red (un prefijo de 26 bits) para las LAN en las sucursales. Nuevamente, el prefijo 
    se elige para estas subredes en función de la necesidad de que cada una de las subredes de sucursal soporte 50 hosts. 
    Para soportar 50 hosts, se deben reservar seis bits de host. Si se reservan seis bits de host, se pueden dar 26 bits a 
    la parte de red. Aunque esto crea 62 hosts posibles por subred, este excedente es aceptable.</p>

    <p>3. La siguiente subred disponible después de la subred 172.16.10.192 /26 es la 172.16.11.0 /26. Esta subred se 
    subdivide aún más en subredes usando 30 bits de red (un prefijo de 30 bits) para los enlaces WAN entre los enrutadores. 
    Nuevamente, el prefijo se elige en función de la necesidad de que cada uno de los enlaces WAN soporte dos hosts. Para 
    soportar dos hosts, se deben reservar dos bits de host. Si se reservan dos bits de host, se pueden dar 30 bits a la 
    parte de red. En este caso, tener exactamente dos direcciones IP de host es aceptable porque estos son enlaces WAN 
    seriales. Los enlaces WAN seriales son punto a punto, lo que significa que solo tienen dos dispositivos. Dado que no 
    más de dos dispositivos pueden conectarse a un enlace punto a punto, no es necesario tener en cuenta el posible 
    crecimiento en esas redes.</p>

    <p>La figura también demuestra la agregación o resumen de rutas. En otras palabras, las diez subredes dentro de la 
    región pueden representarse mediante la subred 172.16.8.0 /21. Esto significa que los enrutadores de otras regiones 
    dentro de la organización tienen solo una entrada en sus tablas de enrutamiento en lugar de diez. Tener una tabla de 
    enrutamiento más pequeña permite que un enrutador reenvíe paquetes más rápido. Además, al tener una ruta resumida, si 
    hay un cambio de topología dentro de la región, los enrutadores de otras regiones no necesitan volver a ejecutar el 
    protocolo de enrutamiento. Esto hace que el reenvío de paquetes sea mucho más rápido.</p>

