 <h2>4.3 El Protocolo de Internet (IP): IPv4, Addressing, IPv6, y Más</h2>
  <p>
    Nuestro estudio de la capa de red hasta ahora en el Capítulo 4 — la noción del componente de plano de datos y de control de la capa de red, nuestra distinción entre <i>forwarding</i> y <i>routing</i>, la identificación de varios modelos de servicio de red, y nuestra mirada dentro de un <i>router</i> — ha sido a menudo sin referencia a ninguna arquitectura o protocolo de red de computadoras específico. En esta sección, nos enfocaremos en aspectos clave de la capa de red en el Internet actual y el célebre Protocolo de Internet (IP).
  </p>
  <p>
    Existen dos versiones de IP en uso hoy en día. Primero examinaremos la versión ampliamente desplegada del protocolo IP, la versión 4, que usualmente se denomina simplemente IPv4 [RFC 791] en la Sección 4.3.1. Examinaremos la versión 6 de IP [RFC 2460; RFC 4291], que ha sido propuesta para reemplazar a IPv4, en la Sección 4.3.4. Entre tanto, cubriremos principalmente el direccionamiento en Internet — un tema que podría parecer bastante seco y orientado a los detalles, pero que veremos es crucial para entender cómo funciona la capa de red del Internet. ¡Dominar el direccionamiento IP es dominar la capa de red del Internet misma!
  </p>

  <h3>4.3.1 Formato de Datagram IPv4</h3>
  <p>
    Recuerda que el paquete de la capa de red del Internet se denomina <i>datagram</i>. Comenzamos nuestro estudio de IP con una visión general de la sintaxis y semántica del <i>datagram</i> IPv4. Podrías estar pensando que nada podría ser más seco que la sintaxis y semántica de los bits de un paquete. Sin embargo, el <i>datagram</i> juega un papel central en el Internet — todo estudiante y profesional de redes necesita verlo, absorberlo y dominarlo. (Y solo para ver que los encabezados de protocolo pueden, de hecho, ser divertidos de estudiar, revisa [Pomeranz 2010]). El formato del <i>datagram</i> IPv4 se muestra en la Figura 4.17. Los campos clave en el <i>datagram</i> IPv4 son los siguientes:
  </p>

  <ul>
    <li>
      <b>Número de versión.</b> Estos 4 bits especifican la versión del protocolo IP del <i>datagram</i>. Al mirar el número de versión, el <i>router</i> puede determinar cómo interpretar el resto del <i>datagram</i> IP. Diferentes versiones de IP usan diferentes formatos de <i>datagram</i>. El formato de <i>datagram</i> para IPv4 se muestra en la Figura 4.17. El formato de <i>datagram</i> para la nueva versión de IP (IPv6) se discute en la Sección 4.3.4.
    </li>
    <li>
      <b>Longitud del encabezado.</b> Debido a que un <i>datagram</i> IPv4 puede contener un número variable de opciones (que están incluidas en el <i>header</i> del <i>datagram</i> IPv4), estos 4 bits son necesarios para determinar dónde en el <i>datagram</i> IP realmente comienza el <i>payload</i> (por ejemplo, el <i>transport-layer segment</i> que está siendo encapsulado en este <i>datagram</i>). La mayoría de los <i>datagrams</i> IP no contienen opciones, por lo que el <i>datagram</i> IP típico tiene un <i>header</i> de 20 bytes.
    </li>
  </ul>
    
    <img src="../../../img/guia_141.png" alt="">

    
    <ul>
    <li>
      <b>Type of service.</b> Los bits de <i>type of service (TOS)</i> fueron incluidos en el <i>header</i> IPv4 para permitir que diferentes tipos de <i>IP datagrams</i> fueran distinguidos entre sí. Por ejemplo, podría ser útil distinguir <i>real-time datagrams</i> (como aquellos usados por una aplicación de telefonía IP) del tráfico <i>non-real-time</i> (por ejemplo, FTP). El nivel específico de servicio a ser provisto es un asunto de políticas determinado y configurado por el administrador de red para ese <i>router</i>. También aprendimos en la Sección 3.7.2 que dos de los bits TOS son usados para <i>Explicit Congestion Notification</i>.
    </li>
    <li>
      <b>Datagram length.</b> Esta es la longitud total del <i>IP datagram</i> (<i>header</i> más datos), medida en bytes. Como este campo tiene una longitud de 16 bits, el tamaño máximo teórico del <i>IP datagram</i> es de 65,535 bytes. Sin embargo, los <i>datagrams</i> raramente son mayores a 1,500 bytes, lo cual permite que un <i>IP datagram</i> quepa en el campo <i>payload</i> de un <i>Ethernet frame</i> de tamaño máximo.
    </li>
    <li>
      <b>Identifier, flags, fragmentation offset.</b> Estos tres campos tienen que ver con la llamada <i>IP fragmentation</i>, cuando un gran <i>IP datagram</i> es dividido en varios <i>IP datagrams</i> más pequeños que son luego reenviados independientemente hacia el destino, donde son reensamblados antes de que sus datos en el <i>payload</i> (ver más abajo) sean pasados hacia la capa de transporte en el <i>destination host</i>. Curiosamente, la nueva versión de IP, IPv6, no permite <i>fragmentation</i>. No cubriremos la fragmentación aquí; pero los lectores pueden encontrar una discusión detallada en línea, entre el material “retirado” de versiones anteriores de este libro.
    </li>
    <li>
      <b>Time-to-live.</b> El campo <i>time-to-live (TTL)</i> se incluye para asegurar que los <i>datagrams</i> no circulen para siempre (debido, por ejemplo, a un <i>routing loop</i> de larga duración) en la red. Este campo se decrementa en uno cada vez que el <i>datagram</i> es procesado por un <i>router</i>. Si el campo TTL llega a 0, un <i>router</i> debe descartar ese <i>datagram</i>.
    </li>
    <li>
      <b>Protocol.</b> Este campo es típicamente usado solo cuando un <i>IP datagram</i> alcanza su destino final. El valor de este campo indica el protocolo específico de la capa de transporte al cual la porción de datos de este <i>IP datagram</i> debe ser pasada. Por ejemplo, un valor de 6 indica que la porción de datos es pasada a TCP, mientras que un valor de 17 indica que los datos son pasados a UDP. Para una lista de todos los posibles valores, ver [IANA Protocol Numbers 2016]. Nota que el número de protocolo en el <i>IP datagram</i> tiene un rol que es análogo al rol del campo número de puerto en el <i>transport-layer segment</i>. El número de protocolo es el pegamento que une la capa de red y la capa de transporte, mientras que el número de puerto es el pegamento que une la capa de transporte y la capa de aplicación. Veremos en el Capítulo 6 que el <i>link-layer frame</i> también tiene un campo especial que une la capa de enlace con la capa de red.
    </li>
    <li>
      <b>Header checksum.</b> El <i>header checksum</i> ayuda a un <i>router</i> a detectar errores de bits en un <i>IP datagram</i> recibido. El <i>header checksum</i> es calculado tratando cada 2 bytes en el <i>header</i> como un número y sumando estos números usando aritmética de complemento a 1. Como se discutió en la Sección 3.3, el complemento a 1 de esta suma, conocido como el <i>Internet checksum</i>, se almacena en el campo de <i>checksum</i>. Un <i>router</i> calcula el <i>header checksum</i> para cada <i>IP datagram</i> recibido y detecta una condición de error si el <i>checksum</i> transportado en el <i>datagram header</i> no es igual al <i>checksum</i> calculado. Los <i>routers</i> típicamente descartan <i>datagrams</i> para los cuales un error ha sido detectado. Nota que el <i>checksum</i> debe ser recalculado y almacenado nuevamente en cada <i>router</i>, ya que el campo TTL, y posiblemente también el campo de <i>options</i>, cambiarán. Una discusión interesante de algoritmos rápidos para calcular el <i>Internet checksum</i> está en [RFC 1071].
      <br><br>
      Una pregunta frecuentemente hecha en este punto es, ¿por qué TCP/IP realiza verificación de errores tanto en la capa de transporte como en la capa de red? Hay varias razones para esta repetición. Primero, nota que solo el <i>IP header</i> es verificado en la capa IP, mientras que el <i>TCP/UDP checksum</i> es calculado sobre el <i>TCP/UDP segment</i> completo. Segundo, TCP/UDP e IP no necesariamente tienen que pertenecer ambos a la misma pila de protocolos. TCP puede, en principio, ejecutarse sobre un protocolo de capa de red diferente (por ejemplo, ATM) [Black 1995]) e IP puede transportar datos que no serán pasados a TCP/UDP.
    </li>

    
  </ul>

