        <ul>
    <li>
      <b>Source and destination IP addresses.</b> Cuando una fuente crea un <i>datagram</i>, inserta su dirección IP en el campo <i>source IP address</i> e inserta la dirección del destino final en el campo <i>destination IP address</i>. A menudo el <i>source host</i> determina la dirección de destino mediante una búsqueda DNS, como se discutió en el Capítulo 2. Discutiremos el direccionamiento IP en detalle en la Sección 4.3.2.
    </li>
    <li>
      <b>Options.</b> Los campos de <i>options</i> permiten que un <i>IP header</i> sea extendido. Las <i>header options</i> fueron pensadas para ser usadas raramente — de ahí la decisión de ahorrar sobrecarga al no incluir la información en los campos de <i>options</i> en cada <i>datagram header</i>. Sin embargo, la mera existencia de <i>options</i> sí complica las cosas — dado que los <i>datagram headers</i> pueden tener longitud variable, no se puede determinar <i>a priori</i> dónde comenzará el campo de datos. Además, ya que algunos <i>datagrams</i> pueden requerir procesamiento de <i>options</i> y otros no, la cantidad de tiempo necesario para procesar un <i>IP datagram</i> en un <i>router</i> puede variar enormemente. Estas consideraciones se vuelven particularmente importantes para el procesamiento IP en <i>routers</i> y <i>hosts</i> de alto rendimiento. Por estas razones y otras, las <i>IP options</i> no fueron incluidas en el <i>IPv6 header</i>, como se discute en la Sección 4.3.4.
    </li>
    <li>
      <b>Data (payload).</b> Finalmente, llegamos al último y más importante campo — ¡la <i>raison d’être</i> del <i>datagram</i> en primer lugar! En la mayoría de las circunstancias, el campo de datos del <i>IP datagram</i> contiene el <i>transport-layer segment</i> (TCP o UDP) que debe ser entregado al destino. Sin embargo, el campo de datos puede transportar otros tipos de datos, tales como mensajes ICMP (discutidos en la Sección 5.6).
      <br><br>
      Nota que un <i>IP datagram</i> tiene un total de 20 bytes de <i>header</i> (asumiendo que no haya <i>options</i>). Si el <i>datagram</i> transporta un segmento TCP, entonces cada <i>datagram</i> lleva un total de 40 bytes de <i>header</i> (20 bytes de <i>IP header</i> más 20 bytes de <i>TCP header</i>) junto con el mensaje de la capa de aplicación.
    </li>
  </ul>



      <h3>4.3.2 IPv4 Addressing</h3>
  <p>
    Ahora dirigimos nuestra atención al <i>IPv4 addressing</i>. Aunque puedas estar pensando que el <i>addressing</i> debe ser un tema sencillo, con suerte al final de esta sección estarás convencido de que el <i>Internet addressing</i> no solo es un tema jugoso, sutil e interesante, sino también uno de importancia central para el Internet. Un tratamiento excelente de <i>IPv4 addressing</i> se puede encontrar en el primer capítulo en [Stewart 1999].
  </p>
  <p>
    Antes de discutir <i>IP addressing</i>, sin embargo, necesitaremos decir unas pocas palabras acerca de cómo los <i>hosts</i> y <i>routers</i> están conectados al Internet. Un <i>host</i> típicamente tiene solo un único enlace hacia la red; cuando IP en el <i>host</i> quiere enviar un <i>datagram</i>, lo hace sobre este enlace. El límite entre el <i>host</i> y el enlace físico se llama una <i>interface</i>. Ahora considera un <i>router</i> y sus <i>interfaces</i>. Debido a que el trabajo de un <i>router</i> es recibir un <i>datagram</i> en un enlace y reenviar el <i>datagram</i> en algún otro enlace, un <i>router</i> necesariamente tiene dos o más enlaces a los que está conectado. El límite entre el <i>router</i> y cualquiera de sus enlaces también se llama una <i>interface</i>. Un <i>router</i> por lo tanto tiene múltiples <i>interfaces</i>, una por cada uno de sus enlaces. Debido a que cada <i>host</i> y <i>router</i> es capaz de enviar y recibir <i>IP datagrams</i>, IP requiere que cada <i>host</i> y cada <i>router interface</i> tenga su propia dirección IP. Así, una dirección IP está asociada técnicamente con una <i>interface</i>, en lugar de con el <i>host</i> o <i>router</i> que contiene esa <i>interface</i>.
  </p>
  <p>
    Cada dirección IP tiene 32 bits de longitud (equivalente a 4 bytes), y por lo tanto hay un total de 2<sup>32</sup> (o aproximadamente 4 mil millones) posibles direcciones IP. Estas direcciones son típicamente escritas en la llamada <i>dotted-decimal notation</i>, en la cual cada byte de la dirección se escribe en su forma decimal y se separa por un punto (<i>dot</i>) de los otros bytes en la dirección. Por ejemplo, considera la dirección IP 193.32.216.9. El 193 es el equivalente decimal de los primeros 8 bits de la dirección; el 32 es el equivalente decimal del segundo conjunto de 8 bits de la dirección, y así sucesivamente. Así, la dirección 193.32.216.9 en notación binaria es:
  </p>
  <p>
    <code>11000001 00100000 11011000 00001001</code>
  </p>
  <p>
    Cada <i>interface</i> en cada <i>host</i> y <i>router</i> en el Internet global debe tener una dirección IP que sea globalmente única (excepto para <i>interfaces</i> detrás de <i>NATs</i>, como se discute en la Sección 4.3.3). Sin embargo, estas direcciones no pueden ser elegidas de manera arbitraria. Una porción de la dirección IP de una <i>interface</i> será determinada por la <i>subnet</i> a la cual está conectada.
  </p>
  <p>
    La Figura 4.18 proporciona un ejemplo de <i>IP addressing</i> e <i>interfaces</i>. En esta figura, un <i>router</i> (con tres <i>interfaces</i>) es usado para interconectar siete <i>hosts</i>. Observa de cerca las direcciones IP asignadas a los <i>hosts</i> y a las <i>router interfaces</i>, ya que hay varias cosas que notar. Los tres <i>hosts</i> en la parte superior izquierda de la Figura 4.18, y la <i>router interface</i> a la cual están conectados, todos tienen una dirección IP de la forma 223.1.1.xxx. Es decir, todos tienen los mismos 24 bits más a la izquierda en su dirección IP. Estas cuatro <i>interfaces</i> también están interconectadas entre sí por una red que no contiene <i>routers</i>. Esta red podría estar interconectada por una <i>Ethernet LAN</i>, en cuyo caso las <i>interfaces</i> estarían interconectadas por un <i>Ethernet switch</i> (como discutiremos en el Capítulo 6), o por un <i>wireless access point</i> (como discutiremos en el Capítulo 7). Representaremos por ahora esta red sin <i>router</i> que conecta estos <i>hosts</i> como una nube, y profundizaremos en los detalles internos de tales redes en los Capítulos 6 y 7.
  </p>
  <p>
    En términos de IP, esta red que interconecta tres <i>host interfaces</i> y una <i>router interface</i> forma una <i>subnet</i> [RFC 950]. (Una <i>subnet</i> también se llama una <i>IP network</i> o simplemente una <i>network</i> en la literatura de Internet). El <i>IP addressing</i> asigna una dirección a esta <i>subnet</i>: 223.1.1.0/24, donde la notación /24 (“slash-24”), a veces conocida como <i>subnet mask</i>, indica que los 24 bits más a la izquierda de la cantidad de 32 bits definen la dirección de la <i>subnet</i>. La <i>223.1.1.0/24 subnet</i> consiste por lo tanto de las tres <i>host interfaces</i> (223.1.1.1, 223.1.1.2, y 223.1.1.3) y una <i>router interface</i> (223.1.1.4). Cualquier <i>host</i> adicional conectado a la <i>223.1.1.0/24 subnet</i> estaría obligado a tener una dirección de la forma 223.1.1.xxx. Hay dos <i>subnets</i> adicionales mostradas en la Figura 4.18: la <i>223.1.2.0/24 network</i> y la <i>223.1.3.0/24 subnet</i>. La Figura 4.19 ilustra las tres <i>IP subnets</i> presentes en la Figura 4.18.
  </p>

