    <h2>Direcciones Clase B</h2>

    <p>En una dirección de Clase B, los dos primeros octetos de la dirección IP se usan como el Network ID, y los dos últimos 
    octetos se usan como el Host ID. Así, una dirección de Clase B se acerca a mi esquema hipotético de dividir la dirección 
    por la mitad, usando la mitad para el Network ID y la otra mitad para el Host ID. Sin embargo, no es idéntico a este 
    esquema, porque los dos primeros bits del primer octeto deben ser <code>10</code>, con el fin de indicar que la dirección 
    es una dirección de Clase B.</p>

    <p>Como resultado, puede existir un total de 16,384 redes de Clase B. Todas las direcciones de Clase B caen dentro del 
    rango <code>128.x.y.z</code> hasta <code>191.x.y.z</code>. Cada dirección de Clase B puede acomodar más de 
    65,000 hosts.</p>

    <p>El problema con las redes de Clase B es que, aunque son mucho más pequeñas que las redes de Clase A, todavía asignan 
    demasiados Host IDs. Muy pocas redes tienen decenas de miles de hosts. Así, la asignación descuidada de direcciones 
    de Clase B puede llevar a que un gran porcentaje de las direcciones de host disponibles se desperdicie en organizaciones 
    que no las necesitan.</p>


