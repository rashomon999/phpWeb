    <h2>Clasificación de Direcciones IP</h2>

  <p>Cuando los diseñadores originales del protocolo IP crearon el esquema de direccionamiento IP, podrían haber asignado un número arbitrario de bits de la dirección IP para el Network ID. Los bits restantes entonces se usarían para el Host ID. Por ejemplo, supongamos que los diseñadores decidieron que la mitad de la dirección (16 bits) se usaría para la red, y los 16 bits restantes se usarían para el Host ID. El resultado de ese esquema sería que Internet podría tener un total de 65,536 redes, y cada una de esas redes podría tener 65,536 hosts.</p>

  <p>En los primeros días de Internet, este esquema probablemente parecía varios órdenes de magnitud más de lo que alguna vez se necesitaría. Sin embargo, los diseñadores de IP se dieron cuenta desde el principio de que pocas redes en realidad tendrían decenas de miles de hosts. Supón que una red de 1,000 computadoras se une a Internet y se le asigna uno de estos hipotéticos Network IDs. Debido a que esa red usará solo 1,000 de sus 65,536 direcciones de host, más de 64,000 direcciones IP se desperdiciarían.</p>

  <p>Como solución a este problema, se introdujo la idea de las clases de direcciones IP. El protocolo IP define cinco diferentes clases de direcciones: A, B, C, D y E. Cada una de las tres primeras clases, A–C, usa un tamaño diferente para la porción de Network ID y Host ID de la dirección. La Clase D es para un tipo especial de dirección llamada <em>multicast address</em>. La Clase E es una clase experimental de direcciones que no se utiliza.</p>

  <p>Los primeros cuatro bits de la dirección IP se usan para determinar en qué clase encaja una dirección en particular, de la siguiente manera:</p>

  <ul>
    <li><strong>Class A:</strong> El primer bit es cero.</li>
    <li><strong>Class B:</strong> El primer bit es uno, y el segundo bit es cero.</li>
    <li><strong>Class C:</strong> Los dos primeros bits son ambos uno, y el tercer bit es cero.</li>
    <li><strong>Class D:</strong> Los tres primeros bits son todos uno, y el cuarto bit es cero.</li>
    <li><strong>Class E:</strong> Los primeros cuatro bits son todos uno.</li>
  </ul>





