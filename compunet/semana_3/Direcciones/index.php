  <h2>Introducción a las direcciones IP</h2>
  <p>Una dirección IP es un número que identifica de manera única cada host en una red IP. Las direcciones IP operan en la capa de red del stack de protocolos TCP/IP, por lo que son independientes de las direcciones MAC de capa de enlace de datos más bajas, tales como las direcciones MAC de Ethernet.</p>

  <p>Las direcciones IP son números binarios de 32 bits, lo que significa que, teóricamente, un máximo de algo en el vecindario de 4 mil millones de direcciones de host únicas puede existir a lo largo de Internet. Pensarías que eso sería suficiente, pero TCP/IP impone ciertas restricciones sobre cómo se asignan las direcciones IP. Estas restricciones limitan severamente el número total de direcciones IP utilizables. Muchos expertos predicen que pronto nos quedaremos sin direcciones IP. Sin embargo, nuevas técnicas para trabajar con direcciones IP han ayudado a aliviar este problema, y se ha adoptado un estándar para direcciones IP de 128 bits, aunque todavía no está en uso generalizado.</p>

  <h2>Redes y hosts</h2>
  <p>IP significa <em>Internet Protocol</em>, y su propósito principal es habilitar comunicaciones entre redes. Como resultado, una dirección IP de 32 bits en realidad consiste en dos partes:</p>

  <ul>
    <li><strong>El Network ID</strong> (o dirección de red): Identifica la red en la cual se puede encontrar una computadora host.</li>
    <li><strong>El Host ID</strong> (o dirección de host): Identifica un dispositivo específico en la red indicada por el Network ID.</li>
  </ul>

  <p>La mayor parte de la complejidad de trabajar con direcciones IP tiene que ver con averiguar qué parte de la dirección IP completa de 32 bits es el Network ID y cuál parte es el Host ID, como se describe en las siguientes secciones.</p>

  <p>Al describir los detalles de cómo se asignan los Host IDs, puedes notar que dos direcciones de host parecen no estar contabilizadas. Por ejemplo, el esquema de direccionamiento de Clase C, que usa ocho bits para el Host ID, permite solo 254 hosts — no los 256 hosts que esperarías. El Host ID no puede ser 0 (el Host ID es todo ceros) porque esa dirección siempre está reservada para representar la red misma. Y el Host ID no puede ser 255 (el Host ID es todo unos) porque ese Host ID está reservado para usarse como una solicitud de broadcast que está destinada a todos los hosts en la red.</p>

  <h2>El baile de los puntos decimales</h2>
  <p>Las direcciones IP usualmente se representan en un formato conocido como notación decimal punteada. En la notación decimal punteada, cada grupo de ocho bits — un octeto — es representado por su equivalente decimal. Por ejemplo, considera la siguiente dirección IP binaria:</p>

  <pre>11000000101010001000100000011100</pre>

  <p>Para convertir este valor a notación decimal punteada, primero divídelo en cuatro octetos, de la siguiente manera:</p>

  <pre>11000000 10101000 10001000 00011100</pre>

  <p>Luego, convierte cada uno de los octetos a su equivalente decimal:</p>

  <pre>
11000000 10101000 10001000 00011100
192       168       136        28
  </pre>

  <p>Después, usa puntos para separar los cuatro números decimales, así:</p>
  <pre>192.168.136.28</pre>

  <p>Este es el formato en el cual usualmente verás representadas las direcciones IP.</p>

