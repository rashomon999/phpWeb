<pre><code>import java.util.Scanner;
</code></pre>

<p>Sin esta importación, el compilador no reconoce la clase.</p>

<h3>Creación de un Scanner</h3>

<pre><code>Scanner entrada = new Scanner(System.in);
</code></pre>

<p>
Aquí se crea un objeto <code>Scanner</code> asociado a la entrada estándar del teclado (<code>System.in</code>).  
La variable <code>entrada</code> es una <strong>referencia</strong> al objeto Scanner, no el objeto en sí.
</p>

<h3>Métodos para leer tipos primitivos</h3>

<p><code>Scanner</code> convierte automáticamente los tokens en valores primitivos:</p>

<table border="1" cellpadding="8" cellspacing="0">
  <tr><th>Método</th><th>Tipo leído</th><th>Ejemplo</th></tr>
  <tr><td><code>nextInt()</code></td><td>int</td><td>int n = entrada.nextInt();</td></tr>
  <tr><td><code>nextLong()</code></td><td>long</td><td>long x = entrada.nextLong();</td></tr>
  <tr><td><code>nextFloat()</code></td><td>float</td><td>float f = entrada.nextFloat();</td></tr>
  <tr><td><code>nextDouble()</code></td><td>double</td><td>double d = entrada.nextDouble();</td></tr>
  <tr><td><code>nextBoolean()</code></td><td>boolean</td><td>boolean b = entrada.nextBoolean();</td></tr>
  <tr><td><code>next()</code></td><td>String (token)</td><td>String s = entrada.next();</td></tr>
  <tr><td><code>nextLine()</code></td><td>String (línea completa)</td><td>String t = entrada.nextLine();</td></tr>
</table>

<h3>Tokens y delimitadores</h3>

<p>
Un <strong>token</strong> es una pieza de texto delimitada por espacios en blanco (por defecto).  
Los métodos como <code>nextInt()</code> leen solo el siguiente token y lo convierten al tipo correspondiente.
</p>

<p>
El método <code>nextLine()</code> es especial: lee <strong>toda la línea</strong> hasta el salto de línea.
Es común necesitar una llamada adicional a <code>nextLine()</code> después de usar <code>nextInt()</code> 
u otros métodos numéricos, para consumir el salto de línea pendiente.
</p>

<h3>Validación de entrada</h3>

<p>
Scanner puede comprobar si el siguiente token coincide con el tipo esperado antes de leerlo:
</p>

<pre><code>if (entrada.hasNextInt()) {
    int n = entrada.nextInt();
} else {
    System.out.println("Entrada inválida");
}
</code></pre>

<p>
Los métodos de verificación (<code>hasNextInt</code>, <code>hasNextDouble</code>, etc.) permiten evitar fallos
por valores no convertibles.
</p>

<h3>Scanner y tipos por referencia</h3>

<p>
Los métodos de <code>Scanner</code> devuelven valores primitivos (como <code>int</code>, <code>double</code>, 
<code>boolean</code>) o cadenas (<code>String</code>, que es un tipo por referencia).
</p>

<p>
El propio <code>Scanner</code> es un objeto, por lo que requiere:
</p>

<ul>
  <li>importación (<code>java.util</code>)</li>
  <li>creación mediante <code>new</code></li>
  <li>una referencia para poder acceder a sus métodos</li>
</ul>

<h3>Cerrar el Scanner</h3>

<pre><code>entrada.close();
</code></pre>

<p>
Cerrar el Scanner libera los recursos asociados.  
No es obligatorio cuando se usa <code>System.in</code> en programas simples, pero sí es buena práctica en aplicaciones más grandes.
</p>

