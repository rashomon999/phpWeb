<h4>Declaración de una clase</h4>

<h3>Estructura básica</h3>
<p>Una aplicación Java siempre define al menos una clase. Ejemplo:</p>

<pre><code>public class Bienvenido1 {
    ...
}
</code></pre>

<p>La palabra clave <strong>class</strong> inicia la declaración. Las palabras clave de Java son reservadas y siempre se escriben en minúscula. Implicación: Si usas mayúsculas en una palabra clave (e.g., Class), el compilador genera un error, ya que Java es case sensitive.</p>

<h3>Implicaciones de modificadores y visibilidad</h3>
<p>Usar <code>public</code> en una clase permite que sea accesible desde otros paquetes, lo cual es esencial para aplicaciones principales. Sin él (default), la clase solo es visible en su paquete, limitando su uso en proyectos grandes. Para el método main, debe ser public para que la JVM lo invoque desde fuera.</p>

<h3>Implicaciones de case sensitive y reglas de nombrado</h3>
<p>Debido a que Java es case sensitive, un error como declarar <code>Main</code> en lugar de <code>main</code> impide que la JVM encuentre el punto de entrada, causando un error en runtime (e.g., "NoSuchMethodError"). Esto enfatiza la precisión en el código.</p>

<h3>Implicaciones de la regla del nombre del archivo</h3>
<p>Ejemplo: Para <code>public class Bienvenido1</code>, el archivo debe ser <code>Bienvenido1.java</code>. Si lo nombras <code>bienvenido1.java</code> (minúsculas), el compilador falla con un error como "class Bienvenido1 is public, should be declared in a file named Bienvenido1.java". Esto asegura consistencia en proyectos multi-archivo.</p>

<h3>Reglas e implicaciones para identificadores</h3>
<ul>
    <li>Pueden contener letras, dígitos, guión bajo (_) y signo de moneda ($). Implicación: Usar otros caracteres (e.g., @) causa error de sintaxis.</li>
    <li>No pueden iniciar con un dígito. Implicación: <code>7boton</code> es inválido; debe ser <code>boton7</code>.</li>
    <li>No pueden contener espacios. Implicación: <code>campo entrada</code> falla; usa CamelCase como <code>campoEntrada</code>.</li>
    <li>Java distingue entre mayúsculas y minúsculas. Implicación: <code>Variable</code> y <code>variable</code> son distintos, lo que puede causar confusiones si no se sigue la convención.</li>
</ul>

<h3>Convención para nombres de clases y su importancia</h3>
<ul>
    <li>Deben iniciar con mayúscula. Implicación: Facilita distinguir clases de variables (que inician en minúscula).</li>
    <li>Se usa CamelCase: <code>EjemploDeClase</code>. Implicación: Mejora la legibilidad en código colaborativo; ignorarla no causa errores, pero viola estándares de Oracle y puede confundir a otros desarrolladores.</li>
</ul>

<h3>El método main: implicaciones como punto de entrada</h3>
<pre><code>public static void main(String[] args)</code></pre>
<p>Los paréntesis definen parámetros; sin main exacto, la JVM lanza "Error: Main method not found", impidiendo la ejecución. <code>static</code> permite invocarlo sin instanciar la clase, ideal para inicio. <code>void</code> significa no retorna valor, y <code>args</code> captura inputs externos (e.g., java Bienvenido1 arg1 arg2), útil para scripts parametrizados.</p>

