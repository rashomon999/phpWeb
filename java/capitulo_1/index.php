<h5>Tipos por Referencia</h5>
<p>
Corresponden a clases, interfaces, arreglos, enumeraciones y anotaciones. Las variables de este tipo no contienen el valor directamente, sino una referencia a un objeto almacenado en el montón (heap). A través de esta referencia se accede a los datos y métodos definidos por el objeto. El valor por defecto es null.
</p>
<p>Categorías principales:</p>
<ul>
    <li><strong>Clases:</strong> Definidas por el usuario o en la biblioteca estándar (e.g., String, ArrayList).</li>
    <li><strong>Interfaces:</strong> Contratos para comportamiento (e.g., List, Runnable).</li>
    <li><strong>Arreglos:</strong> Colecciones de elementos del mismo tipo (primitivos o referencias).</li>
    <li><strong>Enumeraciones:</strong> Conjuntos de constantes (e.g., enum Dia {LUNES, MARTES}).</li>
    <li><strong>Anotaciones:</strong> Metadatos (e.g., @Override).</li>
</ul>

