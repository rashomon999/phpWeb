    <p>Los identificadores SQL se usan para identificar objetos en la base de datos, tales como nombres de tablas, nombres de vistas y columnas. Los caracteres que se pueden usar en un identificador SQL definido por el usuario deben aparecer en un conjunto de caracteres. El estándar ISO proporciona un conjunto de caracteres por defecto, que consiste en las letras mayúsculas A ... Z, las letras minúsculas a ... z, los dígitos 0 ... 9 y el carácter de subrayado (_). También es posible especificar un conjunto de caracteres alternativo. Las siguientes restricciones se imponen sobre un identificador:</p>
