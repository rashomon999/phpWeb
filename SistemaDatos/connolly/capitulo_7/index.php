    <p>El tipo de datos numéricos exactos se utiliza para definir números con una representación exacta. El número consta de dígitos, un punto decimal opcional y un signo opcional. Un tipo de datos numérico exacto consta de una precisión y una escala. La precisión indica el número total de dígitos decimales significativos, es decir, el número total de dígitos, incluidos los decimales, pero excluyendo el punto en sí. La escala indica el número total de lugares decimales. Por ejemplo, el valor numérico exacto –12.345 tiene precisión 5 y escala 3. Un caso especial de tipo numérico exacto ocurre con los enteros.</p>
    
