    <h4>Datos booleanos</h4>
    <p>Los datos booleanos consisten en los valores de verdad distintos <strong>TRUE</strong> (verdadero) y <strong>FALSE</strong> (falso). A menos que esté prohibido por una restricción <code>NOT NULL</code>, los datos booleanos también soportan el valor de verdad <strong>UNKNOWN</strong> (desconocido) como el valor <code>NULL</code>. Todos los valores del tipo de datos booleano y los valores de verdad SQL son mutuamente comparables y asignables. El valor <strong>TRUE</strong> es mayor que el valor <strong>FALSE</strong>, y cualquier comparación que involucre el valor <code>NULL</code> o un valor de verdad <strong>UNKNOWN</strong> devuelve un resultado <strong>UNKNOWN</strong>.</p>
    
