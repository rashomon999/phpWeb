    <p>Cuando se define una columna de cadena de caracteres, se puede especificar una longitud para indicar el número máximo de caracteres que la columna puede contener (la longitud predeterminada es 1). Una cadena de caracteres puede definirse con longitud fija o variable. Si la cadena se define con longitud fija y se introduce una cadena con menos caracteres que esta longitud, la cadena se rellena con espacios en blanco a la derecha para completar el tamaño requerido. Si la cadena se define con longitud variable y se introduce una cadena con menos caracteres que esta longitud, solo se almacenan los caracteres introducidos, utilizando así menos espacio.</p>
    
