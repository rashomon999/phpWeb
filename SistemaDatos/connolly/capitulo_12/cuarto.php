    <strong>    12.4 Tipos de Entidades Fuertes y Débiles
    </strong>
    <br><br>
    Podemos clasificar los tipos de entidades como fuertes o débiles.
    <br><br>
    <strong>Tipo de entidad fuerte:
    </strong>
    Una entidad fuerte es aquella cuya existencia no depende de la existencia de otro 
    tipo de entidad. Ejemplos de entidades fuertes se muestran en la Figura 12.1 e 
    incluyen las entidades Staff, Branch, PropertyForRent y Client.
    <br><br>
    Una característica de una entidad fuerte es que cada una de sus ocurrencias puede 
    ser identificada de manera única utilizando su clave primaria. Por ejemplo, podemos identificar
    de manera única a cada miembro del personal utilizando el atributo staffNo, que es la clave
    primaria de la entidad Staff.
    <br><br>
    <strong>
    Tipo de entidad débil: 
    </strong>
     Una entidad débil es aquella cuya existencia depende de otra entidad. Un ejemplo de una entidad 
    débil llamada Preference se muestra en la Figura 12.12.
    <br><br>
    Una característica de una entidad débil es que no puede ser identificada de manera única
    utilizando solo sus propios atributos. Por ejemplo, en el caso de Preference, no hay una
    clave primaria propia, lo que significa que no podemos identificar cada ocurrencia solo 
    con sus atributos. Para identificar cada Preference, debemos relacionarla con una entidad
    Client, que sí tiene una clave primaria (clientNo).
    <br><br>
    En este caso, la entidad Preference tiene dependencia de existencia respecto a la entidad 
    Client, que se conoce como la entidad propietaria.
    <br><br>
