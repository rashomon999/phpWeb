<h5>Key Terms in the Relational Model</h5>
<img src="../../../img/guia_308.png" alt="">
<ul>
    <li>
        <strong>Attribute:</strong>
        Los atributos son las propiedades que definen una entidad. 
        Por ejemplo: ROLL_NO, NAME, ADDRESS, etc.
    </li>

    <li>
        <strong>Relation Schema:</strong>
        Un esquema de relación define la estructura de la relación y representa el nombre de la relación junto con sus atributos.
        Por ejemplo, <em>STUDENT (ROLL_NO, NAME, ADDRESS, PHONE, AGE)</em> es el esquema de relación de STUDENT.
        Si un esquema tiene más de una relación, se denomina <em>Relational Schema</em>.
    </li>

    <li>
        <strong>Tuple:</strong>
        Una tupla representa una fila en una relación. Cada tupla contiene un conjunto de valores de atributos que describen una entidad particular.
        Por ejemplo, <em>(1, RAM, DELHI, 9455123451, 18)</em> es una tupla en la tabla STUDENT.
    </li>

    <li>
        <strong>Relation Instance:</strong>
        El conjunto de tuplas de una relación en un instante particular de tiempo se denomina instancia de relación.
        Puede cambiar cuando se insertan, eliminan o actualizan datos en la base de datos.
    </li>

    <li>
        <strong>Degree:</strong>
        El número de atributos en una relación se conoce como el grado de la relación.
        Por ejemplo, la relación STUDENT tiene grado 5 porque posee 5 atributos.
    </li>

    <li>
        <strong>Cardinality:</strong>
        El número de tuplas en una relación se conoce como la cardinalidad.
        Por ejemplo, la relación STUDENT definida anteriormente tiene cardinalidad 4.
    </li>

    <li>
        <strong>NULL Values:</strong>
        Un valor que es desconocido o no está disponible se denomina valor <code>NULL</code>.
        Por ejemplo, el atributo PHONE del estudiante con ROLL_NO 4 es <code>NULL</code>.
    </li>
</ul>

<h5>Types of Keys in the Relational Model</h5>

<ul>
    <li>
        <strong>Primary Key:</strong>
        Una clave primaria es una columna o un conjunto de columnas en una tabla cuyos valores 
        identifican de forma exclusiva una fila de la tabla. Una base de datos relacional está 
        diseñada para imponer la exclusividad de las claves primarias permitiendo que haya sólo una 
        fila con un valor de clave primaria específico en una tabla.
    </li>

    <li>
        <strong>Candidate Key (Clave secundaria):</strong>
        Una clave candidata es un conjunto de atributos que puede identificar de manera única una tupla en una relación.
        Cuando una clave candidata no es seleccionada como clave primaria, suele denominarse <em>clave secundaria</em>
        en contextos de enseñanza o diagramas prácticos.
    </li>

    <li>
        <strong>Super Key:</strong>
        Una superclave es un conjunto de atributos que puede identificar de manera única una tupla.
    </li>

    <li>
        <strong>Foreign Key:</strong>
        Una clave foránea es un atributo en una relación que hace referencia a la clave primaria de otra relación.
    </li>

    <li>
        <strong>Composite Key:</strong>
        Una clave compuesta se forma combinando dos o más atributos para identificar de manera única una tupla.
    </li>
</ul>

    
    <img src="../../../img/guia_309.png" alt="" width="600">

    <h5>Domain (Dominio)</h5>

<p>
Un <strong>dominio</strong> es el conjunto de valores atómicos permitidos para un atributo.
Define el tipo de datos, el rango de valores posibles y, en algunos casos,
las restricciones que deben cumplir los valores de un atributo.
</p>

<h5>Participación</h5>

<p>
La <strong>participación</strong> indica si todas o solo algunas ocurrencias de una entidad
deben intervenir en una relación dentro del modelo Entidad–Relación.
Puede ser <strong>total (obligatoria)</strong> o <strong>parcial (opcional)</strong>.
</p>


<h5>Elementos del Modelo Relacional</h5>

<p>
La <strong>relación</strong>, también llamada <strong>tabla</strong>, es el elemento fundamental del modelo relacional.
Representa un concepto abstracto de estructura bidimensional compuesta por filas y columnas.
</p>

<h5>Definición de una Relación</h5>

<p>
Una relación puede definirse de dos formas:
</p>

<ul>
    <li>
        <strong>Por comprensión:</strong>
        se describe la propiedad que deben cumplir los elementos de la relación.
        <br>
        Ejemplo:
        <br>
        <em>
        R = { x | x (identificación, nombre, salario) es empleado de la Universidad }
        </em>
    </li>

    <li>
        <strong>Por extensión:</strong>
        consiste en enumerar explícitamente, uno por uno, todos los elementos
        (filas o tuplas) que pertenecen a la relación.
    </li>
</ul>

<h5>Propiedades de una Relación</h5>

<ul>
    <li>
        Una relación es un <strong>conjunto de filas</strong>; por definición, las filas no tienen orden.
    </li>

    <li>
        Cada instancia o fila de una relación se denomina <strong>tupla</strong>.
    </li>

    <li>
        En una relación <strong>no existen tuplas repetidas</strong>.
    </li>

    <li>
        Las columnas de una relación se denominan <strong>atributos</strong>
        y cada atributo debe tener un nombre único dentro de la relación.
    </li>

    <li>
        Cada relación debe poseer un <strong>atributo o conjunto de atributos</strong>
        que identifique de manera única a cada tupla; a este conjunto se le denomina
        <strong>clave</strong>.
    </li>

    <li>
        Para cada atributo existe un conjunto de valores permitidos,
        denominado <strong>dominio</strong> del atributo.
    </li>

    <li>
        Cada celda de una relación contiene un valor <strong>atómico o univaluado</strong>,
        es decir, no admite valores compuestos ni multivaluados.
    </li>

    <li>
        El orden de las filas y de las columnas <strong>no es relevante</strong>
        para el sistema gestor de bases de datos (DBMS).
    </li>

    <li>
        La relación es el <strong>único elemento</strong> utilizado en el modelo relacional
        para representar tanto entidades como asociaciones entre ellas.
    </li>
</ul>

<h5>Clave de una Relación</h5>

<p>
Una <strong>clave de una relación</strong> es un conjunto de atributos de la relación
que puede utilizarse como identificador único de las tuplas de dicha relación.
</p>

<hr>

<h5>Regla de integridad de entidad: Clave primaria</h5>
<p>Toda relación debe poseer una clave primaria. Atributo  (o conjunto de atributos) tomado   del conjunto de atributos 
    que son claves candidatas.
</p>

<h5>Regla de integridad referencial: Clave foránea
</h5>
<p>Garantiza que los valores de clave foránea en una tabla hija coincidan con valores existentes de clave primaria en la 
    tabla padre, manteniendo 
    relaciones válidas entre tablas.</p>
