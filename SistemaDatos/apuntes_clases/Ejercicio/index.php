    <h1>Lógica del Borrado de Tablas en un Modelo Entidad-Relación</h1>
    <br>
    <strong>se podria ver como que a transacciones no lo apunta nadie (no es padre de nadie),
    entonces es el ultimo hijo (mas dependiente)?</strong>
    <br>
  <p>
    La lógica detrás del borrado de las tablas en este modelo entidad-relación está relacionada con las 
    <strong>dependencias entre tablas a través de claves foráneas (foreign keys)</strong>. En SQL, no puedes 
    eliminar una tabla si otra tabla depende de ella (es decir, si otra tabla tiene una clave foránea apuntando 
    a ella), a menos que primero elimines esas dependencias. Por eso, el orden correcto de eliminación es 
    <strong>de la más dependiente a la menos dependiente (de hojas a raíz)</strong>.
  </p>

  <p>Veamos paso a paso la lógica de la sentencia:</p>

  <ul>
    <li>
      🔽 <strong>DROP TABLE TRANSACCIONES;</strong><br>
      <em>Depende de: CUENTAS</em><br>
      Tiene una clave foránea (<code>NUM_CUENTA</code>) que hace referencia a la tabla <strong>CUENTAS</strong>.<br>
      Por tanto, debe eliminarse primero, ya que depende de <strong>CUENTAS</strong>.
    </li>
    <li>
      🔽 <strong>DROP TABLE CUENTAS;</strong><br>
      <em>Depende de: CLIENTES y SUCURSALES</em><br>
      Tiene claves foráneas:<br>
      <code>ID_CLIENTE</code> → <strong>CLIENTES</strong><br>
      <code>ID_SUCURSAL</code> → <strong>SUCURSALES</strong><br>
      No se puede eliminar hasta que se eliminen sus tablas dependientes (<strong>TRANSACCIONES</strong>), lo cual ya hicimos en el paso anterior.
    </li>
    <li>
      🔽 <strong>DROP TABLE CLIENTES;</strong><br>
      <em>Depende de: CIUDADES</em><br>
      Tiene una clave foránea <code>ID_CIUDAD_CLIENTE</code> que apunta a la tabla <strong>CIUDADES</strong>.
    </li>
    <li>
      🔽 <strong>DROP TABLE SUCURSALES;</strong><br>
      <em>Depende de: CIUDADES</em><br>
      Tiene una clave foránea <code>ID_CIUDAD_SUCURSAL</code> que apunta a <strong>CIUDADES</strong>.
    </li>
    <li>
      🔽 <strong>DROP TABLE CIUDADES;</strong><br>
      <em>Depende de: DEPARTAMENTOS</em><br>
      Tiene una clave foránea <code>ID_DEPARTAMENTO</code> que apunta a <strong>DEPARTAMENTOS</strong>.
    </li>
    <li>
      🔽 <strong>DROP TABLE DEPARTAMENTOS;</strong><br>
      <em>No depende de ninguna tabla.</em><br>
      Es la <strong>tabla raíz o tabla padre</strong>, y puede ser eliminada al final sin restricciones.
    </li>
  </ul>

