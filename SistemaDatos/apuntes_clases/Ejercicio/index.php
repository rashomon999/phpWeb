<h2>8c. Aumento de saldo para cuentas de Cali</h2>
<pre><code>UPDATE CUENTAS c
SET c.SALDO_CUENTA = c.SALDO_CUENTA + 50000
WHERE c.ID_SUCURSAL IN (
    SELECT s.ID_SUCURSAL 
    FROM SUCURSALES s 
    JOIN CIUDADES ci ON s.ID_CIUDAD_SUCURSAL = ci.ID_CIUDAD
    WHERE ci.NOMBRE_CIUDAD = 'Cali'
);

COMMIT;</code></pre>

<div class="explanation">
    <p><strong>Explicación:</strong></p>
    <p><strong>UPDATE con subconsulta:</strong></p>
    <ul>
        <li>Actualiza la tabla CUENTAS (alias 'c')</li>
        <li>Incrementa el saldo en 50,000</li>
    </ul>
    
    <p><strong>Subconsulta JOIN:</strong></p>
    <ul>
        <li>Relaciona SUCURSALES (alias 's') con CIUDADES (alias 'ci')</li>
        <li>Filtra solo sucursales ubicadas en 'Cali'</li>
    </ul>
    
    <p><strong>WHERE con IN:</strong></p>
    <ul>
        <li>Aplica la actualización solo a cuentas cuyas sucursales están en el resultado de la subconsulta</li>
    </ul>
    
    <p><strong>COMMIT:</strong> Confirma los cambios</p>
    
