    <tr><td>2</td><td>3000</td><td>20</td></tr>
    <tr><td>3</td><td>8000</td><td>10</td></tr>
    <tr><td>4</td><td>2000</td><td>10</td></tr>
    <tr><td>5</td><td>6000</td><td>30</td></tr>
  </tbody>
</table>

<h4>Agrupamiento por cod_suc</h4>
<ul>
  <li><strong>Grupo 10:</strong> préstamos 1, 3, 4</li>
  <li><strong>Grupo 20:</strong> préstamo 2</li>
  <li><strong>Grupo 30:</strong> préstamo 5</li>
</ul>

<h4>Conteo de Préstamos por Grupo</h4>
<ul>
  <li><strong>Sucursal 10:</strong> 3 préstamos</li>
  <li><strong>Sucursal 20:</strong> 1 préstamo</li>
  <li><strong>Sucursal 30:</strong> 1 préstamo</li>
