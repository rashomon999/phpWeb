    <h2>Consulta: </h2>

    <pre><code class="language-sql">
    SELECT COUNT(*) 
    FROM PRESTAMOS;
    </code></pre>    
    
 
<p><strong>Devuelve:</strong> Un único número: la cantidad total de registros (filas) que existen en la tabla <code>PRESTAMOS</code>.</p>

<h3>Explicación detallada:</h3>
<ul>
  <li><strong>COUNT(*):</strong> Es una función de agregación que cuenta registros. El asterisco <code>*</code> significa "contar todas las filas sin excepciones".</li>
  <li><strong>FROM PRESTAMOS:</strong> Indica que se deben contar los registros de la tabla <code>PRESTAMOS</code>.</li>
</ul>

<h3>Resultado:</h3>
<p>Un único valor numérico (por ejemplo: <strong>157</strong> si hay 157 préstamos registrados).</p>
<p>No muestra los datos de los préstamos, solo la <strong>cantidad total</strong>.</p>

