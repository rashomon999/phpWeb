    <h2>Cantidad de Préstamos por Código de Sucursal</h2>

<pre><code>SELECT cod_suc, COUNT(*) AS cant
FROM PRESTAMOS
GROUP BY cod_suc;
</code></pre>

<h3>Explicación de la Consulta: Cantidad de Préstamos por Sucursal</h3>

<p>Esta consulta agrupa los préstamos por sucursal y cuenta cuántos hay en cada una.</p>

<h4>Estructura de la Consulta</h4>
<ul>
  <li><strong>GROUP BY cod_suc:</strong> Agrupa todos los registros de la tabla <code>PRESTAMOS</code> que tienen el mismo valor en <code>cod_suc</code> (código de sucursal). Crea un "grupo" para cada sucursal distinta.</li>
  <li><strong>COUNT(*) AS cant:</strong> Cuenta cuántos registros hay en cada grupo. <code>AS cant</code> le da el nombre "cant" a la columna de resultados.</li>
  <li><strong>SELECT cod_suc, ...:</strong> Muestra el código de sucursal y el conteo correspondiente.</li>
</ul>

<h3>Ejemplo Práctico</h3>

<p>Si la tabla <code>PRESTAMOS</code> contiene los siguientes datos:</p>

