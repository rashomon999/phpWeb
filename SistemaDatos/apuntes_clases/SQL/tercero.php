    <h3>¿Qué significa "esto por esto" en SQL?</h3>

    <p>Cuando te dicen algo como <strong>"clientes por vendedor"</strong>, o <strong>"cantidad de 
    préstamos por sucursal"</strong>, te están pidiendo un <strong>conteo agrupado</strong>, es decir:</p>

    <p><em>“¿Cuántos elementos hay por cada valor distinto de alguna columna?”</em></p>

    <p>Eso se traduce en SQL como:</p>

    <pre><code>SELECT columna_de_agrupación, COUNT(*) AS cantidad
    FROM tabla
    GROUP BY columna_de_agrupación;
    </code></pre>
    <hr>
    <h3> COUNT e Ignorar Valores NULL</h3>

    <p>La función de agregación <code>COUNT()</code> <strong>ignora automáticamente los valores <code>NULL</code></strong> cuando se usa con una <strong>columna específica</strong>.</p>

    <h4> <code>COUNT(*)</code></h4>
    <pre><code>SELECT COUNT(*) FROM employees;
    </code></pre>
    <ul>
    <li>Cuenta <strong>todas las filas</strong> de la tabla <code>employees</code>.</li>
    <li><strong>No importa</strong> si hay valores <code>NULL</code> o no.</li>
    <li>Ejemplo: si hay 100 empleados, devuelve <strong>100</strong>.</li>
    </ul>

    <h4> <code>COUNT(COMMISSION_PCT)</code></h4>
    <pre><code>SELECT COUNT(COMMISSION_PCT)
    FROM employees;
    </code></pre>
    <ul>
    <li>Cuenta solo las filas donde <code>COMMISSION_PCT</code> <strong>NO sea NULL</strong>.</li>
    <li>Ignora automáticamente las filas donde ese campo está vacío.</li>
    <li>Ejemplo: si solo 35 empleados tienen un valor en <code>COMMISSION_PCT</code>, devuelve <strong>35</strong>.</li>
    </ul>

    <h4> ¿Por qué es útil?</h4>
    <p>Porque te permite diferenciar entre:</p>
    <ul>
    <li><strong>Total de registros</strong> → <code>COUNT(*)</code></li>
    <li><strong>Registros con datos válidos en una columna específica</strong> → <code>COUNT(columna)</code></li>
    </ul>
