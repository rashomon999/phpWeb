    <h2>Ilustración del Orden Lógico de Ejecución en SQL</h2>

<h3>Consulta SQL:</h3>
<pre><code>SELECT cod_suc, COUNT(*) AS cant
FROM PRESTAMOS
GROUP BY cod_suc;
</code></pre>

<h3>Orden lógico de ejecución (ilustrado):</h3>
<ol>
  <li><strong>FROM PRESTAMOS</strong><br> Se lee toda la tabla <code>PRESTAMOS</code>.</li>
  <li><strong>GROUP BY cod_suc</strong><br> Agrupa las filas según los valores de <code>cod_suc</code>.</li>
  <li><strong>SELECT cod_suc, COUNT(*) AS cant</strong><br> Muestra por cada grupo el código de sucursal y la cantidad de préstamos.</li>
</ol>

<h3> Ejemplo con datos:</h3>
<p>Supongamos que la tabla <code>PRESTAMOS</code> contiene los siguientes datos:</p>

<table border="1" cellpadding="5" cellspacing="0">
  <thead>
    <tr>
      <th>nro_prestamo</th>
      <th>valor</th>
      <th>cod_suc</th>
    </tr>
  </thead>
  <tbody>
    <tr><td>1</td><td>5000</td><td>10</td></tr>
    <tr><td>2</td><td>3000</td><td>20</td></tr>
    <tr><td>3</td><td>8000</td><td>10</td></tr>
    <tr><td>4</td><td>2000</td><td>10</td></tr>
    <tr><td>5</td><td>6000</td><td>30</td></tr>
  </tbody>
</table>

<h3> Paso a paso de ejecución:</h3>
<ol>
  <li><strong>FROM PRESTAMOS</strong><br>Lee todos los registros de la tabla.</li>
  <li><strong>GROUP BY cod_suc</strong><br>Agrupa las filas por sucursal:
    <ul>
      <li>Grupo 10 → préstamos 1, 3, 4</li>
      <li>Grupo 20 → préstamo 2</li>
      <li>Grupo 30 → préstamo 5</li>
    </ul>
  </li>
  <li><strong>SELECT cod_suc, COUNT(*) AS cant</strong><br>Calcula el número de préstamos por grupo y muestra:</li>
</ol>

<table border="1" cellpadding="5" cellspacing="0">
  <thead>
    <tr>
      <th>cod_suc</th>
      <th>cant</th>
    </tr>
  </thead>
  <tbody>
    <tr><td>10</td><td>3</td></tr>
    <tr><td>20</td><td>1</td></tr>
    <tr><td>30</td><td>1</td></tr>
  </tbody>
</table>

    <h3> Conclusión</h3>
    <p>Aunque se <strong>escribe</strong> con <code>SELECT</code> al principio, el <strong>orden lógico de ejecución</strong> real es:</p>

    <pre><code>FROM → GROUP BY → SELECT</code></pre>
