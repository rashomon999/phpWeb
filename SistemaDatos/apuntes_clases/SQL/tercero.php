    <h2>Funciones de agregación</h2>
    <ul>
        <li><strong>Promedio (avg):</strong> aplicable a atributos numéricos, retorna el promedio del atributo.</li>
        <li><strong>Mínimo (min):</strong> retorna el menor elemento no nulo dentro de las tuplas para ese atributo.</li>
        <li><strong>Máximo (max):</strong> retorna el mayor elemento no nulo dentro de las tuplas para ese atributo.</li>
        <li><strong>Total (sum):</strong> aplicable a atributos numéricos, realiza la suma matemática.</li>
        <li><strong>Cuenta (count):</strong> cuenta las tuplas resultantes.</li>
    </ul>
