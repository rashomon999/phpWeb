    <h2>Pattern Match - LIKE</h2>

<p>La coincidencia de patrones (<strong>pattern matching</strong>) permite el uso de expresiones regulares simples para encontrar valores representados como cadenas de caracteres (<code>char</code>, <code>varchar</code>).</p>

<h3>S√≠mbolos de coincidencia de patrones</h3>
