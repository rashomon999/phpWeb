    <li><strong>Rendimiento financiero</strong> (ingresos, utilidades, gastos, pérdidas).</li>
    <li><strong>Otras transacciones</strong> (por ejemplo, pedir un préstamo o emitir acciones).</li>
    </ul>

    <h5>✅ ¿Por qué es importante saber eso?</h5>
    <p>Porque los usuarios de los estados financieros (como inversionistas, prestamistas, etc.) necesitan entender por qué cambian los recursos y las deudas, para así poder:</p>
    <ul>
    <li>Evaluar el <strong>potencial de generar efectivo en el futuro</strong>, y</li>
    <li>Juzgar si la <strong>gerencia está administrando bien la empresa</strong>.</li>
    </ul>

    <h5>📌 ¿Qué dice el párrafo 1.16 sobre el rendimiento financiero?</h5>
    <p>La información sobre el <strong>rendimiento financiero</strong> (es decir, si la empresa ganó o perdió dinero y cómo lo hizo) ayuda a entender qué tan <strong>rentable</strong> es la empresa.</p>
