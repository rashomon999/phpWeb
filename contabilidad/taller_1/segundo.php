    <h2>📌 1. Balance de prueba ajustado</h2>
  <p>Es un informe intermedio dentro del proceso contable.</p>
  <ul>
    <li>Nace después de registrar los ajustes contables (depreciaciones, intereses por pagar, provisiones, ingresos anticipados, etc.).</li>
    <li>Se construye en forma de lista de cuentas con sus débitos y créditos.</li>
    <li>Su objetivo principal es verificar que la contabilidad cuadre después de los ajustes:</li>
    <ul>
      <li>La suma de los débitos debe ser igual a la suma de los créditos.</li>
    </ul>
    <li>No está pensado para usuarios externos, sino como herramienta de control interno del contador.</li>
  </ul>

  <h2>📌 2. Balance general</h2>
  <p>Es un estado financiero formal, elaborado al final del proceso.</p>
  <ul>
    <li>Presenta la situación financiera de la empresa en una fecha determinada.</li>
    <li>Se organiza en tres bloques:
      <ul>
        <li><strong>Activos</strong> (lo que la empresa tiene).</li>
        <li><strong>Pasivos</strong> (lo que la empresa debe).</li>
        <li><strong>Patrimonio</strong> (la parte de los dueños).</li>
      </ul>
    </li>
    <li>Es un reporte oficial para socios, bancos, inversionistas, autoridades tributarias, etc.</li>
    <li>No se presenta en forma de “débitos y créditos”, sino en saldos finales organizados.</li>
  </ul>

