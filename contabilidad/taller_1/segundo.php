  <li><b>Cuentas por pagar</b> → obligación general con terceros (proveedores, facturas, etc.).</li>
</ul>

<p>👉 En contabilidad se busca que las cuentas sean lo más claras y analíticas posibles.<br>
Por eso no todo va en “Cuentas por pagar” genérica:</p>

<ul>
  <li>Si es deuda laboral → va a una cuenta laboral específica.</li>
