  <h2>📌 1. Principio básico</h2>
  <p>
    En contabilidad todo funciona con <strong>partida doble</strong>:
  </p>
  <ul>
    <li><strong>Débito (Dr)</strong> = lado izquierdo</li>
    <li><strong>Crédito (Cr)</strong> = lado derecho</li>
  </ul>
  <p>
    Siempre que registras algo, debes poner al menos un débito y un crédito por el mismo valor.<br>
    👉 El débito no es siempre "entra dinero" ni el crédito siempre "sale dinero".<br>
    Depende del tipo de cuenta.
  </p>

  <h2>📌 2. Cómo funcionan Débito y Crédito según la naturaleza de la cuenta</h2>
  <table border="1" cellpadding="5" cellspacing="0">
    <tr>
      <th>Tipo de cuenta</th>
      <th>Débito (+)</th>
      <th>Crédito (–)</th>
    </tr>
    <tr>
      <td>Activo (caja, bancos, cuentas por cobrar, equipos, etc.)</td>
      <td>Aumenta el activo</td>
      <td>Disminuye el activo</td>
    </tr>
    <tr>
      <td>Pasivo (deudas, obligaciones, cuentas por pagar)</td>
      <td>Disminuye el pasivo</td>
      <td>Aumenta el pasivo</td>
    </tr>
    <tr>
      <td>Patrimonio (capital, reservas)</td>
      <td>Disminuye</td>
      <td>Aumenta</td>
    </tr>
    <tr>
      <td>Ingresos (ventas, intereses ganados)</td>
      <td>Disminuye</td>
      <td>Aumenta</td>
    </tr>
    <tr>
      <td>Gastos (salarios, arrendamiento, depreciación)</td>
      <td>Aumenta</td>
      <td>Disminuye</td>
    </tr>
  </table>

  <h2>📌 3. Ejemplo aplicado al CDT (el que te confundió)</h2>
  <p>
    <strong>Ajuste:</strong> El CDT genera intereses $300
  </p>
  <ul>
    <li><strong>Pregunta 1:</strong> ¿Qué ganó la empresa? → Interés ganado = ingreso → se aumenta → se acredita.</li>
    <li><strong>Pregunta 2:</strong> ¿Cómo se refleja ese derecho? → No entró efectivo, pero hay derecho a cobrar → eso es un activo → se aumenta → se debita.</li>
  </ul>
  <p><strong>Asiento:</strong></p>
  <ul>
    <li><strong>Débito:</strong> Intereses por cobrar $300 (activo ↑)</li>
    <li><strong>Crédito:</strong> Ingreso por intereses $300 (ingreso ↑)</li>
  </ul>

