  <h3>Normativa contable / fiscal</h3>
  <p>
    Siempre que hay utilidad contable, debe calcularse el <strong>impuesto de renta</strong> correspondiente. En tu ejercicio se fijó en 35% como supuesto estándar. Ese valor se descuenta en el Estado de Resultados y se registra en el Balance como pasivo corriente (<em>Impuestos por pagar</em>).
