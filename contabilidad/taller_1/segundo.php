    <p>La diferencia está en que <b>“Cuentas por pagar”</b> es una cuenta genérica de pasivo corriente que se usa cuando la empresa adquiere una obligación con un proveedor por bienes o servicios (ejemplo: factura de teléfono, compra de insumos fiados).</p>

