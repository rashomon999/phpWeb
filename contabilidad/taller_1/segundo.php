  <h3>Normativa societaria (Reserva Legal)</h3>
  <p>
    En Colombia (y en muchos países) las sociedades deben apropiarse de una <strong>reserva legal</strong> mínima (a menudo 10% de la utilidad) hasta que alcance cierto porcentaje del capital. Por eso en el Estado de Resultados se calcula y en el Balance se presenta en <em>Patrimonio – Reservas</em>.
  </p>

  <p>
    👉 Es decir, aunque no estaban en la lista de transacciones (como facturas, aportes o arriendos), se incluyen porque forman parte del cierre contable obligatorio:
  </p>
  <ul>
    <li>El impuesto refleja la carga fiscal de la utilidad.</li>
    <li>La reserva legal refleja la obligación societaria de separar parte de las utilidades.</li>
  </ul>

  <p>
    Si no los agregas, el Balance no cuadraría y el patrimonio quedaría inflado.
