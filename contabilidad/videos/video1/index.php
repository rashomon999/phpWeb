     <h1>Introducción a la contabilidad</h1>

  <p><strong>Para empezar, debemos recordar que la contabilidad es una herramienta útil para tomar decisiones financieras.</strong> Como sistema de información, tiene entradas, un proceso y salidas.</p>

  <h2>Entradas</h2>
<p>En el sistema de información, las entradas se realizan a través de documentos.</p>

<p>En las entradas, tenemos un flujo de documentos. Entre ellos podemos recibir:</p>
<ul>
    <li>Recibos de caja</li>
    <li>Comprobantes de egreso</li>
    <li>Facturas de compra</li>
    <li>Facturas de venta</li>
    <li>Nóminas</li>
    <li>Consignaciones</li>
    <li>Notas de débito y credito</li>
    <li>Pagarés</li>
    <li>Cualquier tipo de documento que contenga información sobre transacciones realizadas</li>
</ul>

 

  <p>Es importante destacar que cuando hacemos información financiera, siempre será por un período de tiempo, así que 
    con toda esa información que se nos ha dado, debemos alcanzar un equilibrio que llamamos una división, donde debemos
     mantener esa partida doble siempre, es decir, de dónde viene el dinero y en qué se convierte, a dónde fue en ese
      proceso.</p>

 


  <h2>Proceso</h2>

    <p>En el proceso, aplicamos un juicio o criterio y la técnica cuenta para el resultado. Entonces tendremos los informes 
    financieros. Veamos cada uno de ellos.</p>


 <ol>
    <li>Revisión y Clasificación</li>
    <li>Medición</li>
    <li>Reconocimiento</li>
    <li>Saldos</li>
    <li>Balance de prueba</li>
    <li>Ajustes</li>
    <li>Cierre</li>
    <li>Saldos de Balance</li>
</ol>
 
  <p>Hacemos el cierre contable, es decir, ya no habrá más transacciones para mostrar los saldos del balance. Con esto 
    habremos terminado lo que llamamos el proceso contable; ahora nos dedicamos a tirar la información financiera que es 
    nuestra salida.</p>
 

  <h2>Salidas — Informes financieros</h2>
  <p>Para ello hacemos algunos estados financieros, algunos de ellos:</p>
  <ul>
    <li>El balance general: que es el que muestra activos, pasivos y patrimonio.</li>
    <li>El estado de resultados: que nos muestra los ingresos y gastos.</li>
    <li>El estado de efectivo: que muestra de dónde proviene el efectivo en la empresa.</li>
    <li>El estado de cambios en el patrimonio: que nos muestra los cambios en patrimonio.</li>
    <li>Las revelaciones: que nos da claridad.</li>
  </ul>

    <p>
    estos estados financieros forman el informe de contabilidad financiera.
    </p>

    <p>
    Pero con esa contabilidad que hemos hecho en ese proceso también 
    podemos llegar a informes que son de contabilidad administrativa
    </p>
  
    <h2>Contabilidad financiera vs contabilidad administrativa</h2>

  <h2>Contabilidad Financiera</h2>
<ul>
    <li>Usuarios externos</li>
    <li>Mandatorio (NIIF)</li>
    <li>Histórica</li>
    <li>Completa y resumida</li>
    <li>Multiusuario, pública</li>
</ul>

<h2>Contabilidad Administrativa</h2>
<ul>
    <li>Usuarios internos</li>
    <li>Opcional</li>
    <li>Enfocada al futuro</li>
    <li>Puede fragmentarse</li>
    <li>Hecha a la medida del usuario</li>
</ul>


  <p> La contabilidad financiera se dirige a usuarios externos, a terceros que tengan una relación 
    económica con nosotros o planeen tenerla. La contabilidad administrativa es para usuarios internos, es decir, para quienes
     manejan nuestra empresa.</p>

  <p>También se diferencian en que la contabilidad financiera es obligatoria en nuestro caso colombiano, la ley 1314 nos dice que debemos llevar las normas internacionales de información financiera. La contabilidad administrativa es opcional; cada empresa tendrá su propia normativa.</p>

  <p>La contabilidad financiera es completamente histórica: no podremos registrar cualquier hecho que no haya ocurrido aunque la contabilidad incluya estimaciones; esas estimaciones se deben a la misma característica de la contabilidad, pero no porque queramos registrar hechos que no sean reales. La contabilidad administrativa se enfoca en el futuro, es decir, en ella pensamos en hacer informes proyectados para poder gestionar el negocio, sabiendo dónde estamos.</p>

  <p>Siguiendo con nuestro objetivo, la contabilidad financiera es completa y resumida, mientras que la contabilidad administrativa puede ser fragmentada, para hacer solo la parte que se requiere. En la contabilidad financiera se proporciona información multiusuario; la información que proviene de la contabilidad financiera es la misma para todos y es pública. En la contabilidad administrativa los informes están adaptados al usuario según lo requiera.</p>

  <p class="note"><strong>En este curso nos vamos a centrar en la contabilidad financiera.</strong> Dejaremos la contabilidad administrativa para revisar más tarde cuando se lleve a cabo nuestro próximo curso.</p>

