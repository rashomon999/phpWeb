    <p>El IASC se originó en 1973 por acuerdo de los representantes de los profesionales de la contabilidad de varios países, Alemania, Australia, Estados Unidos, Francia y los Países Bajos, Irlanda, Japón, México y el Reino Unido, y con el objetivo de formular un conjunto de normas contables que pudieran ser generalmente aceptado y aplicado en diferentes países para promover la armonización de los datos y su comparabilidad.</p>

    <p>El IASC promulgó un conjunto sustancial de normas, entre las que se encuentran las
