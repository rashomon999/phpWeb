    <h2>4.54 Diferencia con la compensación de activos y pasivos</h2>
    <p>El tratamiento de un conjunto de derechos y obligaciones como una unidad de cuenta única difiere de la compensación de activos y pasivos (véase el párrafo 7.10).</p>

    <h2>4.55 Posibles partidas contables separadas</h2>
    <ul>
        <li>(a) Un derecho individual o una obligación individual.</li>
        <li>(b) Todos los derechos, todas las obligaciones o ambos, que surgen de una fuente única (ej. un contrato).</li>
        <li>(c) Un subgrupo de esos derechos u obligaciones (ej. derechos sobre un activo con vida útil distinta).</li>
        <li>(d) Un grupo de derechos combinados u obligaciones que surgen de una cartera de elementos similares.</li>
        <li>(e) Un grupo de derechos combinados u obligaciones que surgen de una cartera de partidas diferentes (ej. activos y pasivos a disponer en una sola transacción).</li>
        <li>(f) Una exposición al riesgo dentro de una cartera de partidas con riesgo común, enfocando la contabilización en la exposición agregada a dicho riesgo.</li>
    </ul>
 

    <h2>Contratos pendientes de ejecución</h2>
<p><strong>4.56</strong> Un contrato pendiente de ejecución es un contrato, o una parte de un contrato, que está sin realizar en partes iguales—ninguna parte ha cumplido ninguna de sus obligaciones, o ambas partes han cumplido parcialmente sus obligaciones en igual medida.</p>
<p><strong>4.57</strong> Un contrato pendiente de ejecución establece un derecho y una obligación combinados para intercambiar recursos económicos. El derecho y obligación son interdependientes y no pueden separarse. Por ello, el derecho y la obligación combinados constituyen un activo o pasivo único. La entidad tiene un activo si los términos del intercambio le son favorables en este momento; tiene un pasivo si los términos del intercambio son actualmente desfavorables. Incluir o no un activo o pasivo en los estados financieros depende de los criterios de reconocimiento (véase el Capítulo 5) y de la base de medición (véase el Capítulo 6) seleccionada para ese activo o pasivo incluyendo, cuando sea aplicable, una prueba para determinar si el contrato es oneroso.</p>
<p><strong>4.58</strong> En la medida en que una parte cumple sus obligaciones del contrato, el contrato deja de estar pendiente de ejecutar. Si la entidad que informa cumple primero con el contrato, ese desempeño es el suceso que cambia el derecho y la obligación de la entidad que informa de intercambiar recursos económicos por un derecho a recibir un recurso económico. Ese derecho es un activo. Si la otra parte cumple primero con el contrato, ese desempeño es el suceso que cambia el derecho y la obligación de la entidad que informa de intercambiar recursos económicos por una obligación de transferir un recurso económico. Esa obligación es un pasivo.</p>

<h2>Esencia de los derechos contractuales y obligaciones contractuales</h2>
<p><strong>4.59</strong> Los términos de un contrato crean derechos y obligaciones para una entidad que sea parte de ese contrato. Para representar esos derechos y obligaciones, los estados financieros informan sobre su esencia (véase el párrafo 2.12). En algunos casos, la esencia de los derechos y obligaciones es clara, a partir de la forma legal del contrato. En otros casos, los términos del contrato, o de un grupo o serie de contratos, requieren un análisis para identificar la esencia de los derechos y obligaciones.</p>
<p><strong>4.60</strong> Se consideran todos los términos en un contrato—de forma explícita o implícita—a menos que carezcan de sustancia. Los términos implícitos podrían incluir, por ejemplo, obligaciones impuestas por ley, tales como obligaciones de garantía legal impuestas a las entidades que celebran contratos para la venta de bienes a clientes.</p>
<p><strong>4.61</strong> Los términos que carecen de sustancia se desestiman. Un término carece de sustancia si no tiene un efecto apreciable sobre los aspectos económicos del contrato. Los términos que carecen de sustancia podrían incluir, por ejemplo:</p>
<ul>
    <li>términos que no obligan a las partes; o</li>
    <li>derechos, incluyendo opciones, que el tenedor no tendrá la capacidad práctica de ejercitar.</li>
</ul>
<p><strong>4.62</strong> Un grupo o serie de contratos puede lograr, o diseñarse para lograr, un efecto comercial global. Para informar de la esencia de estos contratos, puede ser necesario tratar los derechos y obligaciones que surgen de ese grupo o serie de contratos como una unidad de cuenta individual. Por ejemplo, si los derechos u obligaciones en un contrato invalidan simplemente los derechos u obligaciones de otro contrato realizado al mismo tiempo con la misma contraparte, el efecto combinado es que no crea derechos u obligaciones ninguno de los dos contratos. Por el contrario, si un contrato único crea dos o más conjuntos de derechos y obligaciones, que habrían podido ser creados a través de dos o más contratos separados, una entidad puede necesitar contabilizar cada conjunto como si surgiera de contratos separados, con el fin de representar fielmente los derechos y obligaciones (véanse los párrafos 4.48 a 4.55).</p>

<h2>Definición de patrimonio</h2>
<p><strong>4.63</strong> Patrimonio es la parte residual de los activos de la entidad, una vez deducidos todos sus pasivos.</p>
<p><strong>4.64</strong> Los derechos sobre el patrimonio son derechos sobre la parte residual de los activos de la entidad, una vez deducidos todos sus pasivos. En otras palabras, son derechos frente a la entidad que no cumplen la definición de pasivo. Estos derechos pueden establecerse mediante contrato, legislación o instrumentos similares, e incluyen (en la medida en que no cumplen la definición de pasivo):</p>
<ul>
    <li>acciones de varios tipos, emitidas por la entidad; y</li>
    <li>algunas obligaciones para la entidad de emitir otros derechos sobre el patrimonio.</li>
</ul>
<p><strong>4.65</strong> Diferentes clases de derechos sobre el patrimonio, tales como acciones ordinarias y acciones preferentes, pueden conceder a sus tenedores derechos distintos, por ejemplo, derechos a recibir algunos o todos de los siguientes elementos de la entidad:</p>
<ul>
    <li>dividendos, si la entidad decide pagar dividendos a tenedores que cumplan ciertas condiciones;</li>
    <li>el producto de satisfacer los derechos sobre el patrimonio, bien en su totalidad, en el momento de la liquidación, o en parte en otros momentos; o</li>
    <li>otros derechos sobre el patrimonio.</li>
</ul>
<p><strong>4.66</strong> En ocasiones, los requerimientos legales, de regulación o de otro tipo afectan a componentes concretos del patrimonio, tal como al capital en acciones o a las ganancias acumuladas. Por ejemplo, algunos de estos requerimientos permiten que una entidad lleve a cabo distribuciones a los tenedores de derechos sobre el patrimonio solo si tiene suficientes reservas de aquellas que los requerimientos especifican que son distribuibles.</p>
<p><strong>4.67</strong> Las actividades de negocio son, a menudo, llevadas a cabo por entidades tales como empresas de propiedad individual, sociedades, fideicomisos o una variada gama de entidades propiedad del gobierno. Los marcos legales y de regulación de tales entidades son, a menudo, diferentes de los que se aplican a las entidades corporativas. Por ejemplo, puede haber algunas, o bien no haber, restricciones sobre la distribución a los tenedores de derechos sobre patrimonio de estas entidades. No obstante, la definición de patrimonio del párrafo 4.63 del Marco Conceptual se aplica a todas las entidades que informan.</p>

<h2>Definiciones de ingresos y gastos</h2>
<p><strong>4.68</strong> Ingresos son incrementos en los activos o disminuciones en los pasivos que dan lugar a incrementos en el patrimonio, distintos de los relacionados con aportaciones de los tenedores de derechos sobre el patrimonio.</p>
<p><strong>4.69</strong> Gastos son disminuciones en los activos o incrementos en los pasivos que dan lugar a disminuciones en el patrimonio, distintos de los relacionados con distribuciones a los tenedores de derechos sobre el patrimonio.</p>
 
