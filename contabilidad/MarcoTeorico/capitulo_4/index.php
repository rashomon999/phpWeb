<h2>4.10</h2>
<p>Una entidad no puede tener derecho a obtener beneficios económicos de sí misma. Por ello:</p>
<ol type="a">
  <li>Los instrumentos de deuda o instrumentos de patrimonio emitidos por la entidad y recomprados y mantenidos por ella—por ejemplo, acciones propias—no son recursos económicos de esa entidad; y</li>
  <li>Si una entidad que informa comprende más de una entidad legal, los instrumentos de deuda o instrumentos de patrimonio emitidos por una de dichas entidades legales y mantenidos por otra de ellas no son recursos económicos de la entidad que informa.</li>
</ol>


<h2>4.11</h2>
<p>En principio, cada uno de los derechos de una entidad es un activo separado. Sin embargo, a efectos contables, los derechos relacionados se tratan a menudo como una única unidad de cuenta que es un activo único (véanse los párrafos 4.48 a 4.55). Por ejemplo, la propiedad legal de un objeto físico puede dar lugar a varios derechos, incluyendo:</p>
<ol type="a">
  <li>El derecho a utilizar el objeto;</li>
  <li>El derecho a vender derechos sobre el objeto;</li>
  <li>El derecho a pignorar derechos sobre el objeto; y</li>
  <li>Otros derechos no mencionados en (a)–(c).</li>
</ol>


<h2>4.12</h2>
<p>En muchos casos, el conjunto de derechos que surgen de la propiedad legal de un objeto físico se contabiliza como un activo único. Conceptualmente, el recurso económico es el conjunto de derechos, no el objeto físico. No obstante, la descripción del objeto físico como el conjunto de derechos proporcionará, a menudo, una representación fiel de esos derechos de la forma más concisa y comprensible.</p>

<h2>4.13</h2>
<p>En algunos casos, es incierta la existencia de un derecho. Por ejemplo, una entidad y un tercero pueden discrepar si la entidad tiene un derecho a recibir un recurso económico de ese tercero. Hasta que esa incertidumbre sobre la existencia se resuelva—por ejemplo, mediante sentencia judicial—es incierto que la entidad tiene un derecho y, por consiguiente, si existe un activo. (El párrafo 5.14 analiza el reconocimiento de activos cuya existencia es incierta.)</p>

<h1>Potencial para producir beneficios económicos</h1>

<h2>4.14</h2>
<p>Un recurso económico es un derecho que tiene el potencial de producir beneficios económicos. Para que ese potencial exista, no es necesario que sea cierto, ni siquiera probable, que el derecho vaya a producir beneficios económicos. Solo es necesario que el derecho exista en el momento ya y que, en, al menos, una circunstancia, produciría beneficios económicos para esa entidad más allá de los disponibles para todas las demás partes.</p>

<h2>4.15</h2>
<p>Un derecho puede cumplir la definición de un recurso económico, y por ello puede ser un activo, incluso si la probabilidad de que produzca beneficios económicos es baja. No obstante, esa baja probabilidad puede afectar decisiones sobre qué información proporcionar sobre el activo y cómo facilitarla, incluyendo decisiones sobre si se reconoce el activo (véanse los párrafos 5.15 a 5.17), y cómo medirlo.</p>

<h2>4.16</h2>
<p>Un recurso económico podría producir beneficios económicos para la entidad permitiendo u otorgando el derecho a realizar, por ejemplo, uno o más de las siguientes acciones:</p>
<ol type="a">
  <li>Recibir flujos de efectivo contractuales u otro recurso económico;</li>
  <li>Intercambiar recursos económicos con terceros en condiciones favorables;</li>
  <li>
    Producir entradas de efectivo o evitar salidas de efectivo mediante, por ejemplo:
    <ol type="i">
      <li>El uso del recurso económico individualmente o en combinación con otros recursos económicos para producir bienes o prestar servicios;</li>
      <li>El uso del recurso económico para mejorar el valor de otros recursos económicos; o</li>
      <li>El arriendo del recurso económico a otra parte;</li>
    </ol>
  </li>
  <li>Recibir efectivo u otros recursos económicos mediante la venta del recurso económico; o</li>
  <li>Extinguir pasivos mediante la transferencia del recurso económico.</li>
</ol>

<h2>4.17</h2>
<p>Aunque un recurso económico debe su valor a su potencial presente para producir beneficios económicos futuros, el recurso económico es el derecho presente que contiene ese potencial, no los beneficios económicos futuros que el derecho puede producir. Por ejemplo, el valor de una opción comprada procede de su potencial presente para producir beneficios económicos a través del ejercicio de la opción en una fecha futura. Sin embargo, el recurso económico es el derecho presente —el derecho a ejercer la opción en una fecha futura. El recurso económico no son los beneficios económicos futuros que el tenedor recibirá si se ejercita la opción.</p>

<h2>4.18</h2>
<p>Existe una asociación muy estrecha entre la realización de un determinado desembolso y la adquisición de un activo, aunque uno y otro no tienen por qué coincidir necesariamente. Por ello, cuando una entidad incurre en un desembolso, esto puede proporcionar evidencia de que ha buscado los beneficios económicos futuros, pero no proporciona una prueba concluyente de que haya obtenido un activo. De forma análoga, la ausencia de un desembolso relacionado no excluye que una partida cumpla la definición de activo. Los activos pueden incluir, por ejemplo, derechos que un gobierno ha concedido a la entidad sin costo o que han sido donados a la entidad por un tercero.</p>

<h1>Control</h1>

<h2>4.19</h2>
<p>El control vincula un recurso económico con una entidad. La evaluación de si existe control ayuda a identificar el recurso económico que contabiliza la entidad. Por ejemplo, una entidad puede controlar una parte proporcional de una propiedad inmobiliaria sin controlar la totalidad de los derechos que surgen de la propiedad de dicho elemento. En estos casos, el activo de la entidad es su participación en la propiedad que controla, no los derechos que surgen de poseer la propiedad en su totalidad, la cual no controla.</p>


<h2>4.20</h2>
<p>Una entidad controla un recurso económico si tiene la capacidad presente de dirigir el uso del recurso económico y obtener los beneficios económicos que pueden proceder de éste. El control incluye la capacidad presente de impedir que terceros dirijan el uso del recurso económico y la obtención de los beneficios económicos que pueden proceder de éste. De ello se deduce que, si una parte controla un recurso económico, ningún tercero controla ese recurso.</p>

<h2>4.21</h2>
<p>Una entidad tiene la capacidad presente de dirigir el uso de un recurso económico si tiene el derecho a utilizar ese recurso económico en sus actividades, o de permitir que un tercero utilice para sus actividades dicho recurso económico.</p>

<h2>4.22</h2>
<p>El control de un recurso económico habitualmente surge de la capacidad de exigir los derechos legales. Sin embargo, el control puede surgir también si una entidad tiene otros medios de asegurar que tiene la capacidad presente, y no la tiene un tercero, de dirigir el uso del recurso económico y obtener los beneficios que pueden proceder de éste. Por ejemplo, una entidad podría controlar un derecho a usar un conocimiento que no es de dominio público si tiene acceso a dicho conocimiento y la capacidad presente de conservarlo en secreto, incluso si no está protegido por una patente registrada.</p>

<h2>4.23</h2>
<p>Para que una entidad controle un recurso económico, los beneficios económicos futuros procedentes del recurso deben ir a la entidad directa o indirectamente en lugar de a un tercero. Este aspecto de control no supone que la entidad pueda asegurar que el recurso producirá beneficios económicos en cualquier circunstancia. Más bien significa que, si el recurso produce beneficios económicos, la entidad es la parte que los obtendrá directa o indirectamente.</p>

<h2>4.24</h2>
<p>La exposición a variaciones significativas en el importe de los beneficios económicos producidos por un recurso económico puede indicar que la entidad controla el recurso. Sin embargo, solo es un factor por considerar en la evaluación global de si existe control.</p>

 

