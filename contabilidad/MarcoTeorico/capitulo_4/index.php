   <h2>Definición de activo</h2>

  <p><strong>4.3</strong> Un activo es un recurso económico presente controlado por la entidad como resultado de sucesos pasados.</p>

  <p><strong>4.4</strong> Un recurso económico es un derecho que tiene el potencial de producir beneficios económicos.</p>

  <p><strong>4.5</strong> Esta sección analiza tres aspectos de dichas definiciones:</p>
  <ul>
    <li><strong>(a)</strong> Derechos (véanse los párrafos 4.6 a 4.13);</li>
    <li><strong>(b)</strong> Potencial de producir beneficios económicos (véanse los párrafos 4.14 a 4.18);</li>
    <li><strong>(c)</strong> Control (véanse los párrafos 4.19 a 4.25).</li>
  </ul>
 


  <h1>Derecho</h1>

<h2>4.6 Los derechos que tienen el potencial de producir beneficios toman diversas formas, incluyendo:</h2>
<ol type="a">
  <li>
    Derechos que se corresponden con una obligación de un tercero (véase el párrafo 4.39), por ejemplo:
    <ol type="i">
      <li>Derechos a recibir efectivo.</li>
      <li>Derechos a recibir bienes o servicios.</li>
      <li>
        Derechos a intercambiar recursos económicos con terceros en condiciones favorables.
        <p>Estos derechos incluyen, por ejemplo, un contrato a término para comprar un recurso económico en términos que son actualmente favorables o una opción para comprar un recurso económico.</p>
      </li>
      <li>Derechos a beneficios procedentes de una obligación de terceros que consiste en transferir un recurso económico si ocurre un suceso futuro incierto que haya sido especificado (véase el párrafo 4.37).</li>
    </ol>
  </li>
  <li>
    Derechos que no se corresponden con una obligación de un tercero, por ejemplo:
    <ol type="i">
      <li>
        Derechos sobre objetos físicos, tales como propiedades, planta y equipo o inventarios.
        <p>Ejemplos de estos derechos son un derecho a usar un objeto físico o un derecho a beneficios procedentes del valor residual de un objeto arrendado.</p>
      </li>
      <li>Derecho a utilizar propiedad intelectual.</li>
    </ol>
  </li>
</ol>

<h2>4.7</h2>
<p>Muchos derechos se establecen por contrato, legislación o medios similares. Por ejemplo, una entidad puede obtener derechos sobre un objeto físico propio o arrendado, mediante la posesión de un instrumento de deuda o un instrumento de patrimonio, o mediante la posesión de una patente registrada. Sin embargo, una entidad también puede obtener derechos de otras formas, por ejemplo:</p>
<ol type="a">
  <li>Comprando o creando conocimiento que no es de dominio público (véase el párrafo 4.22); o</li>
  <li>A través de una obligación de terceros que surge porque ese tercero no tiene la capacidad práctica de actuar de forma incongruente con sus prácticas comerciales, políticas publicadas o declaraciones específicas (véase el párrafo 4.31).</li>
</ol>

<h2>4.8</h2>
<p>Algunos bienes o servicios—por ejemplo, servicios de empleados—se reciben y consumen de forma inmediata. El derecho de una entidad a obtener beneficios económicos producidos por estos bienes o servicios existe de forma momentánea hasta que la entidad consume los bienes o servicios.</p>

<h2>4.9</h2>
<p>No todos los derechos de una entidad son activos de esa entidad—para ser activos de la entidad, los derechos deben tener el potencial de producir beneficios económicos para la entidad más allá de los beneficios económicos disponibles para todo tercero (véanse los párrafos 4.14 a 4.18) y ser controlados por la entidad (véanse los párrafos 4.19 a 4.25). Por ejemplo, los derechos disponibles para cualquier tercero sin costo significativo—por ejemplo, derechos de acceso a bienes públicos, tales como los derechos públicos de paso sobre terrenos o el conocimiento que es de dominio público—habitualmente no son activos para las entidades que los poseen.</p>

 

 

