    <h3>Materialidad o Importancia relativa</h3>

    <p>2.11 La información es material o tiene importancia relativa si su omisión o expresión inadecuada puede influir 
    sobre decisiones que los principales usuarios de los informes financieros con propósito general (véase el párrafo 1.5) 
    adoptan a partir de esos informes, que proporcionan información financiera sobre una entidad que informa específica.</p>

    <p>En otras palabras, materialidad o importancia relativa es un aspecto de la relevancia específico de la entidad, 
    basado en la naturaleza o magnitud, o ambas, de las partidas a las que se refiere la información en el contexto del 
    informe financiero de una entidad individual.</p>

    <p>Por consiguiente, el Consejo no puede especificar un umbral cuantitativo uniforme para la materialidad o importancia
    relativa, ni predeterminar qué podría ser material o tener importancia relativa en una situación particular.</p>

    <hr>
    <h3>✅ Traducción clara y sencilla:</h3>

  <p><strong>“El Consejo no puede especificar un umbral cuantitativo uniforme...”</strong><br>
  → No se puede decir, por ejemplo, que <em>“todo lo que cueste más de $10,000 es importante”</em>, porque depende del contexto de cada empresa.</p>

  <p><strong>“...ni predeterminar qué podría ser material...”</strong><br>
  → Tampoco se puede hacer una lista fija que diga <em>“estos datos siempre son importantes”</em>, porque algo que es importante en una empresa puede no serlo en otra.</p>

  <h4>📌 ¿Por qué?</h4>
  <p>Porque la materialidad depende de cada caso, de:</p>
  <ul>
    <li>El tipo de empresa,</li>
    <li>El tamaño de la empresa,</li>
    <li>El tipo de transacción,</li>
    <li>Y de cómo podría afectar a quienes usan los estados financieros.</li>
  </ul>

  <h4>🧠 Ejemplo:</h4>
  <p>Para una multinacional, una pérdida de $20,000 puede no ser importante.</p>
  <p>Pero para una pequeña empresa, esa misma cantidad puede ser crucial y afectar decisiones.</p>

  <p><strong>Por eso, no se puede aplicar una regla única para todos.</strong></p>

