    <h2>✅ ¿Qué es la relevancia en información financiera?</h2>
  <p>
    La <strong>relevancia</strong> es una característica cualitativa fundamental de la información financiera.
    Significa que la información sirve para tomar decisiones. Si una pieza de información puede influir en la decisión de un usuario, entonces es relevante.
  </p>

  <h3>📌 Puntos clave:</h3>

  <ol>
    <li>
      <strong>Influencia en la toma de decisiones (párrafo 2.6)</strong><br>
      La información no necesita ser nueva o desconocida para ser relevante.<br>
      Aun si los usuarios ya la conocen o no la usan, puede seguir siendo relevante si tiene el potencial de influir en sus decisiones.
    </li>

    <li>
      <strong>¿Cuándo es relevante la información? (párrafo 2.7)</strong><br>
      Cuando tiene al menos uno de estos dos valores:
      <ul>
        <li>✅ Valor predictivo</li>
        <li>✅ Valor confirmatorio</li>
      </ul>
    </li>

    <li>
