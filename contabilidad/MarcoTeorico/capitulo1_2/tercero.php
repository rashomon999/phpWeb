    <h3>Representación fiel</h3>

    <p>2.12 Los informes financieros representan fenómenos económicos utilizando palabras y números. Para ser útil, 
    la información financiera debe no sólo representar los fenómenos relevantes, sino que también representar de forma 
    fiel la esencia de los fenómenos que pretende representar. En muchas circunstancias, la esencia de un fenómeno 
    económico y su forma legal son las mismas. Si no lo son, el suministro de información solo sobre la forma legal 
    no representaría de forma fiel el fenómeno económico (véanse los párrafos 4.59 a 4.62).</p>

    <p>2.13 Para ser una representación fiel perfecta, una descripción tendría tres características. Sería completa, 
    neutral y libre de error. Naturalmente, la perfección es rara vez alcanzable, si es que se alcanza alguna vez. 
    El objetivo del Consejo es maximizar esas cualidades en la medida de lo posible.</p>

    <p>2.14 Una descripción completa incluye toda la información necesaria para que un usuario comprenda el fenómeno 
    que está siendo representado, incluyendo todas las descripciones y explicaciones necesarias. Por ejemplo, una 
    representación completa de un grupo de activos incluiría, como mínimo, una descripción de la naturaleza de los 
    activos del grupo, una descripción numérica de todos los activos del grupo, y una descripción de qué representa 
    la descripción numérica (por ejemplo, costo histórico o valor razonable).</p>

    <p>Para algunas partidas, una descripción completa puede también conllevar explicaciones de hechos significativos 
    sobre la calidad y naturaleza de las partidas, los factores y las circunstancias que pueden afectar a su 
    calidad y naturaleza, así como el proceso utilizado para determinar la descripción numérica.</p>


