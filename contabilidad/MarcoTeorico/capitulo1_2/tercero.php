      Lo predictivo y lo confirmatorio están conectados.<br>
      ➤ Una información que ayuda a predecir también puede servir luego para confirmar si esas predicciones fueron acertadas.<br>
      ➤ Esto mejora los procesos de predicción en el futuro.
    </li>
  </ol>

