    <h3>Relevancia</h3>

    <p>2.6 La información financiera relevante es capaz de influir en las decisiones tomadas por los usuarios. 
    La información puede ser capaz de influir en una decisión incluso si algunos usuarios eligen no aprovecharla o 
    son ya conocedores de ella por otras fuentes.</p>

    <p>2.7 La información financiera es capaz de influir en las decisiones si tiene valor predictivo, valor 
    confirmatorio o ambos.</p>

    <p>2.8 La información financiera tiene valor predictivo si puede utilizarse como un dato de entrada en los 
    procesos empleados por usuarios para predecir desenlaces futuros. La información financiera no necesita ser una 
    predicción o una previsión para tener valor predictivo. La información financiera con valor predictivo es empleada 
    por los usuarios para llevar a cabo sus propias predicciones.</p>

    <p>2.9 La información financiera tiene valor confirmatorio si proporciona información sobre (es decir, si confirma o cambia) 
    evaluaciones anteriores.</p>

    <p>2.10 El valor predictivo y el valor confirmatorio de la información financiera están interrelacionados. La 
    información que tiene valor predictivo habitualmente también tiene valor confirmatorio. Por ejemplo, la información de 
    ingresos de actividades ordinarias para el ejercicio corriente, que puede ser utilizada como base para la predicción de 
    ingresos de actividades ordinarias en ejercicios futuros, puede también compararse con predicciones de ingresos de 
    actividades ordinarias para el ejercicio actual que se realizaron en ejercicios pasados. Los resultados de esas 
    comparaciones pueden ayudar a un usuario a corregir y mejorar los procesos que se utilizaron para hacer esas predicciones 
    anteriores.</p>

    <hr>
    <strong>parentesis</strong>
    <p>
    resumen2.10: 
    Una información que sirve hoy para predecir el futuro, también servirá en el futuro para verificar si esa predicción 
