      <em>Por ejemplo:</em> si los usuarios ven los ingresos actuales, pueden usarlos para predecir ingresos futuros.
    </li>

    <li>
      <strong>Valor confirmatorio (párrafo 2.9)</strong><br>
      Tiene valor confirmatorio si permite comprobar o corregir evaluaciones pasadas.<br>
