   <h6>📆 ¿Incluyen eventos después del cierre del periodo?</h6>
  <p>Sí, si esos eventos son necesarios para cumplir el objetivo de los estados financieros (es decir, para ayudar a tomar decisiones útiles).</p>

  <h6>👁️‍🗨️ ¿Desde qué perspectiva se hacen?</h6>
  <p>Desde la entidad que informa en su conjunto, no desde el punto de vista de un grupo específico (como solo los inversionistas o prestamistas).</p>

  <h6>🧱 Supuesto de negocio en marcha</h6>
  <p>Los estados financieros asumen que la empresa seguirá funcionando en el futuro.</p>
  <p>Si hay señales de que puede cerrar o liquidarse, se debe usar otra base contable.</p>
  <p>En ese caso, debe explicarse en los estados financieros.</p>

