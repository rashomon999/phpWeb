<h5>La entidad que informa</h5>

  <p><strong>3.10</strong> Una entidad que informa es aquella a la que se le requiere, u opta, por preparar estados financieros. Puede tratarse de una única entidad, una parte de una entidad o un conjunto de entidades. No es necesario que sea una entidad legal.</p>

  <p><strong>3.11</strong> En algunos casos, una entidad (la controladora) tiene control sobre otra (la subsidiaria).<br>
    - Si la entidad que informa incluye tanto la controladora como sus subsidiarias, los estados financieros se denominan <em>estados financieros consolidados</em> (véanse los párrafos 3.15 y 3.16).<br>
    - Si solo incluye a la controladora, se denominan <em>estados financieros no consolidados</em> (véanse los párrafos 3.17 y 3.18).
  </p>

  <p><strong>3.12</strong> Cuando la entidad que informa incluye dos o más entidades sin una relación controladora-subsidiaria, sus estados financieros se llaman <em>estados financieros combinados</em>.</p>

  <p><strong>3.13</strong> Determinar el límite de una entidad que informa puede ser complicado si:<br>
    (a) no es una entidad legal, y<br>
    (b) no está formada solo por entidades legales vinculadas por una relación de control.
  </p>

  <p><strong>3.14</strong> En tales casos, el límite de la entidad se define según las necesidades de los usuarios principales de los estados financieros. Para que la información tenga representación fiel, se requiere que:<br>
    (a) no se incluyan actividades económicas incompletas o arbitrarias;<br>
    (b) la inclusión de actividades no introduzca sesgos (información neutral); y<br>
    (c) se describa claramente cómo se definió el límite de la entidad y qué lo constituye.
  </p>
    <hr>

    <h6>📌 ¿Qué es una entidad que informa?</h6>
  <p>
    Es cualquier unidad (una empresa, parte de una empresa o un conjunto de empresas) que:
  </p>
  <ul>
    <li>Debe hacer estados financieros (por ley, por contrato, etc.), o</li>
    <li>Elige voluntariamente hacerlos.</li>
  </ul>
  <p><strong>🟡 Importante:</strong> No tiene que ser una empresa legalmente constituida (una "entidad legal"). Puede ser una combinación flexible de partes de empresas o empresas distintas.</p>

  <h6>📊 Tipos de estados financieros según el alcance (o límite) de la entidad:</h6>
  <ol>
    <li>
      <strong>✅ Estados financieros consolidados</strong><br>
      Se presentan cuando una empresa matriz (controladora) incluye a sus filiales (subsidiarias).<br>
      Muestran la situación financiera del grupo completo como si fuera una sola entidad.<br>
      <em>Ejemplo:</em> Grupo Éxito + todas sus filiales.
    </li>
    <li>
      <strong>✅ Estados financieros no consolidados</strong><br>
      Solo muestran la información de la empresa controladora, sin combinar con las subsidiarias.<br>
      <em>Ejemplo:</em> Solo Grupo Éxito, sin sus filiales.
    </li>
    <li>
      <strong>✅ Estados financieros combinados</strong><br>
      Se hacen cuando se combinan dos o más empresas que no están legalmente vinculadas como controladora-subsidiaria.<br>
      <em>Ejemplo:</em> Dos empresas del mismo dueño que no tienen relación de control, pero quieren mostrar resultados juntos.
    </li>
  </ol>

  <h6>🧩 ¿Por qué es difícil definir el "límite" de una entidad que informa?</h6>
  <p>A veces no es claro qué se debe incluir como parte de la entidad, especialmente si:</p>
  <ul>
    <li>No es una entidad legal.</li>
    <li>No hay una estructura de control formal entre las partes que se están considerando.</li>
  </ul>

  <h6>🔍 ¿Cómo se define ese límite entonces?</h6>
  <p>Se debe pensar desde la perspectiva del usuario de los estados financieros:</p>
  <p>Los estados financieros deben ofrecer <strong>información relevante y fiel</strong>, por eso:</p>
  <ul>
    <li>
      <strong>a)</strong> No debe haber actividades económicas "incompletas o arbitrarias":<br>
      No se puede mostrar solo las partes que se ven bien o son convenientes.<br>
      Se debe mostrar el conjunto económico completo que tenga sentido.
    </li>
    <li>
      <strong>b)</strong> La información debe ser neutral:<br>
      No se deben incluir o excluir partes solo para causar una impresión positiva o negativa.
    </li>
    <li>
      <strong>c)</strong> Se debe explicar claramente cómo se definió ese límite:<br>
      Qué actividades o entidades se incluyen.<br>
      Por qué se agruparon así.
    </li>
  </ul>

  <h6>🧠 Ejemplo práctico:</h6>
  <p>
    Supón que una universidad quiere presentar informes financieros solo de su facultad de medicina (aunque también tiene derecho, ingeniería, etc.):
  </p>
  <ul>
    <li>¿Es esa facultad una entidad que informa válida por sí sola?</li>
    <li>¿Tiene sentido presentar solo esa parte?</li>
    <li>¿Los usuarios entienden por qué se limitó así el informe?</li>
  </ul>
  <p>Si no se justifica bien, se puede ver como una visión incompleta o engañosa.</p>

  <h6>✅ En resumen:</h6>
  <ul>
    <li>La "entidad que informa" no siempre es una empresa legal.</li>
    <li>Puede ser un conjunto flexible, pero debe definirse claramente.</li>
    <li>El límite de qué se incluye en los estados financieros debe ser justificado, fiel y útil para los usuarios.</li>
  </ul>

