<h5>✅ Objetivo y alcance de los estados financieros</h5>

  <h6>📌 ¿Cuál es su objetivo?</h6>
  <p>Los estados financieros buscan proporcionar información útil para que los usuarios (como inversionistas o prestamistas) puedan:</p>
  <ul>
    <li>Evaluar si la entidad generará entradas de efectivo en el futuro.</li>
    <li>Juzgar cómo se está gestionando el uso de los recursos económicos de la entidad.</li>
  </ul>

  <h6>📄 ¿Qué incluyen los estados financieros?</h6>
  <ul>
    <li><strong>Estado de situación financiera</strong><br>Muestra: activos, pasivos y patrimonio.</li>
    <li><strong>Estado(s) del rendimiento financiero</strong><br>Muestra: ingresos y gastos.</li>
    <li><strong>Notas y otros estados</strong><br>Detalles sobre:
      <ul>
        <li>Elementos reconocidos (y los riesgos asociados).</li>
        <li>Elementos no reconocidos (y sus riesgos).</li>
        <li>Flujos de efectivo.</li>
        <li>Aportaciones y distribuciones a los propietarios.</li>
        <li>Métodos, supuestos y juicios contables utilizados.</li>
      </ul>
    </li>
  </ul>

  <h6>🕒 ¿Qué periodo cubren?</h6>
  <p>Los estados financieros se hacen para un periodo específico, y muestran:</p>
  <ul>
    <li>Situación de activos, pasivos y patrimonio al final o durante ese periodo.</li>
    <li>Ingresos y gastos generados durante ese periodo.</li>
    <li>Información comparativa (al menos del periodo anterior).</li>
  </ul>

  <h6>🔮 ¿Incluyen información sobre el futuro?</h6>
  <p>Sí, pero solo si está relacionada con:</p>
  <ul>
    <li>Activos, pasivos, patrimonio, ingresos o gastos del periodo.</li>
    <li>Y es útil para los usuarios.</li>
  </ul>
  <p>📌 <strong>Ejemplo:</strong> Si se usan flujos de efectivo futuros para valorar un activo, esos cálculos deben explicarse.</p>
  <p><em>Nota: Generalmente, no incluyen planes estratégicos o expectativas generales de la gerencia.</em></p>

   
