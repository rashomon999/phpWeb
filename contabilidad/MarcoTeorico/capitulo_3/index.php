<h3>CAPÍTULO 3 — ESTADOS FINANCIEROS Y LA ENTIDAD QUE INFORMA</h3>

<h4>Estados financieros</h4>


<p>
  <strong>Capítulos 1 y 2:</strong> Analizan los <em>informes financieros con propósito general</em> en términos generales.
</p>
<p>
  <strong>Capítulos 3 a 8:</strong> Se enfocan en los <em>estados financieros</em>, que son una forma específica de esos informes.
</p>

<h6> ¿Qué muestran los estados financieros?</h6>
<ul>
  <li>Los recursos económicos de la entidad</li>
  <li>Los derechos de los acreedores contra la entidad</li>
  <li>Los cambios en esos recursos y derechos</li>
</ul>
<p>Todo esto

<h4>Objetivo y alcance de los estados financieros</h4>
<p>
  3.2 El objetivo de los estados financieros es proporcionar información sobre los activos, pasivos, patrimonio, ingresos y gastos de la entidad que informa<sup>8</sup> que es útil a los usuarios de los estados financieros para evaluar las perspectivas de entradas de efectivo netas futuras a la entidad que informa y la administración de la gestión de los recursos económicos de la entidad (véase el párrafo 1.3).
</p>
<p>
  3.3 Esa información se proporciona:
</p>
<ul>
  <li>(a) en el estado de situación financiera, reconociendo activos, pasivos y patrimonio;</li>
  <li>(b) en el estado (o estados) del rendimiento financiero<sup>9</sup>, reconociendo ingresos y gastos; y</li>
  <li>(c) en otros estados y notas, presentando y revelando información sobre:
    <ul>
      <li>(i) activos, pasivos, patrimonio, ingresos y gastos reconocidos (véase el párrafo 5.1), incluyendo información sobre su naturaleza y sobre los riesgos que surgen de esos activos y pasivos reconocidos;</li>
      <li>(ii) activos y pasivos que no han sido reconocidos (véase el párrafo 5.6), incluyendo información sobre su naturaleza y sobre los riesgos que surgen de ellos;</li>
      <li>(iii) flujos de efectivo;</li>
      <li>(iv) aportaciones de los tenedores de derechos sobre el patrimonio y distribuciones a ellos; y</li>
      <li>(v) los métodos, suposiciones y juicios utilizados al estimar los importes presentados o revelados, y los cambios en ellos.</li>
    </ul>
  </li>
</ul>


    <h4>Periodo sobre el que se informa</h4>
<p>
  3.4 Los estados financieros se preparan para un periodo especificado de tiempo (periodo sobre el que se informa) y 
  proporcionan información sobre:
</p>
<ul>
  <li>(a) activos y pasivos —incluyendo activos y pasivos no reconocidos— y patrimonio que existían al final del periodo 
    sobre el que se informa o durante éste; y</li>
  <li>(b) ingresos y gastos para el periodo sobre el que se informa.</li>
</ul>

<p>
  3.5 Para ayudar a los usuarios de los estados financieros a identificar y evaluar los cambios y tendencias, los estados 
  financieros proporcionan también información comparativa por, al menos, un periodo sobre el que se informa anterior.
</p>

<p>
  3.6 La información sobre posibles transacciones futuras y otros posibles sucesos futuros (información referida al futuro) 
  se incluye en los estados financieros si:
</p>
<ul>
  <li>(a) está relacionada con activos o pasivos de la entidad —incluyendo activos y pasivos no reconocidos— o patrimonio 
    que existía al final o durante el periodo sobre el que se informa o a ingresos o gastos de éste; y</li>
  <li>(b) es útil para los usuarios de los estados financieros.</li>
</ul>
<p>
  Por ejemplo, si un activo o un pasivo se mide estimando los flujos de efectivo futuros, la información sobre las 
  estimaciones de esos flujos de efectivo futuros puede ayudar a los usuarios de los estados financieros a comprender las 
  medidas presentadas. Habitualmente, los estados financieros no proporcionan otros tipos de información referida al futuro, 
  por ejemplo, material explicativo sobre las expectativas y estrategias de la gerencia para la entidad que informa.
</p>

<p>
  3.7 Los estados financieros incluyen información sobre transacciones y sucesos que han ocurrido después del final del 
  periodo sobre el que se informa si la provisión de esta información es necesaria para cumplir el objetivo de los estados 
  financieros (véase el párrafo 3.2).
</p>

<h4>Perspectiva adoptada en los estados financieros</h4>
<p>
  3.8 Los estados financieros proporcionan información sobre transacciones y otros sucesos vistos desde la
  perspectiva de la entidad que informa en conjunto, no desde la perspectiva de cualquier grupo concreto de
  inversores, prestamistas u otros acreedores existentes o potenciales.
</p>

<h4>Hipótesis de negocio en marcha</h4>
<p>
  3.9 Los estados financieros se preparan normalmente bajo el supuesto de que una entidad que informa está en
  funcionamiento y continuará su actividad dentro del futuro previsible. Por lo tanto, se supone que la entidad
  no tiene la intención ni la necesidad de liquidar o cesar su actividad comercial. Si tal intención o necesidad
  existiera, los estados financieros pueden tener que prepararse sobre una base diferente. Si es así, los estados
  financieros describen la base utilizada.
</p>

