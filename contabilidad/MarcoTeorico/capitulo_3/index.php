  <h2>Estados financieros consolidados y no consolidados</h2>

  <p><strong>3.15</strong> Los estados financieros <strong>consolidados</strong> presentan información sobre los activos, pasivos, patrimonio, ingresos y gastos de la controladora y sus subsidiarias como si fueran una única entidad que informa. Esta información es útil para inversores, prestamistas y otros acreedores de la controladora, ya que les ayuda a evaluar las futuras entradas de efectivo netas de la controladora. Estas entradas incluyen las distribuciones que recibe de sus subsidiarias, las cuales dependen a su vez de las entradas de efectivo netas de las subsidiarias.</p>

  <p><strong>3.16</strong> Los estados financieros consolidados <em>no</em> están diseñados para proporcionar información individual de cada subsidiaria. Para eso están los estados financieros propios de cada subsidiaria.</p>

  <p><strong>3.17</strong> Los estados financieros <strong>no consolidados</strong> presentan información únicamente sobre la controladora, excluyendo a sus subsidiarias. Esta información puede ser útil porque:<br>
    (a) los derechos de los acreedores frente a la controladora no se extienden necesariamente a sus subsidiarias; y<br>
    (b) en algunas jurisdicciones, las distribuciones legales a los accionistas dependen exclusivamente de las reservas distribuibles de la controladora.<br>
    Alternativamente, parte de esta información puede incluirse como notas dentro de los estados financieros consolidados.
  </p>

  <p><strong>3.18</strong> Por lo general, los estados financieros no consolidados <em>no son suficientes</em> para cubrir todas las necesidades informativas de inversores, prestamistas y otros acreedores de la controladora. Por eso, cuando se requieren estados financieros consolidados, los no consolidados no los reemplazan. Sin embargo, en algunos casos, una controladora puede estar obligada, o puede optar, a preparar ambos tipos de estados financieros.</p>
