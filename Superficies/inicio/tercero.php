    <p>La razón por la que en algunos casos se despeja \( y \) (o \( x \)) y en otros no, como en el caso de \( k = \sqrt{25 - x^2 - y^2} \), tiene que ver con la forma de la función y el tipo de curvas que genera.</p>

  <h2> Caso 1: \( f(x, y) = x - 2y \)</h2>
  <div class="case">
    <p>Cuando se fija \( f(x, y) = c \), se obtiene una ecuación lineal:</p>
    <p>\( x - 2y = c \)</p>
    <p>Esta es la ecuación de una recta, así que tiene sentido despejar \( y \) o \( x \) para obtener su representación explícita (pendiente-intersección).</p>
    <p>Las curvas de nivel son rectas fáciles de describir al despejar.</p>
  </div>

  <h2> Caso 2: \( f(x, y) = \sqrt{25 - x^2 - y^2} \)</h2>
  <div class="case green">
    <p>Fijamos \( f(x, y) = k \), entonces:</p>
    <p>\( \sqrt{25 - x^2 - y^2} = k \)</p>
    <p>Elevando al cuadrado:</p>
    <p>\( 25 - x^2 - y^2 = k^2 \Rightarrow x^2 + y^2 = 25 - k^2 \)</p>
    <p>Esta es la ecuación de un círculo centrado en el origen, con radio \( \sqrt{25 - k^2} \), siempre que \( 25 - k^2 \geq 0 \).</p>

    <h3>¿Por qué no se despeja \( y \) en este caso?</h3>
    <p>Podrías hacerlo:</p>
    <p>\( y = \pm\sqrt{(25 - k^2) - x^2} \)</p>
    <p>Pero eso complica la expresión y parte el círculo en dos mitades (superior e inferior).</p>
    <p>En cambio, la forma implícita \( x^2 + y^2 = r^2 \) es más clara y natural para describir círculos.</p>
  </div>

  <h2> Conclusión</h2>
  <p>Se despeja \( y \) (o \( x \)) cuando la curva de nivel es una recta o curva sencilla, como en \( f(x, y) = x - 2y \).</p>
  <p>No se despeja \( y \) cuando eso complica innecesariamente la expresión o divide la curva, como en el caso de círculos o formas más simétricas, donde es mejor usar la forma implícita.</p>
