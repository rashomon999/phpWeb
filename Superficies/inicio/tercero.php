    <h2>📌 ¿Qué significa sacar derivadas parciales?</h2>
<p>
  Sacar derivadas parciales de una función como \( f(x, y) = x - y \) significa calcular cómo cambia la función respecto a una de las variables manteniendo la otra constante. Es una herramienta clave en cálculo multivariable.
</p>

<h3>📌 ¿Qué es una derivada parcial?</h3>
<p>
  Dada una función de varias variables, como \( f(x, y) \), la derivada parcial respecto a \( x \) mide cómo cambia \( f \) cuando \( x \) varía y \( y \) se mantiene constante. Lo mismo para la derivada respecto a \( y \).
</p>

<h3>🧮 Derivadas parciales de \( f(x, y) = x - y \)</h3>

<p><strong>Derivada parcial respecto a \( x \):</strong></p>
<p>
  \[
  \frac{\partial f}{\partial x} = \frac{\partial}{\partial x}(x - y)
  \]
</p>
<p>Aquí tratamos \( y \) como constante:</p>
<p>
  \[
  \frac{\partial f}{\partial x} = 1
  \]
</p>

<p><strong>Derivada parcial respecto a \( y \):</strong></p>
<p>
  \[
  \frac{\partial f}{\partial y} = \frac{\partial}{\partial y}(x - y)
  \]
</p>
<p>Aquí tratamos \( x \) como constante:</p>
<p>
  \[
  \frac{\partial f}{\partial y} = -1
  \]
</p>

<h3>🎯 Interpretación geométrica</h3>
<p>
  La derivada parcial \( \frac{\partial f}{\partial x} = 1 \) significa que, si avanzas una unidad en \( x \) manteniendo \( y \) constante, el valor de \( f(x, y) \) aumenta en 1 unidad.
</p>
<p>
  La derivada parcial \( \frac{\partial f}{\partial y} = -1 \) significa que, si avanzas una unidad en \( y \) manteniendo \( x \) constante, el valor de \( f(x, y) \) disminuye en 1 unidad.
</p>

<h3>📐 Visualización</h3>
<p>
  Como \( f(x, y) = x - y \) define un plano inclinado, las derivadas parciales te indican la pendiente del plano en las direcciones de \( x \) y \( y \):
</p>
<ul>
  <li>En dirección \( x \): el plano sube con pendiente 1.</li>
  <li>En dirección \( y \): el plano baja con pendiente -1.</li>
</ul>
