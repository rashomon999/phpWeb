<h3> Ejemplo con: \( f(x, y) = \sqrt{64 - x^2 - y^2} \)</h3>
<img src="../../img/ejemplo.png" alt="" width="600">
<br>
<strong>La funcion en verde es una curva de nivel</strong>
<br>
<h4>🔹 Curvas de nivel:</h4>
<p>Fijas \( z = k \):</p>
<p>\[
\sqrt{64 - x^2 - y^2} = k \Rightarrow x^2 + y^2 = 64 - k^2
\]</p>
<p>Resultado: una <strong>circunferencia</strong> en el plano \( xy \).</p>

<h4>🔹 Trazas:</h4>

<p>
  Si fijamos un valor de <span>\( y \)</span>, por ejemplo <span>\( y = k \)</span>, entonces:
</p>

<p>
  <span>\( z^2 + x^2 = 64 - k^2 \)</span>
</p>

<p>
  que es un círculo en el plano <span>\( xz \)</span>.
</p>
<p><strong>traza y = 0</strong></p>
<p>
  Por lo tanto, <span>\( z^2 + x^2 = 64 \)</span> es la traza cuando <span>\( y = 0 \)</span> (porque <span>\( 64 - 0^2 = 64 \)</span>).
</p>


<p>
  Si fijamos un valor de <span>\( x \)</span>, por ejemplo <span>\( x = k \)</span>, entonces:
</p>

<p>
  <span>\( z^2 + y^2 = 64 - k^2 \)</span>
</p>

<p>
  que es un círculo en el plano <span>\( yz \)</span>.
</p>
<p><strong>traza x = 0</strong></p>
<p>
  Por lo tanto, <span>\( z^2 + y^2 = 64 \)</span> es la traza cuando <span>\( x = 0 \)</span> (porque <span>\( 64 - 0^2 = 64 \)</span>).
</p>



<h3> Conclusión:</h3>
<ul>
  <li><strong>Curvas de nivel:</strong> fijas la salida \( z \) y estudias el conjunto de puntos de entrada que la producen → salen curvas en 2D (como círculos, elipses, etc.).</li>
  <li><strong>Trazas:</strong> fijas una entrada (como \( x \) o \( y \)) y estudias la relación de \( z \) con la otra variable → salen curvas en planos verticales (como parábolas, raíces, etc.).</li>
</ul>

