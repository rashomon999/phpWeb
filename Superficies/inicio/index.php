    <h2><span class="emoji"></span>¿Qué significa "dimensión 2 en el espacio 3D"?</h2>

<p>
  Cuando decimos que una <strong>superficie cónica</strong> (como un cono) tiene <strong>dimensión 2 en el espacio 3D</strong>, estamos diciendo que:
</p>

<ul>
  <li>Vive en un espacio tridimensional (3D) porque tiene coordenadas <code>x, y, z</code>.</li>
  <li>Pero su estructura esencial es bidimensional (2D) porque:
    <ul>
      <li>Para ubicar un punto en esa superficie, solo necesitas dos parámetros independientes (por ejemplo, un ángulo y una distancia).</li>
      <li>Es como una hoja doblada en el espacio: parece 3D, pero localmente tiene solo dos direcciones posibles (como moverte "a lo largo" y "alrededor" del cono).</li>
    </ul>
  </li>
</ul>

