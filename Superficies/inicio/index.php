    <h2>Intersecciones con ejes coordenados</h2>
    <p>Se obtienen fijando dos coordenadas a cero:</p>
    <ul>
      <li>Eje <strong>x</strong>: <strong>y = 0</strong>, <strong>z = 0</strong> → resolver <strong>F(x, 0, 0) = 0</strong>.</li>
      <li>Eje <strong>y</strong>: <strong>x = 0</strong>, <strong>z = 0</strong>.</li>
      <li>Eje <strong>z</strong>: <strong>x = 0</strong>, <strong>y = 0</strong>.</li>
    </ul>

    <p>Ejemplo: para el elipsoide <strong>x²/a² + y²/b² + z²/c² = 1</strong>, los puntos de intersección con los ejes son:</p>
    <ul>
      <li><strong>(±a, 0, 0)</strong>, <strong>(0, ±b, 0)</strong>, <strong>(0, 0, ±c)</strong>.</li>
    </ul>

    <h2>Curvas de nivel (mapa de contorno)</h2>
    <p>Las curvas de nivel de una superficie <strong>z = f(x, y)</strong> son curvas donde <strong>f(x, y) = c</strong> para valores constantes de <strong>c</strong>. Estas curvas están en el plano <strong>xy</strong> y representan la altura constante de la superficie.</p>
    
    <h3>Pasos para construir un mapa de contorno:</h3>
    <ol>
      <li>Elegir valores constantes <strong>c₁, c₂, ..., cₙ</strong>.</li>
      <li>Para cada <strong>cᵢ</strong>, resolver <strong>f(x, y) = cᵢ</strong>.</li>
      <li>Dibujar cada curva en el plano <strong>xy</strong> y etiquetarla.</li>
    </ol>

    <p>Ejemplos:</p>
    <ul>
      <li><strong>z = x² + y²</strong>: curvas de nivel <strong>x² + y² = c</strong> son círculos concéntricos.</li>
      <li><strong>z = x² − y²</strong>: <strong>x² − y² = c</strong> son hipérbolas.</li>
      <li><strong>Plano Ax + By + Cz = D</strong>: curvas de nivel son líneas rectas paralelas.</li>
    </ul>

