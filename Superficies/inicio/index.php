    <h3>Otros ejemplos comunes:</h3>
    <ul>
      <li><strong>Cono elíptico</strong>: <strong>x²/a² + y²/b² = z²/c²</strong>. Cortes <strong>z = cte</strong>: elipses; verticales: parábolas degeneradas.</li>
