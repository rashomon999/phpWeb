    <h2>✅ Superficie cónica (cono cuadrático o simplemente "cono")</h2>
  <p>
    Es una superficie tridimensional generada al extender una recta generatriz que pasa por un punto fijo (el vértice)
    y que recorre una curva base (como un círculo o elipse).
  </p>
  <p><strong>Ejemplo:</strong><br>
    El clásico cono de revolución que ves en un helado, generado al girar una línea recta alrededor de un eje.
  </p>
  <p><strong>Su ecuación típica en coordenadas cartesianas:</strong></p>
  <p><code>z² = x² + y²</code> (cono de dos hojas, si no está restringido)</p>
  <p>Tiene dimensión 2 en el espacio 3D.</p>

  <h2>✅ Sección cónica</h2>
  <p>
    Es una curva bidimensional (plana) que resulta al cortar un cono (como el anterior) con un plano.
  </p>
  <p><strong>Dependiendo de la inclinación del plano, se obtiene:</strong></p>
  <ul>
    <li>Círculo</li>
    <li>Elipse</li>
    <li>Parábola</li>
    <li>Hipérbola</li>
  </ul>
  <p>Por eso se llaman "secciones" cónicas: son los cortes planos del cono.</p>

