    <h2>Definición 1 (Superficies):</h2>
  <p>
    Se llama <strong>superficie</strong> al conjunto de puntos 
    \( (x, y, z) \in \mathbb{R}^3 \) cuyas coordenadas satisfacen una ecuación de la forma:


        <p style="text-align: center;">
    \( F(x, y, z) = 0 \)
  </p>

  <p>
    donde \( F \) es una función real de tres variables. Este conjunto se llama 
    <strong>superficie implícita</strong>.
  </p>

  </p>

    <hr>
    <h2>Definición2: superficies cilindricas</h2>
  <p>
    Un <strong>cilindro</strong> es una superficie generada por un conjunto de líneas rectas paralelas 
    (llamadas <em>generatrices</em>) que pasan por cada punto de una curva fija 
    (llamada <em>directriz</em> o <em>curva generatriz</em>), situada en un plano.
  </p>

  <p>
    Si las generatrices son paralelas a un eje coordenado, el cilindro se llama 
    <strong>cilindro cartesiano</strong>. Dependiendo de la curva directriz, puede ser:
  </p>

  <ul>
    <li><strong>Cilindro elíptico o circular</strong>: si la directriz es una circunferencia o elipse.</li>
    <li><strong>Cilindro parabólico</strong>: si la directriz es una parábola.</li>
    <li><strong>Cilindro hiperbólico</strong>, etc.</li>
  </ul>

