    <h1>Análisis de superficies tridimensionales: trazas, ejes y contornos</h1>
    <p>Para entender superficies 3D (cuádricas como elipsoides, cono elíptico, paraboloides, hiperboloides, etc.), es fundamental estudiar sus trazas (intersecciones con planos coordenados), sus intersecciones con los ejes, y sus curvas de nivel (contour maps). A continuación se explica cada concepto con ejemplos y referencias a recursos educativos confiables.</p>

    <h2>Trazas (secciones con planos coordenados)</h2>
    <p>Las trazas de una superficie son las curvas obtenidas al cortar la superficie con planos paralelos a los ejes coordenados. Formalmente, si la superficie está dada por una ecuación <strong>F(x, y, z) = 0</strong>, las trazas se obtienen estableciendo una coordenada constante:</p>
    <ul>
      <li>Plano <strong>z = c</strong> (horizontal): sustituir <strong>z = c</strong> en la ecuación y graficar la curva resultante en el plano <strong>z = c</strong>.</li>
      <li>Plano <strong>y = c</strong>: sustituir <strong>y = c</strong> y graficar la intersección con ese plano vertical.</li>
      <li>Plano <strong>x = c</strong>: sustituir <strong>x = c</strong> y graficar la intersección en dicho plano.</li>
    </ul>

    <p>Por ejemplo, para el elipsoide <strong>x²/a² + y²/b² + z²/c² = 1</strong>:</p>
    <ul>
      <li>En <strong>z = 0</strong>, la traza es una elipse en el plano <strong>xy</strong>.</li>
      <li>En <strong>y = 0</strong>, una elipse en el plano <strong>xz</strong>.</li>
      <li>En <strong>x = 0</strong>, una elipse en el plano <strong>yz</strong>.</li>
    </ul>
    <p>Si <strong>a = b = c</strong>, estas elipses son círculos y la superficie es una esfera.</p>

    <p>Ejemplo: el paraboloide elíptico <strong>z = x² + y²</strong> tiene:</p>
    <ul>
      <li>Cortes verticales: parábolas.</li>
      <li>Cortes horizontales <strong>(z = c)</strong>: elipses (círculos si coeficientes son iguales).</li>
    </ul>

    <p>El paraboloide hiperbólico <strong>z = x²/a² − y²/b²</strong> tiene:</p>
    <ul>
      <li>Traza en <strong>z = c</strong>: hipérbolas.</li>
      <li>Planos <strong>x = k</strong> o <strong>y = k</strong>: parábolas.</li>
    </ul>


    
