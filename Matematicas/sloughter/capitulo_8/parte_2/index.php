  <p>
    Consideremos la ecuación:
    $$
    \dot{x} = -\frac{t}{x}
    $$
    con \( x(0) = x_0 \ne 0 \). Separando las variables, tenemos:
    $$
    \int_{x_0}^{x} u\,du = -\int_0^t s\,ds.
    $$
