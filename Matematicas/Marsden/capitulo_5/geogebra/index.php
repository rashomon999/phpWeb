  <div class="step">
    <h5>1️⃣ Interpretación de <strong>Surface(u, v, 0, u, a, b, v, c, d) </strong> </h5>

    <p>La forma típica  <strong>Surface(param1, param2, exprZ, u, a, b, v, c, d)</strong>   significa:</p>

    <ul>
      <li>\( u \) y \( v\) → son parámetros que recorrerán el rango indicado por sus límites.</li>
      <li>\( 0\) → es la expresión para la coordenada <strong>\(z\)</strong> (altura). Aquí \(z = 0\) para todos los puntos.</li>
      <li>\( u, a, b \) → el parámetro \(u\) va desde \(a\) hasta \(b\).</li>
      <li>\( v, c, d\) → el parámetro \(v\) va desde \(c\) hasta \(d\).</li>
    </ul>
  </div>

  <div class="step">
    <h5>2️⃣ ¿Qué hace eso en la práctica?</h5>

    <p>Si lo vemos en coordenadas:</p>

    <div class="mathblock">
      \[
        x(u,v) = u,\qquad
        y(u,v) = v,\qquad
        z(u,v) = 0
      \]
    </div>

    <p>Y con los límites:</p>

    <div class="mathblock">
      \[
        a \le u \le b,\qquad c \le v \le d
      \]
    </div>

    <p>Por tanto, esta llamada dibuja el rectángulo en el plano \(z=0\) cuyo dominio en el plano \(xy\) es \([a,b]\times[c,d]\). Los parámetros <code>u</code> y <code>v</code> se usan directamente como las coordenadas \(x\) e \(y\).</p>
  </div>

  <div class="note">
    <strong>Nota:</strong> En otras superficies (por ejemplo paredes laterales) a veces normalizamos uno de los parámetros a \([0,1]\) y luego lo escalamos para obtener la altura real. En la base no hace falta esa normalización porque <code>u</code> y <code>v</code> ya representan directamente las coordenadas reales.
  </div>

