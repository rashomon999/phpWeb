     <h1>Explicación: g = Surface(u, c, v * f(u, c), u, a, b, v, 0, 1) </h1>

  <div class="section">
    <h2>Se entiende como:</h2>
    <p>Los primeros tres argumentos son la asignación de coordenadas \((x,y,z)\):</p>

    <div class="math">
      \[
        x = u,\qquad y = c,\qquad z = v\cdot f(u,c)
      \]
    </div>

    <ul>
      <li> \(x = u\)</li>
      <li> \(y = c\) (una constante fija)</li>
      <li> \(z = v\cdot f(u,c)\), donde \(f(u,c)\) es la altura máxima sobre la arista \(y=c\).</li>
    </ul>

    <p>Luego vienen los límites de los parámetros:</p>
    <ul>
      <li>Parámetro \(u\) va de \(a\) a \(b\) (recorre la arista en el eje \(x\)).</li>
      <li>Parámetro \(v\) va de \(0\) a \(1\) (es una fracción de la altura, normalizado).</li>
    </ul>
  </div>

  <div class="section">
    <h2>¿Qué región genera esto?</h2>

    <p>La arista base está en \(y=c\) y \(x\) recorre el intervalo \([a,b]\). En esa línea la altura máxima es \(f(u,c)\). Por ejemplo, si</p>

    <div class="math">
      \[
        f(x,y) = 5 - x - y \quad\Rightarrow\quad f(u,c) = 5 - u - c.
      \]
    </div>

    <p>La parametrización es</p>

    <div class="math">
      \[
        (u,v)\mapsto \big(x=u,\; y=c,\; z=v\cdot f(u,c)\big),
        \qquad a\le u\le b,\; 0\le v\le 1.
      \]
    </div>

    <p>Así:</p>
    <ul>
      <li>Si \(v=0\) entonces \(z=0\): estamos en la línea base sobre \(z=0\).</li>
      <li>Si \(v=1\) entonces \(z=f(u,c)\): estamos en la arista del “techo”.</li>
      <li>Al recorrer \(u\) de \(a\) a \(b\) y para cada \(u\) llevar \(v\) de 0 a 1, trazamos columnas verticales desde el suelo hasta el techo. El conjunto de todas esas columnas es una pared inclinada.</li>
    </ul>
  </div>

  <div class="section">
    <h2>¿Por qué se ve como un triángulo?</h2>

    <p>Fijando \(y=c\), consideramos la sección en el plano vertical \(y=c\). En esa sección la pared resulta en una figura triangular porque:</p>

    <ul>
      <li>Tiene una base horizontal sobre \(z=0\) desde \(x=a\) hasta \(x=b\).</li>
      <li>La arista superior (el techo) tiene alturas \(z=f(u,c)\) que dependen de \(u\), formando la hipotenusa del triángulo.</li>
      <li>El conjunto de puntos \(\{(x,0,z): x\in[a,b],\; 0\le z\le f(x,c)\}\) es triangular en la sección.</li>
    </ul>

    <div class="diagram">
       /|
      / |
     /  |
    /___|
    </div>

    <p>Donde:</p>
    <ul>
      <li>Base: de \((a,0,0)\) a \((b,0,0)\).</li>
      <li>Hipotenusa: de \((a,0,0)\) a \((b,0,f(b,c))\).</li>
      <li>Lado vertical en \(x=a\) (si se considera).</li>
    </ul>
  </div>

 
    <h2>Parámetros en resumen</h2>
    <ul>
      <li>\(u\in[a,b]\): recorre horizontalmente la arista (genera \(x\)).</li>
      <li>\(v\in[0,1]\): recorre verticalmente la fracción desde \(z=0\) hasta \(z=f(u,c)\).</li>
    </ul>

    <p>Es un método práctico porque parametriza una pared cuya altura cambia con \(u\) simplemente normalizando con \(v\in[0,1]\).</p>
 

         <div class="card">
    <h2><span class="emoji">📌</span> ¿Qué significa \(v\) de \(0\) a \(1\)?</h2>

    <p>
      Aquí \(v\) <strong>no</strong> es una coordenada física directa (ni \(x\), ni \(y\), ni \(z\)), sino un <em>parámetro</em> que controla cuánto de la altura máxima \(f(u,c)\) tomamos.
    </p>

    <ul>
      <li>Cuando \(v = 0\) \(\Rightarrow\) \(z = 0\) (base del triángulo).</li>
      <li>Cuando \(v = 1\) \(\Rightarrow\) \(z = f(u,c)\) (altura máxima para ese \(u\) fijo).</li>
    </ul>

    <p>
      Por eso, \(0\) a \(1\) significa “desde la base hasta la altura máxima”, en una sola dirección vertical (eje \(z\)), mientras \(u\) recorre horizontalmente.
    </p>
  </div>

  <div class="card">
    <h2><span class="emoji">📌</span> ¿Por qué forma un triángulo?</h2>

    <p>Porque para cada \(u\) fijo:</p>

    <ul>
      <li>En \(v=0\) tienes el punto base en \((u,\,c,\,0)\).</li>
      <li>En \(v=1\) tienes el punto más alto en \((u,\,c,\,f(u,c))\).</li>
      <li>Todos los puntos intermedios (\(0 < v < 1\)) están en línea recta entre base y cima.</li>
    </ul>

    <p>
      Si unes esas «líneas» (segmentos verticales) para todos los valores de \(u\in[a,b]\), se forma una superficie triangular (tipo abanico) sobre la línea \(y = c\).
    </p>

