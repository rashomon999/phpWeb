    <h3>1️⃣ Recordemos la SVD</h3>
<p>Para cualquier matriz \(A \in \mathbb{R}^{m \times n}\) (cuadrada o rectangular) se puede hacer:</p>
<p>
\[
A = U \Sigma V^T
\]
</p>
<p>donde:</p>
<ul>
  <li>\(U \in \mathbb{R}^{m \times m}\) → vectores singulares izquierdos</li>
  <li>\(\Sigma \in \mathbb{R}^{m \times n}\) → diagonal con valores singulares \(\sigma_i \ge 0\)</li>
  <li>\(V \in \mathbb{R}^{n \times n}\) → vectores singulares derechos</li>
</ul>

 
<h3>2️⃣ Dónde aparecen \(A^T A\) y \(A A^T\)</h3>
<p>Si multiplicas \(A\) por su transpuesta:</p>

<ol>
  <li><strong>\(A^T A\) (n×n)</strong>
    <ul>
      <li>Es simétrica y positiva semidefinida.</li>
      <li>Sus vectores propios son exactamente las columnas de \(V\).</li>
      <li>Sus autovalores son \(\sigma_i^2\).</li>
    </ul>
    <p>\[
    A^T A = V \Sigma^2 V^T
    \]</p>
  </li>
  <li><strong>\(A A^T\) (m×m)</strong>
    <ul>
      <li>También simétrica y positiva semidefinida.</li>
      <li>Sus vectores propios son las columnas de \(U\).</li>
      <li>Sus autovalores también son \(\sigma_i^2\).</li>
    </ul>
    <p>\[
    A A^T = U \Sigma^2 U^T
    \]</p>
  </li>
</ol>

<hr>

<h3>3️⃣ Por qué sirve esto</h3>
<p>La SVD te da \(U, \Sigma, V^T\), pero la manera de <strong>calcular los vectores singulares</strong> a veces se hace resolviendo los eigenvectores de \(A^T A\) y \(A A^T\):</p>
<ul>
  <li>Para encontrar \(V\) → encuentra los vectores propios de \(A^T A\)</li>
  <li>Para encontrar \(U\) → encuentra los vectores propios de \(A A^T\)</li>
  <li>Los valores singulares \(\sigma_i = \sqrt{\lambda_i}\), donde \(\lambda_i\) son los autovalores de \(A^T A\) o \(A A^T\)</li>
</ul>

<hr>

<h3>4️⃣ Intuición geométrica</h3>
<ul>
  <li>Piensa en \(A\) como una transformación lineal que lleva \(\mathbb{R}^n \to \mathbb{R}^m\).</li>
  <li>\(V\) indica las <strong>direcciones en el espacio de entrada</strong> (\(\mathbb{R}^n\)) que se estiran por \(A\).</li>
  <li>\(U\) indica las <strong>direcciones en el espacio de salida</strong> (\(\mathbb{R}^m\)) hacia donde van esas direcciones estiradas.</li>
  <li>\(A^T A\) “ve” solo el espacio de entrada y te dice cuáles son esas direcciones principales (\(V\)).</li>
  <li>\(A A^T\) “ve” el espacio de salida y te dice hacia dónde van esas direcciones (\(U\)).</li>
</ul>

<hr>

<h3>5️⃣ En pocas palabras</h3>
<ul>
  <li><strong>\(A^T A\)</strong> → para encontrar \(V\) (vectores singulares derechos, direcciones de entrada)</li>
  <li><strong>\(A A^T\)</strong> → para encontrar \(U\) (vectores singulares izquierdos, direcciones de salida)</li>
  <li>Los valores singulares vienen de \(\sqrt{\text{autovalores de } A^T A \text{ o } A A^T}\)</li>
</ul>


