     <h1>Descomposición en Valores Singulares (SVD)</h1>

    <p>Esta página recoge la explicación paso a paso que tenías en texto, ahora formateada en <strong>HTML</strong> usando <strong>MathJax</strong> para las fórmulas matemáticas.</p>

    <h2>1. Descomposición</h2>
    <p>En NumPy se obtiene con:</p>
    <pre><code>U, s, VT = np.linalg.svd(A, full_matrices=False)</code></pre>

    <p>Matemáticamente esto corresponde a la identidad:</p>
    <p style="font-size:1.05rem">\[ A = U \Sigma V^T \]</p>

    <p>Donde:</p>
    <table>
      <thead>
        <tr>
          <th>Variable</th>
          <th>Contenido</th>
          <th>Forma</th>
          <th>Descripción</th>
        </tr>
      </thead>
      <tbody>
        <tr>
          <td><code>U</code></td>
          <td>Matriz ortogonal de vectores singulares <em>izquierdos</em></td>
          <td>\(m\times m\)</td>
          <td>Sus columnas son las direcciones base del espacio de salida de \(A\).</td>
        </tr>
        <tr>
          <td><code>s</code></td>
          <td>Vector con los <strong>valores singulares</strong></td>
          <td>longitud = \(\min(m,n)\)</td>
          <td>Representa los factores de escala (magnitudes de “estiramiento”).</td>
        </tr>
        <tr>
          <td><code>VT</code></td>
          <td>Traspuesta de la matriz de vectores singulares <em>derechos</em></td>
          <td>\(n\times n\)</td>
          <td>Las <em>filas</em> (o columnas de \(V\)) son la base ortogonal del espacio de entrada.</td>
        </tr>
      </tbody>
    </table>

    <hr/>

    <p>Exacto ✅ — pero con un matiz importante:</p>

<p>Si <strong>A</strong> es <strong>simétrica</strong> (A = A<sup>T</sup>):</p>

<ol>
<li>Todos los <strong>valores propios</strong> \( \lambda_i \) de \( A \) son <strong>reales</strong>.</li>
<li>Los <strong>valores singulares</strong> \( \sigma_i \) de \( A \) se relacionan con los valores propios así:
  <br>
  \[
    \sigma_i = |\lambda_i|
  \]
  Es decir, los valores singulares son el <strong>valor absoluto</strong> de los valores propios.
</li>
<li>Los vectores propios de \( A \) coinciden con los vectores singulares derechos (\( V \)) y también con los vectores singulares izquierdos (\( U \)) de la SVD:
  <br>
  \[
    A = U \Sigma V^T \quad \text{y} \quad U = V = Q
  \]
  donde \( Q \) contiene los vectores propios de \( A \).
</li>
</ol>

<p>En resumen:</p>
<ul>
<li><strong>Simétrica</strong> → vectores propios = vectores singulares, valores singulares = |valores propios|.</li>
<li><strong>No simétrica</strong> → vectores propios de \( A \) y vectores singulares de \( A \) <strong>no coinciden</strong>; valores singulares son raíces cuadradas de los valores propios de \( A^T A \).</li>
</ul>

<hr>
<p>La relación que te interesa es:</p>

<p>
\[
B^T B \, v_i = \sigma_i^2 \, v_i
\]
</p>

<h3>1. Qué representa cada cosa</h3>
<ul>
  <li><strong>B</strong>: es la matriz original que estamos analizando, puede ser cualquier matriz (cuadrada o rectangular).</li>
  <li><strong>B<sup>T</sup></strong>: es la transpuesta de B.</li>
  <li><strong>B<sup>T</sup>B</strong>: es una matriz siempre <strong>simétrica</strong> y <strong>semi-definida positiva</strong>, incluso si B no lo es. Esto significa que todos sus valores propios son ≥ 0.</li>
  <li><strong>v<sub>i</sub></strong>: es una columna de la matriz V que obtenemos de la <strong>SVD</strong> de B (vectores singulares derechos).</li>
  <li><strong>σ<sub>i</sub></strong>: es el valor singular correspondiente a v<sub>i</sub>, obtenido también de la SVD.</li>
  <li><strong>σ<sub>i</sub><sup>2</sup></strong>: es el valor propio de B<sup>T</sup>B asociado a v<sub>i</sub>.</li>
</ul>

<h3>2. Qué dice la relación</h3>

<p>
\[
B^T B \, v_i = \sigma_i^2 \, v_i
\]
</p>

<p>Esto significa que <strong>cada vector singular derecho v<sub>i</sub> es un vector propio de B<sup>T</sup>B</strong> y su valor propio es el <strong>cuadrado del valor singular</strong>.</p>

<ul>
  <li>La SVD nos da directamente los vectores v<sub>i</sub> y valores σ<sub>i</sub> de manera que se cumpla esto.</li>
  <li>Por eso, si calculas B<sup>T</sup>B &middot; v<sub>i</sub> y lo comparas con σ<sub>i</sub><sup>2</sup> v<sub>i</sub>, obtienes prácticamente cero en la diferencia (solo errores numéricos).</li>
</ul>


    <hr>

    <h2>1️⃣ Valores propios (eigenvalores)</h2>

<p>Si tienes una matriz <strong>simétrica</strong> \(A\), sus <strong>valores propios</strong> \(\lambda_i\) se obtienen resolviendo:</p>

<p>
$$
\det(A - \lambda I) = 0
$$
</p>

<p>En tu ejemplo:</p>

<pre><code>import numpy as np
A = np.array([[2,1,3],[1,0,-1],[3,-1,2]])
eigvals, Q = np.linalg.eigh(A)
</code></pre>

<p>Esto devuelve <code>eigvals = [-2, 1, 5]</code>.</p>
<p>✅ Significado: son los <strong>coeficientes de la forma cuadrática diagonalizada</strong>. Cada valor propio indica cuánto “estira” o “comprime” la forma cuadrática en la dirección del eigenvector correspondiente.</p>

<hr>

<h2>2️⃣ Vectores propios (eigenvectores)</h2>

<p>Los <strong>vectores propios</strong> \(q_i\) son las columnas de la matriz \(Q\) que devuelve <code>np.linalg.eigh</code>.</p>

<p>Cada vector propio indica <strong>la dirección principal</strong> de la forma cuadrática en el espacio:</p>

<p>
$$
A q_i = \lambda_i q_i
$$
</p>

<p>En tu ejemplo:</p>

<pre><code>[[-0.577 -0.408 -0.707],
 [ 0.577 -0.816 -0.000],
 [ 0.577  0.408 -0.707]]
</code></pre>

<p>✅ Cada columna es un vector propio. Significa:</p>
<ul>
<li>La primera columna corresponde a \(\lambda_1 = -2\)</li>
<li>La segunda columna corresponde a \(\lambda_2 = 1\)</li>
<li>La tercera columna corresponde a \(\lambda_3 = 5\)</li>
</ul>

<p>Estas direcciones <strong>marcan los ejes principales</strong> de la forma cuadrática.</p>



    <hr>
    <h2>2. Construcción de \(\Sigma\)</h2>
    <p>En código:</p>
    <pre><code>m, n = A.shape
Sigma = np.zeros((m, n))
np.fill_diagonal(Sigma, s)</code></pre>

    <p>Esto transforma el vector \(s = [\sigma_1, \sigma_2, \sigma_3, \dots]\) en la matriz diagonal</p>
    <p>
      \[
        \Sigma =
        \begin{bmatrix}
          \sigma_1 & 0 & 0 & \dots \\
          0 & \sigma_2 & 0 & \dots \\
          0 & 0 & \sigma_3 & \dots \\
          \vdots & & & \ddots
        \end{bmatrix}
      \]
    </p>

    <hr/>

    <h2>3. Reconstrucción</h2>
    <p>Reconstruyes la matriz original con:</p>
    <pre><code>A_rec = U @ Sigma @ VT</code></pre>

    <p>Esto implementa la multiplicación matricial:</p>
    <p style="font-size:1.05rem">\[
      A_{\text{rec}} = U \Sigma V^T
    \]</p>

    <hr/>

    <h2>4. Verificación (norma de Frobenius)</h2>
    <p>El residuo se calcula así:</p>
    <pre><code>error = np.linalg.norm(A - A_rec)</code></pre>

    <p>NumPy por defecto usa la <strong>norma de Frobenius</strong> cuando se aplica a matrices, es decir:</p>
    <p>\[
      \|A - A_{\text{rec}}\|_F = \sqrt{\sum_{i,j} (A_{ij} - A_{\text{rec},ij})^2}
    \]</p>

    <div class="note">
      <strong>Interpretación:</strong> si obtenemos un número muy cercano a cero (por ejemplo \(2.9\times 10^{-15}\)), indica que la reconstrucción es correcta dentro de la precisión numérica de la máquina (float64).
    </div>

    <hr/>

    <h2>✅ Resumen del flujo</h2>
    <ol>
      <li><strong>Calcular</strong> la SVD: <code>U, s, VT = np.linalg.svd(A, full_matrices=False)</code>.</li>
      <li><strong>Formar</strong> la matriz diagonal \(\Sigma\) a partir de <code>s</code>.</li>
      <li><strong>Reconstruir</strong>: <code>A_rec = U @ Sigma @ VT</code>.</li>
      <li><strong>Verificar</strong> con la norma: <code>error = np.linalg.norm(A - A_rec)</code>. Si es ~0 → SVD correcta.</li>
    </ol>

    <p class="ok">Si quieres que lo coloque también para la matriz \(B\) de \(4\times4\) y que muestre los valores singulares, el \(U,\Sigma,V^T\) resultantes y su interpretación, dímelo y lo preparo en el mismo formato.</p>
 
