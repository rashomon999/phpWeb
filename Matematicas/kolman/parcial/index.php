    <h2>📘 Norma en NumPy</h2>

<p>
En álgebra lineal, una <strong>norma</strong> mide el “tamaño” o la “longitud” de un vector o matriz.  
Cumple tres propiedades esenciales:
</p>

<ul>
  <li>\( \|x\| \ge 0 \) y \( \|x\| = 0 \iff x = 0 \)</li>
  <li>\( \|\alpha x\| = |\alpha| \|x\| \)</li>
  <li>\( \|x + y\| \le \|x\| + \|y\| \)</li>
</ul>

<h3>🔹 En NumPy: <code>np.linalg.norm</code></h3>

<p>
La función <code>np.linalg.norm(x)</code> calcula la norma de un vector o matriz:
</p>

<ul>
  <li>
    Si \( x \) es un <strong>vector</strong>, calcula su <em>longitud euclidiana</em>:
    \[
    \|x\|_2 = \sqrt{x_1^2 + x_2^2 + \dots + x_n^2}
    \]
  </li>

  <li>
    Si \( x \) es una <strong>matriz</strong>, calcula por defecto la <em>norma de Frobenius</em>:
    \[
    \|A\|_F = \sqrt{\sum_{i,j} |A_{ij}|^2}
    \]
  </li>
</ul>



