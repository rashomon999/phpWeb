 <h1>Comparación: norma espectral vs norma de Frobenius</h1>

    <h2>🔹 Norma espectral</h2>
    <p>
      La <strong>norma espectral</strong> de una matriz \(A\) se define como
      \[
        \|A\|_2 = \sigma_{\max}(A),
      \]
      donde \(\sigma_{\max}(A)\) es el <strong>mayor valor singular</strong> de \(A\).
    </p>
    <p>
      Geométricamente, \(\|A\|_2\) mide el <em>máximo factor de estiramiento</em> que la transformación lineal representada por \(A\) puede aplicar a un vector unitario:
      \[
        \|A\|_2 = \max_{\|x\|=1} \|A x\|.
      \]
      Es decir: <em>cuánto puede transformar o amplificar un vector</em> en su dirección más estirada.
    </p>

    <h2>🔹 Norma de Frobenius</h2>
    <p>
      La <strong>norma de Frobenius</strong> se define como
      \[
        \|A\|_F = \sqrt{\sum_{i,j} |a_{ij}|^2}.
      \]
      Es análoga a la norma Euclidiana aplicada a todos los coeficientes de la matriz.
    </p>
    <p>
      Su interpretación es más bien global: mide el <em>tamaño total</em> o la “energía” de los coeficientes de \(A\), no el máximo estiramiento sobre vectores.
    </p>

    <hr/>

    <h2>🔹 Resumen</h2>
    <table>
      <thead>
        <tr>
          <th>Norma</th>
          <th>Qué mide</th>
          <th>Interpretación</th>
        </tr>
      </thead>
      <tbody>
        <tr>
          <td>\(\|A\|_2\) (espectral)</td>
          <td>Máximo estiramiento</td>
          <td>Cuánto puede <strong>transformar</strong> o <strong>amplificar</strong> un vector (dirección de mayor estiramiento)</td>
        </tr>
        <tr>
          <td>\(\|A\|_F\) (Frobenius)</td>
          <td>Tamaño total de los coeficientes</td>
          <td>Cuánto "peso" o "energía" tiene la matriz en conjunto (suma de cuadrados de entradas)</td>
        </tr>
      </tbody>
    </table>

    <div class="note">
      <strong>Conclusión:</strong>
      <br>
      Si lo que te interesa es <em>cuánto puede estirar un vector</em>, usa la <strong>norma espectral</strong> (el mayor valor singular).  
      Si quieres medir el <em>tamaño global</em> de la matriz o energía de sus entradas (por ejemplo para errores), la <strong>norma de Frobenius</strong> es apropiada.
    </div>

    <blockquote>
      La norma espectral = mayor valor singular = máximo estiramiento. <br>
      La norma de Frobenius = raíz de la suma de cuadrados de las entradas = tamaño total.
    </blockquote>
