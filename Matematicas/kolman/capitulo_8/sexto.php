  <h2>Diagonalización y orden de los vectores propios</h2>

<p>En este ejemplo no se “aplica la fórmula dos veces”, sino que se muestra el <b>mismo proceso de diagonalización</b> con un <b>orden distinto</b> de los vectores propios. Veamos los pasos:</p>

<h3>1️⃣ Fórmula general</h3>

<p>Si una matriz \( A \) es diagonalizable, existe una matriz \( P \) tal que:</p>

<div class="math-block">
\[
P^{-1} A P = D
\]
</div>

<p>donde \( D \) es diagonal (con los valores propios en su diagonal principal).</p>

<h3>2️⃣ Primer caso</h3>

<p>Valores propios:</p>

<div class="math-block">
\[
\lambda_1 = 2, \quad \lambda_2 = 3
\]
</div>

<p>Vectores propios correspondientes:</p>

<div class="math-block">
\[
x_1 = \begin{bmatrix} 1 \\ 1 \end{bmatrix}, \quad
x_2 = \begin{bmatrix} 1 \\ 2 \end{bmatrix}
\]
</div>

<p>Entonces:</p>

<div class="math-block">
\[
P = \begin{bmatrix} 1 & 1 \\ 1 & 2 \end{bmatrix}, \quad
P^{-1} = \begin{bmatrix} 2 & -1 \\ -1 & 1 \end{bmatrix}
\]
</div>

<p>y la diagonalización es:</p>

<div class="math-block">
\[
P^{-1} A P =
\begin{bmatrix}
2 & 0 \\
0 & 3
\end{bmatrix}
\]
</div>

<h3>3️⃣ Segundo caso (orden cambiado)</h3>

<p>Si invertimos el orden de los valores propios:</p>

<div class="math-block">
\[
\lambda_1 = 3, \quad \lambda_2 = 2
\]
</div>

<p>y por tanto el orden de los vectores propios:</p>

<div class="math-block">
\[
x_1 = \begin{bmatrix} 1 \\ 2 \end{bmatrix}, \quad
x_2 = \begin{bmatrix} 1 \\ 1 \end{bmatrix}
\]
</div>

<p>entonces:</p>

<div class="math-block">
\[
P = \begin{bmatrix} 1 & 1 \\ 2 & 1 \end{bmatrix}, \quad
P^{-1} = \begin{bmatrix} -1 & 1 \\ 2 & -1 \end{bmatrix}
\]
</div>

<p>y al aplicar la fórmula:</p>

<div class="math-block">
\[
P^{-1} A P =
\begin{bmatrix}
3 & 0 \\
0 & 2
\end{bmatrix}
\]
</div>

<h3>4️⃣ Conclusión</h3>

<p>Ambos resultados son <b>diagonalizaciones válidas</b> de la misma matriz \( A \):</p>

<div class="math-block">
\[
A = P D P^{-1}
\]
</div>

<p>La diferencia está solo en el <b>orden de las columnas de \( P \)</b> (los vectores propios) y, por tanto, en el <b>orden de los valores propios en la diagonal de \( D \)</b>.</p>


