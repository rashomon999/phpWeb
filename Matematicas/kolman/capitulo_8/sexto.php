<h2>🎯 Definición formal de matriz diagonal</h2>

<p>Una matriz cuadrada \( D = [d_{ij}] \) es <b>diagonal</b> si:</p>

<p style="text-align:center;">
  \( d_{ij} = 0 \quad \text{cuando } i \neq j \)
</p>

<p>Es decir, solo puede tener valores distintos de cero en la diagonal principal (de la esquina superior izquierda a la inferior derecha).</p>

<h4>🔹 Ejemplo</h4>

<p>✅ Matriz diagonal:</p>

<p style="text-align:center;">
  \( D =
  \begin{bmatrix}
  2 & 0 & 0 \\
  0 & 5 & 0 \\
  0 & 0 & -1
  \end{bmatrix} \)
</p>

<p>Aquí todos los elementos fuera de la diagonal son cero.
Los valores en la diagonal (2, 5, -1) pueden ser cualquier número — no necesariamente ceros.</p>

<p>❌ No diagonal:</p>

<p style="text-align:center;">
  \( A =
  \begin{bmatrix}
  2 & 1 \\
  0 & 3
  \end{bmatrix} \)
</p>

<p>porque el elemento \( a_{12} = 1 \neq 0 \).</p>

<p>🔹 Caso especial: la matriz nula</p>

<p style="text-align:center;">
  \( \begin{bmatrix}
  0 & 0 \\
  0 & 0
  \end{bmatrix} \)
</p>

<p>sí tiene ceros en la diagonal y también fuera, así que también es diagonal.
Pero es un caso particular (diagonal con ceros en la diagonal).</p>

<h4>🔹 En el contexto de valores propios</h4>

<p>Cuando diagonalizamos una matriz \( A \), buscamos:</p>

<p style="text-align:center;">
  \( P^{-1} A P = D \)
</p>

<p>donde \( D \) es diagonal, y en su diagonal principal aparecen los valores propios:</p>

<p style="text-align:center;">
  \( D =
  \begin{bmatrix}
  \lambda_1 & 0 & 0 \\
  0 & \lambda_2 & 0 \\
  0 & 0 & \lambda_3
  \end{bmatrix} \)
</p>

<p>Así, fuera de la diagonal todo es 0, y solo los valores propios quedan en la diagonal principal.</p>

