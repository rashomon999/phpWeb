    <h2>¿Para qué diagonalizar si ya conozco los valores y vectores propios?</h2>

<p>Vamos paso a paso para entender por qué sigue siendo útil hacerlo 👇</p>

<h3>🧩 1️⃣ Qué significa “diagonalizar”</h3>

<p>Diagonalizar una matriz \( A \) significa escribirla como un producto de tres matrices:</p>

<p style="text-align:center;">
  \( A = P D P^{-1} \)
</p>

<p>donde:</p>
<ul>
  <li>\( D \) es diagonal, con los valores propios en la diagonal.</li>
  <li>\( P \) contiene los vectores propios de \( A \) como columnas.</li>
  <li>\( P^{-1} \) es la inversa de \( P \).</li>
</ul>

<p>Entonces, “diagonalizar” no es solo conocer los valores y vectores propios, sino reorganizar la matriz completa para que tenga una forma más simple de trabajar con ella.</p>

<h3>💡 2️⃣ ¿Por qué hacerlo?</h3>

<p>Aunque ya sepas los valores y vectores propios, la diagonalización tiene aplicaciones prácticas enormes:</p>

<h4>✅ a) Simplifica cálculos de potencias</h4>

<p>Si necesitas calcular \( A^n \) (por ejemplo, en ecuaciones diferenciales, sistemas dinámicos, cadenas de Markov, etc.), hacerlo directamente es difícil.
Pero si \( A = P D P^{-1} \), entonces:</p>

<p style="text-align:center;">
  \( A^n = P D^n P^{-1} \)
</p>

<p>Y como \( D \) es diagonal:</p>

<p style="text-align:center;">
  \( D^n =
  \begin{bmatrix}
  \lambda_1^n & 0 \\
  0 & \lambda_2^n
  \end{bmatrix} \)
</p>

<p>es facilísimo de elevar a potencias.</p>

<h4>✅ b) Te muestra la estructura interna de la transformación</h4>

<p>Diagonalizar cambia la base para que la transformación representada por \( A \) se vea como simples escalamientos independientes (por los valores propios).  
Visualmente: estás rotando el sistema de coordenadas hasta que \( A \) solo “estira o encoge” en direcciones fijas (los vectores propios).</p>

<h4>✅ c) Sirve para resolver ecuaciones diferenciales o sistemas</h4>

<p>Muchos problemas se reducen a:</p>

<p style="text-align:center;">
  \( \dfrac{d\mathbf{x}}{dt} = A\mathbf{x} \)
</p>

<p>La solución es:</p>

<p style="text-align:center;">
  \( \mathbf{x}(t) = e^{At}\mathbf{x}(0) \)
</p>

<p>Pero \( e^{At} \) se puede calcular fácilmente solo si diagonalizas \( A \), porque:</p>

<p style="text-align:center;">
  \( e^{At} = P e^{Dt} P^{-1} \)
</p>

<p>y \( e^{Dt} \) se calcula simplemente como exponentes de los valores propios.</p>

<h4>✅ d) Permite entender “cómo actúa” la matriz</h4>

<p>Diagonalizar te muestra si \( A \) “estira”, “rota”, “refleja” o “comprime” el espacio.  
Los valores propios te dicen cuánto, y los vectores propios te dicen en qué dirección.</p>

<h3>📐 3️⃣ En tu ejemplo</h3>

<p>Ya sabías que:</p>

<p style="text-align:center;">
  \( \lambda_1 = 2, \quad \lambda_2 = 3 \)
</p>

<p>y sus vectores propios eran:</p>

<p style="text-align:center;">
  \( \mathbf{x}_1 =
  \begin{bmatrix}
  1 \\ 1
  \end{bmatrix}, \quad
  \mathbf{x}_2 =
  \begin{bmatrix}
  1 \\ 2
  \end{bmatrix} \)
</p>

