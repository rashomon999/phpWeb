      <h3>Ejemplo 4</h3>

  <p><strong>Matriz</strong>:</p>
  <p>
    \[
      A = 
      \begin{bmatrix}
        1 & 1 \\
        0 & 1
      \end{bmatrix}
    \]
  </p>

  <h4>1. Valores propios</h4>
  <p>
    <strong>Polinomio característico</strong>:
  </p>
  <p>
    \[
      \det(A - \lambda I) =
      \det
      \begin{bmatrix}
        1 - \lambda & 1 \\
        0 & 1 - \lambda
      \end{bmatrix}
      = (1 - \lambda)^2
    \]
  </p>
  <p>
    Por tanto, los valores propios son \(\lambda_1 = \lambda_2 = 1\) (multiplicidad algebraica 2).
  </p>

  <h4>2. Vectores propios</h4>
  <p>
    Buscamos \(x\) tal que \((A - I)x = 0\), es decir:
  </p>
  <p>
    \[
      \begin{bmatrix}
        0 & 1 \\
        0 & 0
      \end{bmatrix}
      \begin{bmatrix} x_1 \\ x_2 \end{bmatrix}
      =
      \begin{bmatrix} 0 \\ 0 \end{bmatrix}
    \]
  </p>
  <p>
    La primera ecuación da \(x_2 = 0\) y \(x_1\) es libre. Entonces los vectores propios son de la forma
    \(\displaystyle x = \begin{bmatrix} r \\ 0 \end{bmatrix}\) con \(r \neq 0\).
  </p>

  <h4>3. Diagonalizabilidad</h4>
  <p>
    <strong>Observación</strong>: solo existe un vector propio linealmente independiente (todas las direcciones propias son múltiplos de \(\begin{bmatrix}1\\0\end{bmatrix}\)). Para diagonalizar una matriz \(2\times2\) se necesitan dos vectores propios linealmente independientes. Por tanto:
  </p>
  <p><strong>Conclusión:</strong> \(A\) no es diagonalizable.</p>

  <h4>Intuición geométrica</h4>
  <p>
    La matriz \(A\) actúa como una traslación/desplazamiento en la dirección del eje \(x\) además de mantener el eje; ese “deslizamiento” impide obtener dos direcciones propias independientes.
  </p>

  <hr/>

  <h3>Teorema 8.5</h3>

  <p>
    <strong>Enunciado.</strong> Si todas las raíces del polinomio característico de una matriz \(A\) de \(n\times n\) son distintas (es decir, si todos los valores propios son diferentes entre sí), entonces \(A\) es diagonalizable.
  </p>

  <h4>Idea de la demostración</h4>
  <p>
    Sean \(\lambda_1,\lambda_2,\dots,\lambda_n\) los valores propios distintos de \(A\), y sean \(x_1,x_2,\dots,x_n\) vectores propios asociados. Se quiere probar que el conjunto \(S=\{x_1,\dots,x_n\}\) es linealmente independiente.
  </p>
  <p>
    Si existiera una dependencia lineal, se podría elegir el primer vector que dependiera linealmente de los anteriores, es decir,
    \[
      x_j = c_1 x_1 + c_2 x_2 + \cdots + c_{j-1} x_{j-1}.
    \]
    Aplicando \(A\) y usando \(Ax_i=\lambda_i x_i\) se obtienen dos expresiones que, al restarlas, conducen a una combinación lineal de \(x_1,\dots,x_{j-1}\) con coeficientes que involucran \(\lambda_i - \lambda_j\). Como las \(\lambda_i\) son distintas, esto fuerza que todos los coeficientes sean cero, contradicción. Luego los \(x_i\) son linealmente independientes.
  </p>
  <p>
    Con \(n\) vectores propios linealmente independientes en \(\mathbb{R}^n\) (o \(\mathbb{C}^n\)), existe una matriz invertible \(P\) cuya columnas son esos vectores, y se cumple \(P^{-1}AP = D\) con \(D\) diagonal. Es decir, \(A\) es diagonalizable.
  </p>

  <hr/>

  <h3>Resumen</h3>
  <p><strong>Ejemplo 4:</strong> valor propio repetido \(\lambda=1\), solo una dirección propia ⇒ no diagonalizable.</p>
  <p><strong>Teorema 8.5:</strong> valores propios distintos ⇒ existen \(n\) autovectores independientes ⇒ matriz diagonalizable.</p>
    <hr>
    <h3>🔍 Contexto</h3>
  <p>
    Después de demostrar que si todos los valores propios son distintos, la matriz es diagonalizable, el libro agrega esta observación más general:
  </p>
  <p>
    No siempre es necesario que todos los valores propios sean distintos para que la matriz sea diagonalizable —pero hay una condición más precisa.
  </p>

  <h3>🧩 Parte 1: Qué se estableció en el teorema anterior</h3>
  <p>
    El teorema 8.5 probó que:
  </p>
  <p>
    Si una matriz \( A \) de tamaño \( n \times n \) tiene \( k \) valores propios distintos, entonces sus vectores propios asociados \( x_1, x_2, \ldots, x_k \) son linealmente independientes.
  </p>
  <p>
    Por eso, si \( k = n \) (es decir, hay \( n \) valores propios distintos), hay \( n \) vectores propios linealmente independientes ⇒ \( A \) es diagonalizable.
  </p>

  <h3>⚙️ Parte 2: Cuando hay valores propios repetidos</h3>
  <p>
    Si no todas las raíces (valores propios) del polinomio característico son distintas, entonces \( A \) puede o no ser diagonalizable.
    Depende de cuántos vectores propios independientes haya para cada valor propio.
  </p>
  <p>
    El polinomio característico puede escribirse como:
  </p>
  <p>
    \[
      (\lambda - \lambda_1)^{k_1} (\lambda - \lambda_2)^{k_2} \cdots (\lambda - \lambda_r)^{k_r}
    \]
  </p>
  <p>
    donde:
  </p>
  <ul>
    <li>\( \lambda_1, \lambda_2, \ldots, \lambda_r \) son los valores propios distintos de \( A \),</li>
    <li>\( k_1, k_2, \ldots, k_r \) son sus multiplicidades algebraicas (cuántas veces se repite cada raíz),</li>
    <li>y la suma \( k_1 + k_2 + \cdots + k_r = n \).</li>
  </ul>

