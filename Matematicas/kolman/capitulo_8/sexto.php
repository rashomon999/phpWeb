<p style="text-align:center;">
  \( P =
  \begin{bmatrix}
  1 & 1 \\
  1 & 2
  \end{bmatrix}, \quad
  D =
  \begin{bmatrix}
  2 & 0 \\
  0 & 3
  \end{bmatrix} \)
</p>

<p>y la diagonalización es:</p>

<p style="text-align:center;">
  \( A = P D P^{-1} \)
</p>

<p>O equivalentemente,</p>

<p style="text-align:center;">
  \( P^{-1} A P = D \)
</p>

<p>Esa igualdad muestra que en la base de vectores propios, la matriz \( A \) actúa solo como una escala por 2 y por 3, sin mezclar direcciones.
Esa es la esencia y la utilidad de diagonalizar. 🌟</p>

