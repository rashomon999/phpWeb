      <h2>Procedimiento para determinar valores y vectores propios</h2>
    
    <p>El procedimiento para determinar los valores propios y los vectores propios asociados de una matriz, es como sigue.</p>
    
    <p><strong>Paso 1.</strong> Determine las raíces del polinomio característico \( f(\lambda) = \det(\lambda I_n - A) \). Éstas son los valores propios de \( A \).</p>
    
    <p><strong>Paso 2.</strong> Para cada valor propio \( \lambda \), determine todas las soluciones no triviales para el sistema homogéneo \( (\lambda I_n - A)x = 0 \). Éstos son los vectores propios de \( A \), asociados con el valor propio \( \lambda \).</p>
    
    <p>Por supuesto, el polinomio característico de una matriz dada puede tener algunas raíces complejas, e incluso podría carecer por completo de raíces reales. Sin embargo, en el importante caso de las matrices simétricas, todas las raíces del polinomio característico son reales. Estableceremos este resultado en la sección 8.3 (teorema 8.6).</p>
    
    <p>Los valores propios y los vectores propios satisfacen muchas propiedades de gran interés. Por ejemplo, si \( A \) es una matriz triangular superior (inferior) o una matriz diagonal, los valores propios de \( A \) son los elementos de la diagonal principal de \( A \) (ejercicio T.3). El conjunto \( S \) que consiste en todos los vectores propios de \( A \) asociados con \( \lambda_j \), junto con el vector nulo, es un subespacio de \( R^n \) (ejercicio T.1), denominado <strong>espacio propio asociado con \( \lambda_j \)</strong> (también se le conoce como espacio invariante). En los ejercicios de esta sección se desarrollan otras propiedades.</p>
    
    <p>Es preciso hacer hincapié en que el método para determinar los valores propios de una transformación lineal o matriz por medio de la obtención de las raíces del polinomio característico no es práctico para \( n > 4 \), ya que incluye la evaluación de un determinante. En cursos de análisis numérico se estudian métodos numéricos eficientes para la determinación de valores propios y los vectores propios asociados.</p>
    
    <h3>Precaución</h3>
    
    <p>Al determinar los valores propios y los vectores propios asociados de una matriz \( A \), evite cometer el error común de transformar primero \( A \) a la forma escalonada reducida por filas \( B \), y luego determinar los valores y vectores propios de \( B \). Para comprender rápidamente cómo falla este enfoque, considere la matriz \( A \), definida en el ejemplo 4. Sus valores propios son \( \lambda_1 = 2 \, \text{y} \, \lambda_2 = 3 \). Como \( A \) es una matriz no singular, cuando la transformamos a la forma escalonada reducida por filas \( B \), tenemos que \( B = I_2 \). Los valores propios de \( I_2 \) son \( \lambda_1 = 1 \, \text{y} \, \lambda_2 = 1 \).</p>
    
    <h3>Ejemplo ilustrativo</h3>
    
    <p>Considere la matriz del ejemplo 4:</p>
    <p>\[A = \begin{bmatrix} 1 & 1 \\ -2 & 4 \end{bmatrix}\]</p>
    
    <p>Al reducirla a su forma escalonada reducida por filas obtenemos:</p>
    <p>\[B = I_2 = \begin{bmatrix} 1 & 0 \\ 0 & 1 \end{bmatrix}\]</p>
    
    <p>Mientras que los valores propios de \( A \) son 2 y 3, los valores propios de \( B \) son ambos 1. Esto demuestra claramente que <strong>la reducción por filas altera los valores propios</strong>.</p>
    
    <p>A continuación examinaremos brevemente tres aplicaciones de valores y vectores propios. Las primeras dos ya se han analizado en el texto; la tercera es nueva. El capítulo 9 se dedica por completo a un estudio más profundo de varias aplicaciones adicionales de valores y vectores propios.</p>
    
