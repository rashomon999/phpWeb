      <h2> 1 Qué es una <strong>transformación lineal</strong></h2>

  <p>Una <strong>transformación lineal</strong> es simplemente una <strong>regla que transforma vectores en otros vectores</strong> usando una matriz.</p>

  <p>Por ejemplo, si tienes la matriz</p>

  <p>\[
  A = \begin{bmatrix} 2 & 1 \\[4pt] 1 & 2 \end{bmatrix}
  \]</p>

  <p>y un vector</p>

  <p>\[
  \mathbf{v} = \begin{bmatrix} 1 \\[4pt] 0 \end{bmatrix},
  \]</p>

  <p>entonces la transformación se escribe así:</p>

  <p>\[
  A\mathbf{v} =
  \begin{bmatrix} 2 & 1 \\[4pt] 1 & 2 \end{bmatrix}
  \begin{bmatrix} 1 \\[4pt] 0 \end{bmatrix}
  =
  \begin{bmatrix} 2 \\[4pt] 1 \end{bmatrix}.
  \]</p>

  <p>Esto significa que la transformación <strong>A</strong> <em>toma</em> el vector (1,0) y lo convierte en (2,1).</p>

  <hr>

  <h3>2 Qué hace visualmente</h3>

  <p>Cada matriz “deforma” el plano (o el espacio) de una manera diferente:</p>
  <ul>
    <li>Algunas <strong>estiran</strong> vectores.</li>
    <li>Otras <strong>los rotan</strong>.</li>
    <li>Otras <strong>los reflejan</strong> (como un espejo).</li>
    <li>Algunas <strong>combinan</strong> esas acciones.</li>
  </ul>

  <p>Por eso se dice que la matriz <strong>actúa como una máquina que transforma vectores</strong>. De ahí el nombre “transformación lineal”.</p>

  <hr>

  <h3> 3 Qué tienen que ver los valores y vectores propios</h3>

  <p>Cuando aplicas una transformación, la mayoría de los vectores <strong>cambian de dirección y tamaño</strong>.</p>

  <p><strong>Pero hay vectores especiales</strong> que <strong>no cambian de dirección</strong>. Solo cambian su tamaño (se estiran o encogen).</p>

  <p>Esos vectores son los <strong>vectores propios</strong> (eigenvectors), y los factores por los que se estiran son los <strong>valores propios</strong> (eigenvalues).</p>

  <p>Matemáticamente:</p>

  <p>\[
  A\mathbf{v} = \lambda \mathbf{v}
  \]</p>

  <p>significa:</p>
  <blockquote>
    “El vector \( \mathbf{v} \) no cambia de dirección bajo la transformación \(A\), solo cambia su longitud por un factor \( \lambda \).”
  </blockquote>

  <hr>

  <h3> 4 Ejemplo geométrico</h3>

  <p>Imagina que \(A\) transforma el plano estirando todo en una dirección y comprimiendo en otra.</p>

  <ul>
    <li>Un vector \( \mathbf{v}_1 \) (en dirección de estiramiento) → se multiplica por 3 → \( \lambda_1 = 3 \).</li>
    <li>Otro vector \( \mathbf{v}_2 \) (en dirección de compresión) → se multiplica por 0.5 → \( \lambda_2 = 0.5 \).</li>
  </ul>

  <p>En un gráfico (por ejemplo GeoGebra):</p>
  <ul>
    <li>El vector <strong>negro</strong> es \( \mathbf{v} \).</li>
    <li>El vector <strong>rojo</strong> es \( A\mathbf{v} \).</li>
    <li>Cuando están sobre la misma línea, significa que \( \mathbf{v} \) es <strong>propio</strong>.</li>
    <li>Si además el rojo es 3 veces más largo, el valor propio es \( \lambda = 3 \).</li>
  </ul>

