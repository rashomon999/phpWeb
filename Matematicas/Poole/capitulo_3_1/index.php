                <p>Las operaciones elementales de fila <span class="highlight">no cambian</span> el espacio fila.</p>
                <p>Por lo tanto, una base del espacio fila se obtiene a partir de las <span class="highlight">filas no nulas</span> de la forma escalonada (o escalonada reducida) de \( A \).</p>
