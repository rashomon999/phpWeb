<h1>Subespacios, bases, dimensión y rank</h1>
    
 
        <p>Esta sección presenta tal vez las ideas más importantes de todo el libro. Ya se vio que existe una interacción entre geometría y álgebra: con frecuencia es posible usar intuición geométrica y razonar para obtener resultados algebraicos, y la potencia del álgebra con frecuencia permitirá extender los hallazgos más allá de los escenarios geométricos en los que surgieron por primera vez.</p>

        <p>En el estudio de los vectores ya encontró informalmente todos los conceptos en esta sección. Aquí, comenzará a volverse más formal al proporcionar definiciones para las ideas clave. Como verá, la noción de subespacio es simplemente una generalización algebraica de los ejemplos geométricos de rectas y planos que pasan por el origen. El concepto fundamental de base para un subespacio se deriva entonces de la idea de vectores directores para tales rectas y planos. El concepto de base le permitirá dar una definición precisa de dimensión que concuerde con una idea geométrica intuitiva del término, aunque es suficientemente flexible para permitir la generalización a otros escenarios.</p>

        <p>También comenzará a ver que dichas ideas arrojan más luz a lo que ya sabe acerca de las matrices y la solución de sistemas de ecuaciones lineales. En el capítulo 6 encontrará nuevamente todas estas ideas fundamentales con más detalle. Considere esta sección como una sesión de "familiarización".</p>

        <div class="highlight">
            <p>Un plano que pasa por el origen en \(\mathbb{R}^3\) "parece" una copia de \(\mathbb{R}^2\). Intuitivamente, estaría de acuerdo en que ambos son "bidimensionales". Si presiona un poco, también puede decir que cualquier cálculo que pueda hacerse con vectores en \(\mathbb{R}^2\) también puede realizarse en un plano que pasa por el origen. En particular, puede sumar y tomar múltiplos escalares (y, más generalmente, formar combinaciones lineales) de vectores en tal plano, y los resultados son otros vectores en el mismo plano. Se dice que, como \(\mathbb{R}^2\), un plano que pasa por el origen es cerrado con respecto a las operaciones de suma y multiplicación escalar. (Vea la figura 3.2.)</p>
        </div>

        <p>Pero los vectores en este plano, ¿son objetos bidimensionales o tridimensionales? Puede argumentar que son tridimensionales porque existen en \(\mathbb{R}^3\) y por tanto tienen tres componentes. Por otra parte, pueden describirse como una combinación lineal de sólo dos vectores, vectores dirección para el plano, y por tanto son objetos bidimensionales que existen en un plano bidimensional. La noción de subespacio es la clave para resolver este rompecabezas.</p>
   

         <h1>Definición de Subespacio y Ejemplo</h1>
    
    <div class="section">
        <div class="definition">
            <h2>Definición</h2>
            <p>Un subespacio de \( \mathbb{R}^n \) es cualquier colección \( S \) de vectores en \( \mathbb{R}^n \) tal que:</p>
            
            <div class="property">
                <p>1. El vector cero \( \mathbf{0} \) está en \( S \).</p>
            </div>
            
            <div class="property">
                <p>2. Si \( \mathbf{u} \) y \( \mathbf{v} \) están en \( S \), entonces \( \mathbf{u} + \mathbf{v} \) está en \( S \). (<em>\( S \) es <strong>cerrado bajo la suma</strong></em>.)</p>
            </div>
            
            <div class="property">
                <p>3. Si \( \mathbf{u} \) está en \( S \) y \( c \) es un escalar, entonces \( c\mathbf{u} \) está en \( S \). (<em>\( S \) es <strong>cerrado bajo la multiplicación por un escalar</strong></em>.)</p>
            </div>
            
            <div class="divider"></div>
            
            <p>También podría combinar las propiedades (2) y (3) y requerir, de manera equivalente, que \( S \) sea <strong>cerrado bajo combinaciones lineales</strong>:</p>
            
            <div class="math">
                <p>Si \( \mathbf{u}_1, \mathbf{u}_2, \ldots, \mathbf{u}_k \) están en \( S \) y \( c_1, c_2, \ldots, c_k \) son escalares,</p>
                <p>entonces \( c_1\mathbf{u}_1 + c_2\mathbf{u}_2 + \cdots + c_k\mathbf{u}_k \) están en \( S \).</p>
            </div>
        </div>
        
        <div class="example">
            <h2>Ejemplo 3.37</h2>
            <p>Toda recta y plano a través del origen en \( \mathbb{R}^3 \) es un subespacio de \( \mathbb{R}^3 \). Debe ser claro geométricamente que se satisfacen las propiedades (1) a (3). He aquí una demostración algebraica en el caso de un plano a través del origen. En el ejercicio 9 se le pide dar la demostración correspondiente para una recta.</p>
            
            <p>Sea \( \mathcal{P} \) un plano a través del origen con vectores directores \( \mathbf{v}_1, \mathbf{v}_2 \). Por tanto, \( \mathcal{P} = \text{gen}(\mathbf{v}_1, \mathbf{v}_2) \).</p>
            
            <p>El vector cero \( \mathbf{0} \) está en \( \mathcal{P} \), pues \( \mathbf{0} = 0\mathbf{v}_1 + 0\mathbf{v}_2 \).</p>
            
            <p>Ahora sean</p>
            <div class="math">
                <p>\( \mathbf{u} = c_1\mathbf{v}_1 + c_2\mathbf{v}_2 \) y \( \mathbf{v} = d_1\mathbf{v}_1 + d_2\mathbf{v}_2 \)</p>
            </div>
            <p>dos vectores en \( \mathcal{P} \). Entonces</p>
            <div class="math">
                <p>\( \mathbf{u} + \mathbf{v} = (c_1\mathbf{v}_1 + c_2\mathbf{v}_2) + (d_1\mathbf{v}_1 + d_2\mathbf{v}_2) = (c_1 + d_1)\mathbf{v}_1 + (c_2 + d_2)\mathbf{v}_2 \)</p>
            </div>
            <p>Por tanto, \( \mathbf{u} + \mathbf{v} \) es una combinación lineal de \( \mathbf{v}_1, \mathbf{v}_2 \) y por tanto está en \( \mathcal{P} \).</p>
            
            <p>Ahora sea \( c \) un escalar. Entonces</p>
            <div class="math">
                <p>\( c\mathbf{u} = c(c_1\mathbf{v}_1 + c_2\mathbf{v}_2) = (cc_1)\mathbf{v}_1 + (cc_2)\mathbf{v}_2 \)</p>
            </div>
            <p>que muestra que \( c\mathbf{u} \) también es una combinación lineal de \( \mathbf{v}_1, \mathbf{v}_2 \) y por tanto está en \( \mathcal{P} \).</p>
            
            <p>Se demostró que \( \mathcal{P} \) satisface las propiedades (1) a (3) y en consecuencia es un subespacio de \( \mathbb{R}^3 \).</p>
        </div>
        
        <div class="comment">
            <p>Si observa cuidadosamente los detalles del ejemplo 3.37, notará que el hecho de que \( \mathbf{v}_1, \mathbf{v}_2 \) fueran vectores en \( \mathbb{R}^3 \) no tuvo papel alguno en la verificación de las propiedades. En consecuencia, el método algebraico utilizado debe generalizarse más allá de \( \mathbb{R}^3 \) y aplicarse en situaciones donde ya no se pueda visualizar la geometría. Lo hace. Más aún, el método del ejemplo 3.37 puede servir como "plantilla" en escenarios más generales. Cuando el ejemplo 3.37 se generaliza al generador de un conjunto arbitrario de vectores en cualquier \( \mathbb{R}^n \), el resultado es suficientemente importante como para llamarse teorema.</p>
        </div>
    </div>














    <h1>Teorema 3.19: Subespacio Generado</h1>
    
    <div class="section">
        <div class="theorem">
            <h2>Teorema 3.19</h2>
            <p>Sean \( \mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k \) vectores en \( \mathbb{R}^n \). Entonces \(\text{gen}(\mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k) \) es un subespacio de \( \mathbb{R}^n \).</p>
        </div>
        
        <div class="proof">
            <h2>Demostración</h2>
            <p>Sea \( S = \text{gen}(\mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k) \). Para demostrar la propiedad (1) de la definición, simplemente se observa que el vector cero \( \mathbf{0} \) está en \( S \), ya que:</p>
            
            <div class="math">
                <p>\( \mathbf{0} = 0\mathbf{v}_1 + 0\mathbf{v}_2 + \cdots + 0\mathbf{v}_k \)</p>
            </div>
            
            <div class="divider"></div>
            
            <p>Ahora sean</p>
            
            <div class="math">
                <p>\( \mathbf{u} = c_1\mathbf{v}_1 + c_2\mathbf{v}_2 + \cdots + c_k\mathbf{v}_k \quad \text{y} \quad \mathbf{v} = d_1\mathbf{v}_1 + d_2\mathbf{v}_2 + \cdots + d_k\mathbf{v}_k \)</p>
            </div>
            
            <p>dos vectores en \( S \). Entonces</p>
            
            <div class="math">
                <p>\( \mathbf{u} + \mathbf{v} = (c_1\mathbf{v}_1 + c_2\mathbf{v}_2 + \cdots + c_k\mathbf{v}_k) + (d_1\mathbf{v}_1 + d_2\mathbf{v}_2 + \cdots + d_k\mathbf{v}_k) \)</p>
                <p>\( = (c_1 + d_1)\mathbf{v}_1 + (c_2 + d_2)\mathbf{v}_2 + \cdots + (c_k + d_k)\mathbf{v}_k \)</p>
            </div>
            
            <p>Por tanto, \( \mathbf{u} + \mathbf{v} \) es una combinación lineal de \( \mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k \) y por tanto está en \( S \). Esto verifica la propiedad (2).</p>
            
            <div class="divider"></div>
            
            <p>Para demostrar la propiedad (3), sea \( c \) un escalar. Entonces</p>
            
            <div class="math">
                <p>\( c\mathbf{u} = c(c_1\mathbf{v}_1 + c_2\mathbf{v}_2 + \cdots + c_k\mathbf{v}_k) \)</p>
                <p>\( = (cc_1)\mathbf{v}_1 + (cc_2)\mathbf{v}_2 + \cdots + (cc_k)\mathbf{v}_k \)</p>
            </div>
            
            <p>lo que demuestra que \( c\mathbf{u} \) también es una combinación lineal de \( \mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k \) y por tanto está en \( S \).</p>
            
            <p>Se demostró que \( S \) satisface las propiedades (1) a (3) y en consecuencia es un subespacio de \( \mathbb{R}^n \).</p>
        </div>
        
        <div class="conclusion">
            <p>A \(\text{gen}(\mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k) \) se le llamará <strong>subespacio generado</strong> por \( \mathbf{v}_1, \mathbf{v}_2, \ldots, \mathbf{v}_k \). Con frecuencia podrá ahorrar mucho trabajo al reconocer cuándo puede aplicarse el Teorema 3.19.</p>
        </div>
    </div>











