            <div class="math">
                El <span class="highlight">rank</span> de una matriz \( A \) es la dimensión de sus espacios renglón y columna y se denota mediante rango(\( A \)).
            </div>
            <p>Por tanto, para el ejemplo 3.50, puede escribir rango(\( A \)) = 3.</p>
        </div>
        
