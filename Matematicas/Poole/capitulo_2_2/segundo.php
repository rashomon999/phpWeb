        <h2>Método de Eliminación Gaussiana (Hacia la FER)</h2>
        <p>El objetivo de este método es llevar la matriz a la <strong>Forma Escalonada por Renglones (FER)</strong>.</p>
        
        <div class="step">
            <div class="step-title">Proceso:</div>
            <p>Usas operaciones elementales de renglón (intercambiar, multiplicar, sumar) para crear los ceros <strong>por debajo</strong> de los pivotes, formando la "escalera".</p>
        </div>
        
        <div class="step">
            <div class="step-title">Una vez en FER:</div>
            <p>El sistema equivalente es mucho más fácil de resolver, pero requiere un paso final llamado <strong>sustitución hacia atrás</strong>.</p>
        </div>
        
        <div class="step">
            <div class="step-title">Analogía:</div>
            <p>Es como despejar una variable en la última ecuación, luego sustituirla en la penúltima, y así sucesivamente hasta encontrar todas las incógnitas.</p>
        </div>

        <div class="step">
            <div class="step-title">Ejemplo del proceso:</div>
            <div class="matrix">
                \[
                \left[\begin{array}{ccc|c}
                1 & 2 & 3 & 9 \\
                2 & -1 & 1 & 8 \\
                3 & 0 & -1 & 3 \\
                \end{array}\right]
                \quad \xrightarrow{\text{Gauss}} \quad
                \left[\begin{array}{ccc|c}
                1 & 2 & 3 & 9 \\
                0 & -5 & -5 & -10 \\
                0 & 0 & 4 & 8 \\
                \end{array}\right]
                \]
            </div>
            <p>Ahora, desde la FER, se resuelve por sustitución:</p>
            <ol>
                <li>De la tercera fila: \(4z = 8\) ➡️ \(z = 2\)</li>
                <li>Sustituyes \(z\) en la segunda fila: \(-5y -5(2) = -10\) ➡️ \(-5y = 0\) ➡️ \(y=0\)</li>
                <li>Sustituyes \(y\) y \(z\) en la primera fila: \(x + 2(0) + 3(2) = 9\) ➡️ \(x + 6 = 9\) ➡️ \(x=3\)</li>
            </ol>
        </div>
    </div>

    <div class="method">
        <h2>Método de Gauss-Jordan (Hacia la FERR)</h2>
        <p>El objetivo de este método es llevar la matriz directamente a la <strong>Forma Escalonada Reducida por Renglones (FERR)</strong>.</p>
        
        <div class="step">
            <div class="step-title">Proceso:</div>
            <p>Primero aplicas los pasos de la eliminación gaussiana para obtener la FER. Luego, usas operaciones elementales adicionales para:</p>
            <ol>
                <li>Convertir cada pivote en 1.</li>
                <li>Crear ceros <strong>por encima</strong> de cada pivote.</li>
            </ol>
        </div>
        
        <div class="step">
            <div class="step-title">Una vez en FERR:</div>
            <p>La solución del sistema se puede leer <strong>directamente</strong>, sin necesidad de sustitución hacia atrás. Cada columna de una variable pivote tiene un 1 en su fila y 0 en todas las demás.</p>
        </div>
        
        <div class="step">
            <div class="step-title">Analogía:</div>
            <p>Es como despejar cada variable por completo de una vez.</p>
        </div>

        <div class="step">
            <div class="step-title">Ejemplo del proceso (partiendo de la FER anterior):</div>
