                <li>\(\mathbf{p}\) es un punto específico sobre \(\ell\)</li>
                <li>\(\mathbf{d} \neq \mathbf{0}\) es un <span class="vector">vector director</span> para \(\ell\)</li>
                <li>\(t\) es un parámetro real</li>
            </ul>
        </div>

