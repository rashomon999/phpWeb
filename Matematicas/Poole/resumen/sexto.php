  \[
  3 \begin{bmatrix} 1 \\ 0 \\ 3 \end{bmatrix} + 2 \begin{bmatrix} -1 \\ 1 \\ -3 \end{bmatrix} = \begin{bmatrix} 1 \\ 2 \\ 3 \end{bmatrix}
