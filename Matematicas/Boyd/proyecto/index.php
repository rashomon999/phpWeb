  <h2>🔹 3. ¿Por qué a veces es \(\hat{x}\) y otras veces \(\hat{\theta}\)?</h2>
  <p>Es pura notación. Depende del autor y del contexto:</p>
  <ul>
    <li>En <strong>Boyd &amp; Vandenberghe</strong> suelen usar \(x\) por convención del álgebra lineal.</li>
    <li>En <strong>aprendizaje automático</strong> o regresión estadística se usa \(\theta\) para enfatizar que son parámetros del modelo.</li>
  </ul>

  <p class="math">Pero matemáticamente:</p>
  <div class="math">$$\hat{x} = \hat{\theta}$$</div>

