  <h2>🔹 1. Qué estamos haciendo</h2>
  <p>Sí, exactamente:</p>
  <blockquote>
    <strong>El objetivo del problema de mínimos cuadrados es encontrar los parámetros que mejor predicen las salidas (b o y) a partir de las entradas (A o X).</strong>
  </blockquote>

  <p class="math">Matemáticamente:</p>
  <div class="math">$$\min_{\theta} \; \|A\theta - y\|^2$$</div>

  <p class="note">Queremos el vector de parámetros \(\theta\) (también llamado <em>vector de pesos</em>) que haga que las predicciones \(A\theta\) se acerquen lo más posible a los valores reales \(y\).</p>

  <hr>

  <h2>🔹 2. ¿Qué representa cada símbolo?</h2>
  <table>
    <thead>
      <tr><th>Símbolo</th><th>Significado</th><th>Nombre habitual</th></tr>
    </thead>
    <tbody>
      <tr><td><code>A</code></td><td>Matriz de datos o de características (cada fila = un ejemplo, cada columna = una variable)</td><td><em>design matrix</em></td></tr>
      <tr><td><code>y</code></td><td>Vector de resultados observados (lo que queremos predecir)</td><td><em>targets / labels</em></td></tr>
      <tr><td> \( \theta \)  o  \(x\) </td><td>Vector de parámetros desconocidos (los pesos del modelo)</td><td><em>weights / coefficients</em></td></tr>
