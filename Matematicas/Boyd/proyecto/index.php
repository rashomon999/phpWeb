  <h2 id="least-squares">14.2 Clasificador de mínimos cuadrados</h2>

  <p>
    Se han desarrollado muchos métodos sofisticados para construir un modelo booleano o clasificador a partir de un conjunto de datos.
    La <em>regresión logística</em> y la <em>máquina de vectores de soporte</em> son dos métodos ampliamente utilizados, pero fuera del alcance de este libro.
    Aquí discutimos un método muy simple, basado en mínimos cuadrados, que puede funcionar bastante bien, aunque no tan bien como los métodos más sofisticados.
  </p>

  <p>
    Primero llevamos a cabo el ajuste ordinario por mínimos cuadrados de valor real del resultado, ignorando por el momento que el resultado <span>\(y\)</span> solo toma los valores \(-1\) y \(+1\).
    Elegimos funciones de base \(f_1,\ldots,f_p\), y luego escogemos los parámetros \(\theta_1,\ldots,\theta_p\) de forma que minimicen el error cuadrático sumado
  </p>

  <div class="formula">
    \[
      (y^{(1)} - \tilde{f}(x^{(1)}))^2 + \cdots + (y^{(N)} - \tilde{f}(x^{(N)}))^2,
    \]
  </div>

  <p>
    donde
  </p>

  <div class="formula">
    \[
      \tilde{f}(x) = \theta_1 f_1(x) + \cdots + \theta_p f_p(x).
    \]
  </div>

  <p>
    Usamos la notación \(\tilde{f}\) porque esta función no es nuestro modelo final \( \hat{f}(x) \)
  </p>

La función <span>\(\tilde{f}\)</span> es el ajuste por mínimos cuadrados sobre nuestro conjunto de datos, y <span>\(\tilde{f}(x)\)</span>, para un vector general <em>x</em>, es un número.</p>

  <p>Nuestro clasificador final se toma entonces como</p>

  <p style="text-align:center;">\[
  \hat{f}(x) = \operatorname{sign}(\tilde{f}(x))
  \tag{14.1}
  \]</p>

  <p>donde \(\operatorname{sign}(a)=+1\) para \(a \ge 0\) y \(-1\) para \(a<0\). Llamamos a este clasificador el <em>clasificador de mínimos cuadrados</em>.</p>

  <p>La intuición detrás del clasificador de mínimos cuadrados es simple. El valor \(\tilde{f}(x)\) es un número, que (idealmente) está cerca de \(+1\) cuando \(y^{(i)}=+1\), y cerca de \(-1\) cuando \(y^{(i)}=-1\). Si nos vemos forzados a adivinar uno de los dos posibles resultados \(+1\) o \(-1\), es natural elegir \(\operatorname{sign}(\tilde{f}(x))\). (De hecho, \(\operatorname{sign}(\tilde{f}(x))\) es el vecino más cercano de \(\tilde{f}(x)\) entre los puntos \(-1\) y \(+1\).) La intuición sugiere que el número \(\tilde{f}(x)\) puede relacionarse con nuestra confianza en nuestra suposición \(\hat{y} = \operatorname{sign}(\tilde{f}(x))\): cuando \(\tilde{f}(x)\) está cerca de 1 tenemos confianza en que \(\hat{y}=+1\); cuando es pequeño y negativo (por ejemplo, \(\tilde{f}(x)=-0.03\)), suponemos \(\hat{y}=-1\), pero nuestra confianza en la suposición será baja. No profundizaremos en esta idea más en este libro, excepto en clasificadores multi-clase, que discutimos en §14.3.</p>

  <p>El clasificador de mínimos cuadrados se usa a menudo con un modelo de regresión, es decir, \(\tilde{f}(x) = x^T \beta + v\), en cuyo caso el clasificador tiene la forma</p>

  <p style="text-align:center;">\[
  \hat{f}(x) = \operatorname{sign}(x^T \beta + v)
  \tag{14.2}
  \]</p>

  <p>Podemos interpretar fácilmente los coeficientes en este modelo. Por ejemplo, si \(\beta_7\) es negativo, significa que cuanto mayor es el valor de \(x_7\), más probable es que supongamos \(\hat{y}=-1\). Si \(\beta_4\) es el coeficiente con la mayor magnitud, entonces podemos decir que \(x_4\) es la característica que más contribuye a nuestra decisión de clasificación.</p>

