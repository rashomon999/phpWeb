<h1>14.1 Clasificación</h1>

<p>En el problema de ajuste de datos del capítulo 13, el objetivo es reproducir o predecir el resultado <em>y</em>, que es un número escalar, basado en un vector <em>n</em>-dimensional <em>x</em>. En un <strong>problema de clasificación</strong>, la variable dependiente o resultado <em>y</em> solo toma un número finito de valores, y por esta razón a veces se le llama <em>etiqueta</em> (label), o en estadística, una variable <em>categórica</em>. En el caso más simple, <em>y</em> solo tiene dos valores, por ejemplo, TRUE o FALSE, o SPAM o NO SPAM. Esto se llama <em>problema de clasificación de dos vías</em>, <em>problema de clasificación binaria</em> o <em>problema de clasificación booleana</em>, ya que el resultado <em>y</em> solo puede tomar dos valores. Comenzamos considerando el problema de clasificación booleana.</p>

<p>Codificaremos <em>y</em> como un número real, tomando <em>y = +1</em> para TRUE y <em>y = -1</em> para FALSE. (También es posible codificar los resultados usando <em>y = +1</em> y <em>y = 0</em>, o cualquier otro par de dos números diferentes). Como en el ajuste de datos con valores reales, asumimos que existe una relación aproximada de la forma \(y \approx f(x)\), donde \(f: \mathbb{R}^n \to \{-1,+1\}\). (Esta notación significa que la función <em>f</em> toma un vector <em>n</em>-dimensional como argumento y da un valor resultante que es +1 o -1). Nuestro modelo tendrá la forma \(\hat{y} = \hat{f}(x)\), donde \(\hat{f}: \mathbb{R}^n \to \{-1,+1\}\). El modelo \(\hat{f}\) también se llama <em>clasificador</em>, ya que clasifica vectores <em>n</em>-dimensionales en aquellos para los cuales \(\hat{f}(x) = +1\) y aquellos para los que \(\hat{f}(x) = -1\). Como en el ajuste de datos reales, elegimos o construimos el clasificador \(\hat{f}\) usando algunos datos observados.</p>

<h2>Ejemplos</h2>

<p>Los clasificadores booleanos se utilizan ampliamente en muchas áreas de aplicación:</p>

<ul>
<li><strong>Detección de spam en correo electrónico.</strong> El vector <em>x</em> contiene características de un mensaje de correo electrónico. Puede incluir el conteo de palabras en el cuerpo del mensaje, otras características relacionadas con el origen del correo, etc. El resultado es +1 si el mensaje es SPAM, y -1 de lo contrario. Los datos para construir el clasificador provienen de usuarios que han marcado explícitamente algunos mensajes como spam.</li>

<li><strong>Detección de fraude.</strong> El vector <em>x</em> proporciona un conjunto de características asociadas a un titular de tarjeta de crédito, como nivel promedio de gastos mensuales, precio medio de compras recientes, número de compras en diferentes categorías, saldo promedio, y algunas características de transacciones específicas. El resultado <em>y</em> es +1 para una transacción fraudulenta y -1 de lo contrario. Los datos para construir el clasificador provienen de registros históricos de transacciones verificadas como fraudulentas o legítimas.</li>

<li><strong>Clasificación de documentos booleanos.</strong> El vector <em>x</em> es un conteo de palabras (o histograma) para un documento, y el resultado <em>y</em> es +1 si el documento pertenece a un tema específico (por ejemplo, política) y -1 de lo contrario. Los datos pueden provenir de un corpus de documentos con temas etiquetados.</li>

<li><strong>Detección de enfermedades.</strong> Los ejemplos corresponden a pacientes, con resultado <em>y = +1</em> si el paciente tiene cierta enfermedad y <em>y = -1</em> si no. El vector <em>x</em> contiene características médicas relevantes asociadas al paciente (edad, sexo, resultados de pruebas, síntomas específicos, etc.). Los datos provienen de registros hospitalarios o estudios médicos.</li>

<li><strong>Receptor de comunicaciones digitales.</strong> En un sistema moderno de comunicaciones electrónicas, <em>y</em> representa un bit (tradicionalmente 0 o 1) que se envía de un transmisor a un receptor. El vector <em>x</em> representa <em>n</em> mediciones de la señal recibida. El predictor \(\hat{y} = \hat{f}(x)\) se llama <em>bit decodificado</em>. En comunicaciones, el clasificador \(\hat{f}\) se llama <em>decoder</em> o <em>detector</em>. Los datos para construir el decoder provienen de una <em>señal de entrenamiento</em>, una secuencia de bits conocida por el receptor que se transmitió.</li>
</ul>


