  <p>Una vez que tenemos \(\hat{\theta}\), el modelo puede predecir la salida para un nuevo vector de entrada \(x_{\text{nuevo}}\):</p>
  <div class="math">$$\hat{y}_{\text{nuevo}} = x_{\text{nuevo}}^\top \hat{\theta}$$</div>

