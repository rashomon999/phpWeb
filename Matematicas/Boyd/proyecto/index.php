  <h2>🔹 4. La solución</h2>
  <p>El vector óptimo se obtiene (cuando corresponde) con la fórmula de la solución normal:</p>
  <div class="math">$$\hat{\theta} = (A^\top A)^{-1} A^\top y$$</div>

  <p>O, en forma compacta usando la pseudoinversa de Moore–Penrose:</p>
  <div class="math">$$\hat{\theta} = A^{\dagger} y$$</div>

  <p class="note">Esto es lo que el algoritmo <em>aprende</em>: los parámetros del modelo lineal que mejor ajustan los datos en el sentido de mínimos cuadrados.</p>

  <hr>

