     <h1>Derivación de la Ecuación Normal en Mínimos Cuadrados</h1>

<p>El problema de mínimos cuadrados consiste en encontrar los parámetros <b>&theta;</b> que aproximan mejor un vector de observaciones <b>y</b> a un modelo lineal <b>A&theta;</b>. Formalmente, queremos minimizar la suma de los residuos al cuadrado:</p>

<p>
$$
f(\theta) = \|A\theta - y\|^2
$$
</p>

<p>Este es un problema de optimización cuadrática, y para solucionarlo utilizamos el <b>gradiente</b>. La idea es que el gradiente de la función respecto a &theta; nos indica la dirección de mayor crecimiento, y al igualarlo a cero encontramos el mínimo.</p>

<h2>1. Reescribiendo la función</h2>

<p>Podemos escribir la norma al cuadrado como un producto matricial:</p>

<p>
$$
f(\theta) = (A\theta - y)^T (A\theta - y)
$$
</p>

<h2>2. Expandiendo el producto</h2>

<p>
$$
(A\theta - y)^T (A\theta - y) = \theta^T A^T A \theta - 2 y^T A \theta + y^T y
$$
</p>

<p>Explicación de los términos:</p>
<ul>
<li><b>&theta;<sup>T</sup> A<sup>T</sup> A &theta;</b> → producto de A&theta; consigo mismo.</li>
<li><b>-2 y<sup>T</sup> A &theta;</b> → términos cruzados entre A&theta; y y.</li>
<li><b>y<sup>T</sup> y</b> → constante, no depende de &theta;.</li>
</ul>

<h2>3. Derivando con respecto a &theta;</h2>

<p>Usamos las reglas de derivación matricial:</p>
<ul>
<li>\(\frac{\partial}{\partial \theta} (\theta^T B \theta) = 2 B \theta\), si B es simétrica</li>
<li>\(\frac{\partial}{\partial \theta} (c^T \theta) = c\), c constante</li>
</ul>

<p>Aplicando esto a nuestra función:</p>
<p>
\[
\nabla_\theta f(\theta) = 2 A^T A \theta - 2 A^T y
\]
</p>

<h2>4. Igualando el gradiente a cero</h2>

<p>Para encontrar el mínimo, igualamos el gradiente a cero:</p>

<p>
\[
2 A^T A \theta - 2 A^T y = 0 \quad \Rightarrow \quad A^T A \theta = A^T y
\]
</p>

<p>Esta ecuación se llama <b>ecuación normal</b> del problema de mínimos cuadrados.</p>

<h2>5. Resumen</h2>
<p>
- El objetivo es encontrar los parámetros &theta; que minimizan el error cuadrático. <br>
- La derivación utiliza el <b>gradiente</b> de la función de error. <br>
- El término <b>A<sup>T</sup></b> aparece al derivar (producto matricial transpuesto) siguiendo las reglas de derivadas de matrices. <br>
- Resolviendo <b>A<sup>T</sup> A &theta; = A<sup>T</sup> y</b> obtenemos los parámetros óptimos <b>&theta;̂</b>.
</p>




