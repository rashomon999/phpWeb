    <h2>Nivel de Significancia (\(\alpha\)) y Error Tipo II (\(\beta\))</h2>

    <h2>Definiciones</h2>
    <ul>
        <li><strong>Nivel de Significancia (\(\alpha\)):</strong> Es la probabilidad de cometer un <strong>error tipo I</strong>, es decir, rechazar la hipótesis nula (\(H_0\)) cuando en realidad es verdadera.</li>
        <li><strong>Error Tipo II (\(\beta\)):</strong> Es la probabilidad de cometer un <strong>error tipo II</strong>, es decir, no rechazar la hipótesis nula (\(H_0\)) cuando en realidad es falsa.</li>
    </ul>

    <h2>Relación entre \(\alpha\) y \(\beta\)</h2>
    <ul>
    <li>Si se reduce \(\alpha\) (mayor exigencia para rechazar \(H_0\)), \(\beta\) tiende a aumentar, lo que implica un mayor riesgo de no detectar un cambio real.</li>
        <li>Si se reduce \(\beta\) (se aumenta el poder de la prueba), se puede incrementar \(\alpha\), aumentando la probabilidad de rechazar \(H_0\) erróneamente.</li>
    </ul>

    <h2>Poder de la Prueba</h2>
    <p>El poder de la prueba es la probabilidad de detectar correctamente un cambio real, y se calcula como:</p>
    <p class="formula">Poder = 1 - \(\beta\)</p>

    <p>En el problema, si \(\beta = 0.05\), entonces el poder de la prueba es:</p>  
    <p class="formula">1 - 0.05 = 0.95</p>

    <p>Esto significa que hay un <strong>95%</strong> de probabilidad de detectar correctamente la
     reducción de la media a 4.99 metros.</p>


