    <h3>
    Relación entre el error tipo II y el poder de la prueba
    </h3>  
    El poder de la prueba es la probabilidad de detectar un cambio real cuando este ocurre. Se calcula como \(1 - \beta\).  
    En este caso, como \(\beta = 0.05\), el poder de la prueba es 95%, lo que significa que hay un 95% de probabilidad de detectar correctamente la reducción de la media a 4.99 metros.

