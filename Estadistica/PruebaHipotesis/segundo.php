    - Un menor error tipo II (\(\beta\)) implica un mayor poder de prueba, lo que requiere una muestra más grande para asegurar que se detecte el cambio cuando ocurre.
    - Si se permitiera un mayor error tipo II (por ejemplo, 10% en lugar de 5%), se necesitaría una muestra más pequeña.
