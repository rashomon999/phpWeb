<h3>3.4. TIC para posibilitar medios de vida</h3>
<p>
Los medios de vida son otra área afectada por las TIC. Los medios de vida de los migrantes y sus familias están
altamente entrelazados con aspectos de migración/desarrollo, incluidas las remesas y las inversiones (Duffield, 2006;
Sørensen et al., 2002), que están cada vez más enredados con las TIC (Hackl, 2018; Steel et al., 2017). Sin embargo,
se sabe muy poco sobre la interacción contemporánea entre las TIC, los medios de vida y la migración/(in)movilidad/
desarrollo, particularmente en el contexto del “Sur Global” (Scoones, 2009; Steel et al., 2017). Los estudios existentes
tendieron a pasar por alto el papel de las TIC; concentrarse en hogares rurales con una visión estrecha de “localismo
ingenuo” (Scoones, 2009); y ver la mejora de los medios de vida solo como aumento de ingresos (Czaika y De Haas,
2014). El desdén por el papel de las TIC ha estado en contradicción con el uso cada vez mayor que las personas hacen
de estas tecnologías en sus actividades cotidianas relacionadas con los medios de vida. De hecho, como subrayó Heeks
(2006), este problema es prevalente en el ámbito más amplio de las TIC y el desarrollo—“ha habido un sesgo hacia la
acción, no un sesgo hacia el conocimiento. Estamos cambiando el mundo sin interpretarlo o comprenderlo” (p. 1).
</p>
<p>
Sin embargo, vale la pena mencionar algunos trabajos limitados, en términos generales, sobre TIC y medios de vida
relacionados con la migración—ingresos, empleo y habilidades; remesas; y medios de vida en medio de desplazamientos
relacionados con el medio ambiente. Con respecto a lo primero, por ejemplo, Steel et al. (2017) en sus estudios de caso
en Camerún, Ruanda y Sudán encontraron que las conexiones digitales y el flujo de personas, bienes, servicios e
información están mejorando los medios de vida. Sin embargo, algunos se ven afectados por la desigualdad digital y
la política de la movilidad (Cresswell, 2010)—incapaces de moverse o acceder a nuevas tecnologías debido a
desigualdades sociales—“poblaciones atrapadas” (Boas, 2017). Aquellos sin acceso a teléfonos móviles se vuelven
cada vez más dependientes de intermediarios tradicionales para oportunidades laborales e información de mercado.
También alguna literatura gris presentó estudios de caso de trabajo digital relacionados con refugiados y desplazados
internos como “Samasource”<sup>1</sup> (Hatayama, 2018; Samasource, 2019). Hackl (2018, p. 158) afirma “(…)
‘los medios de vida digitales’ ya se están convirtiendo en un nuevo mantra del desarrollo […] los refugiados deberían
aprovechar las economías digitales convirtiéndose en limpiadores de datos y trabajadores de multitudes, construyendo
así un futuro más brillante, del dinero por comida a la tecnología por comida”. No obstante, tal optimismo parece estar
mal ubicado ya que la premisa representa solo grupos limitados—jóvenes expertos en tecnología y refugiados sirios
relativamente acomodados (Borkert et al., 2018; Kaufmann, 2018), como se indica en el estudio cualitativo de Mansour
(2018) que muestra que dos tercios de los refugiados sirios que cruzaron a Egipto tienen altas habilidades de alfabetización
digital.
</p>
<p>
Hatayama (2018) informó cómo internet, teléfonos móviles, aplicaciones y plataformas permitieron a algunos refugiados
y desplazados internos mejorar los ingresos y los recursos financieros—incluido mediante la impartición de formación
lingüística en línea, micromecenazgo, préstamos entre pares, comercio electrónico y emprendimiento; y también,
acceder y compartir información y desarrollar habilidades—incluida información sobre oportunidades de empleo local
y regulaciones, y acceder a contenido y docentes de instituciones de educación superior de forma remota. También hay
una tendencia creciente a impartir codificación informática y otras habilidades de TI a refugiados y desplazados internos
en campos de refugiados (Hatayama, 2018). Las soluciones de tecnología financiera y biométrica como las cadenas de
bloques y las tarjetas de identidad biométricas también se presentan como asistentes para que refugiados y desplazados
internos accedan a servicios financieros y esenciales. Sin embargo, no está claro si y cómo se han abordado los notorios
problemas de privacidad y ética relacionados con el uso de tales tecnologías en el contexto de la vulnerabilidad
(Madianou, 2019; Maitland, 2018; Thomas, 2005). El informe de Hatayama (2018) parece representar solo casos
limitados ya que tales aplicaciones están en gran medida ausentes en muchos contextos, incluidos vastos campos de
refugiados en el Cuerno de África. Se ha informado que los sistemas de dinero móvil como M-PESA en Kenia facilitan
pagos y transacciones financieras a los pobres, incluidos “solicitantes de asilo” y refugiados en campos que en su mayoría
están privados de servicios financieros tradicionales (Miao et al., 2018; Suri & Jack, 2016).
</p>
<p>
Con respecto a las remesas y los medios de vida en medio de desplazamientos relacionados con el medio ambiente, la
investigación sobre remesas tendió a centrarse en los hogares en países emisores de migrantes, y el papel de las TIC ha
sido en gran medida ignorado. Aunque la investigación específica sobre el papel de las TIC en los medios de vida durante
el desplazamiento relacionado con el medio ambiente generalmente falta (Boas, 2017), algunos investigadores han
destacado la importancia de las TIC en tales situaciones. Por ejemplo, el análisis de Lu et al. (2016) de la movilidad de
las personas afectadas por el ciclón Mahasen en Bangladesh utilizando datos de teléfonos móviles indica la utilidad de
los teléfonos móviles para comprender la movilidad humana de rápido cambio, lo que puede ofrecer información crucial
sobre los medios de vida y la seguridad de las personas en el momento del desastre. Hiremath y Misra (2006) presentaron
cómo una intervención de información sobre migración habilitada por TIC en Gujarat, India abordó problemas críticos
de medios de vida locales. Señalaron que un simple centro de información para migrantes habilitado por telefonía
inalámbrica en bucle local (WLL) apoyó enormemente la seguridad social y económica de los migrantes vulnerables al
reducir los costos de migración, mejorar la comunicación y la creación de redes,
