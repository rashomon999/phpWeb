 <h3>4.1. Migrantes y riesgos digitales</h3>
<p>
Aparte de los riesgos que comparten con todos los demás usuarios de TIC, como los riesgos de salud inducidos por las 
computadoras, los riesgos digitales asociados con los migrantes pueden verse desde dos grandes lentes. Son la visibilidad 
digital, la vigilancia y el control fronterizo, por un lado, y la trata de personas y la extorsión, por el otro, como se 
elucida a continuación.
</p>

<h4>4.1.1. Migrantes y visibilidad digital, vigilancia y control fronterizo</h4>
<p>
Existen riesgos crecientes asociados con la visibilidad y vigilancia habilitadas por TIC de los migrantes, principalmente 
cuando huyen de casa y viajan dentro y a través de las fronteras en busca de lugares más seguros 
(Mancini et al., 2019; Wall et al., 2017). Estos incluyen la vigilancia por parte de los estados nación de origen y destino, 
los contrabandistas, traficantes y bandas criminales (Latonero & Kift, 2018; Vukov & Sheller, 2013), los riesgos vinculados 
al uso de datos digitales, como las identificaciones biométricas digitales que pueden exponer las identidades de los 
migrantes (Maitland, 2018), y los datos generados como resultado del uso de TIC por parte de los migrantes, como los 
datos de uso de teléfonos móviles y redes sociales—citados a menudo como “big-data”.
</p>

<p>
El uso generalizado de tecnologías de vigilancia después del 11-S, “tecnologías de control”, en las fronteras y más allá 
se ha visto exacerbado en medio de la migración “irregular” asistida por TIC, en particular desde la “crisis de refugiados” 
de 2015/16. Algunos investigadores han destacado la creciente sofisticación de estas tecnologías y cómo su uso está 
socavando cada vez más los derechos de protección y privacidad de los migrantes vulnerables (Jumbert, 2018; Madianou, 2019; 
Vannini et al., 2019). Las fronteras digitales se han extendido más allá de las fronteras físicas, mediante la cual grandes 
cantidades de datos de migrantes—a menudo perfilados racialmente—son recopilados y almacenados en bases de datos grandes y 
distribuidas y compartidos entre varios actores. 
</p>

<p>
Además, muchas autoridades europeas de inmigración y fronteras han buscado y obtenido inmunidad legal para registrar 
(e incluso hackear) los teléfonos de los migrantes (Frouws et al., 2016; Jumbert et al., 2018). Si bien tal actividad puede 
informar algunos elementos de los propósitos previstos—como verificar e identificar a los migrantes frente a otras amenazas 
de seguridad, la dependencia excesiva de tales tecnologías también puede conducir a resultados erróneos y riesgosos—por 
ejemplo, los migrantes pueden autocensurarse; el uso del teléfono móvil puede haber sido compartido entre muchos usuarios; 
y la identidad en línea de los migrantes puede ser hackeada y falsificada por otros.
</p>

<p>
Algunos estudios destacaron diferentes formas de estos riesgos. Por ejemplo, Beduschi (2017) destacó el riesgo de los 
migrantes de ser rastreados y descubiertos por traficantes, contrabandistas, guardias fronterizos y delincuentes comunes a 
través de aplicaciones de sistema de posicionamiento global (GPS). Chouliaraki y Musarò (2017) observaron cómo las 
autoridades en Grecia e Italia utilizaron las TIC para interceptar a los migrantes antes de que llegaran a las fronteras. 
En su estudio del viaje de refugiados sirios e iraquíes a Francia, Gillespie et al. (2018) destacaron la tensión entre la 
importancia de los teléfonos móviles como “líneas de vida, tan importantes como el agua y la comida” y los teléfonos móviles 
como nuevas fuentes de explotación, abuso y vigilancia para los migrantes, ya que la migración ha sido criminalizada por la 
política europea y financierizada por los contrabandistas. 
</p>

<p>
Los teléfonos móviles a veces han sido fuente de tortura e incluso de muerte, como cuando la posesión de información 
sensible por parte de los migrantes es descubierta por los perpetradores. Otros estudios destacaron preocupaciones sobre el 
creciente uso de sistemas de identificación biométrica en relación con migrantes vulnerables por varias partes, incluidos 
gobiernos, organizaciones humanitarias y privadas (Kingston, 2014). Las identificaciones biométricas pueden exponer fácilmente 
a las personas, ya que implican patrones fisiológicos y de comportamiento de una persona como huellas dactilares, ADN, iris, 
rostro, voz, forma de la oreja, olor corporal, firma manuscrita, pulsación de teclas y forma de andar (Kingston, 2014; 
Thomas, 2005). Jumbert et al. (2018, p. 4) enfatizaron que:
</p>

<blockquote>
De particular preocupación son las implicaciones de una forma híbrida de gobernanza, donde las empresas privadas, las 
autoridades estatales, las ONG y las organizaciones internacionales no logran comprender las capacidades de vigilancia de 
los dispositivos digitales o no logran establecer altos estándares de salvaguardias digitales.
</blockquote>

<p>
Además, el mayor uso de TIC por parte de los migrantes ha producido abundantes datos digitales que pueden caracterizar el 
comportamiento en línea de los migrantes, así como sus identidades. Si bien el uso ético, cauteloso y responsable de dichos 
datos podría ser beneficioso de muchas maneras, como “para la protección de los derechos humanos de los migrantes al mejorar 
tanto la toma de decisiones como las medidas para prevenir muertes innecesarias en el mar, el maltrato y la trata de 
migrantes” (Beduschi, 2017, p. 981), el uso inapropiado de dichos datos también podría poner en riesgo la vida y la privacidad 
de los migrantes. Sin embargo, falta investigación que examine la magnitud del riesgo planteado o exhibido sobre los migrantes 
y sus redes como resultado del uso inapropiado de los datos digitales de los migrantes.
</p>

<p>
No obstante, debe señalarse que los migrantes no siempre son víctimas pasivas de la vigilancia, ya que a veces se apropian de 
su uso de las TIC. Por ejemplo, Gillespie et al. (2018) documentaron cómo los refugiados sirios usaban las TIC de manera 
creativa para evitar la vigilancia, como proteger sus identidades digitales, usar aplicaciones móviles encriptadas, grupos 
cerrados, seudónimos y avatares en redes sociales para compartir información sensible. En resumen, el creciente uso de 
“tecnologías de control” y las huellas digitales de los migrantes por parte de varios actores plantea un riesgo serio para 
los migrantes vulnerables, a pesar de que este tema importante solo se plantea de manera pasajera en la mayor parte de la 
literatura existente (Ver Tabla 1).
</p>
        





<section>
  <h2>4.1.2. TIC, trata de personas y extorsión</h2>
  <p>
    La visibilidad digital y las “tecnologías de control” no son los únicos riesgos digitales para los migrantes. 
    Algunos investigadores reflexionaron sobre cómo las TIC, principalmente los teléfonos móviles y las redes sociales, 
    han sido utilizadas por contrabandistas, tratantes y pandillas para desinformar, esclavizar, extorsionar y torturar 
    a los migrantes para obtener rescates (Latonero, 2011; Van Esseveld, 2019), así como también cómo los migrantes 
    pueden consumir información engañosa y falsa de las redes sociales que a veces puede tener consecuencias mortales 
    (Borkert et al., 2018).
  </p>
  <p>
    Van Esseveld (2019) y Reisen y Mawere (2017) arrojaron luz sobre cómo los teléfonos móviles están siendo utilizados 
    por traficantes y grupos armados en países de tránsito como Libia para abusar y extorsionar a los migrantes. 
    Como se destaca en “Torturados en cámara: El uso de las TIC en la trata con fines de rescate” (Van Esseveld, 2019), 
    los traficantes han utilizado llamadas en vivo mientras los migrantes son torturados y gritan, así como imágenes y videos 
    grabados de torturas para recaudar rescates de familiares y amigos lejanos.
  </p>
  <p>
    Beşer & Elfeitori (2018) subrayan cómo la asociación europea con Libia para evitar que los migrantes lleguen a Europa 
    ha fomentado violaciones que incluyen detención arbitraria, esclavitud, desnutrición, falta de higiene, tortura y abuso sexual en Libia. 
    Newell et al. (2016), en su estudio sobre las prácticas de información de los migrantes indocumentados en la frontera entre 
    Estados Unidos y México, destacaron cómo poseer contactos de amigos y familiares expuso a los migrantes a riesgos de extorsión 
    y abuso por parte de narcotraficantes, tratantes de personas, ladrones y oficiales corruptos; y cómo los migrantes desconfiaban y rechazaban 
    la asistencia basada en TIC, incluso de actores humanitarios, por temor a la vigilancia.
  </p>
  <p>
    Además, la dependencia de los migrantes de los locales para servicios básicos como la suscripción a telefonía móvil debido a la 
    falta de documentos esenciales y derechos legales los expuso a fraudes, chantajes y explotación (Dekker et al., 2018; 
    Newell et al., 2016; Vannini et al., 2019). En resumen, aunque la investigación específica es escasa, la evidencia emergente 
    muestra el creciente uso de las TIC para la trata de personas y la extorsión de migrantes (véase Tabla 1).
  </p>
</section>

<section>
  <h2>4.2. Comunicación habilitada por TIC, cargas y presiones sobre los migrantes</h2>
  <p>
    El aumento de la comunicación y la conexión habilitada por las TIC de los migrantes conlleva costos de carga y presión, 
    como la vigilancia social, las obligaciones y los compromisos (Awad & Tossell, 2019). Por ejemplo, el estudio de campo de Horst (2006) 
    en Jamaica destacó cómo el mayor acceso y uso de teléfonos móviles conduce a cargas imprevistas y obligaciones sociales, 
    donde los migrantes distantes están “cargados por amor y la obligación de dar” y a una mayor vigilancia de género 
    e involucramiento en la vida cotidiana de los demás.
  </p>
  <p>
    El estudio de Akuei (2005) sobre refugiados sudaneses reasentados en Estados Unidos muestra cómo las remesas, 
    como obligaciones sociales, generan presiones imprevistas en los refugiados reasentados. 
    “Al saber lo que sus familiares enfrentan en El Cairo y otros lugares de asilo, los refugiados reasentados expresaron sentir 
    una profunda responsabilidad de cumplir con estas obligaciones. […] con sus crecientes remesas y escasos recursos para mejorar 
    sus propias vidas, son los refugiados reasentados quienes ahora se sienten más pobres” (p. 13).
  </p>
  <p>
    Nedelcu y Wyss (2016) señalaron cómo la copresencia facilitada por las TIC no solo complementa el bienestar y la calidad 
    de las relaciones familiares, sino que también su efecto “ambivalente” agrega presión y compromisos adicionales, 
    principalmente sobre los migrantes distantes, para responder a las llamadas en cualquier momento. 
    Las TIC también permitieron una mayor vigilancia cultural, por lo que aquellos con relaciones débiles experimentaron 
    un aumento de conflictos (Horst, 2006; Madianou, 2016b). Por lo tanto, junto con los beneficios, 
    la comunicación y la conexión habilitadas por TIC conllevan costos de carga y presión sobre los migrantes.
  </p>
</section>




