  <h2>3. LAS LIBERALIDADES DE LAS TIC PARA LOS MIGRANTES</h2>
  <p>
    En la mayoría de la literatura, las TIC son vistas como liberadoras para los migrantes con poca o ninguna
    consideración a sus aspectos negativos. Esta sección examina dicha literatura con respecto a cuatro temas que surgieron
    en el análisis; el papel facilitador de las TIC para: las conexiones “diaspóricas”, transnacionales y familiares; la
    planificación y ejecución de la migración; la “integración” de los migrantes; y los medios de vida relacionados con la migración.
  </p>


  <h3>3.1. TIC para facilitar conexiones “diaspóricas”, transnacionales y familiares</h3>
<p>
Si bien concentrado en los contextos y perspectivas del “Norte Global”, un cuerpo relativamente grande y establecido de literatura explora cómo la “diáspora” o comunidad transnacional, a menudo asentada en países “occidentales”, está conectada. Subraya cómo las TIC han proporcionado una plataforma para que los migrantes se mantengan conectados con su familia y comunidad, manteniendo así su cultura, identidades y lazos transnacionales; aunque las TIC son en su mayoría implícitas (Hall, 1990; Levitt y Jaworsky, 2007). Dicha literatura examina cómo las TIC permiten la conexión amplia y multifacética entre los migrantes, su red y entorno, y cómo las relaciones familiares, intimidades y responsabilidades han sido facilitadas (Véase Tabla 1).
</p>
<p>
Por ejemplo, investigadores como Diminescu (2008), Hamel (2009) y Lim et al. (2016) enfatizan cómo las TIC permiten a los migrantes “circular” mientras crean y mantienen conexiones cercanas y lejanas. Sayad (2004) destacó cómo las TIC están transformando cada vez más la “doble ausencia” de los migrantes en una presencia dual y concurrente de “aquí y allá”. Ya sea en forma de simples interacciones “conversacionales” para sustituir la ausencia o de una presencia “conectada” para asegurar una presencia virtual continua (Diminescu, 2008), esta literatura refleja la exploración y utilización continua de las TIC por parte de los migrantes (Gifford & Wilding, 2013; Komito, 2011), incluyendo para las relaciones familiares y la intimidad.
</p>
<p>
Las solidaridades familiares, compromisos compartidos y la noción de familia se han manifestado de manera diferente, incluso por sustitución: objetos representativos o personas que compensan la ausencia de un individuo, ya sea físicamente o mediante una copresencia imaginada (Nedelcu y Wyss, 2016). Estas formas se amplían y se vuelven cada vez más realistas con los avances de las TIC, mediante los cuales emociones, cuidados y apoyo se intercambian a distancia, como a través de videollamadas en vivo. Algunos académicos han explorado cómo las TIC se utilizan cada vez más para “hacer” y conectar a la familia, brindar cuidado a distancia y compartir recursos, emociones y responsabilidades.
</p>
<p>
Por ejemplo, con base en investigación etnográfica sobre mujeres migrantes filipinas en el Reino Unido, Madianou (2016a, 2016b) demostró cómo las nuevas TIC —“polimedia”— posibilitaron la “copresencia ambiental” de familias en múltiples lugares y reforzaron la “tranquilidad emocional de bajo nivel” para quienes tenían relaciones fuertes. De manera similar, una investigación cualitativa con migrantes rumanos en Suiza realizada por Nedelcu y Wyss (2016) demostró cómo las TIC han dado forma a las comunicaciones “rituales”, “omnipresentes” y “reforzadas” y a sus efectos “ambivalentes”, tales como sentimientos, emociones y solidaridad; y cómo las TIC permitieron la “cosmopolitización” de la vida cotidiana al posibilitar la capacidad y sensación de “estar y hacer cosas juntos” a distancia (véase Tabla 1 para más ejemplos). En general, las TIC ofrecen enormes beneficios para las comunicaciones multifacéticas de los migrantes, y esto ha sido demostrado en un cuerpo relativamente grande de literatura concentrada en los contextos y perspectivas del “Norte Global”.
</p>

<h3>3.2. TIC para facilitar la planificación y ejecución de los viajes migratorios</h3>
<p>
Además de facilitar las conexiones transnacionales y familiares, las TIC, principalmente los teléfonos móviles, también han sido elogiadas por ser “cajas de herramientas” esenciales, particularmente para los migrantes “irregulares” durante sus viajes migratorios a menudo fragmentados y peligrosos. Algunos investigadores han esclarecido esta interacción. 
</p>
<p>
Por ejemplo, Gillespie et al. (2018), Dekker et al. (2018), Dekker & Engbersen (2014), Gough & Gough (2019) y Kang et al. (2018) señalaron cómo las TIC permitieron a los migrantes planear, navegar y documentar sus viajes y acceder a servicios en ruta. Los teléfonos móviles se han utilizado para calcular distancias, conocer terrenos, condiciones climáticas y monedas, aprender y traducir idiomas, navegar lugares y servicios —puntos de abastecimiento, transferencias de dinero y refugios temporales—, iluminar caminos oscuros, conectarse con amigos y familiares, actores humanitarios y traficantes. Han servido como “salvavidas” al proporcionar una plataforma para señalar y comunicar peligros (Dekker et al., 2018; Mancini et al., 2019). También proporcionaron alternativas digitales para almacenar y transportar recuerdos personales y documentos cruciales, mapas, diccionarios, libros y efectivo, lo cual puede marcar una diferencia inmensa para los migrantes durante viajes difíciles. En resumen, a pesar de los desafíos y riesgos asociados (Sección 4), como demostraron varios investigadores (véase Tabla 1), las TIC se han convertido en herramientas críticas de navegación, creación de redes y “salvavidas” para los migrantes durante sus a menudo peligrosos y fragmentados viajes migratorios.
</p>

 <h3>3.3. TIC para facilitar la “integración”</h3>
<p>
Esta sección ofrece una visión general del papel de las TIC en la “integración” de los migrantes. La “integración” es un concepto muy debatido, pero en su mayoría se entiende como un proceso dinámico, multidimensional y de adaptación bidireccional en una sociedad “anfitriona” (Ager & Strang, 2008; Fortunati et al., 2011a). Aunque limitado a los contextos y perspectivas del “Norte Global”, existe una investigación emergente significativa sobre el papel de las TIC en la “integración”/inclusión de los migrantes que se basa en la literatura anterior sobre la “diáspora”. Esto puede clasificarse en dos categorías superpuestas.
</p>
<p>
La primera categoría enfatiza las prácticas de información facilitadas por las TIC de los migrantes en relación con sus necesidades de información y el fortalecimiento del capital social para la “integración”. Aunque generalmente faltan estudios de impacto, esta categoría explora el uso de TIC por parte de los migrantes —principalmente teléfonos móviles, internet y redes sociales— en relación con diferentes dimensiones de la integración como ingresos y empleo, idioma y cultura, educación y habilidades, y capital social.
</p>
<p>
Sin embargo, las opiniones sobre la manera en que las TIC influyen en la “integración” son contradictorias. Una visión resalta el papel positivo de las TIC. Por ejemplo, con base en su análisis de las prácticas de TIC de refugiados reasentados en Nueva Zelanda, Andrade y Doolin (2018) identificaron ocho patrones de uso de TIC en relación con la mejora del bienestar y la “participación afectiva”, incluyendo comunicación, transacciones en línea y expresión de identidad cultural. Con base en un análisis cualitativo de refugiados sirios, AbuJarour y Krasnova (2017) indicaron que el uso de teléfonos móviles puede facilitar la inclusión social de los migrantes refugiados. La investigación cualitativa de Alencar (2018) con 18 refugiados sirios, eritreos y afganos en los Países Bajos destacó la importancia vital de los sitios de redes sociales para la alfabetización lingüística y cultural y el desarrollo del capital social. Kaufmann (2018, p. 882) concluyó que “los smartphones tienen un potencial no explotado para los procesos de integración” basándose en su análisis de las prácticas de TIC de refugiados sirios mediante entrevistas, chats de “WhatsApp” e investigación participativa. Aretxabala y Riezu (2013) concluyeron que las TIC aumentan el capital social de los migrantes. Una investigación de métodos “mixtos” realizada por Felton (2015) con “migrantes de habla no inglesa” en Australia reveló cómo el internet móvil se ha utilizado ampliamente para buscar alojamiento e información sobre ubicaciones relacionadas con el empleo.
</p>

        


    
