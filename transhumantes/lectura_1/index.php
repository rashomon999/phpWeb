   <h1>LAS LIBERALIDADES Y TIRANÍAS DE LAS TIC PARA MIGRANTES VULNERABLES: EL STATU QUO, BRECHAS Y DIRECCIONES</h1>

  <p><strong>Yidnekachew Redda Haile</strong>, Royal Holloway University of London, Yidu.Haile.2019@live.rhul.ac.uk / yidnekr@yahoo.com</p>

  <h2>Resumen</h2>
  <p>
    Las tecnologías de la información y la comunicación (TIC) se han vuelto cada vez más vitales para
    las personas en movimiento, incluidas las casi 80 millones de personas desplazadas debido a conflictos, violencia
    y violaciones de los derechos humanos a nivel mundial. Sin embargo, la investigación existente sobre TIC y migrantes,
    que casi en su totalidad se centró en el uso de las TIC por parte de los migrantes “en ruta” o dentro de economías desarrolladas,
    principalmente desde las perspectivas de investigadores de estas regiones, es muy fragmentaria, lo que dificulta
    la comprensión de los objetos clave de investigación. Además,
    a menudo se celebran las TIC como liberadoras y aprovechables a discreción racional de los migrantes,
    aunque son “armas de doble filo” con riesgos, cargas, presiones
    y desafíos de desigualdad significativos, particularmente para los migrantes vulnerables, incluidos aquellos desplazados por la fuerza
    y traficados. Con el fin de abordar estas limitaciones e iluminar direcciones futuras, este artículo, en primer lugar,
    examina la investigación existente en relación con el papel liberador y autoritario de las TIC, particularmente para los migrantes vulnerables,
    explicando así cuestiones clave en el dominio de investigación. En segundo lugar, identifica brechas clave y oportunidades para futuras
    investigaciones. Utilizando una metodología adaptada, se revisó amplia literatura relacionada con las TIC y
    la migración/desarrollo publicada en el período 1990-2020, lo que resultó en
    157 publicaciones seleccionadas que fueron evaluadas críticamente en relación con los temas clave,
    las principales tecnologías tratadas y las metodologías y teorías/conceptos adoptados.
    Además, se destacan ideas clave, tendencias, brechas y oportunidades de investigación futura relacionadas con
    los objetos de investigación existentes y faltantes en TIC y migración/desarrollo.
  </p>

  <p><strong>Palabras clave:</strong> TIC y migración; tecnologías de la información y la comunicación y migración; tecnología digital y migración; ICT4D y migración; tecnología, migrantes vulnerables, riesgos, cargas, presiones y desigualdad; revisión de la literatura sobre tecnología y migración; revisión de la literatura sobre TIC y migración; revisión de la literatura sobre TIC y refugiados</p>

  <h2>1. INTRODUCCIÓN</h2>
  <p>
    A pesar de una desaceleración del 27 por ciento precipitada por la COVID-19, para 2020, más de 281 millones de personas,
    incluidas alrededor de 80 millones desplazadas por conflictos, violencia y violaciones de los derechos humanos —de las cuales alrededor de 34 millones
    son refugiados que cruzan fronteras y “solicitantes de asilo”— están en movimiento (UN DESA, 2020; UNHCR, 2020).
    Mientras tanto, las tecnologías de la información y la comunicación (TIC) o “tecnologías digitales” se están convirtiendo cada vez más
    en una parte integral de la dinámica. Las TIC, principalmente los teléfonos móviles y el Internet, están afectando significativamente la movilidad humana
    y la migración contemporáneas, los flujos de trabajo y recursos, la economía globalizada y el intercambio de información. Como tales, estas
    tecnologías se han vuelto componentes esenciales en la vida de las personas en movimiento, especialmente de los migrantes vulnerables como los desplazados,
    traficados o explotados, así como de sus familias y redes en todas las etapas de la migración —pre-migración, viajes migratorios,
    (re)asentamiento/“integración” y migración de retorno (Hamel, 2009; Massey, 2009; Surak, 2017). Descripciones como
    “migrantes conectados” (Diminescu, 2008), “migrante/diaspora/refugiado digital” (Everett, 2009; Jumbert, 2018),
    “migrante mediatizado” (Hepp et al., 2012) y “presencia conectada” (Licoppe, 2004)
    reflejan el entrelazamiento de estas tecnologías con los migrantes. Los avances en estas tecnologías
    han complicado cada vez más los movimientos y conexiones de las personas hacia patrones más circulatorios, complejos,
    dinámicos, fluidos e impredecibles (Castells, 2011). La distinción entre movilidad y
    migración, así como otros conceptos relacionados como migrante, extranjero, inmigrante, diáspora, nómada,
    refugiado, migrante asilado, persona desplazada (internamente) (IDP), migrante irregular, migrante económico
    e incluso sedentario (Douglas et al., 2019), se ha vuelto más difusa y problemática (Crawley &amp; Skleparis, 2018).
  </p>



    <p>
    Recientemente, el interés en este entrelazamiento de las TIC y la migración/desarrollo ha aumentado enormemente
    entre académicos, organizaciones de la sociedad civil, agencias internacionales y gobiernos.
    Aunque la investigación explícita sobre el uso de nuevas TIC por parte de los migrantes puede rastrearse al menos hasta la década de 1990,
    el área aún sigue siendo joven y exige más investigación (Andersson, 2019). Esto podría atribuirse
    a la complejidad y dinamismo en constante cambio en la forma en que las personas se mueven, interactúan y se conectan,
    en medio de los rápidos giros y cambios en la tecnología y la innovación. La investigación existente en TIC y migrantes
    —que casi en su totalidad se centra en el uso de la tecnología por parte de los migrantes “en ruta” o dentro de
    países con economías desarrolladas (“Norte Global”), principalmente desde las perspectivas de investigadores
    de estas regiones— sigue siendo muy fragmentada. Además, las frases cada vez más usadas como
    “migrant tech” y “refugee tech” son tan optimistas que celebran las TIC como liberadoras y utilizables
    a discreción y elección racional de los migrantes, a pesar de la realidad de que son “armas de doble filo” con
    riesgos, cargas, presiones y desafíos de desigualdad significativos, particularmente para los migrantes vulnerables.
  </p>

  <p>
    Este artículo intenta, cuando menos, una contribución doble a la investigación sobre TIC y migrantes para abordar
    las limitaciones mencionadas. Primero, examina el statu quo de dicha investigación en relación con el poder
    liberador y autoritario de las TIC, particularmente para los migrantes vulnerables, explicando así objetos clave de
    investigación para futuras teorizaciones. En segundo lugar, identifica brechas clave y oportunidades para futuras
    investigaciones. Con este fin, se revisó amplia literatura relacionada con TIC y
    migración/desarrollo publicada entre 1990 y 2020, produciendo 157 publicaciones seleccionadas
    —no todas citadas en el texto por motivos de brevedad y limitación de espacio/palabras, pero sí enumeradas en la
    Tabla 1—. La literatura seleccionada fue evaluada críticamente en relación con los temas clave, las principales tecnologías
    tratadas, y las metodologías y teorías/conceptos adoptados. También se extrajeron ideas y tendencias clave.
  </p>

  <p>
    Tradicionalmente, la movilidad se ha entendido como un movimiento transitorio de personas (también capital,
    bienes y servicios) en el curso de su vida cotidiana, incluyendo movilidad de sustento (Benz, 2014;
    Steel et al., 2017) y movilidad residencial (Cooke & Shuttleworth, 2018); y la migración como un
    cambio permanente de ubicación, a menudo entre países o áreas urbano-rurales (Gmelch, 1980).
    Diversos actores adoptan su versión de los conceptos relacionados con la migración para sus fines (como legales,
    administrativos, académicos y estadísticos) informados por factores espaciales, temporales, metodológicos, legales,
    culturales y políticos (IOM, 2007; 2019). Atributos como los lugares de nacimiento y residencia,
    derechos legales y ciudadanía y duración de la estadía influyen en la manera en que estos conceptos se definen y utilizan.
    Como afirman Sironi et al. (2019), tales definiciones son a veces “inclusivistas” y otras veces
    “residualistas” (excluyen a algunas partes como aquellos que huyen de la guerra o la persecución).
  </p>

  <blockquote>
    <p>
      Por ejemplo, la agencia de la ONU, OIM define migración como:<br><br>
      Un proceso de movimiento, ya sea a través de una frontera internacional o dentro de un Estado. Es un movimiento poblacional,
      que abarca cualquier tipo de movimiento de personas, cualquiera sea su duración, composición y causas; incluye
      la migración de refugiados, personas desplazadas, personas desarraigadas y migrantes económicos (IOM, 2004, p. 41).
    </p>
  </blockquote>

  <blockquote>
    <p>
      De manera similar, según la Convención de Ginebra de 1951, “refugiado” se define como:<br><br>
      Una persona que, debido a un temor fundado de persecución por motivos de raza, religión, nacionalidad,
      pertenencia a un determinado grupo social u opinión política, se encuentra fuera del país de su [sic] nacionalidad y
      no puede o, debido a tal temor, no quiere acogerse a la protección de dicho país; o que, careciendo de nacionalidad y
      encontrándose fuera del país de su anterior residencia habitual como resultado de tales acontecimientos,
      no puede o, debido a tal temor, no quiere regresar a él (IOM, 2021).
    </p>
  </blockquote>

  <p>
    Este artículo se centra principalmente en migrantes vulnerables, como aquellos desplazados, traficados y
    explotados, sin perjuicio de los problemas asociados con el etiquetado e intento de una distinción clara
    entre diferentes “tipos” de migrantes mencionados anteriormente. El autor cree que este trabajo es
    el primer análisis riguroso general sobre TIC y migrantes; no obstante, existen algunos trabajos relacionados —
    por ejemplo, Brown et al. (2019) revisaron 35 artículos sobre el uso de TIC por parte de refugiados;
    Collin et al. (2015) identificaron tres áreas de investigación en el uso de TIC por parte de migrantes:
    preparar la migración, mantener el vínculo y variaciones en el uso; Chonka & Haile (2020) revisaron literatura relacionada
    con el papel de las TIC en la movilidad y la migración pertinente al Cuerno de África; y Andersson (2019) ofreció una
    visión general sobre la “diáspora” y el uso de nuevos medios. Este análisis tiene el potencial de informar
    sustancialmente futuras investigaciones y políticas; y arrojar luz sobre la importancia de las TIC,
    la movilidad y la migración para la investigación en TIC para el Desarrollo (ICT4D).
  </p>

  <p>
    Después de esta introducción, la Sección 2 presenta la metodología diseñada y utilizada. Las Secciones 3 y
    4, respectivamente, dilucidan evidencia empírica, métodos, tendencias e ideas sobre el poder liberador y
    restrictivo de las TIC para los migrantes basándose en la literatura existente; seguidas por la Sección 5,
    que denota las brechas y oportunidades de investigación futura. El artículo termina con una breve conclusión
    en la Sección 6.
  </p>



